                  <strong>Session V: New Research tools and technologies</strong><br>

                  <br>

This session highlighted recent technological advances that may be helpful in the study of dysferlin or muscular dystrophies.  New research tools and technologies have a profound impact on the kinds of scientific questions that can be addressed and our ability to determine the pathological mechanisms that underlie LGMD2B/ Miyoshi.  This session was subdivided into two parts, one part covering new advances in imaging technologies for muscular dystrophy that will help researchers follow the progression of muscle diseases with increased accuracy and in some cases non-invasively; and the second part on progress in developing dysferlin-deficient myoblast cell lines that will help facilitate a variety of research endeavors, including high throughput screening for small molecule therapies and fundamental studies of dysferlin function.   <br><br>

<em>Advances in imaging technologies</em><br>
<br>

<strong>Dr. Tom Rando</strong> (Stanford University School of Medicine) discussed the development of two genetically altered strains of mice that use a Luseap reporter gene to track skeletal muscle destruction and regeneration.  This system uses two distinct reporters, serum alkaline phosphatase and luciferase (luciferase activity can be measured non-invasively).  These markers are inducible and tissue specific, and should be very useful for tracking the destruction and regeneration of muscle fibers noninvasively once crossed with dysf-null mice.  One mouse strain will express the Luseap reporter only in mature muscle fibers and will serve as a marker of muscle loss; in the other mouse strain, the reporters will only be expressed in satellite cells and serve as a marker of muscle regeneration.  Examples of the luciferase <em>in vivo</em> imaging technology were shown from normal mice with luciferase expression in mature skeletal muscle fibers.  One limiting factor of this technology is that because it is light based, the signal intensity can be influenced by a number of factors such as the presence/absence of fur, skin pigmentation, and thickness of the skin/subcutaneous fat.  Because of these factors, it was noted that the strength of the signal doesn’t correlate well with the sites of greatest muscle bulk on an individual animal.  However, the ability to monitor the same muscles in a single live animal over time should ameliorate these issues and give a good estimate of changes in muscle pathology or regeneration.  The mice carrying the Luseap reporter gene were recently crossed with the SJL model of dysferlin deficiency, and it is anticipated that initial studies on these mice will proceed by late summer 2008.   <br><br>
 
<strong>Dr. Daniel Stockholm</strong> (Généthon) began his talk with an overview of the advantages and limitations of current imaging technologies.  This discussion included photonic imaging, such as 3D confocal microscopy, Fluorescence Resonance Energy Transfer (FRET) and Fluorescence Recovery After Photobleaching  (FRAP), as well as some examples of medical imaging techniques such as MRI, CT and gamma-scintigraphy, as applied to animals.  The advantages of photonic imaging include the ability to examine the fine distribution of labeled proteins in living mice, and in some cases to track them in real time at the cellular and sub-cellular level.  The major disadvantage is that in order to obtain high resolution images of myofibers, the skin of the animal must be opened, making it difficult to examine the same animal over time.  Furthermore, photonic imaging has very few clinical applications.  Medical technologies such as MRI, CT, and gamma-scintigraphy have the advantage of being non-invasive and thus able to sample the same patient or animal multiple times in a longitudinal study.  However, MRI, CT and gamma-scintigraphy don’t offer the fine resolution or specific labeling of a molecule of interest that can be achieved with photonic imaging. <br> 

Dr. Stockholm then presented some preliminary results from his attempts to use technicium-labeled albumin to track changes in muscle membrane integrity.  This project combines the commonly used membrane permeability assay based on Evan’s blue dye uptake (Evan’s blue dye is known to bind to albumin in the blood) with the non-invasive imaging techniques of gamma-scintigraphy and MRI.  By labeling albumin with the radioactive tracer technicium, Dr. Stockholm is hoping to non-invasively monitor the membrane permeability of muscle fibers <em>in vivo</em>.  The development of this technique may be useful for monitoring disease progression in patients, or during clinical trials, since technicium-labeled albumin is already commonly used in the clinic for other purposes. 
<br><br> 

<em>Development of dysferlin deficient myoblast cell lines</em><br><br>

<strong>Dr. Terry Partridge</strong> (Children’s National Medical Center) began by describing several complications associated with using immortalized myoblast models for studying muscle diseases such as dysferlin deficiency.  The major concern with immortalized muscle cell lines is the genetic drift that occurs while they are in culture.  This changes their characteristics over time, and it is difficult to compare results between different labs even when they use the same cell line.  Dr. Partridge then described how the immortomouse has been used to generate immortalized myoblast cell lines that are superior to the commonly used C2C12 cell line in their ability to fuse and form myotubes, and that do not show the high level of genetic drift that has been reported to occur in C2C12 cells.  Because the immortomouse takes advantage of a temperature-sensitive mutation, there is some concern that growing the cells under permissive conditions (33C, plus 10ng/ml interferon gamma) could impact other aspects of cell function.  However, the ease of use, genetic stability, and high myogenicity of these lines are significant advantages over traditional cell lines.  Furthermore, crossing the immortomouse with mouse models of muscular dystrophy is a strategy that may facilitate the production of high quality muscle lines to study various forms of muscular dystrophy.  Dr. Partridge has plans to do just that, and is currently crossing the dysferlin-deficient A/J mouse line with the immortomouse to create a high quality dysferlin-deficient myoblast cell line.  <br>
<br> 

<strong>Dr. Robert Brown</strong> (Harvard Medical School) presented his work on the development and characterization of a C2C12 cell line that stably expresses shRNAs that suppress dysferlin expression.  His laboratory began the experiments with commercially available (Sigma) siRNAs and then modified the sequences for greater efficacy.  These sequences were used to create plasmids that were stably introduced into the C2C12 cells.  He now has a mixed population of C2C12 cells that show an 80% knock-down of dysferlin.  Selection and expansion of single clones to create a more homogeneous cell population is planned.  Dr. Brown also presented gene expression data suggesting that a number of genes involved in myogenesis and regeneration are increased in his dysferlin-deficient C2C12 cells.  He is interested in using this information to identify biomarkers that are specific to dysferlin deficiency.  Dr. Brown also discussed preliminary experiments in which primary cultures of dysferlin-deficient cells were used in a detergent-based wounding assay (.025% SDS for 2min) that successfully demonstrated the inability of the dysferlin-deficient cells to reseal membrane wounds.  The ultimate goal of the project is to develop a method for high throughput screening of small molecules that can help compensate for the loss of dysferlin.  Finally, Dr. Brown discussed his attempts to over-express dysferlin in transgenic mice and the surprising discovery that too much dysferlin expression causes a severe muscle pathology. 
