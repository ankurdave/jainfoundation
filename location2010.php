				  <p><strong>Arrival</strong></p>
<p> Conference attendees should plan to fly into Seattle via Seattle-Tacoma International Airport (SEA).&nbsp;  Transportation to and from the airport is  provided by a shuttle from the hotel arranged by and paid for by the Jain  Foundation.&nbsp; Please look for the &quot;Jain  Foundation Inc&rdquo; sign.&nbsp; Travel time between the airport and hotel is approximately 40 minutes.&nbsp; Rental cars are available at the airport as well as at the hotel (Avis) and can be arranged by the attendee at his/her expense. </p>



<p>&nbsp;</p>

<p><strong>Hotel Charges</strong></p>
<p>The Jain Foundation has reserved a block of rooms at the Hyatt, therefore it is imperative that you tell us your arrival and departure dates, so that we can give the hotel an accurate count of the number of rooms that will be needed.  Unless you are sharing a room, you will book your own room at the Hyatt. A link to the hotel reservation site is coming soon. If you are sharing a room, do not book your own room at the Hyatt, as this will result in double booking.  The room rate is $142/night + 14.5% (tax) for single or double occupancy. &nbsp;A third person in the room is $25 per night additional (children under 18 are free). &nbsp;This special rate is available to conference attendees for 3 days before and after the meeting (the nights of September 8-17). &nbsp;However the availablity of rooms before and after the meeting is not guaranteed, so the sooner we hear from you, the more likely that they will be available. &nbsp;Rooms will be assigned from the Jain Foundation block on a first come first served basis upon arrival. </p>
<p>Participants  will be responsible for any incidental costs associated with their room &ndash; this includes parking ($7/day), phone calls, food/beverages taken from or ordered to the room, dry  cleaning, pay-per-view movies, etc.</p>

