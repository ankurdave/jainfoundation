                  <strong>Session I: Dysferlinopathy – Mechanisms of Pathology I</strong><br>

                  <br>
The talks in this session focused on changes that occur in muscle as a consequence of the loss of dysferlin function, and how these changes lead to muscle fiber degeneration and pathology.<br><br>

<strong>Robert Bloch</strong> (USA) presented research on the role of dysferlin in the repair of damaged muscle fibers in vivo. Dysferlin’s role in muscle plasma membrane repair in vitro is widely accepted, yet it is not clear what happens to muscle fibers in vivo following injury in dysferlin-deficient animal models. His laboratory employs acute large strain eccentric contractions (Lovering et al. 2007) to induce muscle injury in control and dysferlin-deficient A/J mice. They then monitor the extent of contractile function loss and recovery, membrane disruption and resealing, and markers of muscle regeneration and inflammation over a time course of several weeks. The results demonstrate that A/J mice are no more susceptible to large strain muscle injury than control mice, but that they take longer to recover from muscle damage and require myogenesis to replace the fibers that were damaged directly or indirectly by injury (Roche et al. 2008). Remarkably, however, the muscle fibers in A/J mice that were initially damaged are impermeant to fluorescent dextrans three days after the injury, suggesting that their plasma membranes reseal the initial wound. Examination of injured muscles during the recovery period reveals a massive inflammatory response in A/J but not control mice, which Dr. Bloch suggests may be responsible not only for triggering myogenesis, but also for causing additional damage to fibers that had not been injured during the initial large strain contractions.  <br><br>

<strong>Jeffery Molkentin</strong> (USA) discussed the role of Ca2+ in muscle degeneration. Ca2+ can enter cells through sarcolemmal breaches, which are common in muscular dystrophies due to increased susceptibility to contraction-induced membrane tears or, in the case of dysferlin deficiency, due to reduced ability to repair tears. Dr. Molkentin hypothesized that in both cases, unregulated entry of Ca2+ into the muscle fiber is the key event that triggers muscle fiber death through apoptosis and necrosis. By generating transgenic mice expressing a variety of cation exchangers and channels, he demonstrated that excess Ca2+ influx is sufficient to cause extensive muscle pathology, and that Ca2+ efflux can ameliorate muscle degeneration in mouse models of muscular dystrophy. To determine whether this is also the case in dysferlin deficiency, he plans to cross the Ca2+ channel and exchanger overexpressing mice with dysferlin-deficient mice, which have indicators of intercellular Ca2+ overload such as swollen mitochondria.  Dr. Molkentin has also generated transgenic mice that expresses dysferlin from a muscle specific promoter. Crossing these mice to dysferlin-deficient A/J mice corrects the dysferlin-deficient pathology, demonstrating that dysferlin only needs to be expressed in skeletal muscle (and not other cell types, such as immune cells) to prevent disease.

