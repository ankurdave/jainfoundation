                  <strong>Molecular studies of patients showing  dysferlin-deficiency on biopsy, but undetectable dysferlin mutations</strong>

                <br>Akanchha Kesari, PhD, Heather Gordish-Dressman, PhD,  and Eric P Hoffman, PhD

                <br>

                  <br>

                  Both  LGMD2B and Miyoshi myopathy show loss of dysferlin protein at the myofiber  membrane in muscle. By immunoblotting, patients can show either complete  deficiency, or partial deficiency. Published reports have suggested that the  majority of dysferlin-deficient patients show mutations of the corresponding 55  exon dysferlin gene.&nbsp;However, there are also patients that show  dysferlin-deficiency on muscle biopsy, and a clinical picture consistent with  LGMD, yet do not show detectable mutations of the dysferlin gene by sequencing  of all exons.&nbsp;We routinely provide dysferlin immunoblot testing on  subjects referred for a biochemical panel for muscular dystrophy proteins. Our  diagnostic panel includes dystrophin (immunostaining, immunoblotting),  dysferlin immunoblotting, sarcoglycan immunostaining, merosin/laminin alpha 2 immunostaining,  and histology.&nbsp;

We selected 25 patients showing complete  deficiency or partial deficiency (&lt;20% normal levels) of dysferlin by duplicate  immunoblot analyses.&nbsp;DNA was isolated from muscle, and subjected to DHPLC  analyses of all 55 exons from each subject.&nbsp; Heteroduplexes were then  sequenced.&nbsp;Of the 25 subjects studied, we identified pathogenic mutations  of the dysferlin gene in 19 subjects.&nbsp;Of the 27 mutations identified,  about 60% were novel to this series.&nbsp; Our finding of 6 dysferlin-deficient  subjects with no detectable mutations could indicate difficulty in identifying  deep intronic mutations or promoter mutations, or this could reflect secondary  deficiency of dysferlin protein due to some other disorder. To test for  potential candidate genes in these mutation-negative subjects, we used a novel  expression profiling approach, where genome-wide mRNA profiles of each of the  five subjects were systematically compared to other dystrophies (FKRP [LGMD2I]  mutation-positive n=9; LGMD2B mutation positive n=10), as well as normal  controls and ALS groups. Unsupervised hierarchical clustering to map patient  profiles relative to each other showed that LGMD2I, LGMD2B, and the  mutation-negative dysferlin subjects were all closely related in transcriptome,  similar to data published by Bakay et al. 2006.&nbsp;To query the possibility  that the dysferlin-negative mutation-negative patients represent a new  diagnostic group, we tested for probe sets that were statistical outliers for  each mutation-negative sample, and then investigated them as candidate genes  for novel types of muscular dystrophy.<strong><span class="style17"><br>

