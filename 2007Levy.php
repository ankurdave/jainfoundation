                  <strong>Clinical and molecular defects in dysferlinopathies:  from patients to therapies</strong></span><br>

                  <br>

                  Dysferlinopathies  belong to the heterogeneous group of autosomal recessive muscular dystrophies.  Mutations in the gene encoding dysferlin (<em>DYSF</em>) lead to distinct  phenotypes, mainly Limb Girdle Muscular Dystrophy type 2B (LGMD2B) and Miyoshi  myopathy (MM). Due to the clinical  heterogeneity, initial dysferlin protein analysis on muscle biopsy samples is  essential to orientate diagnosis. However, diagnosis should be confirmed by  molecular analysis of the <em>DYSF</em> gene. The large size of the <em>DYSF</em> gene (&gt; 150 kbp, 55 exons) makes this a challenging task on a routine basis.  Methods for mutation screening are particularly useful in this regard. We  report the results of mutational screening in the largest cohort reported to  date. Altogether, 144 patients presenting dysferlin protein deficiency  identified on muscle biopsy samples using immunohistochemical or Western-blot  analysis have already been explored and included. Genomic DNA was screened for <em>DYSF</em> mutations using DHPLC analysis, and subsequent sequencing of detected

variants, in a routine diagnostic setting. In  100 patients (70%), molecular analysis identified two disease-causing  mutations, confirming the diagnosis of primary Dysferlinopathy on a genetic  basis. Furthermore, one mutation was identified in 17 (12%) patients, without  identification of a second deleterious allele. We are currently developing  complementary analysis for patients in whom only one or no disease-causing  allele could be identified using the genomic screening procedure. Among the  identified mutations, 37 have not been reported before, which corresponds to  more than 10% of all <em>DYSF</em> mutations reported to date. Most of the  identified mutations are predicted to produce a truncated protein or one  amino-acid substitution, but we report a high proportion of nonsense mutations  as compared to previous series. These molecular data have been recorded in the  Universal Mutation Database for Dysferlin (UMD/<em>DYSF</em>). In this database, detailed clinical and molecular data from  our series as well as from the literature have been included. All together, 550  variations in <em>DYSF</em> are enlisted, a short majority of them corresponding  to our own series. Data analysis reveals that single nucleotide changes are  predominant, without apparent mutational &quot;hotspots&quot;. Patients in whom  a single or no deleterious mutation has been identified are included for  transcriptional analysis (mRNAs extracted from muscle biopsy samples or  monocytes), based on cDNA sequencing and quantitative analysis (RTQ-PCR). Such  a database and subsequent mutational analysis are of great help for designing  targeted therapeutic strategies. Indeed, the various mutation types make it  difficult to imagine a single approach for all patients in the future. For  example, we have identified a homozygous deletion spanning 200Kb at the genomic  level and removing 39 exons, but sparing the trans-membrane domain and  associated to an atypical/mild phenotype in one patient. This observation and  others from the literature strongly support the modular flexibility of  Dysferlin and the relevance of exon-skipping or mini-dysferlin based strategies  (see poster by Krahn et al.) as potential therapeutic strategies. In our  opinion and based on the mutational survey, AAV transfer, exon-skipping, and  readthrough using aminoglycosides should be considered as different therapeutic  alternatives for different types of mutations. Finally, for evaluating the  efficiency of different strategies, reproducible and easy functional tests will  be necessary in the future. Monocytes appear to be an excellent model to estimate  the level of Dysferlin before and after treatment. Diagnosis/functional tests  are thus designed using immunocytochemistry and flow cytometry from monocytes; preliminary  results on these strategies will also be presented. </p>

