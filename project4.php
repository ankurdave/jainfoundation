                       <a name="2" id="2"></a><br>
                       <strong><font color="#0175C2"><span class="style5">Novel membrane repair proteins</span><br>
                        </font></strong>Rumaisa Bashir, University of Durham (Durham, UK)<br>
                        <br>
                        <strong>Project Objective</strong><br>
                      Investigate the candidacy of a novel gene for non-dysferlin Miyoshi myopathy linked with defective membrane repair <br>
  <br>
                      We have previously shown that defective membrane repair is a feature of some forms of non-dysferlin Miyoshi myopathy, designated MMD3. Recently we have identified the chromosomal location of the MMD3 gene. In two MMD3 linked families we have identified non-synonymous sequence variants in a novel gene that may be potentially pathogenic. The role of this candidate gene in MMD3 is presently unclear and to resolve this we will (i) Examine the protein expression of the encoded protein in MMD3 patient cells using a specific antibody and (ii) Generate muscle cells deficient for the protein using siRNAs for membrane repair assays. Our work may lead to further understanding of muscle membrane repair and identification of further therapeutic strategies for muscular dystrophies linked with impaired sarcolemmal integrity.<br>
