					    <br>
					    <strong>Project Objective</strong><br>
					     <br>
					    Although it is clear that dysferlin is essential for rapid calcium-mediated membrane resealing, very little is known regarding how it performs this role. The seven putative C2 domains within dysferlin suggest a high degree of modulation by intracellular calcium, likely regulating protein- and/or lipid- binding interactions central to membrane repair functions. Our project seeks to define the functionality of dysferlin C2 domains, with a specific emphasis on identifying which domains of dysferlin are essential for membrane repair, and whether calcium-binding to identified C2 domains modulates this process.</p>
					 
