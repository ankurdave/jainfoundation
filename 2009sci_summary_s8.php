                  <strong>Session VIII: Lessons from the Clinic – Clues to a Cure</strong><br>

                  <br>

The talks in this session covered aspects of the natural history and pathology of dysferlinopathy, genetic diagnostics, and lessons on organizing multicenter trials from other forms of muscular dystrophy.<br><br>

<strong>Isabel Illa</strong> (Spain) discussed recent studies on disease progression in dysferlinopathy patients. Dysferlin deficiency has been associated with a wide variety of clinical diagnoses, such as Miyoshi myopathy (MM), LGMD2B, distal anterior compartment myopathy, hyperCKemia, and others, based on the patient's initial pattern of muscle weakness at presentation. Dr. Illa's lab analyzed muscle MRIs in a group of 27 (14 MM, 12 LGMD2B, 1 hyperCK) dysferlinopathy patients and assessed the images by the degree of fat replacement on a four-level scale. Dr. Illa found that dysferlinopathy patients have similar muscle involvement in their upper and lower legs, regardless of their clinical diagnosis, but that the pattern differs from other muscular dystrophies. Dr. Illa also performed a natural history pilot study of 5 patients diagnosed with LGMD2B and 4 with MM over an 18-month period, including manual muscle testing (MMT), quantitative muscle testing (QMT), MRI imaging, and functional testing. A significant decline in muscle strength was noted over the 18-month period for pooled data from LGMD2B and MM patients, and the two clinical groups had similar evolution of symptoms.<br><br>

<strong>Kate Bushby</strong> (UK) recently analyzed UK patients (Klinge et al. 2009) and showed that out of 36 patients from the UK diagnosed with dysferlin deficiency, 25% had onset prior to the age of 13. Also, 53% reported performing well in sports prior to onset, although this athletic activity did not appear to influence the later course of the disease (in contrast to some evidence presented by Dr. Corrado Angelini at the 2008 Dysferlin conference). These data indicate that the generally accepted age of onset range (15-30) may need to be modified and that there may be a “pre-symptomatic” period in dysferlinopathy. Dr. Bushby has also found that dysferlin-deficient mice (with the SJL strain mutation backcrossed onto the BL/10 background) are not more susceptible to damage by contraction than wild type mice, but that the repair process after damage is abnormal. Following needle and saponin wounding, the dysferlin-deficient mice have a reduction in cytokine release and in neutrophil recruitment. Regeneration is also abnormal (Chiu et al. 2009), including slower recovery of contractile force, fibers at an arrested state of regeneration, and permanent damage resulting from repeated bouts of injury. These findings suggest that dysferlin may play a role in secretion of chemokines responsible for attracting neutrophils to damaged muscle.<br><br>

<strong>Nicolas Lévy</strong> (France) is developing a chip-based approach to detect mutations, including deep intronic mutations and copy number variations (duplications and deletions), in dysferlin, calpain-3, caveolin-3, and 45 other candidate genes. This approach should aid in identifying mutations in patients with confirmed dysferlin protein deficiency but whose mutations have been difficult to detect by standard diagnostic techniques. Dr. Lévy also discussed how molecular diagnosis of patients can provide valuable clues for new therapeutic approaches. He described a patient with a mild phenotype who has a naturally-occurring mini-dysferlin missing 40 exons. This fragment of dysferlin appears to be at least partially functional, based on localization (monitored by a GFP tag) and membrane wound repair assays. Dr. Lévy is refining the construct by adding additional C2 domains to allow for increased “physiological” interactions with known partner proteins, while keeping the construct small enough to fit into an AAV vector for gene therapy. Dr. Lévy also discussed a “natural” proof-of-concept for exon skipping strategies: a patient with an in-frame deletion of exon 32 of dysferlin and only a moderate phenotype. Based on this clinical finding, Dr. Lévy is testing an exon skipping strategy for patients with mutations in exon 32. <br><br> 

<strong>Eric Hoffman</strong> (USA) discussed a recent study to identify genetic polymorphisms that modify the phenotype of Duchenne muscular dystrophy (DMD) through the CINRG network in collaboration with Dr. Elena Pegoraro in Padova, Italy. Candidate SNPs were identified through studies of muscle strength in normal populations (FAMUSS cohort) (Orkunoglu-Suer et al. 2008) and in severe versus mild DMD boys by mRNA profiling. The investigators have identified a genetic polymorphism that influences the severity of the DMD phenotype, and it is possible that this polymorphism may also have an effect on dysferlinopathy. Dr. Hoffman also described a study of the effect of exercise on dysferlin-deficient A/J mice, done by Dr. Kanneboyina Nagaraju’s laboratory. There was a relatively mild yet statistically significant improvement of behavioral and physiological endpoints (including forced treadmill running and voluntary wheel running) in A/J mice with training, and the greatest improvement coincided with the known histological onset of the disease in A/J mice. 

