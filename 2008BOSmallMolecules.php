<br>
<strong>Development of Small Molecule Therapies</strong><br><br>

Moderators:  Laura Rufibach, Esther Hwang <br> 
Participants:  Lorraine Boss, Glen Humphrey, Se-Jin Lee, Michele Maxwell, Paul McNeil, Anna McNeil, Carrie Miceli, Stan Nelson, Josh and Eunha Thayer, Mohan Viswanathan, Kathryn Wagner, Josh Zimmerberg<br><br>

1.	What kinds of assays are needed to screen for molecules to treat dysferlinopathy?<br>
•	High-throughput vs. low-throughput<br>
•	in vitro vs. in vivo<br>
•	Assays that screen for multiple dysferlin functions (repair, fusion, immune, etc.)<br>
•	Parameters needed (quantitative, qualitative, reproducible)<br><br>

2.	What are the best libraries to use?<br>
•	Systems to effect (muscle regeneration, calcium, up regulation of alternative pathways)<br>
•	FDA approved vs. Novel (pros/cons)<br>
•	Source of libraries<br><br>

3.	What practical issues need to be considered when screening for small molecules?<br>
•	Academic to Industry transition (when and how)<br>
•	Intellectual property/Patent issues<br>
•	Secondary assays<br>
•	Scalability, Toxicity, Specificity<br><br><br>

<span class="style13">The discussion yielded several experiments that could help address some of the questions listed above. These experiments have been organized into the project listed below. </span><br><br>

<strong>Development of high throughput assays for screening small molecules</strong><br>
The aim of this project is to design and develop assays for screening small molecules and drugs that may compensate for dysferlin-deficiency in mammalian cells. Specific examples of such assays include:<br>
1.	Testing compounds that upregulate membrane repair in general. This assay may be performed in WT cells, and will not be dysferlin-specific.<br>
2.	Other methods of wounding the cell membrane that can then be used for dysferlin-specific or non-specific membrane repair assays.<br>
3.	Restoration of normal rate of phagocytosis in dysferlin-deficient macrophages.<br>
4.	Restoration of normal growth rate and cell to cell fusion in dysferlin-deficient skeletal muscle cell culture.<br>
