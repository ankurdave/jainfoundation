                <td valign="top"><p class="section">Research Projects - New Models of Dysferlinopathy </p>

                    <p class="home"><strong><font color="#0175C2"><span class="style1">Identification of a Canine Model of Dysferlin Deficient Muscular Dystrophy</span><br>

                    </font></strong>Diane Shelton, University of California, San Diego<br>

                        <br>

                         <strong>Project Objective</strong><br>

<br>
The goal of our project is the identification of a spontaneously occurring canine or feline model of dysferlin deficient muscular dystrophy that can be used in pre-clinical therapeutic trials.  The  value of the canine model of X-linked muscular dystrophy in therapeutic trials for Duchenne muscular dystrophy is well established.  Our laboratory, the Comparative Neuromuscular  Laboratory at the University of California, San Diego (http://vetneuromuscular.ucsd.edu), is an international reference center  for the diagnosis and study of spontaneously occurring neuromuscular  diseases in companion animals. Thus, we are in a unique position to identify dysferlin deficient dogs or cats. Muscle biopsies submitted for diagnostic purposes will be screened by immunohistochemistry and immunoblotting for the presence or absence of dysferlin protein.  Once a dysferlin deficient animal is identified, we will aid researchers in the molecular identification of specific mutations and the establishment of a colony of affected dogs for pre-clinical treatment trials.  Identification of successful treatments is of utmost importance not only to human patients, but also to our veterinary patients.
<br>

