 <p class="home"><strong><font color="#0175C2"><span class="style1">Generating Immortal Cell Lines from Dysferlin Patients</span><br>

                    </font></strong>Gillian Butler-Browne and Vincent Mouly, Institut de Myologie, Paris, France<br>

                        <br>

                         <strong>Project Objective</strong><br>

<br>
High quality immortalized dysferlin deficient cells lines are an essential model for high throughput drug screening and are also an important tool for discovery research. However establising such a line is difficult and time consuming. We have recently established an approach to immortalize human myoblasts from control subjects and patients (see Zhu et al 2007, in Aging Cell). This approach relies on the control of telomere shortening through expression of exogeneous telomerase (hTERT), combined with the neutralization of the pathway responsible for premature proliferative arrest, by overexpression of cdk-4. This technology is being used to establish immortalized muscle cell lines from patients with a variety of  mutations in dysferlin.  Since muscle biopsies are not always easily available from patients, we have also developed an approach to immortalize fibroblasts isolated from skin biopsies that can then be converted to myogenesis by conditional overexpression of MyoD. MyoD expression can be induced both in vitro or in vivo after implantation of the cells into regenerating muscles of immunodeficient mice. Ultimately we hope to create a panel of immortalized muscle cell lines that will be available to the dysferlin research community.  <br>

