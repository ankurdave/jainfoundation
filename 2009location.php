<p><strong>Parking</strong></p>
<p>For local attendees, we have negotiated a $15/day self-park rate at the Hilton Boston Back Bay.  To receive this rate, please use the self-park and bring your parking ticket to the conference.  We will stamp the ticket so that at exit, the special rate will be honored.  There are no restricted entry/exit times from the parking lot.  However, you are not permitted to park in the lot overnight.</p>

<p>&nbsp;</p>

<p><strong>Hotel Charges</strong></p>

<p>The Jain Foundation has reserved a guaranteed block of rooms for our conference for the nights of June 1, 2, 3, and 4, 2009.  We only have a limited number of rooms blocked for the night of Monday, June 1.  <u>Therefore, unless you are coming from outside the US, from the US West Coast, or attending the Natural History Satellite Meeting the morning of Tuesday, June 2, please do not book a room for the night of June 1.</u>  If this is going to be a problem please contact Dr. Laura Rufibach (Lrufibach@jain-foundation.org) to discuss the situation <u><strong>PRIOR TO BOOKING YOUR ROOM.</strong></u><br><br>

In addition, if you need nights outside the nights of June 1, 2, 3, and 4, contact Dr. Laura Rufibach (Lrufibach@jain-foundation.org) <u><strong>PRIOR TO BOOKING YOUR ROOM</strong></u> to see if she is able to provide additional nights at the conference rate of $229/night + tax.  <br><br>

The conference room rate is $229 USD/night + 12.45% tax for single or double occupancy.  A third or fourth person in the room is $20 per night extra for each additional person. </p>




<p>PLEASE NOTE:  ATTENDEES WHO WILL BE SHARING A ROOM WITH A ROOMATE ASSIGNED BY THE JAIN FOUNDATION SHOULD NOT BOOK THEIR HOTEL RESERVATIONS DIRECTLY.  INSTEAD, PLEASE CONTACT DR. LAURA RUFIBACH (LRUFIBACH@JAIN-FOUNDATION.ORG) AT THE JAIN FOUNDATION WITH YOUR ARRIVAL AND DEPATURE DATES AND SHE WILL MAKE YOUR RESERVATIONS AND SEND YOU A CONFIRMATION.<br><br>

Booking a reservation online is simple. To begin the process, simply go to the following webpage (<a href="http://www.hilton.com/en/hi/groups/personalized/BOSBHHH-JAI-20090601/index.jhtml" target="blank">http://www.hilton.com/en/hi/groups/personalized/BOSBHHH-JAI-20090601/index.jhtml</a>) and click on "Book a Room".  If you prefer to speak with someone directly you can make your reservation by calling the Hilton Back Bay directly at 1-617-236-1100 or by calling the Hilton reservations toll free number at 1-800-445-8667.  Mention the group code “JAI” or the Jain Foundation to receive the conference discount.  <br><br>

If you get a message saying that the conference rate is not available <u><strong>DO NOT</strong></u> book your room.  Instead contact Dr. Laura Rufibach at the Jain Foundation so that she can help you get the conference rate.
</p>

