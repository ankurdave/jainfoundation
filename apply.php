                <td valign="top"><p class="section">Apply for Funding - Submission Guidelines </p>

                    <p class="home"> If you are a researcher interested in applying

                      for funding from the Jain Foundation, please read the guidelines

                      on this page.<br>

                      <br>

                      The explicit goal of the Jain Foundation is to expedite
                      development of a treatment or cure for LGMD2B/Miyoshi.
                      Your proposed project MUST have application to this disease, or it will not be considered. For examples of the types of projects we fund, please view our <a href="projects.php" class="style3">currently funded projects</a> or last year's <a href="RFPs.php" class="style3">Requests For Proposals</a> (from fall 2008).<br>
                      <br>
					  
                      <strong> Proposals are reviewed on a rolling basis</strong>.   <br><br>
                      <em><strong>All grants are reviewed in-house by the Jain Foundation employees</strong></em> (<a href="contact.php">click here</a> for brief biographies). Typically we respond to grant submissions within 4-6 weeks.  Researchers are welcome to contact the Jain Foundation with a &quot;pre-proposal&quot; to gauge our level of interest at any time. <br>
                      <br>
                      For submission, follow the instructions below and send a detailed
                      
                      description of your proposed project, and any supporting
                      
                      work, to Dr. Plavi Mittal at <img src="images/pmemail.jpg" align="absmiddle">.
                      <br>
                      
                      <em><br>
                        </em><span class="style2">&lt;--- </span><strong>Please also take advantage of the resources on the left, which may be useful in designing your proposed experiments. </strong><br>
                      
                        <br>
                        <br>
                      
                        <strong>Instructions for submitting a proposal:</strong><br>
                      
                        <br>
                      
                        <strong>1.</strong> The project must have direct relevance to dysferlin deficiencies -- either
                      
                      to extend our understanding of the disease or a unique approach to a treatment
                      
                      for the disease.<br>
                          <br>
                      
                          <strong>2.</strong> The project should not have been previously performed elsewhere and sufficient
                      
                      due diligence should have been done to find out if the same project is currently
                      
                      being done in another lab. We will confirm this independently.<br>
                      
                          <br>
                      
                          <strong>3.</strong> Please include a clear description of the scientific background and a demonstration
                      
                      of the techniques needed to make the proposed project a success. Past work in
                      
                      your lab and/or published papers directly related to the proposed project are
                      
                      helpful. Evidence of access to techniques and collaboration with others is equally
                      
                      important.<br>
                      
                          <br>
                      
                          <strong>4.</strong> Preliminary data to assess feasibility of the proposed project are required
                      
                      and should be completed before funding can be initiated.<br>
                      
                          <br>
						  <strong>5.</strong> Please fill out all information on the <a href="http://www.jain-foundation.org/downloads/CoverPage_Grant.doc" target="_blank" class="style4">cover page</a> provided. <br><br>
 <strong>6.</strong> Please follow the preferred proposal format as outlined in the table of contents of the <a href="http://www.jain-foundation.org/downloads/CoverPage_Grant.doc" target="_blank" class="style4">cover page</a>. <br>
 <br>
                      
                      
                          <strong>7.</strong> Please include a detailed and aggressive timeline of experiments leading to the results of
                      
                      the project. Please refer to the  <a href="http://www.jain-foundation.org/downloads/GrantTimeline_Template.doc" target="_blank" class="style4">sample timeline chart</a> as an example. Proposals without a detailed timeline will not be reviewed. <br>
                      
                          <br>
                      
                          <strong>8.</strong> Please include a detailed budget (including personnel, material, and all other costs) and justification for each expense for the proposed project on the <a href="http://www.jain-foundation.org/downloads/GrantBudget_Template.doc" target="_blank" class="style4">budget template</a> provided. Proposals without a detailed budget and justification will not be reviewed. <br>
                      
                          <br>
                      
                          <strong>9.</strong> The average project will require a proposal of about 6-10 pages  (excluding references) to cover the level of detail that we expect, though individual proposals may vary in length. The experiments should be described in enough detail for  us to understand why each step in the experimental plan will require the time  that is alotted for that step in the timeline.<br>
                      
                          <br>
                      
                          <br>
                      Please view the terms of the <a href="http://www.jain-foundation.org/downloads/Funding Agreement - US.pdf" target="_blank" class="style4">Jain Foundation Funding Agreement</a>.<br>
                      This agreement is required to initiate any funding with us, and is designed with
                      
                      a single goal in mind: to expedite development of a treatment for LGMD2B/Miyoshi. <br>
