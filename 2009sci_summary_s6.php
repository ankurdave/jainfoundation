                  <strong>Session VI: Stem Cell Therapy</strong><br>

                  <br>


This session discussed the characterization and use of muscle- and blood-derived stem cells as a therapeutic tool for the treatment of dysferlinopathy. <br><br>
 
<strong>Amy Wagers </strong>(USA) first discussed her attempt to identify systemic blood-circulating factors that ameliorate dysferlinopathy by surgically joining the circulatory systems (parabiosis) of dysferlin-deficient mice to wild type partners. Dr. Wagers also discussed SMP cells, a subpopulation of satellite cells that express satellite cell and early myogenic markers, but lack differentiation markers: CD45-, Sca-1-, Mac-1-, CXCR4+, ß1-integrin+ (Sherwood et al. 2004; Cerletti et al. 2008). These cells exhibit robust, lineage-selective myogenic differentiation, long-term engraftment capability, and self-renewal capacity (Cerletti et al. 2008). Similar to her previous observations in dystrophin-deficient mdx mice (Cerletti et al. 2008), Dr. Wagers presented data showing significant changes in the SMP number in dysferlin-deficient mice (carrying the SJL mutation on a BL10 background) that correlate with disease severity. She believes that SMPs are relevant targets for cell-based therapy in muscle based on her finding that SMPs intramuscularly injected into dystrophic mice can achieve high levels of muscle chimerism and enhance muscle function. Dr. Wagers ended her talk by discussing her recent efforts to find a method of expanding SMPs using Notch activation and iPS technology. <br><br>

<strong>Mayana Zatz </strong>(Brazil) described her approach to stem cell therapy for dysferlinopathy using mesenchymal stem cells (Vieira et al. 2008(1); Vieira et al. 2008(2)). Dr. Zatz has performed intravenous injections of human adipose stromal cells (hASCs) into three groups of 7 dysferlin-deficient SJL mice weekly and then once a month for 6 months (Vieira et al. 2008(2)). The three groups received either no injections (control), undifferentiated hASCs, or hASCs pre-committed to the myogenic phenotype. She found unexpectedly that undifferentiated hASCs were not rejected after systemic injection into the SJL mouse, even without immunosuppression. Furthermore, the injected undifferentiated hASCs cells (but not the pre-committed cells) were able to fuse with the host muscle, express a significant amount of human muscle proteins, and improve motor ability. <br><br>

<strong>Morayma Reyes</strong> (USA) has isolated wild type skeletal muscle perivascular cells (SMPCs) by FACS (CD45-, Sca-1+, CD31-, CD34+) and found that they express dysferlin perinuclearly when undifferniated and at the plasma membrane upon differentiation into myogenic cells. SMPCs from dysferlin-deficient mice appear to have impaired cell-cell fusion. Dr. Reyes transduced the dysferlin-deficient SMPCs with lentiviral vectors expressing full-length dysferlin containing one of the two exon 1 variants and found that both variants independently correct the fusion defect, suggesting that they have redundant functions. Dr. Reyes also showed that autologous and allogenic transplants of SMPCs result in high levels of engraftment over 2 weeks. She performed allogenic transplant experiments into dysferlin-deficient mice preceded by a bone marrow (BM) transplant with cells from the same donor to induce immune tolerance, and found that while BM irradiation initially resulted in the death of satellite cells and a worsening of the dystrophic phenotype, injected mice showed increased muscle mass in the injected leg compared to the contralateral leg.<br><br>

<strong>Jacques Tremblay </strong>(Canada) discussed his clinical trials using myoblast transplantation as a treatment for muscular dystrophies. Myoblasts are muscle precursor cells responsible for the regeneration of muscle fibers in vivo and can be obtained by proliferation of satellite cells ex vivo. Dr. Tremblay showed that human allogenically-derived myoblasts transplanted into mice maintain their myogenic capacity and do not cause tumor formation. He then performed a Phase 1A clinical trial with 9 Duchene muscular dystrophy (DMD) patients who received 25-100 injections of allogenically-derived myoblasts into a 1 cm square section of one leg. One month post transplantation, 8 out of 9 patients showed some level of dystrophin positive fibers (Skuk et al. 2004; Skuk et al. 2006). Dr. Tremblay also conducted a trial in the thenar muscle of the hand in one DMD patient and found increased strength 1-14 months post transplantation and dystrophin expression for up to 18 months (Skuk et al. 2007). Finally, in a preliminary study he has demonstrated that transplantation of human myoblasts into mouse muscle also results in human dysferlin expression. <br><br>

<strong>Michele Calos</strong> (USA) described her efforts to combine autologous stem cell therapy with phiC31 integrase-based gene therapy. PhiC31 integrase is a site-specific recombinase that can catalyze the integration of a transgene into a small number of sequence-specific integration sites in the host genome (Calos 2006). This system has no size limit for the transgene and achieves robust lifetime expression even through differentiation and cell division. Dr. Calos is attempting to implement ex vivo integrase-mediated integration of dysferlin into two different stem cell types: mesoangioblasts and mesenchymal stem cells (MSC). Mesoangioblasts are multipotent stem cells derived from blood vessels (Minasi et al. 2002), while MSC cells are myogenic stem cells that can be isolated from a number of tissues, including white adipose tissue (Crisan et al. 2008). Preliminary injections of phiC31 integrase-modified mesoangioblasts into both mdx and dysferlin-deficient mice show good engraftment into both the injected and contralateral TA muscles.<br><br>

<strong>Yvan Torrente</strong> (Italy) discussed autologous transplantation of CD133+ stem cells that have been modified to skip mutation-containing exons of dysferlin. CD133+ cells are adult muscle- or blood-derived progenitor cells that express muscle cell markers and display clonogenic, self-renewal, and multi-potency properties (Torrente et al. 2004; Benchaouir et al. 2007). CD133+ stem cells isolated from both muscle and blood express dysferlin. Using human myoblasts from an LGMD2B patient carrying mutations in exons 27 and 22, Dr. Torrente tested anti-sense oligonucleotides (AONs) designed to skip the mutation-containing exons. While he has observed the desired skipped product, the results are not always reproducible and the efficiency is low. He plans to use bifunctional AONs cloned into a lentiviral backbone to improve efficiency. Dr. Torrente is also studying the functionality of the shortened dysferlin proteins that would result from skipping exons 22-24, 24-29, and 22-29.

