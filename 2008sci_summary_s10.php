                  <strong>Session X: Muscle Stem Cells </strong><br>

                  <br>

This session highlighted the characterization and use of muscle- and blood-derived stem cells as a therapeutic tool for the treatment of dysferlinopathies.  While the use of stem cells as a therapy for dysferlinopathies is promising, there are still a number of challenges that need to be addressed.  These include the best stem cell type to use (could vary depending on use), the best way to compare/contrast the different stem cell types, the best delivery method(s), and the best donor type (allogenic vs. autologous).  The speakers in this session addressed a few of these issues.  Each presentation highlighted a different stem cell type, focusing on its isolation, characterization, dysferlin expression/function, and potential therapeutic use. <br><br>
  
<strong>Dr. Amy Wagers</strong> (Harvard Stem Cell Institute) discussed the characteristics of skeletal muscle precursor cells (SMPs), evidence that they are muscle stem cells, and their potential use as a cell-based therapy for dystrophic muscle.  SMPs were originally isolated from myofiber-associated mononuclear cells based on their high myogenic capacity in culture and can be isolated by FACS based on their expression of cell surface markers (CD45-, Sca-1-, Mac-1-, CXCR4+, ß1-integrin+).  SMP cells are a subpopulation of satellite cells that express satellite cell and early myogenic markers, but lack differentiation markers.  These cells exhibit robust, lineage-selective myogenic differentiation, long-term engraftment capability, and self-renewal capacity.  Dr. Wagers believes that SMPs are relevant targets for cell-based therapy in muscle based on her findings that SMPs can achieve a high level of muscle chimerism and enhance muscle function in intramuscularly-injected mdx mice.  Similar to what she previously observed in aged and dystrophic mdx mice, dysferlin-deficient mice (from Reginald Bittner, with the SJL mutation crossed onto a BL10 background) have fewer SMP cells in their muscle, but show no difference in SMP clonal efficiency, average proliferation, or differentiation capacity.  <br><br>

Dr. Wagers also believes that soluble factors and/or cellular factors (such as SMP cells) could modulate muscle function, repair, or regeneration in dystrophic muscle, and she is trying to identify these factors using parabiosis experiments in which the blood systems of two different mice are surgically combined.  Using this technique, she has previously shown that factors present in the blood of young mice can improve the regenerative capacity of aged muscle.  She is currently testing whether she can identify similar systemic factors that can enhance regeneration and ameliorate dystrophy in dysferlin-deficient mice by parabiosing them with wild type animals.
<br><br>

<strong>Dr. Morayma Reyes</strong> (University of Washington) presented the isolation and characterization of various muscle stem cell types by FACS based on the expression of cell surface markers (i.e., endothelial, perivasular, and satellite cells) and the role that dysferlin may play in these cell populations.  In the satellite cell population (CD45-, Sca-1-, CD31-, a7-integrin+), she identified two different sub-populations, one that is CD34- and one that is CD34+.  CD34+ satellite cells express higher levels of myogenic commitment markers and may represent the more differentiated satellite cell population compared to the CD34- satellite cell population, which appears to be less committed and more quiescent.  This is supported by Dr. Reyes’ cardiotoxin injury experiments, which show that CD34-/GFP+ transplanted cells can give rise to CD34+/GFP+ cells and that after cardiotoxin injury in a wild type mouse, the number of CD34- cells declines, while the number of CD34+ cells increases.  When she performed similar experiments in the dysferlin-deficient A/J mouse, she showed that the A/J mouse has impaired and delayed regenerative capacity.  <br><br>

Dysferlin appears to be expressed in a number of adult muscle-derived stem cells, such as perivascular MAPC cells, endothelial cells, and both satellite cell subtypes (CD34- and CD34+), and the lack of dysferlin in these cells appears to affect cell fusion.  In order to determine whether the two dysferlin exon 1 variants can rescue the cell fusion defect, Dr. Reyes transduced dysferlin-deficient cells (satellite and MAPC cells) with lentiviral vectors expressing the two different variants.  Preliminary results indicate that both variants are independently able to correct the fusion defect, suggesting that they have redundant functions.  Additional studies with the two variants and stem cell transplants of various muscle stem cells into the A/J mouse model are planned.  
 <br><br>

<strong>Dr. Yvan Torrente</strong> (University of Milan) discussed autologous transplantation of exon-skipped CD133+ stem cells for the treatment of dysferlinopathies.  CD133+ cells are adult progenitor cells derived either from muscle or blood.  They display clonogenic, self-renewal, and multi-potency properties and even the blood-derived cells express muscle cell markers.  CD133+ stem cells isolated from both muscle and blood show dysferlin mRNA and protein expression and are therefore ideal templates for an exon-skipping strategy.  Using human myoblasts from an LGMD2B patient carrying a deletion in exon 22, Dr. Torrente tested, both individually and in combination, a number of anti-sense oligonucleotides (AONs) that targeted the donor and acceptor splice sites, as well as the enhancer splicing elements in exons 22 and 23.  In order to get an in-frame product, exons 22-23 or 22-23-24 need to be skipped.  Of the five oligos tested, only one AON produced a robust, efficiently skipped in-frame product deleted for exons 22, 23, and 24.  Dr. Torrente went on to show that these in vitro skipped CD133+ positive cells derived from blood tissue can express dysferlin and muscle markers when co-cultured with A/J murine myotubes.  In addition, the exon 22-24 skipped dysferlin was able to correct the membrane repair defect of dysferlin-deficient cells.  These results are proof-of-principle for the combined approach of stem cell therapy and exon-skipping and indicate that some regions of dysferlin may be removed without affecting function.  On the other hand, the results of the skipping experiments highlight the unpredictable nature of exon-skipping and the need to increase the specificity and efficiency of this process before planning its use for therapy.



