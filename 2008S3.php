                  <strong>A Functional Link Between Exocytosis and Endocytosis in Plasma Membrane Repair</strong><strong>&nbsp; &nbsp;</strong><br>

Christina Tam*, Vincent Idone* and Norma W. Andrews  * these authors contributed equally to this work<br><br>

LGMD2B/Miyoshi has been linked to defects in plasma membrane repair, a process that involves calcium-dependent exocytosis of intracellular vesicles.  Work from our laboratory showed that lysosomes are the main organelles capable of fusing with the plasma membrane in response to calcium influx.  We found that lysosomes contain in their membranes a calcium sensor, synaptotagmin VII (Syt VII), which regulates calcium-dependent exocytosis.  Syt VII-deficient mice develop autoimmune inflammatory myopathy, with foci of myofiber degeneration in their skeletal muscle.  This is intriguing, considering that both Syt VII and dysferlin (the protein carrying the mutations responsible for LGMD2B/Myoshi) contain calcium-binding C2 domains, and that cells from animals deficient in either Syt VII or dysferlin have plasma membrane repair defects.  These observations are consistent with an important role for calcium sensor molecules containing C2 domains in the regulation of exocytosis and plasma membrane repair.  
	Despite such advances, the mechanism by which exocytosis promotes membrane repair has remained obscure.  Recently we found that calcium-dependent resealing after permeabilization with the bacterial toxin streptolysin O (SLO) requires endocytosis via a novel pathway that removes the toxin pores from the plasma membrane.  Importantly, we found that endocytosis is similarly required to repair lesions formed in mechanically wounded cells.  Inhibition of endocytosis by sterol depletion inhibits repair, while enhancement of endocytosis through disruption of the actin cytoskeleton facilitates resealing.  When lysosomal exocytosis is inhibited, both endocytosis and plasma membrane repair are impaired, demonstrating that wound-induced exocytosis and endocytosis are functionally linked.  These results indicate that calcium influx through membrane wounds triggers lysosomal exocytosis, which is then followed by endocytosis – the event that promotes resealing, by removing lesions from the membrane.  These findings provide an important new insight into the mechanism of plasma membrane resealing, opening new avenues of research in the search for therapeutic interventions in LGMD2B/Miyoshi. </p>

