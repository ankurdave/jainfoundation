				<p><span class="style10">Session III: Membrane Fusion and Repair </span><br>


				  <br><br>

                  <strong><span class="style17">Paul L. McNeil, PhD</span></strong><br>

                  <em>Medical College of Georgia
pmcneil@mcg.edu
</em><br><br>

                  <img src="images/conference2008/McNeil.jpg" width="135"><br><br>

                  <strong>Annexin in Skeletal Muscle Membrane Repair</strong><strong>&nbsp; &nbsp;</strong><br>

<br>

We have previously shown, using fibroblast model systems, that membrane repair requires participation of the Ca2+-activated, phospholipid binding protein, annexin A1.  We are now investigating its role in skeletal muscle myocytes.  I will present imaging data consistent with a role for annexin A1 in repair of myocyte disruptions.  Annexin A1-EGFP, and its ligand, S-100C-EYFP, rapidly concentrate, during the time course of active repair, at the disruption site in C2C12 myotubes.  Annexin A2, previously implicated in myocyte membrane repair as a binding partner of dysferlin, also rapidly concentrates at disruption sites.  A mutant annexin-EGFP, defective in Ca2+ binding, does not concentrate at the disruption site, but instead gradually leaks out of the wounded myotube.  I will also present quantitative tests, including the following data supportive of a role in repair:

1)	Expression of mutant annexin-EGFP, defective in Ca2+ binding, inhibits membrane repair. 
2)	Annexin A1 siRNA treatment also inhibits C2C12 myoblast membrane repair. 
3)	Cultured myotubes from the annexin A1 KO mouse are repair defective. 
4)	Myotubes in intact muscle of the annexin KO mouse are defective in repair.

I will, in an unrelated segment of the talk, outline a new method for wounding cultured cells in a 96-well format, and for quantitating repair in a plate reader.  I will present preliminary evidence indicating the possible usefulness of this method for high-throughput assays of repair.


