                  <strong>Using Two-Photon Microscopy to Investigate the Role of Dysferlin in Wound Repair</strong><strong>&nbsp; &nbsp;</strong><br>



Mutations in dysferlin are known to cause Limb Girdle Muscular Dystrophy (LGMD2B) and Miyoshi Myopathy1-5.  Dysferlin is involved in membrane repair mechanisms6, but the function of dysferlin itself is not known.  Membrane repair mechanisms use the calcium triggered fusion of internal membranes to form a patch membrane to seal the lesion7-9. Proteins structurally related to dysferlin are thought to be involved in membrane fusion mechanisms10-13 leading to the hypothesis that dysferlin facilitates the fusion of biological membranes. In Drosophila dysferlin is involved in cell division14, but the significance of this observation is not apparent.  Here we provide preliminary data using dysferlin morpholinos that suggests that dysferlin is required for cell division in sea urchin as well.  Cell division in sea urchin is thought to require exocytosis at the cleavage furrow15. Recently we have shown that 2 methods of inhibiting endocytosis in sea urchin, treatment with phenylarsine oxide and over-expression of Src kinase, also inhibit cell division suggesting that cell division might require endocytosis, a mechanism that often compensates for exocytosis16. Preliminary data using the florescent membrane marker FM1-43 suggests that dysferlin might also be involved in endocytosis.  Such a broad spectrum of biological functions for dysferlin (membrane repair, fusion, cell division, exocytosis, and endocytosis) might all be manifestations of a single mechanistic function for dysferlin, such as the mediation of membrane fusion. The proteins that mediate calcium triggered membrane fusion in sea urchins have not been identified, but do not appear to be SNAPS or SNARE proteins17-21.  We are specifically testing the hypothesis that dysferlin is a fusion protein in sea urchins.</p>
