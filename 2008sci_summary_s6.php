                  <strong>Session VI: Gene Therapy</strong><br>

                  <br>

This session focused on the different therapeutic strategies that are being explored to restore dysferlin expression in patients with LGMD2B/Miyoshi.  Gene delivery strategies include intramuscular injection or systemic delivery via the blood, using different DNA delivery vehicles ranging from viral delivery to direct injection, or reimplantation of progenitor cells taken from the patient and modified by <em>ex vivo</em> gene therapy.  This session included discussion of potential challenges associated with genetic therapies that specifically relate to the dysferlinopathies.  These challenges include how to achieve sufficient and sustained dysferlin expression, how to express dysferlin in the right cell types (e.g. immune cells and muscle cells), how to circumvent the issue of the large size of the dysferlin gene, and whether an immune response to the delivery agent (e.g. virus) and/or to the newly-expressed dysferlin protein might be an issue.  <br><br>

<strong>Dr. Isabelle Richard</strong> (Généthon) discussed the use of adeno-associated viruses (AAV) for delivery of the dysferlin gene.  The advantages of AAV vectors include their lack of pathogenicity, lower immunogenicity than other vectors, and efficiency of muscle cell transduction.  Some issues to be resolved include the administration route, the possibility of an immune response, and the 4.5-kb upper limit to the capacity of the virus (since the dysferlin cDNA is almost 7-kb without any regulatory elements).  To address the latter problem, Dr. Richard has taken advantage of a concatamerization strategy that uses two different AAV vectors to deliver the dysferlin gene in two pieces, which contain specific sequences that allow them to reassemble.  She first demonstrated effective concatemerization of 2 separate AAV vectors that produce a full length dysferlin protein in cell culture.  She then administered the AAV particles either intramuscularly or intravenously into dysferlin-deficient mice.  Injection into the tibialis anterior resulted in dysferlin expression, detectable by western blot, up to 12 months later.  Functional restoration of dysferlin in the injected mice was tested by a membrane repair assay: FM 1-43 entry into wounded fibers was found to be decreased for fibers from AAV-injected dysferlin-deficient mice compared to fibers from uninjected dysferlin-deficient mice, demonstrating improved repair.  In an assay for locomotor activity conducted one month after systemic injection of the AAV vectors, the mice expressing the concatamerized dysferlin gene showed significant improvements in their level of activity, speed, and distance travelled when compared to untreated dysferlin-deficient mice. <br><br>

Dr. Richard is also backcrossing the dysferlin-deficient A/J mice onto a C57BL/6 background in order to have a dysferlin-deficient mouse model with a strain-matched control. This is very important as the A/J mice are known to have mutations in several other genes.  Initial characterization of the F4 generation showed that it has a similar index of centronucleation to the parental A/J line.  Dr. Richard very generously offered to distribute the mice freely to anybody interested in using them.
<br>
<br>


<strong>Dr. Michele Calos</strong> (Stanford University School of Medicine) discussed two different strategies to restore dysferlin expression in muscles.  In both strategies, her laboratory takes advantage of phiC31 integrase, a site-specific recombinase, to incorporate the dysferlin gene stably into the genome of the recipient cells.  Initial experiments to test gene delivery to mice by hydro-dynamic injection into the saphenous vein yielded limited expression of GFP in the muscle fibers.  When naked DNA was directly injected into the hindlimb muscles and followed by electroporation, the efficiency of delivery was significantly higher as measured by luciferace imaging in the mice as well as immuno-fluorescence staining.  <br><br>

The second strategy being used by Dr. Calos is cell therapy transplantation with mesoangioblast cells, which are multipotent, self-renewing stem cells that can differentiate into skeletal muscle, smooth muscle, osteoblasts, adipocytes and cardiomyocytes in vitro and can migrate to sites of muscle degeneration within the body.  Dr. Calos has shown that these mesoangioblasts can be successfully transfected and has shown stable DNA integration mediated by the phiC31 integrase.  She further demonstrated the capacity of the modified mesoangioblasts to undergo differentiation into myotubes in vitro.  Mdx mice injected with phiC31 integrase-modified mesoangioblasts showed expression of luciferase in the hindlimbs of the mice, and, more importantly, correctly-localized dystrophin expression in muscles.  Future studies will focus on delivery of dysferlin to dysferlin-null mesoangioblasts, followed by transplantation into dysferlin-null mice.
<br>
<br>

<strong>Dr. Nicolas Lévy</strong> (Hopital d’enfants de la Timone) presented his work on the identification and characterization of dysferlin mutations.  Primary dysferlinopathies have a large mutational spectrum.  Analysis of the dysferlin gene has revealed no mutational hot spots, with most disease-causing mutations being missense or frame-shift mutations, and about 20-25% being nonsense mutations.  Dr. Lévy described a patient with atypical and moderate primary dysferlinopathy whose cells still produce a mini-dysferlin protein.  In this patient, there is a large multi-exonic genomic dysferlin deletion (exons 2 to 40).  The truncated protein is 73 KDa in size, and detectable on the plasma membrane in blood monocytes (by IF, FACS and WB).  This truncated dysferlin is at least partially functional in the patient as well as in a membrane repair assay.  Since the size of the truncated dysferlin’s cDNA is within the packaging size limit of AAV-vectors, it might provide a therapeutic alternative to full length dysferlin for gene therapy.  Importantly, the partial functionality of this mini-dysferlin molecule demonstrates the partial modularity of wild type dysferlin, which is an essential pre-requisite for exon-skipping strategies in dysferlinopathies. <br><br>

Dr. Lévy also discussed the DYSFonCHIP project, in which he is using comparative genomic hybridization (CGH) and sequence capture arrays to identify mutations in dysferlin and related partners.  CGH is a powerful tool that he showed can be used to detect several types of dysferlin mutations, including the previously-described heterozygous large deletion (exons 2-40), as well as a short homozygous deletion.  Sequence capture arrays detect point mutations and small insertion/deletions.  Currently dysferlin mutational analysis is an expensive and time consuming task that often fails to find both pathogenic mutations.   The use of the CGH and sequence capture arrays could eliminate these issues by making mutational analysis cheap, accurate, and quick.  The use of these arrays has the potential to revolutionize the way mutational analysis is performed for dysferlin, as well as many other genes.

