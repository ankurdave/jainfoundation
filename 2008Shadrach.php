                  <strong>Impact of Blood-Circulating Factors on the Progression of Dysferlin-Linked Muscular Dystrophy  </strong><strong>&nbsp; &nbsp;</strong><br>

Jennifer Shadrach and Amy J. Wagers

 <br><br>
Mutations that disrupt the function of the dysferlin gene cause two related muscle degenerative diseases – Limb Girdle Muscular Dystrophy type 2B (LGMD2B) and Miyoshi myopathy.  Both LGMD2B and Miyoshi cause slowly progressive muscle weakness and wasting affecting predominantly the shoulder and hip muscles.  Interestingly, both LGMD2B and Miyoshi mutations can be found throughout the coding sequence of the dysferlin gene, with no apparent mutational “hotspots”.  In addition, there is no obvious correlation between the specific type of dysferlin mutation present and the particular distribution of affected muscles or the severity of the disease.  These data suggest that additional genetic modifiers affect the presentation and outcome of dysferlin deficiencies.  While the identities of these predicted modifiers are currently unknown, they likely represent novel therapeutic targets for the treatment of LGMD2B/Miyoshi.
	While accelerated in many congenital myopathies, a progresive decline in the regenerative capacity of skeletal muscle is also a hallmark signs of normal organismal aging.  In previous studies, we found that in vivo exposure of “old” muscle to “young” blood circulating factors enables rejuvenation of aged muscle function.  While we have yet to elucidate the identity of these factors, we have hypothesized that, just as they restore function in aging muscle, these factors may provide a salutary effect in the context of congenital myopathies.  We are testing this hypothesis in two mouse models of LGMD2B/Miyoshi, diseases which, as mentioned above, generally exhibit a relatively delayed onset, increasing severity with age, and influence by unknown genetic modifiers.
	Using parabiosis, a surgical technique that allows the establishment of a common circulatory system in conjoined pairs of animals, we are testing whether exposure of C57BL/10.SJL-Dysf or A/J dysferlin-deficient (dysfdef)mice to wild-type (WT) blood circulatory factors may slow or reverse the progression of muscle disease.  These two models exhibit differing severities of muscle disease, different dysferlin mutations, and divergent genetic backgrounds, allowing us to assess the relative impact of circulating factors in diverse genetic contexts and in both slowly progressing and rapidly progressing muscle dystrophy.  In preliminary results, we have demonstrated effective cross-circulation in isotypic and heterotypic WT::dysfdef pairs.  We also have observed an encouraging increase in dysferlin+ fibers in abdominal muscles of A/J mice joined to WT (A/HeJ) mice for only 4 weeks.  Dysferlin staining of the control A/J isotypic mouse pair (n=1) did not show the same pattern or intensity of staining.  We have also assayed muscle precursor cell frequency and function in WT and dysfdef mice using direct flow cytometric quantification and in vitro satellite cell proliferation assays.  These studies reveal a striking age-associated reduction in muscle precursor cell frequency in the affected (abdominal) muscle of dysfdef, as opposed to WT, mice.  Future studies will evaluate whether parabiosis to a WT partner may have a protective effect on maintenance of the muscle precursor pool, and will further investigate the potential of in vivo blood circulating factors to help ameloriate or reverse the progression of dysferlin-deficient myopathies.
