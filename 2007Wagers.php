                  <strong>Local and systemic factors regulating muscle  stem cell function</strong></span><br>

                  <br>

                  Skeletal muscle satellite cells, which reside  beneath the basal lamina of mature muscle fibers, function as myogenic  precursors and are required for normal muscle growth and repair. Satellite  cells share a common anatomical localization, yet they exhibit substantial  phenotypic and functional heterogeneity.&nbsp; We have used cell surface marker  profiling and fluorescence activated cell sorting to identify and separate  distinct satellite cell populations.&nbsp; Through this approach, we define a  lineage of prospectively isolatable myogenic cells and demonstrate that a  unique subset of muscle satellite cells exhibits hallmark properties of  skeletal muscle stem cells.&nbsp; These self-renewing skeletal muscle  precursors (SMPs) mediate robust muscle regeneration in vivo.&nbsp; When  transplanted into the muscle of <em>mdx</em> mice, which model several features  of human Duchenne&rsquo;s muscular dystrophy, GFP-labeled SMPs contribute to up to  90% of myofibers, restoring wild-type levels of dystrophin expression in these  fibers and significantly improving muscle

contraction  force.&nbsp; Moreover, transplanted SMPs also enter and renew the satellite  cell compartment, seeding the endogenous pool of myogenic precursors in the  recipient&rsquo;s muscle with wild-type cells. Interestingly, ongoing studies  indicate that SMP frequency is dynamically regulated during the early weeks of  life, and may be modulated by disease, injury, and aging.&nbsp; Impaired  establishment, maintenance and myogenic activity of the SMP population appear  to contribute in many instances to muscle dysfunction and can result from both  cell intrinsic deregulation of SMP function and from cell extrinsic local and  systemic influences that alter the SMP niche.&nbsp; Based on these findings, we  have hypothesized that a lack of positive factors, or a buildup of negative  factors, in the blood of diseased animals may inhibit efficient muscle repair  and/or function in animals suffering from chronic muscle degenerative disease.  Our future experiments will test this hypothesis by assaying whether provision  of a &ldquo;normal&rdquo; systemic milieu may promote or prolong muscle function in  dysferlin-deficient animals.&nbsp; These studies will identify new regulatory  mechanisms controlling muscle growth, homeostasis, and repair and will provide  insights into the pathophysiology of dysferlin-deficiency in LGMD2 and Miyoshi  myopathies.&nbsp;</p>

