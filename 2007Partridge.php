                  <br>

                  </span><strong>Assessing the prospects of therapeutic exon  skipping in dysferlinopathies</strong><strong> &nbsp;</strong><br>

                  <br>

                  Over  the past few years, the dystrophin gene has served as an archetype for developing  the application of exon skipping strategies to rescue functional proteins from  mutated genes.&nbsp;&nbsp;From this work we can derive a number of principles  that serve as a basis for investigating the potential of this approach to other  genetic diseases.&nbsp; In principle, this strategy could be applied to any  protein of modular construction in which a mutation has occurred in a part of  the gene whose loss does not entail a major loss of function. &nbsp;The dysferlin gene product contains a series  of domains which may serve similar functions and substitute for one another,  suggesting that on this basis at least it may be possible to rescue a functional  protein by skipping small mutations.&nbsp; Some clue that these conditions may  obtain is given by the natural occurrence of exon skipping to give rise to  functional internally truncated versions of the protein.&nbsp; This is seen in  the case of dystrophin in the form of so called &lsquo;revertant&rsquo; muscle fibres in  many individuals who carry a defect of the

dystrophin gene. &nbsp;The presence of apparently &lsquo;revertant&rsquo;  dysferlin-positive muscle fibres in patients suffering from dysferlin deficiency  encourages the view that exon skipping may have therapeutic  possibilities.&nbsp; The easiest way to test whether a given group of exons can  be excluded while retaining function would be to make a series of expression  constructs encoding the various partial products and to transfect them by  direct injection into the muscles of a dysferlin-null mouse; looking with  antibodies to see whether significant amounts of a the resultant protein are  produced and localize correctly within the muscle fibres.&nbsp; Promising  candidates should then be tested as transgenes in the dysferlin-null background  to look for rescue from disease.&nbsp; Any constructs that pass this test would  then be good candidates for simulation by exon skipping designed to achieve the  same end product.&nbsp; This would sensibly be tested by use of systemically  delivered morpholinos but might subsequently be achieved by use of AAV  delivered expression plasmids based on the U1 or U7 RNAs.&nbsp; One concern  with the application of exon skipping to the dysferlin gene is that it is  expressed in different isoforms in skeletal muscle and in cells of the  monocyte/macrophage series.&nbsp; Since malfunctions of both of these cell  types may be implicated in the inflammatory phase of dysferlin deficiency  myopathies, it is important to consider the possible effects of exon skipping  on the function of dysferlin in the two contexts.</p>

