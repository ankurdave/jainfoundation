                  <strong>Towards the Structural Characterization of Human Dysferlin</strong><br>

Kerry Fuson1, Danijela Dukovski2, Tom Walz2, R. Bryan Sutton1
1Department of Cell Physiology and Molecular Biophysics,
Texas Tech Health Science Center, Lubbock, TX 79430, 2The Howard Hughes Medical
Institute and the Department of Cell Biology, Harvard Medical School, Boston, MA
02115<br><br>

Dysferlin has been implicated in Ca2+ dependent membrane repair of muscle tissue.
Dysferlin deficient mice are unable to effectively repair normal damage to muscle tissue
membranes. Various mutations in the dysferlin gene lead to limb-girdle muscular
dystrophy type 2B and Miyoshi myopathy. Both diseases are characterized by
progressive weakness and wasting of voluntary skeletal muscles. Currently, knowledge
of dysferlin and its role in the disease is limited. Using secondary structural analysis,
dysferlin likely contains seven C2 domains (C2A – C2G) and two domains that have yet
to be fully characterized (DysfN and DysfC). In general, C2 domains are associated with membrane trafficking and
cellular signaling and often bind calcium. At least some of the C2 domains of dysferlin are likely involved in Ca2+
dependent membrane interaction, two domains, C2A and C2F, have been shown to bind calcium. The ultimate goal
of our project is to characterize the 3D structure of the dysferlin protein to better understand its role in muscle repair.
To accomplish this, we will acquire high resolution X-ray crystal structures of each individual C2 domain, as well as
an EM reconstruction of the cytosolic portion of the full length dysferlin protein. Currently, we have successfully
cloned and purified two of the seven C2 domains of the human dysferlin protein and are in the process of cloning the
remaining domains. We have preliminary X-ray data on the C2A domain and are currently analyzing the X-ray data.
The C2F domain is expressed, purified and currently in crystal trials. In addition to the structural characterization of
the individual domains, we are working in collaboration with Dr. Tom Walz at Harvard Medical School to obtain
structural information of the full length protein. His lab has successfully expressed and has begun the purification
process of the full length protein in preparation for EM studies. </p>

