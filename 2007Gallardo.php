				  <br><br>Dysferlin expression in monocytes and  skeletal muscle in different pathological conditions in vivo and in vitro</strong><strong> </strong><br>

                  <br>

                  <strong>Monocytes:</strong> There is a definite  correlation between the expression of dysferlin in skeletal muscle and  peripheral blood monocytes, both in patients and controls. This observation has  allowed us to design a blood test for diagnostic purposes. Since it is a less  invasive method than a muscle biopsy, we have extended its use to the diagnosis  of carriers of mutations in the dysferlin gene and identified that some carriers can be symptomatic.&nbsp; We also  use it to successfully perform genetic analysis using cDNA. We suggest that the  fact that dysferlin expression in monocytes mirrors the expression in muscle  would be a powerful tool to monitor repeatedly and non-aggressively the  efficiency of future treatments aimed to increase dysferlin expression. Additionally,  we have reported that a high percentage of patients with dysferlinopathy  present inflammatory infiltrates, predominantly of macrophages, in muscle  biopsies. Using an in vitro model, based on trans-well analysis combined with  microarray studies, we have identified soluble factors<strong> </strong>that may be involved  in this process. The manipulation of these factors could be used to enhance migration of repairing cells or stem  cells for therapy. &nbsp;<strong>Muscle:</strong> The  analysis of human skeletal muscle primary cultures from patients with  dysferlinopathies and controls showed that the absence of dysferlin impairs  muscle differentiation in vitro, suggesting that dysferlin has other functions  apart from membrane repair. We found that the expression levels of myogenin, a  transcription factor necessary for muscle differentiation, were very low in  skeletal muscle primary cultures from dysferlinopathy patients. We are  currently refining the signaling pathways that may link dysferlin and myogenin.  Finally, by means of bidimensional electrophoresis and MALDI-TOF spectrometry  we are also performing protein maps corresponding to skeletal muscle and  monocytes from patients with dysferlinopathy, other muscular dystrophies and  controls. The aim of this project is to identify proteins differentially  expressed which are specific for dysferlinopathies that may lead us to a better  understanding of the different phenotypes and the pathophysiology of the  disease.&nbsp;&nbsp;</p>

