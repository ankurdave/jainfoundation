                  <strong>Preventing Misfolding and Aggregation of Dysferlin   </strong><strong>&nbsp; &nbsp;</strong><br>

Romy Siegert, Hannelore Haase, Ingo Morano, Simone Spuler

 <br><br>
Some missense mutations in dysferlin lead to aggregation of dysferlin within the endoplasmic reticulum.  We aim to find a way to target dysferlin, trapped in the ER, to the sarcolemma.  In this project the three dysferlin variants c.4022T>C, c.895G>A and c.6217A>G will be investigated.
We want to study the effect of small molecules such as chaperones on dysferlin misfolding.  Our first idea was to analyze if there might be any interaction of peptides with the misfolded dysferlin.  Therefore the C2-domain containing the mutation L1341P has been cloned into His-Tag-vector pRSETA and expressed as recombinant protein.  Peptides of 10-15 AA spanning the mutated region of the protein have been synthesized. 
We performed protein-binding studies by surface plasmon resonance (Biacore 2000). Biacore uses the sensor chip technology. A thin gold film on a glass slide is divided into four flow cells.  To these flow cells  molecules can be immobilized as ligands by several methods.  The binding partner of the ligand is injected over the surface.  On the backside polarized light is directed to the gold film.  If binding occurs there will be small changes in the reflection of the monochromatic light from the metallic chip.
Association and dissociation are presented as a real-time-graph of response units (RU) against time.  For proteins 1000 RU represents about 1ng bound to the cell surface.  In our case the peptides have been immobilized as ligands on the sensor chip surface.  As an analyte we used different concentrations of the expressed recombinant protein DYSF Mut L1341P.  In this experiment no binding occurred.
In the next experiments the mutated variants of dysferlin (c.4022T>C, c.895G>A and c.6217A>G) will be transfected into murine dysferlin-null myoblasts and myotubes to analyze the effect of TAT-fused dysferlin peptides on facilitating the transport of dysferlin to the sarcolemma in vivo.  Confirmatory experiments will be performed with transfected human dysferlin-null myotubes and with myotubes from LGMD2B/MM patients harbouring the respective mutations.  Expression of dysferlin and redistribution of dysferlin on the sarcolemma will be monitored by fluorescent microscopy.
