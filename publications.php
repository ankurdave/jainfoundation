    <a href="http://www.ncbi.nlm.nih.gov/pubmed/20096397?itool=EntrezSystem2.PEntrez.Pubmed.Pubmed_ResultsPanel.Pubmed_RVDocSum&ordinalpos=1" target="_blank"><strong>Recessive Mutations in the Putative Calcium-Activated Chloride Channel Anoctamin 5 Cause Proximal LGMD2L and Distal MMD3 Muscular Dystrophies.</strong></a><br>
Bolduc V, Marlow G, Boycott KM, Saleki K, Inoue H, Kroon J, Itakura M,
Robitaille Y, Parent L, Baas F, Mizuta K, Kamata N, Richard I, Linssen WH,
Mahjneh I, de Visser M, Bashir R, Brais B.<br>

<em> Am J Hum Genet. </em>2010<br>

<br>
The recently described human anion channel Anoctamin (ANO) protein family comprises at least ten members, many of which have been shown to correspond to calcium-activated chloride channels. To date, the only reported human mutations in this family of genes are dominant mutations in ANO5 (TMEM16E, GDD1) in the rare skeletal disorder gnathodiaphyseal dysplasia. We have identified recessive mutations in ANO5 that result in a proximal limb-girdle muscular dystrophy (LGMD2L) in three French Canadian families and in a distal non-dysferlin Miyoshi myopathy (MMD3) in Dutch and Finnish families. These mutations consist of a splice site, one base pair duplication shared by French Canadian and Dutch cases, and two missense mutations. The splice site and the duplication mutations introduce premature-termination codons and consequently trigger nonsense-mediated mRNA decay, suggesting an underlining loss-of-function mechanism. The LGMD2L phenotype is characterized by proximal weakness, with prominent asymmetrical quadriceps femoris and biceps brachii atrophy. The MMD3 phenotype is associated with distal weakness, of calf muscles in particular. With the use of electron microscopy, multifocal sarcolemmal lesions were observed in both phenotypes. The phenotypic heterogeneity associated with ANO5 mutations is reminiscent of that observed with Dysferlin (DYSF) mutations that can cause both LGMD2B and Miyoshi myopathy (MMD1). In one MMD3-affected individual, defective membrane repair was documented on fibroblasts by membrane-resealing ability assays, as observed in dysferlinopathies. Though the function of the ANO5 protein is still unknown, its putative calcium-activated chloride channel function may lead to important insights into the role of deficient skeletal muscle membrane repair in muscular dystrophies. Copyright © 2010 The American Society of Human Genetics. Published by Elsevier Inc. All rights reserved.
<br><br><br>

					                        <a href="http://www.ncbi.nlm.nih.gov/portal/utils/file_backend.cgi?Db=pubmed&HistoryId=NCID_1_96626545_130.14.22.28_9001_1263250766&QueryKey=3&Sort=&Filter=all&CompleteResultCount=1&Mode=file&View=abstract&p$l=Email&portalURL=http://www.ncbi.nlm.nih.gov/sites/entrez&UserId=CE8832F5B4BAD4A1_0074SID" target="_blank"><strong>Calcium influx is sufficient to induce muscular dystrophy through a
TRPC-dependent mechanism.</strong></a><br>
Millay DP, Goonasekera SA, Sargent MA, Maillet M, Aronow BJ, Molkentin JD.<br>

<em> Proc Natl Acad Sci U S A. </em>2009<br>

<br>
Muscular dystrophy is a general term encompassing muscle disorders that cause
weakness and wasting, typically leading to premature death. Membrane instability,
as a result of a genetic disruption within the dystrophin-glycoprotein complex
(DGC), is thought to induce myofiber degeneration, although the downstream
mechanism whereby membrane fragility leads to disease remains controversial. One 
potential mechanism that has yet to be definitively proven in vivo is that
unregulated calcium influx initiates disease in dystrophic myofibers. Here we
demonstrate that calcium itself is sufficient to cause a dystrophic phenotype in 
skeletal muscle independent of membrane fragility. For example, overexpression of
transient receptor potential canonical 3 (TRPC3) and the associated increase in
calcium influx resulted in a phenotype of muscular dystrophy nearly identical to 
that observed in DGC-lacking dystrophic disease models, including a highly
similar molecular signature of gene expression changes. Furthermore,
transgene-mediated inhibition of TRPC channels in mice dramatically reduced
calcium influx and dystrophic disease manifestations associated with the mdx
mutation (dystrophin gene) and deletion of the delta-sarcoglycan (Scgd) gene.
These results demonstrate that calcium itself is sufficient to induce muscular
dystrophy in vivo, and that TRPC channels are key disease initiators downstream
of the unstable membrane that characterizes many types of muscular dystrophy.
<br><br><br>
					                        <a href="http://www.ncbi.nlm.nih.gov/pubmed/20008164?itool=EntrezSystem2.PEntrez.Pubmed.Pubmed_ResultsPanel.Pubmed_RVDocSum&ordinalpos=1" target="_blank"><strong>Myoferlin is required for insulin-like growth factor response and muscle growth.</strong></a><br>
Demonbreun AR, Posey AD, Heretis K, Swaggart KA, Earley JU, Pytel P, McNally EM.<br>

<em> FASEB J. </em>[Epub ahead of print]<br>

<br>
Insulin-like growth factor (IGF) is a potent stimulus of muscle growth. Myoferlin is a membrane-associated protein important for muscle development and regeneration. Myoferlin-null mice have smaller muscles and defective myoblast fusion. To understand the mechanism by which myoferlin loss retards muscle growth, we found that myoferlin-null muscle does not respond to IGF1. In vivo after IGF1 infusion, control muscle increased myofiber diameter by 25%, but myoferlin-null muscle was unresponsive. Myoblasts cultured from myoferlin-null muscle and treated with IGF1 also failed to show the expected increase in fusion to multinucleate myotubes. The IGF1 receptor colocalized with myoferlin at sites of myoblast fusion. The lack of IGF1 responsiveness in myoferlin-null myoblasts was linked directly to IGF1 receptor mistrafficking as well as decreased IGF1 signaling. In myoferlin-null myoblasts, the IGF1 receptor accumulated into large vesicular structures. These vesicles colocalized with a marker of late endosomes/lysosomes, LAMP2, specifying redirection from a recycling to a degradative pathway. Furthermore, ultrastructural analysis showed a marked increase in vacuoles in myoferlin-null muscle. These data demonstrate that IGF1 receptor recycling is required for normal myogenesis and that myoferlin is a critical mediator of postnatal muscle growth mediated by IGF1.-Demonbreun, A. R., Posey, A. D., Heretis, K., Swaggart, K. A., Earley, J. U., Pytel, P., McNally, E. M. Myoferlin is required for insulin-like growth factor response and muscle growth.
<br>
<br>
 <br>
					                        <a href="http://www.ncbi.nlm.nih.gov/pubmed/19929428?itool=EntrezSystem2.PEntrez.Pubmed.Pubmed_ResultsPanel.Pubmed_RVDocSum&ordinalpos=2" target="_blank"><strong>Exclusion of Mutations in the Dysferlin Alternative Exons 1 of DYSF-v1, 5a, and 40a in a Cohort of 26 Patients.</strong></a><br>
Krahn M, Labelle V, Borges A, Bartoli M, Lévy N.<br>

2009 Nov 23.<em> Genet Test Mol Biomarkers. </em>Ahead of Print.<br>

<br>
Mutations in the gene encoding dysferlin (DYSF; MIM# 603009, 2p13, GenBank NM_003494.2) cause primary dysferlinopathies, which are autosomal recessive muscular dystrophies. DYSF has a large mutational spectrum, and genetic diagnosis is complicated by incomplete mutation detection rates. Recently, novel dysferlin transcripts were characterized by identifying alternative exons 1 of DYSF-v1 (GenBank DQ267935), exon 5a (GenBank DQ976379), and exon 40a (GenBank EF015906). To evaluate the frequency of possible mutations in the newly identified DYSF alternative exons, we screened the corresponding genomic regions for mutations in a cohort of 26 patients, carrying only one mutation undoubtedly considered as disease causing in the 55 canonical DYSF exons. No disease-causing mutation was identified in alternative exons 1 of DYSF-v1, exon 5a, and exon 40a, demonstrating a low frequency of disease-causing mutations in these exons.
<br>
<br><br>
					                        <a href="http://www.ncbi.nlm.nih.gov/pubmed/19953532?itool=EntrezSystem2.PEntrez.Pubmed.Pubmed_ResultsPanel.Pubmed_RVDocSum&ordinalpos=1" target="_blank"><strong>Efficient bypass of mutations in dysferlin deficient patient cells by antisense-induced exon skipping.</strong></a><br>
Wein N, Avril A, Bartoli M, Beley C, Chaouch S, Laforêt P, Behin A, Butler-Browne G, Mouly V, Krahn M, Garcia L, Lévy N.<br>

2009 Dec 1.<em> Hum Mutat. </em>Ahead of Print.<br>

<br>

Mutations in DYSF encoding dysferlin, cause primary dysferlinopathies, autosomal recessive diseases which mainly present clinically as Limb Girdle Muscular Dystrophy type 2B and Miyoshi myopathy. More than 350 different sequence variants have been reported in DYSF. Like dystrophin, the size of the dysferlin mRNA is above the limited packaging size of AAV vectors. Alternative strategies to AAV gene transfer in muscle cells must then be addressed for patients. A gene therapy approach for Duchenne muscular dystrophy was recently developed, based on exon-skipping strategy. Numerous sequences are recognized by splicing protein complexes and, when specifically blocked by antisense oligoucleotides (AON), the corresponding exon is skipped. We hypothesized that this approach could be useful for patients affected with dysferlinopathies. To confirm this assumption, exon 32 was selected as a prioritary target for exon skipping strategy. This option was initially driven by the report from Sinnreich and coll. (2006) of a patient with a very mild and late onset phenotype associated to a natural skipping of exon 32. Three different antisense oligonucleotides were tested in myoblasts generated from control and patient MyoD transduced fibroblasts, either as oligonucleotides or after incorporation into lentiviral vectors. These approaches led to a high efficiency of exon 32 skipping. Therefore, these results seem promising and could be applied to several other exons in the DYSF gene. Patients carrying mutations in exons whose the in frame suppression has been proven to have no major consequences on the protein function, might benefit of exon-skipping based gene correction. (c) 2009 Wiley-Liss, Inc.
<br>
<br>
 <br>
					                        <a href="http://www.ncbi.nlm.nih.gov/pubmed/19923419?itool=EntrezSystem2.PEntrez.Pubmed.Pubmed_ResultsPanel.Pubmed_RVDocSum&ordinalpos=1" target="_blank"><strong>Extensive mononuclear infiltration and myogenesis characterize the recovery of dysferlin-null skeletal muscle from contraction-induced injuries.</strong></a><br>
Roche JA, Lovering RM, Roche R, Ru LW, Reed PW, Bloch RJ.<br>

2009 Nov 18.<em> Am J Physiol Cell Physiol. </em>Ahead of Print.<br>

<br>

We studied the response of dysferlin-null and control skeletal muscle to large and small strain injuries to the ankle dorsiflexors in mice. We measured contractile torque and counted fibers retaining 10 kDa fluorescein dextran, necrotic fibers, macrophages, fibers with central nuclei and expressing developmental myosin heavy chain, to assess contractile function, membrane resealing, necrosis, inflammation and myogenesis. We also studied recovery after blunting myogenesis with X-irradiation. We report that dysferlin-null myofibers retain 10 kDa dextran for 3 days after large-strain injury but are lost thereafter, following necrosis and inflammation. Recovery of dysferlin-null muscle requires myogenesis, which delays the return of contractile function compared to controls, which recover from large-strain injury by repairing damaged myofibers without significant inflammation, necrosis, or myogenesis. Recovery of control and dysferlin-null muscles from small-strain injury involved inflammation and necrosis followed by myogenesis, all of which were more pronounced in the dysferlin-null, which recovered more slowly. Both control and dysferlin-null muscles also retained 10 kDa dextran for 3 days after small-strain injury. We conclude that dysferlin-null myofibers can survive contraction-induced injury for at least 3 days but are subsequently eliminated by necrosis and inflammation. Myogenesis to replace lost fibers does not appear to be significantly compromised in dysferlin-null mice.
<br>
<br>
					                   <br>     <a href="http://www.ncbi.nlm.nih.gov/pubmed/19594366?itool=EntrezSystem2.PEntrez.Pubmed.Pubmed_ResultsPanel.Pubmed_RVDocSum&ordinalpos=9" target="_blank"><strong>Identification of different genomic deletions and one duplication in the dysferlin gene using multiplex ligation-dependent probe amplification and genomic quantitative PCR.</strong></a><br>
Krahn M, Borges A, Navarro C, Schuit R, Stojkovic T, Torrente Y, Wein N, Pécheux C, Lévy N.<br>

2009 Aug.<em> Genet Test Mol Biomarkers. </em>13(4):439-42.<br>

<br>

We report for the first time the characterization of disease-causing exonic rearrangements in the large-sized gene encoding dysferlin. A newly developed kit for multiplex ligation-dependent probe amplification analysis of the dysferlin gene was used for a total of 12 samples from patients with suspected diagnosis of primary dysferlinopathy. This analysis and subsequent genomic quantitative real-time PCR evidenced exonic rearrangements in five patients, including four different exonic deletions and one duplication. Altogether, our findings confirm the existence of exonic rearrangements as disease-causing mutations in primary dysferlinopathies.
<br>
<br>					  <br>
					                        <a href="http://www.ncbi.nlm.nih.gov/pubmed/19834057?itool=EntrezSystem2.PEntrez.Pubmed.Pubmed_ResultsPanel.Pubmed_RVDocSum&ordinalpos=1" target="_blank"><strong>Genetic manipulation of dysferlin expression in skeletal muscle: novel insights into muscular dystrophy.</strong></a><br>
Millay DP, Maillet M, Roche JA, Sargent MA, McNally EM, Bloch RJ, Molkentin JD.<br>

2009.<em> Am J Pathol. </em>175(5):1817-23.<br>

<br>

Mutations in the gene DYSF, which codes for the protein dysferlin, underlie Miyoshi myopathy and limb-girdle muscular dystrophy 2B in humans and produce a slowly progressing skeletal muscle degenerative disease in mice. Dysferlin is a Ca(2+)-sensing, regulatory protein that is involved in membrane repair after injury. To assess the function of dysferlin in healthy and dystrophic skeletal muscle, we generated skeletal muscle-specific transgenic mice with threefold overexpression of this protein. These mice were phenotypically indistinguishable from wild-type, and more importantly, the transgene completely rescued the muscular dystrophy (MD) disease in Dysf-null A/J mice. The dysferlin transgene rescued all histopathology and macrophage infiltration in skeletal muscle of Dysf(-/-) A/J mice, as well as promoted the rapid recovery of muscle function after forced lengthening contractions. These results indicate that MD in A/J mice is autonomous to skeletal muscle and not initiated by any other cell type. However, overexpression of dysferlin did not improve dystrophic symptoms or membrane instability in the dystrophin-glycoprotein complex-lacking Scgd (delta-sarcoglycan) null mouse, indicating that dysferlin functionality is not a limiting factor underlying membrane repair in other models of MD. In summary, the restoration of dysferlin in skeletal muscle fibers is sufficient to rescue the MD in Dysf-deficient mice, although its mild overexpression does not appear to functionally enhance membrane repair in other models of MD.
<br>
<br>					  <br>
					                        <a href="http://www.ncbi.nlm.nih.gov/pubmed/19286669?ordinalpos=2&itool=EntrezSystem2.PEntrez.Pubmed.Pubmed_ResultsPanel.Pubmed_DefaultReportPanel.Pubmed_RVDocSum" target="_blank"><strong>Attenuated muscle regeneration is a key factor in dysferlin deficient muscular dystrophy.</strong></a><br>
Chiu YH, Hornsey MA, Klinge L, Jørgensen LH, Laval SH, Charlton R, Barresi R, Straub V, Lochmüller H, Bushby K.<br>

2009.<em> Hum Mol. Gen. </em>Mar 13, Epub ahead of print.<br>

<br>

Skeletal muscle requires an efficient and active membrane repair system to overcome the rigours of frequent contraction. Dysferlin is a component of that system and absence of dysferlin causes muscular dystrophy (dysferlinopathy) characterised by adult onset muscle weakness, high serum creatine kinase levels and a prominent inflammatory infiltrate. We have observed that dysferlinopathy patient biopsies show an excess of immature fibres and therefore investigated the role of dysferlin in muscle regeneration. Using notexin-induced muscle damage, we have shown that regeneration is attenuated in a mouse model of dysferlinopathy, with delayed removal of necrotic fibres, an extended inflammatory phase and delayed functional recovery. Satellite cell activation and myoblast fusion appear normal, but there is a reduction in early neutrophil recruitment in regenerating and also needle wounded muscle in dysferlin deficient mice. Primary mouse dysferlinopathy myoblast cultures show reduced cytokine release upon stimulation, indicating that the secretion of chemotactic molecules is impaired. We suggest an extension to the muscle membrane repair model where in addition to fusing patch repair vesicles with the sarcolemma dysferlin is also involved in the release of chemotactic agents. Reduced neutrophil recruitment results in incomplete cycles of regeneration in dysferlinopathy which combines with the membrane repair deficit to ultimately trigger dystrophic pathology. This study reveals a novel pathomechanism affecting muscle regeneration and maintenance in dysferlinopathy and highlights enhancement of the neutrophil response as a potential therapeutic avenue in these disorder.
<br>
