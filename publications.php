<a href="http://www.ncbi.nlm.nih.gov/pubmed/20154340?itool=EntrezSystem2.PEntrez.Pubmed.Pubmed_ResultsPanel.Pubmed_RVDocSum&ordinalpos=1" target="_blank"><strong>
EFFICIENT RECOVERY OF DYSFERLIN DEFICIENCY BY DUAL ADENO-ASSOCIATED VECTORS
MEDIATED GENE TRANSFER.</strong></a><br>
Lostal W, Bartoli M, Bourg N, Roudaut C, Bentaïb A, Miyake K, Guerchet N,
Fougerousse F, McNeil P, Richard I.<br>

<em> Hum Mol Genet. </em>2010 Feb 13<br>

<br>

Deficiency of the dysferlin protein presents as two major clinical phenotypes:
Limb Girdle Muscular Dystrophy type2B (LGMD2B) and Miyoshi Myopathy (MM).
Dysferlin is known to participate in membrane repair, providing a potential
hypothesis to the underlying pathophysiology of these diseases. The size of the
dysferlin cDNA prevents its direct incorporation into an Adeno-Associated-Virus
(AAV) vector for therapeutic gene transfer into muscle. To bypass this
limitation, we split the dysferlin cDNA at the exon 28/29 junction and cloned it 
into two independent AAV vectors carrying the appropriate splicing sequences.
Intramuscular injection of the corresponding vectors into a dysferlin-deficient
mouse model led to the expression of full-length dysferlin for at least one year.
Importantly, systemic injection in the tail vein of the two vectors led a
widespread although weak expression of the full length protein. Injections were
associated with an improvement of the histological aspect of the muscle, a
reduction in the number of necrotic fibers, restoration of membrane repair
capacity and a global improvement in locomotor activity. Altogether, these data
support the use of such a strategy for the treatment of dysferlin deficiency.
<br><br>

    <a href="http://www.ncbi.nlm.nih.gov/pubmed/20080405?itool=EntrezSystem2.PEntrez.Pubmed.Pubmed_ResultsPanel.Pubmed_RVDocSum&ordinalpos=1" target="_blank"><strong>Dexamethasone induces dysferlin in myoblasts and enhances their myogenic
differentiation.</strong></a><br>
Belanto JJ, Diaz-Perez SV, Magyar CE, Maxwell MM, Yilmaz Y, Topp K, Boso G,
Jamieson CH, Cacalano NA, Jamieson CA.<br>

<em> Neuromuscul Disord. </em>2010<br>

<br>

Glucocorticoids are beneficial in many muscular dystrophies but they are
ineffective in treating dysferlinopathy, a rare muscular dystrophy caused by loss
of dysferlin. We sought to understand the molecular basis for this disparity by
studying the effects of a glucocorticoid on differentiation of the myoblast cell 
line, C2C12, and dysferlin-deficient C2C12s. We found that pharmacologic doses of
dexamethasone enhanced the myogenic fusion efficiency of C2C12s and increased the
induction of dysferlin, along with specific myogenic transcription factors,
sarcolemmal and structural proteins. In contrast, the dysferlin-deficient C2C12
cell line demonstrated a reduction in long myotubes and early induction of
particular muscle differentiation proteins, most notably, myosin heavy chain.
Dexamethasone partially reversed the defect in myogenic fusion in the
dysferlin-deficient C2C12 cells. We hypothesize that a key therapeutic benefit of
glucocorticoids may be the up-regulation of dysferlin as an important component
of glucocorticoid-enhanced myogenic differentiation. Copyright © 2009 Elsevier
B.V. All rights reserved.
<br><br>

