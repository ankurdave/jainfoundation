                      <strong><font color="#0175C2"><span class="style2"><em>C. elegans</em> High Throughput Compound Screen</span><br>

                        </font></strong>Mohan Viswanathan, Cambria Biosciences (Woburn,  MA)<br>

                      <br>

                      <strong>Project Objective</strong><br>

                      Identify chemical suppressors of <em>C. elegans fer-1</em> <br>

                      <br>

                      We are using the powerful invertebrate model  organism <em>Caenorhabditis elegans</em>, a microscopic roundworm, as a platform  for high-throughput compound screening to identify potential small molecule  therapeutics for the treatment of Limb-Girdle Muscular Dystrophy 2B (LGMD2B) and  Miyoshi Myopathy (MM). <em>C. elegans</em> FER-1 is the founding member of the  ferlin family of transmembrane proteins and is homologous to human dysferlin,  the protein mutated in LGMD2B/MM patients. <em>C. elegans</em> <em>fer-1</em> mutants show defects in spermatocyte maturation, which profoundly affect sperm  motility and egg fertilization. The biological basis of this block in spermatocyte  development is a defect in regulated exocytosis; whereby intracellular vesicles,  known as membranous organelles, fail to fuse properly to the plasma membrane of  spermatocytes following a Ca2+- dependent activation signal. This  cellular process is highly homologous to the membrane repair defect found in  mammalian dysferlin mutants, namely an inability to fuse vesicles containing  new membrane to existing plasma membrane in muscle cells. The major focus of  our experimental strategy will be to identify chemical suppressors of the <em>C.  elegans</em> <em>fer-1</em> fertility defect. Compounds that restore fertility  hold promise as leads toward therapeutic drugs for the treatment of LGMD2B and  MM.<br>

