                    <p class="home"><strong><font color="#0175C2"><span class="style2">In Vitro High Throughput

                      Screening Assay</span><br>
                    </font></strong>Kanneboyina Nagaraju, CNMC (Washington, DC)<br>
                    <br>
                    <strong>Project Objective</strong><br>
Develop an assay system to test drugs for muscle membrane

                      repair<br>
                      <br>
Plasma membrane disruption and resealing are common

                      events in several cell types including skeletal muscle.

                      It is known that disruptions in the plasma membrane

                      in dysferlin deficient muscle are due to defects in

                      the plasma membrane repair. Currently there are no

                      therapies for dysferlin deficient patients. Identification

                      of drugs/compounds that facilitate membrane repair

                      in muscle may not only help in reducing cell damage

                      and dysfunction but also improve muscle functions

                      in dysferlin deficient patients. This project will

                      develop a robust, reproducible plasma membrane repair

                      assay that is amenable for automated analysis to test

                      drugs that facilitate membrane repair in dysferlin-deficient

                      muscle cells.<br>
                      <br>
                      <img src="images/Kanneboyina.jpg" width="160" height="140" align="left"> <br>
                      <br>
                      <strong><a href="http://www.dcchildrens.com/CNMCResearch/" target="_blank">Kanneboyina Nagaraju, DVM, PhD</a></strong><br>
                      <br>
Dr. Nagaraju is an associate Professor of Pediatrics

                      and the director of the Murine Drug testing facility

                      at the CNMC (Washington DC).</p>

