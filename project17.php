						 <p class="home"><strong><font color="#0175C2"><span class="style2">Membrane Repair Assays </span><br>

                    </font></strong>Paul McNeil, Medical College of Georgia (Augusta, GA)<br>

                    <br>

                    <strong>Project Objective</strong><br>

To  probe the molecular mechanism of muscle cell membrane repair and to  develop novel high-throughput assays that can be used in the discovery  of small chemicals capable of repair facilitation.<br>

<br>

This  application has 3 major aims, all of which arise from our ongoing  research aimed at understanding membrane repair in general, and  limb-girdle type IIB muscular dystrophy and Myoshi myopathy (LGMD2B/MM)  in particular. 1.  Test the role of annexins, and other protein candidates, in membrane  repair by skeletal muscle cells and the effect of dysferlinopathy on  annexin-related and other repair pathways. 2.  Develop techniques for quantitatively monitoring membrane repair that  can be used in a high-throughput assay capable of detecting small  chemicals having the potential for promoting membrane repair by  dysferlin null muscle fibers. 3.  Continue to act as a community resource, providing advice and help in  developing membrane repair assays, and conducting laser-based assays of  membrane repair.<br>

