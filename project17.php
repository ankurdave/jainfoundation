					                    <p class="home"><strong><font color="#0175C2"><span class="style2">Membrane &quot;Bandage&quot; </span> <br>

                    </font></strong>Kate Bushby, University of Newcastle upon

                    Tyne (UK)<br>

                        <br>

                         <strong>Project Objective</strong><br>

                         Explore Poloxamer 188&#8217;s ability to repair muscle

                         cell membrane<br>

                          <br>

                          The muscle membrane is key to muscle survival. Different

                           types of muscular dystrophy cause the cell membrane to be damaged

                           in different ways. Dysferlin deficiencies show good muscle

                           performance until the late teens or early twenties, when muscle damage becomes critical

                           and results in the onset of muscular dystrophy. A substance called

                           poloxamer 188 has been shown to have protective properties

                           in mouse heart muscle in a different form of muscular

                           dystrophy. This work will explore if and how poloxamer

                           188 could protect cell and animal models of Miyoshi.<br>

                           <br>

                           The Jain Foundation also provided a supplemental grant to Dr. Bushby's lab for the purchase of equipment  for measuring the mechanical properties of muscles in experimental animals (the In Situ Muscle Test System). This equipment will allow detailed analysis of muscle degeneration and of the benefits of drugs, like poloxamer 188, in animal models of all types of muscular dystrophy.<br>

