                  <strong>Dysferlin in Healthy and Injured Skeletal Muscle: Cellular Localization and Levels of
Expression</strong><br>

Wendy G. Resneck, Joseph A. Roche, Robert J. Bloch<br><br>

Background: The purpose of this ongoing study is to assess the expression and
localization of dysferlin in healthy and injured skeletal muscle. Methods: To study the
localization and expression of dysferlin in different skeletal muscles and in the heart, we
collected perfusion-fixed muscles and unfixed muscles from 3 month old male and female
Sprague-Dawley rats. To study the localization and expression of dysferlin after a muscle
strain injury, we subjected the tibialis anterior (TA) muscle of the rat to 10 large-strain
lengthening contractions, harvested the muscle at different times after injury, and studied
them by immunofluorescence and immunoblotting. Both methods used the NCL Hamlet
antibody. Results: We found that dysferlin in uninjured TA muscles is concentrated at the
level of the sarcolemma, with faint punctate labeling in the cytosol. Western blots of muscle
homogenates showed that ~100% of the dysferlin is soluble in 1% Nonidet P-40. Dysferlin levels did not differ between
fast vs. slow muscles, forelimb vs. hindlimb muscles, limb vs. postural muscles, diaphragm vs. other skeletal muscles,
and skeletal muscle vs. cardiac muscle. Muscles from male and female animals also did not show significant differences.
Within 10 minutes after a large-strain injury, many fibers of the TA muscle showed a large increase in punctate labeling
of dysferlin in the cytoplasm. Three days after injury, when the muscle has recovered most of its contractile function,
labeling was again limited to the sarcolemma, similar to uninjured muscle, but Western blots showed increases in
dysferlin levels at 3 days after injury, and further increases 7 days after injury. Conclusion: We conclude that dysferlin
levels do not correlate with sex, fiber type or functional role of skeletal muscles. However, the localization and levels of
expression of dysferlin are altered in TA muscle after a large-strain injury. </p>
