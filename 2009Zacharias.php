                  for Molecular Medicine, Berlin, Germany<br><br>
                  Dysferlin (DYSF) gene mutations cause limb girdle muscular dystrophy 2B and Miyoshi myopathy due to defects in muscle membrane repair. Dysferlin deficiency is associated with a down regulation of myostatin, Smads and the complement inhibitor CD55 (1). The  subcellular localization of dysferlin, its time course of expression and the role of dysferlin in membrane fusion events like myoblast fusion are still a matter of debate. Therefore, we compared the time course of myotube formation in normal and dysferlin-deficient  myoblasts. Dysferlin deficient myoblasts differentiate and form multinucleated myotubes in a way similar to normal human myoblasts. Additionally, the time course of dysferlin  translocation and insertion into the plasma membrane has been evaluated using a custom-made antibody against the extracellular C-terminal part of dysferlin. Next, we are going to investigate the regulation and the cellular effects of myostatin and myostatin antagonists on dysferlin-deficient human myoblasts in comparison to normal human myoblasts and to other muscular dystrophies.</p>
