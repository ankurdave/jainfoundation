				  <a name="5"></a><br>

                  <strong><span class="style17">Michele P. Calos, PhD</span></strong><br>

                  <em>Stanford University School of Medicine, Palo Alto, CA
</em><br><br>

                  <img src="images/Conf09/Calos.jpg" width="135"><br><br>

                  <strong>A Gene-Modified Stem Cell Strategy for Correction of Dysferlin Deficiency</strong><br>
Annahita Keravala, Lauren Woodard, Eddie Jung, Michele Calos<br><br>

A series of impressive studies from the laboratory of Guilio Cossu and others have documented the ability of blood vessel-derived mesoangioblast stem cells to migrate and engraft in degenerating muscles and produce competent muscle fibers. Building upon this work, we have set out to demonstrate a cell therapy strategy that utilizes autologouslyderived patient-specific stem cells, in order to avoid immune rejection and the need for immunosuppression. We correct autologous mesoangioblasts by addition of the wild-type dysferlin gene using the phiC31 integrase system. This phage integrase performs efficient sequence-specific integration in mammalian genomes. We have demonstrated that this plasmid-mediated gene addition system can be readily transfected into murine mesoangioblasts and results in efficient integration of therapeutic and marker genes into the genome. The gene-modified mesoangioblasts are then injected into disease model mice, where the injected cells are tracked by luciferase live imaging, GFP fluorescence, and dysferlin staining. We will present the results of our studies to date, examining the in vitro and in vivo differentiation potential of gene-modified mesoangioblasts and their capacity to migrate and engraft in vivo and produce functional muscle fibers.</p>
<br><br>
				  <a name="6"></a><br>

                  <strong><span class="style17">Yvan Torrente, MD, PhD</span></strong><br>

                  <em>Stem Cell Laboratory, University of Milan, Milan, Italy</em><br><br>

                  <img src="images/Conf09/Torrente.jpg" width="135"><br><br>

                  <strong>Exon-Skipping of Dysferlin in CD133+ Stem Cells Isolated from Patients Affected with
Dysferlinopathies</strong><br>
C. Navarro1, A. Farini1, P. Razini1 , M. Krahn2, L. Garcia2, N. Lévy3, Y. Torrente1
1Stem cell Laboratory, Department of Neurological Science, Fondazione IRCCS Ospedale
Maggiore Policlinico, Centro Dino Ferrari, University of Milan, Italy. 2UMR S 787,
INSERM/UPMC, Institut de Myologie, Facultè de Medecine Pierre et Marie Curie, Paris
Cedex 13, France. 3INSERM U910 "Genetique Medicale et Genomique Fonctionnelle" ;
Laboratoire de Genetique Moleculaire, Hopital d’enfants de la Timone, Marseille, France<br><br>

Mutations in the gene encoding Dysferlin are involved in LGMD-2B and MM. Currently there is no treatment available for these diseases. We characterized dysferlin expression in normal human CD133+ stem cells obtained from both circulating blood and muscle, either alone or in co-culture experiments with normal or dysferlin null mouse-derived myotubes. We designed anti-sense oligonucleotides (AONs) targeting consensus regions important to direct splicing machinery, for patients carrying relevant mutations in dysferlin exons. We also tested novel approaches for compensating mutations at the 3’ end of introns that affect lariat formation or acceptor site recognition, involving bi-functional (“tailed”) antisense RNAs. The efficiency of skipping was tested by RT-PCR on CD133 + stem cell isolated from LGMD-2B or MM patients. Moreover, we tested the function of re-expressed dysferlin, before and after engraftment of human skipped CD133+ cells in a immuno-depressed mouse model of dysferlinopathy (Scid/Dysf-/-). The expected outcome is a set of versatile molecular tools and approaches for antisense delivery, applicable to an extended number of mutations in the context of dysferlinopathies.</p>            
			
