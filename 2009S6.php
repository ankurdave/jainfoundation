				<p><span class="style10">Session VI: Stem Cell Therapies </span><br>


				  <br><br>

                  <strong><span class="style17">Amy J. Wagers, PhD</span></strong><br>

                  <em>Joslin Diabetes Center and Harvard Stem Cell Institute, Boston, MA
</em><br><br>

                  <img src="images/Conf09/Wagers.jpg" width="135"><br><br>

                  <strong>Role of Muscle Stem Cells in the Progression and Treatment of Dysferlinopathy</strong><br>

<br>

Limb Girdle Muscular Dystrophy type 2B (LGMD2) and Miyoshi myopathy arise from mutations that disrupt the function of the dysferlin gene and cause slowly progressive weakening and wasting of skeletal muscle. We are investigating the mechanisms by which loss of dysferlin leads to impairment of muscle maintenance and regenerative potential. To this end, we have used direct cell sorting based on surface marker expression to quantify and purify skeletal muscle precursors (SMPs), which function as endogenous muscle stem cells, from dysferlin-deficient (dysfdef) mice. These analyses demonstrated a profound ageassociated reduction in SMP frequency in dystrophic muscles of dysfdef mice, and a significant impairment of their proliferative capacity. Transplantation of wild-type SMPs into dysfdef recipients yielded robust engraftment, with extensive incorporation of dysferlin expressing donor nuclei into the host tissue. In contrast, delivery of wild-type blood cells or circulating factors, through parabiosis to a wild-type partner, failed to support cell engraftment and did not slow disease progression, as assessed histologically. Together, these studies reinforce the notion that muscle pathology in dysfdef animals arises primarily through muscle-intrinsic defects and demonstrate that dysfunction of muscle stem cells may be a contributing factor that limits repair potential in dysfdef muscle. Transplantation of muscle regenerative SMPs may present a viable approach for cell based therapy to restore dysferlin function in dysfdef muscle.</p>
<p><a name="2"></a>

				  <br>

                  <strong><span class="style17">Mayana Zatz, PhD</span></strong><br>

                  <em>Human Genome Center, São Paulo, Brazil</em><br><br>

                  <img src="images/Conf09/Zatz.jpg" width="135"><br><br>

                  <strong>Dysferlinopathies: From Gene Mapping to Preclinical Studies</strong><br>

Vieira NM1, Bueno Junior CR1, Brandalise V1, Moraes LV2, Zucconi E1, Secco M1, Suzuki
MF2, Bartolini P2, Camargo MM3, Brum PC2, Vainzof M1, Zatz M1
1Human Genome Research Center, 2Biosciences Institute; Biotechnology Department,
3National Nuclear Energy Commission-Department of Immunology, Instituto de Ciências
Biomédicas, University of São Paulo, São Paulo, Brazil<br><br>

The limb-girdle muscular dystrophies (LGMD) are a heterogeneous group of genetically determined progressive muscle disorders characterized by great inter and intrafamilial clinical variability. At least 20 different LGMD genes have been mapped or identified to date. LGMD2B or dysferlinopathy, accounts for ~ 20% of autosomal recessive LGMD in the Brazilian population. The search for treatment has been a great challenge for all forms of muscular dystrophies. Stem cell research has opened new promising possibilities allowing the investigation of gene expression in cell lineages derived from affected patients as well as through cell therapy. We are currently investigating the potential of mesenchymal stem cells from different origins, as a source of muscle cells, in various animal models. The murine model for LGMD2B, the SJL mice, carry a deletion in the dysferlin gene that causes a reduction in the protein levels to 15% of normal. We have injected human adipose stromal cells (hASCs) intravenously, without immunosuppression, into 14 SJL mice, divided in two groups of 7. One group received injections of undifferentiated hASCs while the other received hASCs pre-committed to myogenic phenotype, expressing MyoD. These animals were injected each month for 6 months, followed for 9 months and compared with a control group of 7 non-injected SJL mice. Our aim was to assess the hASCs ability to engraft into recipient dystrophic muscle, form chimeric human/mouse muscle fibers, express human muscle proteins in the dystrophic host and improve muscular performance. We showed for the first time that undifferentiated hASCs were not rejected after systemic injection even without immunosuppression. Further, they were able to fuse with the host muscle, express a significant amount of human muscle proteins and improve motor ability. These results may have important applications in future therapies for muscular dystrophies. </p>

<p>

				  <a name="3"></a><br>

                  <strong><span class="style17">Morayma Reyes, MD, PhD</span></strong><br>

                  <em>University of Washington, Seattle, WA
</em><br><br>

                  <img src="images/Conf09/Reyes.jpg" width="135"><br><br>

                  <strong>Skeletal Muscle Perivascular Cells: Possible Cell Therapy Source for Dysferlinopathy</strong><br>
<br>

We have characterized skeletal muscle perivascular cells (SMPC) identified as Sca-1+/CD34+/CD31-/CD45- cells that exhibit myogenic and angiogenic potential in vivo and in vitro. These cells can be ex vivo expanded for more than 10 passages and can be efficiently transduced with lentiviral vectors. In order to assess the potential use of these perivascular cells for cell therapeutics for dysferlinopathy, we studied their capacity to engraft and restore dysferlin expression in two transplantation settings: autologous and allogeneic transplants. For autologous transplants, SMPC were derived from A/J muscles and transduced with lentiviral vectors containing dysferlin variant 1 and dysferlin variant 2. We obtained high transduction efficiency (50-80%) with vectors carrying both variants. We also obtained high myogenic and angiogenic engraftment when autologous, reconstituted SMPC were transplanted intra-muscularly in A/J mice. Allogeneic transplantation of GFP SMPC also results in high myogenic and angiogenic engraftment in short term transplants (2 weeks). However, immune rejection of GFP SMPC is seen after 4 weeks. Thus, we explored the use of bone marrow (BM) chimeras to induce immune tolerance to wild type SMPC. BM mononuclear cells and SMPC were harvested from transgenic GFP mice, which ubiquitously express GFP under the chicken b-actin promoter. GFP BM cells were transplanted intravenously into lethally irradiated A/J mice and SMPC derived from muscles of the same donor mice were ex vivo expanded. 6 months post BM marrow transplants, GFP SMPC from the same donor were intramuscularly transplanted in the left TA and left quadriceps of BM chimeras as well as age and gender-matched immune-competent A/J mice. 2 months post SMPC transplant, more than 50% of all perivascular cells were GFP positive (donor derived) in BM chimeras whereas no GFP positive cells survived in the immune competent transplants. These studies were confirmed by western blots and FACS. In summary, SMPC are perivascular cells that exhibit myogenic and angiogenic capacity when transplanted in A/J mice and show promise to be used in autologous and allogeneic transplants as cell therapy for dysferlinopathy.</p>

