                  <strong><span class="style17">Jacques P. Tremblay, PhD</span></strong><br>

                  <em>Laval University and CRCHUQ, Quebec, Canada</em><br><br>

                  <img src="images/Conf09/Tremblay.jpg" width="135"><br><br>

                  <strong>Myoblast Transplantation: a Potential Treatment for LGMD2B</strong><br>
Jacques P. Tremblay, Daniel Skuk, Martin Paradis<br><br>

Myoblasts are muscle precursor cells obtained by the proliferation of muscle stem cells called satellite cells. They are the main cells responsible for natural repair of the muscle fibers following their damage. During this repair process, the myoblasts fuse with the remaining muscle fibers and introduce their own nuclei into the muscle fiber syncytia. When these myoblasts are obtained from a healthy donor, they contain a complete set of normal genes and thus introduce in the muscle fiber copies of genes that can correct several recessive muscular dystrophies. Thus myoblast transplantation is a form of gene therapy where the vector is a cell that naturally repairs muscle fibers. The great advantage of myoblasts is that they can be extensively proliferated, indeed 20 millions myoblasts can be obtained from a single satellite cell. Although, the transplantation of freshly isolated satellite cells has been reported to be more effective on a per cell basis, the extensive proliferation of myoblasts give them an important advantage for a human treatment because the size of the muscle biopsy of a donor is limited. Moreover, our recent experiments have demonstrated that the transplanted myoblasts not only fuse with the damaged muscle fibers and form new muscle fibers, but also generate mononucleated muscle-precursor cells (probably satellite cells) that can participate in further muscle repair. Our recent Phase I clinical trial and a compassionate treatment of myoblast transplantation in Duchenne Muscular Dystrophy patients have clearly proven that this treatment can restore the expression of normal dystrophin in up to 34% of the patient muscle fibers. The presence of the normal gene and the normal mRNA was clearly proven by PCR and RT-PCR amplifications of exons deleted in the patients genome. Moreover, in several of the cases, the presence of the normal dystrophin protein in the sarcolemma was demonstrated with monoclonal antibodies specific for the part of the protein coded by the exons deleted in the patients. Previous experiments of our research group in SJL mice have shown that the transplantation of normal myoblasts can restore the expression of dysferlin in these mice (Leriche-Guérin et al. Neur. Dis. 2002). Thus the possibility of treating LGMD2B by myoblast transplantation merits to be tested in a Phase I clinical trial.</p>

