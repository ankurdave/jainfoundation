                  <strong>Session IV: Clinical Aspects of Dysferlinopathy</strong><br>

                  <br>

are many dysferlinopathy-related clinical challenges that need to be addressed and studied in order for us to better understand the disease process and to more accurately diagnose, treat, and care for dysferlinopathy patients.  Some questions include: what is the best, most accurate, and cost effective way to diagnose patients; what causes the late onset of disease and the highly variable phenotype; and what issues need to be addressed before recruiting dysferlinopathy patients into clinical trials.  The presentations in this session focused on these three different, but equally important clinical challenges: dysferlin mutational analysis and interpretation, factors that contribute to disease onset and progression, and issues related to the planning and analysis of clinical trials.<br><br>

<strong>Dr. Martin Krahn</strong> (Hopital d’enfants de la Timone) discussed mutational analysis tools currently being developed in France for the identification and characterization of dysferlin mutations.  Tools such as these are urgently needed because of the size of the dysferlin gene, the high proportion of missense/intronic variants, the high number of unique variants, and the large percentage of patients without two confirmed pathogenic dysferlin mutations.  Dr. Krahn first described the UMD Locus Specific Database developed for dysferlin.  As of June 2008, the UMD-DYSF database contains 629 entries that describe 227 mutants and 201 protein variants.  Along with information on the individual variants, the database contains bioinformatic tools to aid in predicting the pathogenicity of new variants.  These tools include the splice site analysis/ splice site sequence finder for analysis of intronic variants, the UMD predictor for analysis of missense/ isosematic variants, and Global Analysis for statistical analysis of mutational events.  

Dr. Krahn went on to describe two mutational analysis techniques that could help identify additional mutations in the 30% of patients that have 0-1 dysferlin mutations identified.  The first technique involves sequencing the alternative exons 1, 5, and 40.  In 29 patients tested, no mutations in these alternative exons were found, implying that if mutations in these alternative exons exist, they are rare.  The second technique is Multiplex Ligand-dependent Probe Amplification (MLPA), developed by the MRC-Holland company, which identifies large intragenic rearrangements.  Dr. Krahn showed how this technique has been used to identify single to multi-exon deletions and duplications in a number of dysferlin patients.  The development and use of these important tools and techniques will be essential to help us diagnose dysferlinopathy patients, better understand the disease, and develop new therapeutic approaches.
<br><br>

<strong>Dr. Corrado Angenlini</strong> (University of Padova) discussed prognostic factors that may adversely affect dysferlinopathy disease progression.  He began his presentation by describing the distribution and clinical course of the various clinical phenotypes that are associated with a dysferlinopathy.  Distal myopathies, such as Miyoshi Myopathy, accounted for the largest phenotypic group at 80%, following by HyperCKemia at 12%, and LGMD2B at 8%.  By comparing the correlation between muscle strength (using the MRC grade) and various muscle functional scores (gait, Gower sign, etc.), Dr. Angelini showed that patients who present with a distal Miyoshi phenotype have a more rapid disease progression compared to those patients who present with a more proximal or mixed proximodistal phenotype, even though onset generally occurs later for the Miyoshi subgroup.  Therefore, distal weakness at disease onset is an adverse prognostic factor.  

Another adverse prognostic factor appears to be strenuous physical activity prior to disease onset.  Dr. Angelini evaluated 11 molecularly confirmed LGMD2B patients using a modified Garner-Medwin and Walton clinical scale.  Five out of the 11 patients never performed regular physical activity, while 6 out of the 11 performed competitive physical activity prior to onset of the disease.  By comparing the time it took to reach the “loss of Gowers” stage on the modified Gardner-Medwin and Walton scale, he showed that the physically active (“sportive”) patients reached this stage after only 5.75 years compared to 10 years for the non-sportive patients.  Therefore, Dr. Angelini suggests that patients who are suspected to have a dysferlinopathy should limit their physical activity.  The third adverse prognostic factor that he described was prominent inflammation.  He showed that an increased inflammatory response in muscle biopsies correlated with faster disease progression and decreased fiber regeneration, as documented by fetal myosin histopathological studies.
<br><br>

<strong>Dr. Kathryn Wagner</strong> (Johns Hopkins School of Medicine) discussed the recently completed Phase I/II MYO-029 trial of myostatin inhibition in adult muscular dystrophy.  Myostatin inhibits muscle growth and directly regulates fibrosis, and inhibition of myostatin has been shown to improve muscle regeneration.  These properties were the rationale for developing neutralizing antibodies to myostatin, including MYO-029, as treatment for muscular dystrophies.  The Phase I/II clinical trial was a multicenter, double-blind, placebo controlled, randomized trial of MYO-029 sponsored by Wyeth Pharmaceuticals and included adult patients with a number of different muscular dystrophies, including LGMD2B.   

Dr. Wagner discussed a number of findings from the trial, including the following:<br>

•	Short term, partial myostatin inhibition is likely safe and well tolerated.<br>

•	Myostatin inhibition can increase muscle mass and shows positive trends in the percent change in lean body mass and muscle fiber diameter.<br>

•	At the doses used in this trial, MYO-029 does not produce substantial changes in muscle strength or function. <br><br>

In addition, while the exploratory endpoints were found to be feasible for all disease populations studied, outcome measurements need to be refined based on the previously underappreciated amount of fibrosis observed.  

She also discussed why the trial was unsuccessful in answering the following questions:<br>

•	Can MYO-029 reduce myostatin levels or activity? Many of the patients’ myostatin levels were below the limit of quantification.<br>

•	Can MYO-029 increase muscle function?  The "power" to detect change in function was low in this study.<br>

•	Are some conditions more responsive to myostatin inhibition than others?  There were too few patients in each disease category.  <br><br>

While this trial provides some hope for the use of myostatin inhibition as a treatment for adult muscular dystrophies, Dr. Wagner believes that there are a number of issues that need to be considered before moving forward with another trial in the future.  These include identifying biomarkers as a way to assess myostatin activity, determining surrogate endpoints to assess efficacy, and conducting phase 0 trials with a small number of patients to provide proof-of-principle.

