                  <strong>Dysferlinopathies: Clinical and Histopathological Findings in 28 Patients Seen in
a Tertiary Neurological Care Center</strong><br>
A. Nalini, N. Gayathri<br><br>

Objective: To describe the clinical features in 28 patients with dysferlin deficiency
confirmed by muscle immunohistochemistry (IHC).
Design: Case studies from the Neurology department of the National Institute of Mental
Health and Neurosciences, a tertiary national referral center.
Patients: Twenty-eight Indian patients (Twenty male and eight female) manifesting
distal myopathy or limb girdle muscular dystrophy and with absent dysferlin staining on
IHC, were analyzed.
Results: The 28 patients presented predominantly with distal myopathy of either distal
Miyoshi (MM) or proximal LGMD type and lacked dysferlin staining by immunohistochemistry. Two patients presented with the proximodistal form and two with
tibial muscular weakness. The main clinical feature in these patients was onset in late adolescence or early adulthood (mean age at onset for MM was 22.0 +/- 6.7 years and for LGMD2B was 19.4 +/- 5.1 years). There was early and
predominant involvement of posterior compartment muscles of leg or proximal pelvic girdle muscles, dystrophic
features with necrotic regeneration pattern without vacuoles and markedly elevated serum creatine kinase with mean
of 10033.8 +/- 9283 units /L (range 402 – 27460). Consanguinity was reported in 46.4% patients. The mean duration of
illness was 6.4 ? 4.2 years at the time of diagnosis. Among these 28 patients, 26 were ambulatory at the time of
evaluation.
Conclusions: In our experience dysferlinopathies formed 23% of all pathologically confirmed dystrophies seen
during the study period of two years from 2005 to 2007. This is the largest series of dysferlinopathy patients reported
from India. </p>
