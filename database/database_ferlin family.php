                  <p><strong>The Ferlin Family</strong></p>
                  <ol start="1" type="1">
                    
					<strong><li>What other proteins are related to Dysferlin?</li></strong>
					<br>Dysferlin is now realized to be a member of a family of proteins, called Ferlins, which are found in humans as well as many other organisms.<br>
					
					<br><strong><li>Which Ferlin protein was discovered first?</li></strong>
					<br>The first Ferlin to be discovered was the protein Fer-1, in the nematode <em>C. elegans</em>. This protein is required for reproduction in <em>C. elegans</em> (it is involved in vesicle fusion during spermatid maturation), and therefore its name is an abbreviation for “fertilization factor 1”.  The nomenclature for the human Ferlins, Fer1Lx, means “Fer-1 like number x”.<br>
					
					<br><strong><li>How many human Ferlins are known?</li></strong>
					<br>There are six known human Ferlins.  In the order of their discovery, they are: Dysferlin (Fer1L1), Otoferlin (Fer1L2), Myoferlin (Fer1L3), Fer1L4, Fer1L5, and Fer1L6.  It appears from a search of the reference sequences of the human genome, that there are no other undiscovered human Ferlins.<br>
					
					<br><strong><li>Are human Ferlins associated with genetic diseases?</li></strong>
					<br>A deficiency of the Dysferlin protein causes the autosomal recessive disorders LGMD2B, and Miyoshi Myopathy. A lack of Otoferlin, which is normally expressed in the inner ear and brain, causes a recessive form of deafness, DFNB9. No human diseases have been associated with Myoferlin yet, although a mouse model lacking Myoferlin shows a myopathy and has very small muscles.  The other three Ferlins, Fer1L4, Fer1L5, and Fer1L6, have not been associated with any diseases in humans or animals.<br>
					
					<br><strong><li>Which human Ferlins most closely resemble Dysferlin?</li></strong>
					<br>The Ferlins can be grouped into two sub-families with three proteins each, which more closely resemble each other than they do with members of the other sub-family.  One group contains Dysferlin, Myoferlin, and Fer1L5; the other contains Otoferlin, Fer1L4, and Fer1L6. An easy way to remember the grouping is that all the odd numbers (Fer1L1, Fer1L3, Fer1L5), and all the even numbers (Fer1L2, Fer1L4, Fer1L6) group together. So, Myoferlin and Fer1L5 are the two human Ferlins which most closely resemble Dysferlin.<br>
					
					<br><strong><li>Do other mammals have the same Ferlins as humans?  How similar are Ferlin proteins between species?</li></strong>
					<br>Although the genomes of most other mammals have not yet been analyzed as closely as the human genome, all of the identified Ferlins have also been identified in many other mammals: mice, rats, dogs, chimpanzees, cows, etc.  The sequences are very similar to the human sequence.  For instance, the human and mouse sequences for Dysferlin have 94% amino acid identity, and 97% amino acid similarity.<br>
					
					<br><strong><li>What other invertebrate Ferlins are known?</li></strong>
					<br>There is a <em>ferlin</em> gene in <em>Drosophila</em> called <em>misfire</em>, which like <em>fer-1</em>, is also involved in reproduction.  In the purple sea urchin (<em>Strongylocentrotus purpuratus</em>), there are three identified Ferlins.  Also, some proteins in yeast contain functional domains that are unique to Ferlins.<br>
					
					<br><strong><li>What do the Ferlin proteins do?</li></strong>
					<br>
					It appears that in general Ferlins are involved in vesicle fusion events.  When cell membranes fuse together there is an energy barrier to the fusion process, and various factors, including Ferlins and other proteins such as Synaptotagmins, are implicated in facilitating the fusion process.  For example, membrane fusion is involved in the repair of muscle sarcolemma , and critically requires Dysferlin. Also, Otoferlin has  been shown to interact with SNAREs and functions in synaptic exocytosis in cochlear hair cells.
