                  <ol start="1" type="1">
                    <strong><li>What is the size of the Dysferlin protein?</li></strong>
					<br>The “original” human Dysferlin splice variant contains 2080 amino acids, whereas “variant-1” is 2081 amino acids long.<br>
					<br><strong><li>What is known about the structural domains of Dysferlin?</li></strong>
					<br>A structure for the Dysferlin protein has not yet been elucidated. However, several domains have been identified based on sequence homology to other proteins. Please <a href="https://www.jain-foundation.org/downloads/human_protein.pdf" target="_blank">click here</a> to see a color-coded domain-distribution of Dysferlin.<br>
					<br><strong><li>What is known about the functional roles of the different domains?</li></strong>
					<br>
					<strong>C2 domains</strong> are Ca2+-binding motifs that exhibit a wide range of functions such as phospholipid-binding, signaling and membrane-trafficking. The function of <strong>DysF (dysferlin domain)</strong> is not known. The protein contains two copies of DysF, one of which is nested within the other, thereby giving rise to a DysFN and DysFC at the N- and C-termini of DysF respectively. The function of the conserved <strong>ferlin-family domains</strong> is unknown. The <strong>transmembrane-domain</strong> facilitates membrane insertion of Dysferlin.<br>
					<br><strong><li>What is the cellular localization of Dysferlin?</li></strong>
					<br>Dysferlin is predominantly localized at the sarcolemma of skeletal muscle cells and can be clearly visualized at the cell periphery by immunostaining of normal muscle. Dysferlin also shows cytoplasmic presence, and associates with the T-tubule network. Data suggests that cytoplasmic vesicles also harbor Dysferlin, though a direct localization to the vesicular compartment has not been demonstrated yet.<br>
