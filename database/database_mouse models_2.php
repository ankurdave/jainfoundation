                      <p><strong>What type of Dysferlin mutations do the different mouse models have?  Do they express any Dysferlin protein?</strong></p>
                     
					  <p>The <strong>SJL/J</strong> mice are homozygous for a missense mutation in <em>dysferlin</em>. The mutation is in the 3’ splice junction of Exon 45, which causes a deletion of 171 base pairs corresponding to amino acids 1628-1685 of the Dysferlin protein.  This deletion removes a part of the fifth C2 domain (C2E) of the protein.  SJL mice have partial Dysferlin expression; expression has been reported to be about 15% of the WT level.  In addition, Dysferlin is mislocalized, and is spread throughout the muscle cells rather than being concentrated at the sarcolemma.</p>
					  
<p>The <strong>A/J</strong> mouse has an ETn retrotransposon (5-6kb) inserted in Intron 4 of the <em>dysferlin</em> gene. The mice are homozygous for this insertion.  The mice do not express any Dysferlin.</p>

<p>In the <strong>B6.129-Dysf<sup>tm1Kcam</sup>/Mmmh</strong> mice, a 12-kb region of the genome, containing the last three exons (Exons 53-55, aa1983-2080) of <em>dysferlin</em>, is deleted. This deletion removes the transmembrane domain of the protein. The mice are of a mixed 129SvJ and C57BL/6 background and are homozygous for this deletion.  They do not express any Dysferlin.</p>

<p>In the <strong>Dysf-/-</strong> mice from the Brown laboratory, a 1.8-kb region of the genome, containing Exon 45 (aa1628-1685) of <em>dysferlin</em>, is deleted. This deletion removes a part of the fifth C2 domain (C2E) of the protein. The mice are of a mixed 129SvJ and C57BL/6 background and are homozygous for this deletion.</p>

