                      <p><strong>What mouse models are available to study Dysferlin deficiency?</strong></p>
                      <p>There are four known mouse models for Dysferlin deficiency: 
<p>Two of these are naturally occurring models, and were routinely used in a number of laboratory studies before their Dysferlin deficiency was discovered. These are the <strong>SJL/J</strong> mouse and the <strong>A/J</strong> mouse.  In addition, the Dysferlin mutation of the SJL mouse has been transferred to the C57 Black 10 strain (this cross is called <strong>C57BL/10.SJL-Dysf</strong>), so that the C57BL/10 mouse can be used as a control in experiments.</p> 

  <p>Two research groups have also created Dysferlin knockout mice by introducing nonsense mutations into the dysferlin gene.  These are the Dysf-/- (<strong>B6.129-Dysf<sup>tm1Kcam</sup>/Mmmh</strong>) mouse developed in the laboratory of Dr. Kevin Campbell at the University of Iowa, and the <strong>Dysf-/-</strong> mouse developed in the laboratory of Dr. Robert Brown at the Massachusetts General Hospital.  </p>

