					<br><strong><li>How big is the human <em>dysferlin</em> gene?</li></strong>
					<br>The coding sequence of <em>dysferlin</em> is comprised of 55 exons. Two splice variants of <em>dysferlin</em> have been described, “original” and “variant-1” that are 6907bp and 6543bp in length respectively. Please see below for more information on <em>dysferlin</em> splice variants.<br>
					<br><strong><li>Are there any splice variants of <em>dysferlin</em>?</li></strong>
					<br>There are two splice variants of human <em>dysferlin</em> that differ from each other with respect to their first exon (exon 1). The two isoforms are designated as DYSF (original) and DYSF_v1 (variant 1), and their GenBank accession numbers are NM_003494 and DQ267935 respectively. Corresponding splice-isoforms are also found in mice, and their accession numbers are NM_001077694 (mouse transcript variant 2; similar to “original” human dysferlin) and NM_021469 (mouse transcript variant 1; similar to “variant-1” human dysferlin). <em>These splice isoforms are available through the Jain Foundation at no cost. Please contact the foundation to request the plasmids.</em>
