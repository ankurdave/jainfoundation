                      <p><strong>What are the phenotypes of the different mouse models?</strong></p>
                     
					  <p>In all Dysferlin-deficient strains except A/J, the age of onset is similar, with muscle weakness appearing at about 2 months of age.  In A/J, the onset and progression is somewhat slower, with weakness appearing at about 3 months of age.  In general, the mice strains show initial weakness primarily in the proximal and abdominal muscles, although the C57BL/10.SJL-Dysf strain has been reported to have variable patterns of weakness between animals.</p>
					  
