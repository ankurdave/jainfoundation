                  <img src="images/Conf09/Shadrach.jpg" width="135"><br><br><br>

                  <strong>Local, but Not Systemic Factors, Contribute to Dysferlin-deficient Dystrophy</strong><br>
                  Jennifer L. Shadrach, Morgan Maeder, Amy J. Wagers<br>
                  Section on Developmental and Stem Cell Biology, Joslin Diabetes Center, Department of<br>
                  Stem Cell and Regenerative Biology, Harvard University, and Harvard Stem Cell Institute,<br>
                  Boston, MA, 02215.<br><br>
Mutations that disrupt the dysferlin gene result in two related muscle degenerative diseases:
limb girdle muscular dystrophy type 2B (LGMD2B) and Miyoshi myopathy (MM). LGMD2B
and MM are characterized by a slow, progressive muscle weakness and wasting which
predominately affects the shoulder and hip muscles. Dysferlin in the muscle fiber has been
shown to be an important mediator of plasma membrane repair. Interestingly, LGMD2B and
MM mutations can be found throughout the dysferlin coding sequence, with no apparent
mutational “hotspots” or correlations between the specific type of dysferlin mutation and the distribution of affected
muscles or severity of the disease. Such variability among dysferlinopathies suggests that significant genetic and/or
environmental modifiers can impact disease onset and progression. We have taken two complementary approaches to
clarify the pathological mechanisms underlying dysferlin deficiency. We first used parabiosis (a surgical technique that
allows establishment of a common circulatory system in conjoined pairs of animals) to analyze the impact of the systemic
environment on dysferlin-deficient (dysfdef) disease progression. Histology and inflammatory responses of dysfdef muscle
exposed to a wild-type (WT) environment indicated that blood-circulating factors could not rescue dysfdef histopathology;
however, systemic factors from dysfdef mice had a mild deleterious effect on WT muscle. We next studied dysfdef muscle
stem cells by directly assaying their frequency and function using flow cytometric quantification and in vitro functional
assays. Interestingly, these studies revealed a striking age-associated reduction in muscle stem cell frequency,
specifically within the most dystrophic muscles of dysfdef mice. Additionally, in vitro assays showed a reduction in the
proliferative capacity of dysfdef muscle stem cells, but no deficits in their ability to differentiate. Taken together, our data
indicate that disease pathology of dysferlinopathy is mediated primarily through muscle-intrinsic defects in renewal and/or
repair, with only a modest impact of systemic factors on disease severity.</p>
