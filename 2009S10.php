				<p><span class="style10">Session X: New Tools for Studying Dysferlin</span><br>
				  
				  
				  <br>
				  <br>
				  
				  <strong><span class="style17">Paul L. McNeil, PhD</span></strong><br>

                  <em>Medical College of Georgia, Augusta, GA</em><br><br>

                  <img src="images/Conf09/McNeilP.jpg" width="135"><br><br>

                  <strong>Developing a High-Throughput Assay Capable of Detecting Dysferlin-Defective
Membrane Repair</strong><br>

<br>

We have developed a high-throughput assay capable of detecting Ca2+-dependent membrane repair in various cultured cell models, including C2C12 and primary myoblasts. Plasma membrane disruptions are made by scraping cells from the culture substratum, and this injury step can be applied rapidly (< 60 sec) to all 96-wells of a multi-well culture dish. Repair is detected, after transfer of the suspended (injured) population to a fresh 96-well dish, by the addition of a live/dead stain (~ 5 min required using manual pippetting). Cells that repair the disruptions created by scraping, stain with the live reagent; those that fail, stain with the dead reagent. The live/dead ratio is measured in a fluorescence plate reader (~ 2 min per plate read). We are now attempting to apply this assay to cultured myotube models. However, two obstacles remain. First, myotubes contract when scraped from the substratum and this (or some other problem related to the multi-nucleate myotube phenotype) impairs repair. We are therefore seeking an alternative to scraping, one that disrupts the plasma membrane but does not dislodge the myotube; or an alternative to the myotube model. Second, a suitable dysferlin-defective model system must be defined, namely one that exhibits defective Ca2+- dependent repair. It is our hope that discussion resulting from this presentation will be helpful in solving these two problems. </p>
				<p><a name="2"></a>

				  <br>

                  <strong><span class="style17">Daniel Stockholm, PhD</span></strong><br>

                  <em>Généthon, Évry, France</em><br><br>

                  <img src="images/Conf09/Stockholm.jpg" width="135"><br><br>

                  <strong>In Vivo Imaging Techniques for Membrane Permeabilization Assesment in Dysferlin
Deficient Muscle</strong><br>
Perrine Borel1, Mehdi Cherrar1, Jessica Petiot1, Joseph A. Roche2, Robert J. Bloch2,
Philippe Chaumet-Riffaud3, Michel Bessodes4, Daniel Scherman1,4, Daniel Stockholm1
1Généthon, 1 rue de l'Internationale, 91000 Évry, France, Department of Physiology, School
of Medicine, 2University of Maryland Baltimore. Baltimore, MD USA, 3Service de médecine
nucléaire, CHU Le Kremlin Bicetre, 94275 Le Kremlin Bicetre, France, 4Unité de
Pharmacologie Chimique et Génétique, CNRS UMR8151 - INSERM U640 Université René
Descartes Paris, France<br>
<br>

Non invasive imaging techniques for dysferlinopathy could be helpful to understanding disease physiopathology, provide a way to follow the evolution of the disease, and hopefully allow us to follow regression of the disease after treatment. Visible photonic imaging has some interesting potential for mouse muscle studies despite a limited penetration in tissue (from several hundred of micrometers to a few millimeters). We have started to investigate the characterisation of membrane tears in dysferlin deficient mouse using fluorescence imaging and hope to be able to follow in real time the entrance of fluorescent dextran in several muscle conditions including Large Strain Injury. We also plan to cross a transgenic mouse expressing a fluorescent probe for calpain activation assessment with BlaJ mice in order to have a fluorescent dysferlin deficient mouse model that facilitates the imaging of dysferlin studies for fundamental studies or preclinical studies. Fluorescence imaging of muscle is largely incompatible with clinical applications, so we are also exploring non invasive imaging derived from medical imaging. We developed a scintigraphy imaging inspired by the Blue Evans assays used for the determination of membrane permeability. By using labelled albumin with the technetium radioelement, we were able to follow and quantify in real time muscle damage and obtain three dimensional data (SPECT-CT). However, in basal condition the phenotype of Bla/J is too mild to be detected with this method. We hope to develop a reproducible injury protocol compatible with this imaging methodology that will enable the assessment of dysferlin-deficient muscle damage and compare it with an MRI imaging method.</p>

