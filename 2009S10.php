				  <a name="3"></a><br>

                  <strong><span class="style17">Robert H. Brown, Jr., MD, DPhil</span></strong><br>

                  <em>University of Massachusetts School of Medicine, Worcester, MA</em><br><br>

                  <img src="images/Conf09/Brown.jpg" width="135"><br><br>

                  <strong>Studies of Dysferlin-Negative C2C12 Muscle Cell Lines</strong><br>

Robert H. Brown, Jr. 1, Michele Maxwell2
1Department of Neurology, University of Massachusetts School of Medicine, Worcester, MA
01655, 2Department of Neurology, Massachusetts General Hospital, Boston, MA 02114
<br><br>

As reported at the Jain Foundation meeting in 2008, we have used RNAi-mediated silencing of the dysferlin gene to generate lines of C2C12 cells that are dysferlin deficient. Our studies indicate that these cells retain the expected myogenic potential of standard C2C12 lines. We have used these cells, and mock-silenced sister C2C12 cell lines to generate profiles of expressed mRNA and microRNA species in both myoblasts and myotubes, with and without dysferlin. We have further compared data from these in vitro studies with analogous data from muscle in vivo in mice that are both dysferlin-positive and –negative. Using these cell lines, we have also extended on-going efforts to develop in vitro methods to quantify the adverse impact of dysferlin deficiency on membrane repair in muscle cells in vitro. We anticipate that the availability of well characterized dysferlin-deficient cell lines will facilitate therapy development in Miyoshi myopathy and LGMD2B.</p>


