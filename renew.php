                <td valign="top"><p class="section">Renewal Applications - Submission Guidelines </p>

                    <p class="home"> If you are a researcher interested in renewing an award from the Jain Foundation, please read the guidelines

                      on this page.<br>

                      <br>

                      The explicit goal of the Jain Foundation is to expedite
                      development of a treatment or cure for LGMD2B/Miyoshi.
                      Your proposed project MUST continue to have application to this disease, or it will not be considered.<br>		  
					  <br>
                   
					  In general, the renewal process is informal, and results in a dialogue between the Jain Foundation and the researcher that is required for clarification and discussion of various aspects of the proposal.  This process typically takes 2 to 3 weeks. <strong>Therefore, renewals should be submitted at least 1 month prior to the end of the current award to avoid lapses in funding.</strong> Please bear this in mind when planning the submission of your renewal, as the Jain Foundation is not responsible for covering gaps that occur in funding.  <br>
 <br>
                        <br>
                      
                        <strong>Instructions for submitting a renewal:</strong><br>
                      
                        <br>
                      
                        <strong>1. </strong>A summary of the work completed over the previous funding period(this can be a compilation of the update summaries).<br>
                          <br>
                      
                          <strong>2.</strong> A detailed description of the specific aims and their related experiments for the coming year.<br>
                      
                          <br>
                      
                          <strong>3.</strong> A clear description of the scientific background and a demonstration
                      
                      of the techniques needed to make the proposed project a success. Because this is a renewal proposal, we only need background information for areas of the project that are new, and these can be integrated into the justification for the aims they support. Evidence of access to new techniques and collaborations with others are also required.<br>
					  <br>
 <strong>4.</strong> Please fill out all information on the <a href="http://www.jain-foundation.org/downloads/CoverPage_Grant.doc" target="_blank" class="style4">cover page</a> provided. <br><br>
 <strong>5.</strong> Please follow the preferred proposal format as outlined in the table of contents of the <a href="http://www.jain-foundation.org/downloads/CoverPage_Grant.doc" target="_blank" class="style4">cover page</a>. <br>
                      
                          <br>
 <strong>6.</strong> A detailed and aggressive timeline of experiments leading to the results of
                      
                      the project. Please refer to the <a href="http://www.jain-foundation.org/downloads/GrantTimeline_Template.doc" target="_blank" class="style4">sample timeline chart</a> as an example. Proposals without a detailed timeline will not be reviewed. <br>
                      
                          <br>
                      
                          <strong>7.</strong> A detailed budget (including personnel, material, and all other costs) and justification for each expense for the proposed project on the <a href="http://www.jain-foundation.org/downloads/GrantBudget_Template.doc" target="_blank" class="style4">budget template</a> provided. Proposals without a detailed budget and justification will not be reviewed. <br>
                      
                          <br>
                      
                          <strong>8.</strong> The average renewal will require  about 5-7 pages  (excluding references) to cover the level of detail that we expect, though individual renewals may vary in length. The experiments should be described in enough detail for  us to understand why each step in the experimental plan will require the time  that is alotted for that step in the timeline.<br>
                      
                          <br>
                      

                          <br>
                      *Please view the terms of the <a href="http://www.jain-foundation.org/downloads/Funding Agreement - US.pdf" target="_blank" class="style4">Jain Foundation Funding Agreement</a>.<br>
                      This agreement is required to initiate any funding with us, and is designed with
                      
                      a single goal in mind: to expedite development of a treatment for LGMD2B/Miyoshi. <br>
