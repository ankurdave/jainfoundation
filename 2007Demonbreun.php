                  <strong>Characterization of the myoferlin promoter  for upregulation therapy for LGMD 2B</strong><strong> &nbsp;</strong><br>

Alexis Demonbreun,  Konstantina Heretis, and Elizabeth McNally<br><br>

Two genes, myoferlin and dysferlin, have been  implicated in muscle fusion events.&nbsp; Dysferlin is encoded by the gene  associated with Limb Girdle Muscular Dystrophy type 2B, a progressive disorder  of muscle weakness.&nbsp; Myoferlin is highly related to dysferlin with an  overall 74% similarity at the amino acid level.&nbsp; Myoferlin is highly  expressed in developing muscle and myoblasts (Davis et al., 2000) and  participates in myoblast fusion (Doherty et al., 2005).&nbsp; In contrast,  dysferlin is more highly expressed in mature myotubes and myofibers where it  participates in membrane resealing associated with repairing disruptions in the  sarcolemma.&nbsp; It is this later activity that is thought to be responsible  for muscular dystrophy.&nbsp; Myoferlin is very highly upregulated in response  to damage.&nbsp; This expression pattern and the overall similarity between  myoferlin and dysferlin suggest that these two proteins may have molecular  overlapping functions.&nbsp; Taken further, myoferlin

may  be able to substitute for dysferlin in LGMD 2B patients.&nbsp; More generally,  upregulation of myoferlin may promote myoblast fusion/repair and may be used as  a therapeutic tool to aide patients with muscular dystrophy.&nbsp; Therefore,  we are characterizing the myoferlin promoter with the goal towards myoferlin  upregulation.&nbsp; Using VISTA  (http://genome.lbl.gov/vista/index.shtml),  the putative promoter regions of the human and murine myoferlin genes were  compared.&nbsp; This analysis revealed a 1500bp region directly upstream of the  myoferlin start site that is as high as 75% conserved between the two  species.&nbsp; Immediately upstream of this region, homology decreased  substantially to below 50% conservation.&nbsp; Hypothetical transcription  factor binding sites were plotted and are clustered in the regions of DNA that  are most highly conserved.&nbsp; Regions of homology were ligated into  the Pgl2 luciferase reporter vector and transfected into C2C12 cells.&nbsp; We identified that the1500 base pair region  strongly activates luciferase expression at greater than 60 fold above background.&nbsp;  This data implies the 1500 base pairs upstream of the myoferlin start site are  sufficient to promote luciferase expression and regulate myoferlin expression. Ongoing  studies are exploring the transcriptional regulators of myoferlin expression  and determining whether myoferlin upregulation is a therapeutic avenue for LGMD  2B patients. </p>

