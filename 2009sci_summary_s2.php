                  <strong>Session II: Dysferlinopathy – Mechanisms of Pathology II</strong><br>

                  
 <br>The talks in this session focused on new evidence that intercellular signaling pathways, in addition to membrane resealing, may be disrupted in the absence of dysferlin.<br><br>

<strong>Steven Vogel</strong> (USA) discussed recent experiments on the function of a sea urchin homolog of dysferlin. He has previously shown that morpholino-mediated knockdown of the sea urchin ferlin in one cell of a 2-cell stage embryo inhibits the cytokinesis step of cell division and decreases the rate of uptake of FM1-43 dye into cells, suggesting reduced endocytosis. Now he has also found that wounding a cell in an embryo causes a flash of high intracellular Ca2+ that is subsequently observed in neighboring unwounded cells, both in the same embryo and in nearby embryos in the culture. ATP added to the medium can also trigger such Ca2+ flashes, while apyrase (which degrades ATP and ADP), cadmium (a voltage-gated Ca2+ channel blocker), and omega-agatoxin IVA (a P-type voltage-gated Ca2+ channel blocker) all block the Ca2+ pulses in nearby cells but not in the initially wounded cell. Dr. Vogel hypothesizes that wounding results in depolarization of the wounded cell and thus opens voltage-gated Ca2+ channels, which allows an influx of Ca2+ into the cell that triggers exocytotic release of ATP. Knockdown of the sea urchin ferlin also inhibits release of this signal from wounded cells, a phenotype that can be rescued by microinjection of the human dysferlin mRNA. This result is consistent with a role for dysferlin in exocytosis of ATP-containing vesicles. Dr. Vogel mentioned that there is evidence in the literature that extracellular ATP stimulates muscle satellite cell differentiation, suggesting a potential physiological role for wounding-induced ATP release in mammalian muscle.<br><br>

<strong>Todd Lamitina</strong> (USA) discussed his research on the role of the C. elegans homolog of dysferlin, fer-1, in C. elegans muscle. Fer-1 was originally identified for its role in sperm motility and fertility (Argon and Ward 1980; Achanzar and Ward 1997). Dr. Lamitina has now shown that fer-1 is expressed in C. elegans body wall muscle, and that the expression of genes involved in locomotion and muscle sarcolemma stabilization is altered in two different fer-1 mutants. Based on the recent finding that fer-1 mutations confer resistance to normally fatal doses of aldicarb and levamisole, drugs that increase cholinergic signaling, Dr. Lamitina hypothesized that fer-1 plays a post-synaptic role in muscle cells at the neuromuscular junction (NMJ). He found that post-synaptic acetylcholine receptors (AchRs) in fer-l mutant worms did not have the normal punctate staining associated with their proper localization to synaptic varicosities. He also found reduced muscle power and force generation in fer-1 mutant worms, which was improved by increasing acetylcholine signaling using a mild dose of aldicarb. These results suggest that dysferlin is required for proper localization of AchRs at the muscle cell membrane and point to a role for dysferlin in membrane cycling.<br><br>

<strong>Tejvir Khurana</strong> (USA) hypothesized that dysferlin-deficient A/J mice may have a defect in response to cholinergic signaling at the NMJ, as do C. elegans fer-1 mutants (see above), and that their pathology might be improved by treatment with drugs that increase the efficiency of cholinergic signaling. He found that although the NMJs in A/J mice appear normal by histology, the mice have reduced muscle force and electromyogram intensity after repetitive low frequency muscle stimulation (a typical test for NMJ signaling defects). Acetylcholinesterase inhibitors are used to treat a similar cholinergic signaling defect in myasthenia gravis patients, and oral administration of the acetylcholinesterase inhibitor pyridostigmine bromide to post-symptomatic A/J mice eliminated the force generation and electromyogram defects. Although A/J mice typically have a normal lifespan, 3 out of 7 untreated A/J mice died by 14.5 months of age in this trial, compared to none of the treated A/J mice. The results of this pilot study suggest that acetylcholinesterase inhibitors may improve pathology in dysferlinopathy patients. 
