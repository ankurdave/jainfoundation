                  <strong>Session I: Dysferlinopathy – Mechanisms of Pathology</strong><br>

                  <br>

                  The <strong>“Dysferlinopathy – Mechanisms of Pathology”</strong> session addressed the mechanisms that contribute to dysferlinopathy at the molecular and cellular levels.  Although defective membrane repair plays a role in pathology, the details of how and why dysferlin mutations cause disease, and what happens downstream of a torn, unrepaired sarcolemma in vivo, remain poorly understood.  Resolution of these key questions is critical for choosing and developing appropriate drugs and other therapeutic strategies.<br><br>
				  
<strong>Dr. Sandra Cooper </strong> (Children’s Hospital at Westmead) discussed the trafficking and membrane biology of both wild type (WT) dysferlin and a missense mutant isolated from a patient that results in a F1871S amino acid substitution in the last C2 domain of the dysferlin protein.  To study trafficking, she made tagged dysferlin constructs that code for the full-length WT and mutant proteins, as well as deletion fragments that contain only the C2F and transmembrane domains (C2F-TM, F1871S C2F-TM).  These proteins were tagged with EGFP and His/Myc at the N- and C-termini, respectively, thus allowing for differential visualization of the intracellular and extracellular domains.  Her studies evaluated both the protein half-life and the plasma membrane half-life of dysferlin.  
She found that dysferlin requires the Golgi secretory pathway for trafficking to the plasma membrane (PM) and that it has a relatively transitory life at the PM (several hours), confirming that dysferlin indeed cycles to and from the PM.  This trafficking pattern may help explain the abnormal localization of dysferlin in many patients with muscular dystrophy.  In contrast, the deletion construct lacking the majority of the N-terminal cytosolic domain  displays a much shorter half-life at the PM and is rapidly endocytosed and degraded, implying that element(s) within the dysferlin N-terminal domain are required for stable anchorage at the PM.  The F1871S mutant is inefficiently targeted to the PM, and is largely retained intracellularly.  Dr. Cooper speculates that this is probably due to a subtle folding defect and/or failure to interact with a factor that facilitates dysferlin’s transport to the PM.  Further investigation of additional deletion constructs and elucidation of relevant trafficking pathways are currently underway to clarify the molecular details involved.<br><br>
 
<strong>Dr. Jeff Molkentin</strong> (Cincinnati Children’s Hospital Medical Center) discussed the role of Ca2+ in muscle degeneration.  Ca2+ can enter cells through the sarcolemmal breaches that are encountered in various forms of muscular dystrophy.  Mutations in a number of different sarcolemma-associated factors (such as dystrophin and the sarcoglycans) make the membrane more prone to contraction-induced damage, while mutations in dysferlin leave the membrane unable to repair itself following membrane tears.  Dr. Molkentin hypothesizes that in either case, the result is unregulated entry of Ca2+ into the muscle fiber that in turn initiates intracellular signaling cascades culminating in muscle fiber death through apoptosis and necrosis.  In order to test this hypothesis, Dr. Molkentin developed cyclophilin D null mice (a component of the mitochondrial permeability transition pore that plays a role in mitochondrial disruption and cell death) and crossed them with d-sarcoglycan and laminin-a2 knockout mice.  The resulting animals showed improvement in muscle pathology and amelioration of the dystrophic phenotype.

Further support for the Ca2+ hypothesis came from studies using the drug Debio-025, a cyclophilin inhibitor, which upon administration to mdx mice prevented dystrophic changes in the animals.  In addition, Dr. Molkentin has generated several transgenic mice expressing different types of cation exchangers and channels to study how Ca2+ influx and efflux influence muscle degeneration.  Recent studies with dysferlin-deficient A/J mice have revealed the presence of swollen mitochondria in muscle fibers, suggesting that elevated intracellular Ca2+ may indeed contribute to the muscle pathology observed in dysferlinopathy.  Future work will include crossing dysferlin-deficient mice with transgenic animals that overexpress various Ca2+ channels, pumps and exchangers to directly test whether Ca2+ is involved in mediating muscle degeneration associated with dysferlin deficiency. 
 <br>
<br>
<strong>Dr. Robert Bloch</strong> (University of Maryland School of Medicine) presented research  on the role of dysferlin in repair of damaged muscle fibers <em>in vivo</em>.&nbsp; Dysferlin&rsquo;s  role in muscle plasma membrane repair <em>in  vitro</em> is widely accepted, yet it is not clear what happens to muscle fibers <em>in vivo</em> following injury in dysferlin-deficient animal models. &nbsp;Joseph Roche, in the Bloch laboratory,  employed acute large strain eccentric contractions to induce muscle injury in  control and dysferlin-deficient A/J mice, and monitored the extent of membrane  disruption and the process of recovery from the injury by labeling muscle  fibers with Fluorescein dextran and Rhodamine dextran, as well as markers of  muscle regeneration and inflammation, and by monitoring contractile function. &nbsp;They found that A/J mice are no more susceptible  to muscle injury than control mice, but differ in how they recover after the  injury protocol. &nbsp;In particular, the  results suggested that dysferlin-deficient mice take longer than WT mice to recover  from muscle damage and that recovery in dysferlin-deficient mice involves  myogenesis rather than membrane resealing.&nbsp;  Myogenesis in A/J mice eventually replaces all the fibers that were  damaged directly or indirectly by injury.&nbsp;  Remarkably, however, the muscle fibers in A/J mice that were initially  damaged by injury remain impermeant to both fluorescent dextrans for 3 days  after injury, suggesting that their plasma membranes do initially reseal. &nbsp;Examination of injured muscles during the  recovery period revealed a potent inflammatory response in A/J but not control  mice, which Dr. Bloch suggests is responsible not only for triggering  myogenesis, but also for causing additional damage to fibers that were unharmed  by the initial large strain contractions. &nbsp;These results highlight dysferlin&rsquo;s role in  sarcolemmal stability and the potential role of the immune system in  pathogenesis in dysferlin-deficient muscular dystrophy.</p>
