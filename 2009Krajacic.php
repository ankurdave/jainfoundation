                  <strong>A Quantitative Screening Platform for Functional Analysis of C. elegans Muscular
Dystrophy Mutant Phenotypes</strong><br>
Predrag Krajacic1,3, Josue Snitzman2, Paulo Arratia2, Tejvir Khurana1,3 Todd Lamitina<br>
1Dept. of Physiology, 2Dept. of Mechanical Engineering and Applied Mechanics,<br>
3Pennsylvania Muscle Institute, University of Pennsylvania, Philadelphia, PA<br><br>

Background: C. elegans expresses homologs of many human muscular dystrophy disease
genes, such as Dysferlin and Dystrophin. While many labs have used qualitative assays to
study C. elegans muscle function, quantitative assays of C. elegans kinematics and
biomechanical properties, and the effect of muscular dystrophy mutations on these
properties, is lacking. Results: In liquid media, C. elegans swims by curving its body to
generate wave-like oscillations in its body posture. Proper movement requires coordinated
contractions and relaxations of the intact body wall muscle cells, which are the functional
and anatomical equivalent of human skeletal muscle. Remarkably, worms carrying
mutations in the muscular dystrophy genes (e.g. Dysferlin/fer-1) and (Dystrophin/dys-1) do not exhibit qualitatively
obvious motility defects. To better understand if and how such mutants affect C. elegans movement, we coupled a novel
imaging-based approach with mathematical modeling to quantitatively define kinematic and biomechanical patterns of C.
elegans movement. Using high-speed (125 frames/sec) video microscopy, we imaged the swimming behavior of C.
elegans carrying mutations in the Dysferlin homolog (fer-1 (hc24)), as well as worms mutant for the dystrophin homolog
(dys-1(cx18). Matlab based computational analysis was implemented to derive several movement parameters, including
body-bend frequency, velocity, body curvature, force, and power. While both fer-1 and dys-1 mutants appear
qualitatively normal, our assay revealed that body bending frequency, swimming velocity, force, and power are reduced
in both mutants. Both fer-1 and dys-1 have similarly reduced tissue stiffness (Young’s modulus). However, dys-1
mutants also have significantly reduced tissue viscosity as compared to wild type and fer-1 mutants, suggesting that fer-1
and dys-1 alter muscle functional properties in different ways. Using this screening platform, we have recently identified
a small molecule that can correct defective fer-1 biomechanical properties. Conclusions: For the first time, we have
quantitatively measured the biomechanical properties of C. elegans movement and discovered that mutations in two
muscular dystrophy genes dramatically alter whole-animal muscle function. With this new level of resolution, our findings
may lead to the development of a sensitive, multiparametric, high-throughput live animal platform for future identification
of drugs or genes that can reverse LGMD2B and other MD phenotypes. </p>
