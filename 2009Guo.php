                  <strong>Identification of a Canine Model of Dysferlin Deficient Muscular Dystrophy</strong><br>
                  1Ling Guo, 2Eva Engvall, 1G. Diane Shelton
1Department of Pathology, University of California San Diego, La Jolla, CA, 2The
Burnham Institute for Medical Research, La Jolla, CA<br><br>

Our ultimate goal is the identification of a canine model of dysferlin-deficient muscular
dystrophy. Diagnostic muscle biopsy specimens submitted to the Comparative
Neuromuscular Laboratory (CNL) at the University of California, San Diego were
screened for the presence or absence of dysferlin using commercially available
antibodies. A major problem we have encountered is the weak and often unreliable
dysferlin immunostaining signal. Here we present our recent work optimizing conditions
for immunostaining using various commercially available monoclonal and polyclonal
dysferlin antibodies on frozen sections of quadriceps muscle from a normal dog. Antibodies included NCL-Hamlet
and NCL-Hamlet2 (Novocastra), rabbit anti-dysferlin polyclonal antibody (Abcam), rabbit anti-dysferlin monoclonal
antibody (Epitomics) and goat polyclonal antibodies C19 and E20 (Santa Cruz Biotechnology). Frozen muscle
sections were fixed using five different methods (1: 100% acetone; 2: 50% acetone and 50% methanol; 3: 100%
methanol; 4: 4% paraformaldehyde; 5: 5% acetic acid in ethanol). Under each fixation condition, each antibody was
tested at different dilutions (1:10 to 1:240). Dramatic differences in staining intensities were observed under the
different fixation conditions. The best results were obtained with Epitomics rabbit monoclonal dysferlin antibody with
tissues fixed in 100% acetone. Good results were observed with NCL-Hamlet and NCL-Hamlet2 following fixation in
50% acetone and 50%methanol. Only weak staining was observed with the Abcam dysferlin antibody. C19 and E20
antibodies did not work on frozen canine muscle tissues by immunostaining. In conclusion, good and reliable
immunostaining results can be obtained with the current commercially available dysferlin antibodies using these
optimization procedures on frozen canine muscle. Over the past year, we have screened 300 muscle biopsies with
inflammatory or unidentified dystrophic phenotypes from dogs of all ages. Although we have not yet identified a
dysferlin deficient dog, using these optimized immunostaining procedures may enhance our ability to accurately
detect dysferlin deficiency. </p>
