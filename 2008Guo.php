                  <strong>Screening for a Canine Model of Dysferlin Deficient  Muscular Dystrophy</strong><strong>&nbsp; &nbsp;</strong><br>

Ling Guo MD, PhD, Diane Shelton DVM, PhD<br><br>

The objective of this study is to identify a canine model of dysferlin deficient muscular dystrophy by screening archived and newly received muscle biopsy specimens submitted to the Comparative Neuromuscular Laboratory (CNL) at the University of California, San Diego.  The CNL is an international reference center for the diagnosis of spontaneous neuromuscular diseases in companion animals and not available on this scale anywhere else in the world.  Over 110,000 specimens have been received from veterinary institutions throughout the world since the laboratory first started in 1990.  New biopsy specimens (50-75) are received weekly.  Muscle biopsies will be chosen for screening that have inflammatory changes and are not dystrophin, laminin alpha 2, or sarcoglycan deficient.  Muscle extracts will first be tested by standard immunoblotting using the NCL-Hamlet 1 antibody.  If dysferlin protein is absent or decreased, we will confirm the results using the immunofluorescent approach directly in frozen tissue sections.  During the last several years, we have identified dystrophin deficiency in many breeds, identified the first laminin alpha 2 deficiency in dogs and cats, and the first canine model of sarcoglycan deficiency in Boston terrier dogs.  Although we cannot guarantee that we will find a dysferlin deficient dog, we believe that this large scale screening will increase our chances of discovering a canine model of dysferlin deficient muscular dystrophy.  We expect to screen over 500 cases in the coming year. 


