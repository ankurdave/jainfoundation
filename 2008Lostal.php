                  <strong>Characterization of the Consequences of the A/J Mutation on a C57Bl/6 Background </strong><strong>&nbsp; &nbsp;</strong><br>

William Lostal, Nathalie Bourg-Alibert, Carinne Roudaut, Marc Bartoli and Isabelle Richard
Généthon, CNRS FRE3087, 1 rue de l'Internationale, 91000 Évry, France 
<br><br>

 Naturally occurring mouse models are available to study dysferlinopathies in vivo. However, there is a lack of good strain-matched controls, which are essential to any mouse study. To address this issue, we backcrossed the dysferlin-deficient A/J mice with the widely used C57BL/6 mice in order to obtain dysferlin-deficient mice on the C57BL/6 background.  Animals resulting from this cross were named B6.A/J-dysfprmd.  In subsequent backcrosses, heterozygous females were crossed with C57BL/6 males.  In parallel, heterozygous animals were crossed to each other to obtain homozygous dysferlin-deficient mice for characterization studies. 
Characterization of homozygous mice was performed on the F1 and F4 generations, and will be continued until F8 when the C57BL/6 background is >99%.  B6.A/J-dysfprmd F1 animals were compared with A/J and C57BL/6 at 2 and 4-months of age (6 and 9 month-old in progress).  Hematoxylin Phloxine Saffron (HPS) staining revealed dystrophic muscles at 2 months, followed by an increase in pathology at 4 months.  These histological changes were characterized by the presence of centronucleated fibers which are indicative of necrosis and regeneration, and by the presence of inflammatory infiltrates.  Though a quantitative measurement is pending, these animals also show heterogeneity in muscle fiber size as compared to healthy muscle.  Quadriceps were affected the most, followed by the Tibialis Anterior and then the Gastrocnemius.  Characterization of F4 animals (~94% C57BL/6 background) also revealed dystrophic signs in the muscle at 2 months of age.  However, the muscles were less affected than those of similarly aged F1 mice.  Though the number of centronuleated fibers was less in F4 mice than F1, it was equivalent to that observed in the A/J mice, thereby demonstrating phenotypic conservation of our mouse model throughout the backcross.
