				  <strong><span class="style17">Amy J. Wagers, PhD</span></strong><br>

                  <em>Joslin Diabetes Center and Harvard Stem Cell Institute
amy.wagers@joslin.harvard.edu
</em><br><br>

                  <img src="images/conference2008/Wagers.jpg" width="135"><br><br>

                  <strong>Systemic Regulation of Skeletal Muscle Repair</strong><strong>&nbsp; &nbsp;</strong><br>

<br>

The repair of injured skeletal muscle depends critically on the activity of a specialized population of myogenic satellite cells found in close association with muscle fibers.  In response to muscle damage, satellite cells become activated to proliferate, differentiate, and migrate to regenerate the muscle.  The signals that control satellite cell activation are as yet incompletely understood, but appear to include both local-acting factors and blood-borne regulators.  Thus, alterations in the local or systemic environment may impact the effectiveness of muscle repair and may modulate the response of satellite cells to muscle injury.  We have studied systemic regulators of satellite cells and muscle regenerative activity during age-related muscle degeneration (sarcopenia) and in muscular dystrophies (Limb-Girdle Muscular Dystrophy, Type 2b) using in vivo parabiosis models and in vitro satellite cell culture assays.  Our studies reveal a profound influence of systemic factors on both the activity and gene expression profile of skeletal muscle precursors (SMPs) within the satellite cell pool.  These effects are mediated, at least in part, by humoral elements of the blood, though additional contributions from circulating blood leukocytes, red cells, or platelets cannot be excluded.  Ongoing studies are aimed at molecularly identifying the systemic regulatory factors that control SMP function, so that they may be exploited to preserve or enhance muscle regenerative activity to combat muscle disease.				  </p>
