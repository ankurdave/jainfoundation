                  <strong>Exon Skipping of CD133+ Cells Isolated from the Blood and Muscle of Patients Affected by Mutations in Dysferlin Gene</strong><strong>&nbsp; &nbsp;</strong><br>

1C. Navarro, 1A. Farini, 1M. Meregalli, 1M. Belicchi , 1D. Parolini,  1P. Razini , 2 M. Krahn,  2N. Wein, 3N. Bourg, 3M. Bartoli, 3I. Richard, 2N. Lévy, 1Y. Torrente.
1Stem cell Laboratory, Department of Neurological Science, Fondazione IRCCS Ospedale Maggiore Policlinico, Centro Dino Ferrari, University of Milan, Italy. 
2 INSERM U910 "Genetique Medicale et Genomique Fonctionnelle" ; Laboratoire de Genetique Moleculaire, Hopital d’enfants de la Timone, Marseille, France.
3 Généthon CNRS FRE3018, Evry, France. INSERM U910 "Genetique Medicale et Genomique Fonctionnelle", Faculte de Medecine de la Timone Marseille, France. 
<br><br>

The main objective of this project is to investigate the combination of stem cell therapy and gene therapy, based on an exon skipping approach, for the rescue of dysferlinopathies.  All mutations in the DYSF gene found to date are point mutations, small deletions or insertions distributed over the entire coding sequence.  In some cases, forced exclusion (skipping) of a single exon could restore the reading frame, giving rise to a shorter, but still functional dysferlin protein.  In this case, we used a mono- and multi-skipping approach in order to perform the skipping of the exons 22-23 of the dysferlin gene of LGMD2B patients.  Blood-derived CD133+ stem cells were collected from a ?22 LGMD2B patient.  We confirmed the skipping of these cells in vitro by nested PCR and sequences analysis.  The removal of exons 22-23 leads to the expression of dysferlin protein by immunohistochemistry but the amount was too low to be visualized by western blot.  These data provide the proof of principal that blood-derived CD133+ stem cells isolated from LGMD2B patients can be skipped ex vivo.  Further studies are needed to improve our exon-skipping protocol in order to obtain efficient skipping of LGMD2B stem cells.  The in vivo regenerative potential of blood-derived CD133+ cells and the capacity to rescue human dysferlin after exon-skipping are prerequisites for autologous stem cell engraftment in LGMD2B and MM patients. </p>


