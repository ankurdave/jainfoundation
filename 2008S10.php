                  <strong>Dysferlin Expression and Function in Muscle Stem Cells</strong><strong>&nbsp; &nbsp;</strong><br>

<br>

Our lab has developed a flow cytometry protocol that allows analysis and sorting of multiple cell populations in the skeletal muscle including hematopoietic, endothelial, perivascular, and satellite cells.  We have demonstrated by RT-PCR, western blot and immunohistochemistry staining that dysferlin is expressed in endothelial cells, perivascular and satellite cells derived from mouse skeletal muscle.  This is the first report of dysferlin expression in mouse endothelial cells and satellite cells.  The studies of dysferlin expression and function in endothelial cells will be presented by Gayathri Balasundaram in the poster section.  

In order to gain insights into the function of dysferlin in satellite cells, we cultured satellite cells from A/J mice.  A/J satellite cells showed a significant impairment in proliferation and myoblast fusion capacity as compared to wt satellite cells.  Thus we hypothesize that dysferlin may play an important role in satellite cell proliferation, commitment and myoblast fusion.  We are very interested in understanding how a single protein can exert important functions in at least two completely different cell processes:  proliferation and myoblast fusion.  To study this we developed lentiviral vectors carrying either dysferlin variant 1 or variant 2.  We then transduced freshly sorted satellite cells from A/J mice with these vectors separately.  We obtained good transduction efficiency and it seems that the A/J satellite cells expressing either variant proliferated better and formed much more robust myotubes as compared to untransduced A/J satellite cells.  The possible mechanism(s) of dysferlin function will be discussed during the presentation.
				  </p>

