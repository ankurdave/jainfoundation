                  <strong>Membrane Repair and Inflammation in the Recovery of Dysferlin-Null Muscle After Contraction-Induced Injury </strong><strong>&nbsp; &nbsp;</strong><br>
Joseph A. Roche, Richard M. Lovering and Robert  J. Bloch</p>
				<p>				  Dysferlin-null myotubes and myofibers fail to repair their plasma membranes (sarcolemmae) after injury in vitro, but dysferlin’s role in adult muscle after physiological injury in situ is unclear.  We subjected the ankle dorsiflexors of A/J and control mice to 15 large-strain eccentric (lengthening) contractions, which decreased specific contractile torque by ~ 50%.  Mice were injected with fluorescein-dextran (FDx, 10 kDa) before injury to mark fibers with damaged sarcolemmae.  We examined muscles at 3, 7, 14 and 21 d after injury for recovery of muscle function (contractile torque), myogenesis (centrally nucleated fibers: CNFs; developmental myosin heavy chain: dMHC), and inflammation (mononucleate cells in H&E stained sections).  We also examined myofibers on d3 for myoplasmic mouse IgG and the ability to exclude rhodamine-dextran (RDx, 10 kDa).  
Control muscles (A/WySnJ, C57Bl6) recovered full contractile function by 7 d after injury.  Controls retained FDx for >2 wks, without developing CNFs, dMHC expression or inflammation.  A/J muscles required 14 d to recover function.  FDx-labeled fibers persisted for 3 d, then disappeared.  Inflammation peaked on d3 and lasted through d7.  dMHC labeling peaked at d7 and CNFs on d14.  Thus, wild type mouse muscle recovers from acute, large strain eccentric injury through sarcolemmal repair without myogenesis.  By contrast, A/J muscle recovers by myogenesis after a large inflammatory response.  
Many A/J fibers that labeled with RDx injected on d3 were unlabeled by FDx and so were undamaged on d0.  Many fibers on d3 also showed intracellular label for mouse IgG but not for FDx.   These results suggest that injured A/J myofibers reseal their sarcolemmae to 10 kDa dextrans for 3d, but that inflammation induced by injury leads to massive fiber degeneration and regeneration throughout the muscle.  Thus, inflammation, rather than defective membrane resealing, may underlie the response of dysferlin-null muscle to eccentric injury.<br>
