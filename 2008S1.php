				<p><span class="style10">Session I: Dysferlinopathy - Mechanisms of Pathology </span><br>

				  <br>

				  <br>

                  <strong><span class="style17">Sandra Cooper, PhD</span></strong><br>

                  <em>Institute for Neuromuscular Research, Children’s Hospital at Westmead
sandrac3@chw.edu.au
</em><br>
<br>

                  <img src="images/conference2008/Cooper.jpg" width="135"><br><br>

                  <strong>Trafficking and Membrane Biology of Dysferlin and Dysferlin Missense Mutant F1871S </strong><strong>&nbsp; &nbsp;</strong><br>

Frances J. Evesson and Rachel A Peat, Angela Chen, Fabienne Brilot-Turville, Harriet P. Lo, Alison Compton, Russell Dale, Kathryn N. North and Sandra T. Cooper<br><br>

Membrane resealing of skeletal muscle fibres likely involves a calcium-activated co-ordinated response of many protein components.  Dysferlin plays a key role in membrane resealing, although its precise function in this process remains poorly understood.  Dysferlin is a tail-anchored protein, and contains multiple C2 domains, both SNARE-like features that may underlie its role as a calcium-activated regulator of membrane repair.  
To start to unravel the cellular role of dysferlin, we have focussed our initial studies on defining fundamental properties of dysferlin cell biology; the lifetime of dysferlin protein, the lifetime of dysferlin at the plasma membrane, and its rate and route of endocytosis.  
In this presentation, I will highlight our findings with wild-type dysferlin, and present results from our study of a patient missense mutant F1871S, a highly conserved residue within the last dysferlin C2 domain.  Introduction of F1871S into a full-length dysferlin expression construct significantly impairs targeting to the plasma membrane.  Interestingly, removal of large regions of the dysferlin cytosolic domain improves plasma membrane targeting of the F1871S mutant, perhaps due to reduced demands on tertiary folding.  Half-life analyses, combined with studies using proteosomal inhibitor MG132, suggest that F1871S does not undergo gross misfolding, and does not display extreme lability either when endogenously expressed in patient myotubes, nor when heterologously expressed in C2C12 myotubes.  
It is not yet clear why the F1871S protein is stably expressed in patient myotubes, although is undetectable in patient skeletal muscle, but elevated levels of calcium and/or protein surveillance pathways may play a role in accelerating F1871S dysferlin degradation with time.  Overall, our study of the F1871S substitution implicates a subtle defect in tertiary folding, and/or impaired interaction with trafficking components, as the basis for disease.  F1871S is unable to be effectively transported to the plasma membrane to perform partial function.  


                </p>
