                  <strong>Ca2+ as a Central Mediator of Degeneration in Muscular Dystrophy </strong><strong>&nbsp; &nbsp;</strong><br>



<br> 
Many forms of muscular dystrophy are caused by a mutation in proteins that reside within the dystrophin-glycoprotein complex (DGC), or in proteins such as dysferlin that help maintain membrane integrity. Without a completely functional DGC, the muscle cell membrane is compromised, allowing for contraction-induced membrane tears and Ca2+ entry, along with dysregulation of Ca2+ channels to collectively increase cytosolic Ca2+ levels in dystrophic myofibers.  Increased cytosolic Ca2+ can cause myofiber degeneration through a number of downstream effectors, such as mitochondrial swelling and rupture, activation of calpain, and activation of cell death pathways.  Thus, we hypothesize that Ca2+ is a central mediator of skeletal muscle degeneration in MD.  To investigate the role of calcium in skeletal muscle degeneration we generated two independent skeletal muscle specific transgenic mice that overexpress membrane calcium/cation channels.  One transgenic expresses the sodium/calcium exchanger (NCX1), which our preliminary studies showed increased baseline calcium levels skeletal muscle disease as judged by histological analysis (central nuclei, fiber area, fibrosis).  Furthermore, the quadriceps muscle of 15 month old NCX TG (high line) mice had a large amount of fat replacement. Ironically, the NCX1 transgene protected the diaphragm after crossing the NCX1 TG with a model of MD, which lacks d-sarcoglycan (Scgd-/-, a component of the DGC).  We have also crossed NCX1 transgenic mice with mice lacking dysferlin to investigate the calcium hypothesis in Miyoshi myopathy.  We have also generated mice overexpressing transient receptor potential channel of the canonical subclass (TRPC3).  Similarly, these mice exhibit disease at 2 months of age, characterized by central nucleation and develop more fatty replacement at an early age.  Overall, our preliminary results indicate that altered calcium handling can cause disease without a mutation in the DGC or dysferlin and our future results should allow for analysis of whether enhanced removal of intracellular calcium benefits MD pathogenesis as a novel therapeutic approach.                </p>
                
