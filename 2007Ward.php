                  </span><strong>FER-1 function in <em>C. elegans</em></strong><strong>&nbsp;  &nbsp;</strong><br><br>

                  The <em>C. elegans  fer-1 </em>gene was initially identified by temperature-sensitive mutations that  prevent both male and hermaphrodite sperm from fertilizing eggs at restrictive  temperature.&nbsp; Nematode sperm are unusual crawling cells.&nbsp; During the  final maturation of spermatids to form spermatozoa they extend a pseudopod and  begin crawling by pseudopod membrane flow propelled by assembly of distinct  cytoskeletal filaments.&nbsp; While the pseudopod is protruding, golgi-derived  membrane vesicles partially fuse with the plasma membrane on the sperm cell  body, which adds proteins and lipids to the membrane and forms stable fusion  pores on the cell body.&nbsp; The cellular phenotype of mutants in all 10  characterized <em>fer-1 </em>alleles, including null alleles, is essentially  identical: sperm are made in normal numbers, and they complete meiosis forming  normal-looking sessile spermatids with vesicles.&nbsp; When these spermatids  activate to form spermatozoa, however,&nbsp;electron microscopy shows that  membrane vesicle fusion is completely

blocked, although the membrane vesicles  appear to be docked normally to the plasma membrane.&nbsp; Spermatozoa do form  with short pseudopods, which wiggle but cannot propel the spermatozoa.&nbsp;  When the <em>fer-1 </em>gene was first cloned and sequenced it was similar only  to a few short human ESTs, but when the gene for LGMD was identified in humans,  its protein was found to similar to FER-1 over almost its entire length and was  named Dysferlin.&nbsp; We have characterized the phenotypes of all <em>fer-1 </em>mutants  further with an in vitro assay for membrane fusion using a membrane  intercalating fluorescent dye.&nbsp; Normal vesicle fusion can be blocked by  calcium chelators inside the cell, but not outside the cell, so fusion appears  to require calcium release from an internal store.&nbsp; Temperature-sensitive  missense mutations in FER-1 C2 domains alter calcium sensitivity at permissive  temperatures, suggesting that these domains may be directly involved in the  calcium binding.&nbsp; The importance of the C2 domains is also indicated by  their conservation of both sequence and position throughout the ferlin family,  and by our finding that nearly all the <em>fer-1 </em>missense mutations are in  C2 domains.&nbsp; In normal cells FER-1 is located predominately in the sperm  membrane vesicles.&nbsp; It is not detected in the plasma membrane until after  the vesicles fuse in normal cells, and then FER-1 appears in both the cell body  and pseudopod membranes.&nbsp; Other proteins in the vesicle are also added to  the plasma membrane during fusion.&nbsp; Using Western blots probed with  antibodies specific for FER-1, we found that much of the FER-1 protein in  spermatids is cleaved into two smaller protein isoforms, and all isoforms are  missing in null mutations confirming that these proteins are indeed FER-1. The  defect in vesicle fusion in <em>fer-1</em> mutant sperm resembles the defective  vesicle fusion phenotype in muscles of patients with Dysferlin mutations.&nbsp;  Although nematode sperm cells could hardly be more different from human muscle  cells, this common defect in a fundamental cellular process of membrane fusion  suggests that the molecular function of ferlin family proteins has been conserved  throughout animal evolution.<strong></strong></p>

