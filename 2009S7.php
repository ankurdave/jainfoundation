				<p><span class="style10">Session VII: Emerging Therapies for Dysferlinopathy </span><br>


				  <br><br>

                  <strong><span class="style17">H. Lee Sweeney, PhD</span></strong><br>

                  <em>University of Pennsylvania School of Medicine, Philadelphia, PA</em><br><br>

                  <img src="images/Conf09/Sweeney.jpg" width="135"><br><br>

                  <strong>Functional Rescue of Dysferlin-Deficient Myotubes in Culture</strong><br>

H. Lee Sweeney1, Bing Jing Wang1, Becky Brisson1, Elisabeth R. Barton1, and Robert H.<br>
Brown2
1University of Pennsylvania, Philadelphia, PA, 2University of Massachusetts School of<br>
Medicine, Worcester, MA<br><br>

The drug PTC124 has been shown to allow read-through of premature stop codons(nonsense mutations) in mouse models of DMD and CF, both in cultured myotubes and in muscles in vivo. The drug also allows production of dystrophin in myotubes from DMD patients with nonsense mutations. Since a small subset of Miyoshi/LGMD2B cases harbor nonsense mutations in dysferlin, we obtained a muscle biopsy from a patient with one allele containing a nonsense mutation that leads to R1905X dysferlin. Since evidence for production of dysferlin protein does not necessarily imply a rescue of dysferlin function, we first developed an assay for functional dysferlin in cultured mouse myotubes and demonstrated that adenoviral-driven expression of dysferlin resulted in functional rescue in A/J mouse and human dysferlin-deficient cultured myotubes. We then demonstrated that exposure to PTC124 resulted in ~20% of normal dysferlin levels in myotubes of the dysferlindeficient (R1905X) patient and rescue of function in culture. While the amount of dysferlin necessary to rescue Miyoshi/LGMD2B patient muscles is unknown, the therapeutic use of PTC124 should be explored in Miyoshi/LGMD2B patients with nonsense mutations.</p>
<p><a name="2"></a>

				  <br>

                  <strong><span class="style17">Joshua Zimmerberg, MD PhD</span></strong><br>

                  <em>NICHD/National Institutes of Health</em><br><br>
<img src="images/Conf09/Zimmerberg.jpg" width="135"><br><br>

                  <strong>Insights From Membrane Biophysics Towards Finding Dietary Supplements to
Ameliorate Dysferlinopathic Muscle Wasting</strong><br>

Joshua Zimmerberg, Elena Mekhedov, Ludmila Bezrukov, Alexandr Chanturiya, Paul S.
Blank, Glen W. Humphrey
<br><br>

Mutations in the gene coding for dysferlin cause late-onset muscle wasting syndromes in humans and mice (e.g. Miyoshi Myopathy and LGMD2B). The muscle wasting disease is thought to be due to loss of a calcium-activated plasma membrane repair process, requiring dysferlin. According to lipidic pore theory, there is a critical radius for a pore (determined by both line and surface tensions) beyond which it spontaneously widens, but below which it spontaneously closes. Our hypothesis is that decreasing surface tension and increasing line tension will increase the value of the critical radius of membrane pores such that the membrane can spontaneously repair without dysferlin and repair faster with normal or defective dysferlin. Furthermore, increasing line tension should minimize the formation of microscopic holes, and if they do form, they should reseal spontaneously before significant calcium can leak into muscle cells. For therapy, we propose that dietary lipids can change muscle plasma membrane composition in ways that alter the line and surface tension of a lipidic pore. We will test this therapy paradigm by increasing the content of polyunsaturated fatty acids (PUFA) in plasma membrane lipids and measure the stability of muscle cell membranes in the absence of dysferlin. PUFA-containing lipids have negative spontaneous curvature, and they should increase membrane line tension. Increasing the PUFA content of membrane lipids could also expand membrane surface area and reduce membrane stress during muscle contraction because PUFAs exist in thinner liquid-disordered domains. Both spontaneous curvature and domain properties of PUFA-enhanced membranes are predicted to favor pore resealing. We have devised cell-based assays for membrane resealing using myogenic cell lines: GREG cells derived from the dysferlin-deficient A/J mouse and C2C12 cells (wild type). To increase the PUFA content of membrane phospholipids, myotubes are cultured with exogenous PUFAs (i.e. arachidonic acid [AA], 20:4, n-6; docasohexanoic acid [DHA], 22:6, n-3) added to the medium. The myotubes can tolerate 75 uM PUFA for up to 4 days in culture. Myotubes cultured with DHA exhibit altered morphology, indicating possible changes in membrane properties. Preliminary data suggests that incorporation of exogenous DHA alters the laser wounding threshold. We will examine the effects of PUFAs on membrane resealing using a newly developed electrophysiological assay, in the presence or absence of calcium.</p>

<p>

				  <a name="3"></a><br>

                  <strong><span class="style17">Isabelle Richard, PhD</span></strong><br>

                  <em>Laboratoire Dystrophies des Ceintures, Généthon-CNRS, Évry, France</em><br><br>

                  <img src="images/Conf09/Richard.jpg" width="135"><br><br>

                  <strong>Therapeutic Strategies for Dysferlin Deficiencies</strong><br>

William Lostal1, Marc Bartoli1, Martin Krahn2, Azzedine Bentaib1, Nathalie Bourg1, Carinne
Roudaut1, Katsuya Miyaké3, Rumaisa Bashir4, Paul McNeil3, Nicolas Levy2, Isabelle
Richard1
1Généthon, CNRS, 1, rue de l’Internationale, 91000 Evry, France, 2Hôpital d'Enfants de la
Timone and Inserm UMR910, Université de la Méditerranée, 13000 Marseille, France,
3Medical college of Georgia, Augusta, 30912, Georgia, U.S.A, 4School of Biological and
Biomedical Sciences, University of Durham, Durham, UK
<br><br>

Deficiency of dysferlin presents as two major phenotypes: Limb Girdle Muscular Dystrophy type 2B (LGMD2B) and Miyoshi Myopathy (MMD1). Dysferlin is known to participate in membrane repair, providing a preliminary understanding of the pathophysiological mechanism in these diseases. In this project, we are testing several strategies to compensate for dysferlin deficiency. The first one is the transfer of the dysferlin gene by means of an Adeno-Associated Viral vector (AAV). Considering the large size of the dysferlin cDNA, the strategy is based on splitting the dysferlin cDNA into head and tail vectors and relies on the concatemerization property of the AAV vector. After intramuscular and intravascular injection in a murine model for LGMD2B, we observed full-length dysferlin expression in muscle at the RNA and protein levels. We also showed that expression of the transgene is associated with restoration of the membrane repair potential and recovery of locomotor activity of the mice. In the second and third strategies, we are testing the capacity of a minidysferlin and of myoferlin, another member of the ferlin family, to increase the membrane repair capacity and to reverse the pathological signs in the dysferlin deficient model, respectively. Results of on-going experiments will be presented.</p>

<p><a name="4"></a><br>
<strong><span class="style17">Simone Spuler, MD</span></strong><br>

                  <em>Muscle Research Unit, ECRC, Charité, Berlin, Germany</em><br><br>

                  <img src="images/Conf09/Spuler.jpg" width="135"><br><br>

                  <strong>Peptide-Mediated Retargeting of Dysferlin</strong><br>

Simone Spuler1, Romy Siegert2, Verena Sch&ouml;wel1<br>
1Muscle Research Unit, ECRC, Charit&eacute;, 2University Medicine Berlin, Laboratory of<br>
Molecular Muscle Physiology, Max-Delbr&uuml;ck Center for Molecular Medicine<br><br>

Point mutations in DYSF can lead to misfolding of dysferlin, to aggregation of dysferlin in the endoplasmic reticulum (ER) and to amyloid formation (1,2). ERADI and ERADII appear to be involved in the degradation of aberrantly folded dysferlin (3). We aimed to prevent ER degradation of dysferlin harbouring point mutations and to retarget dysferlin to the sarcolemma. The following mutations were selected: DYSF p.G299R, DYSF p.L1341P and DYSF p.M2071V. 10mer to 15mer peptides were designed that were derived from the dysferlin sequence and corresponded to the mutated region. All peptides were covalently bound to the cell penetrating peptide TAT to facilitate entry into the muscle cell. Control peptides consisted of nonsense sequences. The experiments were performed on C2C12 cells transfected with human wildtype or mutated dysferlin and on primary human myotubes harbouring the mutations. We show that addition of TAT-labeled peptides derived from the dysferlin sequence lead to redistribution of mutated dysferlin within the myotube and that mutated dysferlin can be detected at the sarcolemma.<br>
References<br>
1. Wenzel K et al. Novel sequence variants in dysferlin-deficient muscular dystrophy leading to mRNA decay and possible C2-domain misfolding. Hum Mutat 2006;27:599-600.
2. Spuler S et al. Dysferlin-deficient muscular dystrophy features amyloidosis. Ann Neurol 2008;63:323-328.
3. Fujita E et al. Two endoplasmic reticulum-associated degradation (ERAD) systems for the novel variant of the mutant dysferlin: ubiquitin/proteasome ERAD(I) and autophagy/lysosome ERAD(II). Hum Mol Genet 2007;16:618-629.</p>

<p><a name="5"></a><br>
<strong><span class="style17">Se-Jin Lee, MD, PhD</span></strong><br>

                  <em>Johns Hopkins University School of Medicine, Baltimore, MD</em><br><br>

                  <img src="images/Conf09/Lee.jpg" width="135"><br><br>

                  <strong>Myostatin: A Molecular Rheostat for Muscle Mass</strong><br>

Simone Spuler1, Romy Siegert2, Verena Sch&ouml;wel1<br>
1Muscle Research Unit, ECRC, Charit&eacute;, 2University Medicine Berlin, Laboratory of<br>
Molecular Muscle Physiology, Max-Delbr&uuml;ck Center for Molecular Medicine<br><br>

Myostatin is a transforming growth factor-ß (TGF-ß) family member that plays a critical role in regulating muscle growth. Myostatin mutations in mice, cattle, sheep, dogs, and humans have all been shown to cause dramatic increases in muscle mass, and inhibition of myostatin activity in adult mice has been shown to induce muscle fiber growth. In recent years, our work has focused primarily on understanding the mechanisms by which myostatin activity is regulated and by which myostatin signals to target cells. We have shown that following proteolytic processing of the myostatin precursor protein, myostatin is maintained as a latent complex of the N-terminal propeptide and the mature C-terminal dimer, the latter of which is the ultimate signaling molecule. We have also shown that this latent complex is activated by proteolytic cleavage of the propeptide by members of the BMP-1/tolloid family of metalloproteases. In addition to the propeptide, a number of other proteins, including follistatin, FLRG/FSTL3, and GASP-1/2 are capable of binding the myostatin C-terminal dimer and inhibiting its activity. It is now clear that the control of muscle homeostasis is further complicated by the existence of other TGF-ß family members that seem to cooperate with myostatin to limit muscle growth. In my talk, I will summarize our recent studies providing experimental support for these regulatory mechanisms.</p>

