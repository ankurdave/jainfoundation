                  <strong>Membrane Repair Defects in Muscular Dystrophy are Linked to Altered Interaction
Between MG53, Caveolin-3 and Dysferlin</strong><br>
Noah Weisleder1, Chuanxi Cai1, Haruko Masumiya2, Miyuki Nishi2,3, Yoshihide Sunada4<br>
Shinji Komazaki5, Hiroshi Takeshima2,3, Jianjie Ma1,6.<br>
1Departments of &Dagger;Physiology and Biophysics and 6Medicine, Robert Wood Johnson Medical<br>
School, Piscataway, New Jersey 08854, 2Department of Medical Chemistry, Tohoku<br>
University Graduate School of Medicine, Miyagi 980-8575, Japan, 3Department of Biological<br>
Chemistry, Kyoto University Graduate School of Pharmaceutical Sciences, Kyoto 606-8501,<br>
Japan,4Department of Internal Medicine, Kawasaki Medical School, Kurashiki,<br>
Japan.5Department of Anatomy, Saitama Medical University, Saitama 350-0495, Japan<br><br>

Membrane recycling and remodeling contribute to multiple cellular functions, including cell fusion events during
myogenesis. We have identified a tripartite motif (TRIM72) family member protein named MG53 and defined its role in
mediating the dynamic process of membrane fusion and exocytosis in striated muscle. MG53 is a muscle-specific protein
that contains a TRIM motif at the amino terminus and a SPRY motif at the carboxyl terminus. We recently discovered that
MG53 contributes to intracellular vesicle trafficking and is an essential component of the membrane repair machinery in
striated muscle (Nat Cell Biol. 2009 Jan;11(1):56-64.) Live cell imaging of green fluorescent protein-MG53 fusion
construct in cultured myoblasts showed that although MG53 contains no transmembrane segment it is tightly associated
with intracellular vesicles and sarcolemmal membrane. Overexpression of MG53 enhanced vesicle trafficking to and
budding from sarcolemmal membrane. Co-expression studies indicated that MG53 activity is regulated by a functional
interaction with caveolin-3. Further studies show that MG53 interacts with dysferlin and Cav3 to regulate membrane
repair in skeletal muscle. MG53 mediates active trafficking of intracellular vesicles to the sarcolemma and is required for
movement of dysferlin to sites of cell injury during repair-patch formation. Mutations in Cav3 (P104L, R26Q) that cause
retention of Cav3 in Golgi apparatus result in aberrant localization of MG53 and dysferlin in a dominant-negative fashion,
leading to defective membrane repair. Our data reveal that a functional complex formed by MG53, dysferlin and Cav3 is
essential for repair of muscle membrane damage, and also provide a therapeutic target for treatment of muscular and
cardiovascular diseases that are linked to compromised membrane repair. </p>
