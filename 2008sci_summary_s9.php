                  <strong>Session IX: The Ferlin Family--Beyond Muscle</strong><br>

                  <br>

Ferlins are found in many different organisms, and are expressed in many tissues besides skeletal muscle.  The expression and functions of ferlins in other tissues and organisms may provide clues  to their roles in muscle.  This session included two presentations that highlight the role of ferlins in non-muscle tissues.  <br><br>

<strong>Dr. John Robinson</strong> (Ohio State University) presented studies of the expression of dysferlin and myoferlin in the human placenta.  In the placenta, dysferlin and myoferlin are both highly expressed in the syncytiotrophoblast.  The syncytiotrophoblast is a large polynuclear cell structure formed by fusion of cytotrophoblasts (mononuclear precursor cells), roughly analogous to the relationship between myocytes and a mature muscle fiber.  The syncytiotrophoblast is the largest polynuclear structure in the human body, having roughly the area of a tennis court in a full-term placenta.  It forms the boundary between maternal blood and the fetus.  Cytotrophoblasts are fetal cells, so a lack of dysferlin in the syncytiotrophoblast would be due to the genotype of the fetus, not the mother.<br><br>

One of the technical challenges in studying protein expression in the syncytiotrophoblast was finding a method of isolating it, since it only forms a small portion of the placenta.  Once purified, the syncytiotrophoblast tissue was found to express extremely high levels of both dysferlin and myoferlin.  Trophoblastic cell lines were found to express myoferlin, but did not express dysferlin until they were induced to fuse together.  In the syncytiotrophoblast, new cytotrophoblasts are continuously added to increase the syncytiotrophoblast’s size.  The nuclei of the newly-added cells die after a few days, and are removed from the syncytiotrophoblast though exocytosis.  Therefore, an explanation for the high expression levels of dysferlin and myoferlin in the syncytiotrophoblast may be the need for extensive membrane remodeling due to the high rate of exocytosis.  The fact that there does not seem to be a defect in gestation of dysferlin-deficient and myoferlin-deficient animals indicates that a deficiency of either of these proteins can be compensated in the placenta.
<br><br>

<strong>Dr. Barbara Wakimoto</strong> (University of Washington) discussed the Drosophila ferlin protein.  Drosophila has a single ferlin gene called <em>misfire</em>, which lacks a DysF domain.   Comparison with other insect species indicates that the ancestor of current insects had two ferlin genes, one of which Drosophila lost in the course of evolution.  <em>Misfire</em> was originally identified in a search for genes whose mutations cause male infertility.  The <em>misfire</em> gene encodes multiple mRNA transcripts, including four expressed in males and three in females.  Interestingly, some of the transcripts predict isoforms lacking a transmembrane domain.  In females, <em>misfire</em> regulates dorsal/ventral egg patterning during oogenesis.  From an analysis of mutants, it appears that the long isoform (containing five C2 domains) is necessary for male fertility.  The isoform necessary for egg patterning in females contains the last two C2 domains, C2E (mutated in the SJL mouse) and C2F, as well as the transmembrane domain.<br><br>

In Drosophila, fertilization is different from that of mammals in that it does not involve a membrane fusion event.  Instead, the sperm enters the egg intact and sperm plasma membrane breakdown (PMBD) must occur to allow the sperm nucleus to participate in development.  <em>Misfire</em> and another highly conserved protein called <em>Sneaky</em> are essential for PMBD and both are expressed in the acrosome, a vesicle located at the tip of the sperm head.  The co-localization of the proteins and similarities in their mutant phenotypes suggest a signaling role for these acrosomal membrane proteins in PMBD.  Like dysferlin, these proteins are proposed to function in mediating membrane-membrane interactions.  It would be interesting to know if dysferlin, like the Drosophila ferlin, is associated with a member of the Sneaky protein family in humans.  

