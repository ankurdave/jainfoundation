                  <strong>Session V: Muscle Satellite Cells</strong><br>

                  <br>
Talks in this session focused on methods to study disease pathology and the muscle regenerative process in dysferlinopathy by making use of muscle progenitor cells.<br><br>

<strong>Gillian Butler-Browne</strong> (France) described the generation of immortalized cell lines from primary cells of dysferlinopathy patients. These cell lines will help overcome the bottlenecks posed by limited availability of primary patient cells, and provide an inexhaustible source of cells harboring different pathogenic dysferlin mutations. Expression of telomerase is sufficient to immortalize fibroblasts but not myoblasts, indicating fundamental differences in how senescence is achieved in these two cell types. Senescent myoblasts have high p16 and cyclin D1 activity, and low levels of phospho-Rb. Further studies showed that myoblast immortalization can be achieved by the combined expression of telomerase and inhibition of the p16 pathway through expression of cdk4 (Zhu et al. 2007). Importantly, the immortalized myoblasts retain the ability to differentiate and form normal myotubes in culture. Dr. Butler-Browne is currently working to immortalize fibroblasts and cells from muscle biopsies from 3 dysferlinopathy patients. <br><br>

<strong>Terence Partridge</strong> (USA) described the use of thymidine analogues, BrdU and EdU, for tracking and quantifying loss and regeneration of muscle fibers in mouse models of muscular dystrophies. The technique involves administering the thymidine analog to the animals twice daily during the first post-natal week so that it is incorporated into many myonuclei. BrdU-labeled nuclei will be diluted by addition of non-labeled nuclei during growth and will be lost from fibers that degenerate and regenerate. The frequency of BrdU labeled myonuclei therefore gives an estimate of muscle fiber growth and degeneration. Labeling levels in pathological versus wild-type muscle can be used to monitor muscle degeneration during disease progression in vivo and to test the effects of therapeutic interventions. Another potential application is administering the label prior to an acute muscle injury to measure the robustness of the regenerative process. Dr. Partridge has shown that mdx (dystrophin-deficient) mice almost entirely lose the BrdU signal by 50 days of age, in contrast to wild type controls, suggesting rapid myonuclear turnover and massive muscle degeneration in these dystrophic mice. Similar studies are planned with dysferlin-deficient mice. <br><br>

<strong>Thomas Rando</strong> (USA) described the generation of reporter mice that can be used to follow disease progression through non-invasive whole animal imaging and serum-enzyme monitoring. Luciferase and Alkaline Phosphatase are expressed using a constitutive promoter and an IRES sequence, respectively, in one mouse strain (LUSEAP mice) (Nishijo et al. 2009). The promoter is immediately followed by a transcriptional stop signal that can be floxed out in a regulated manner upon conditional expression of Cre. Another set of animals express Cre under the control of either the Pax7 promoter (satellite cell-specific; Pax7-Cre-ER mice) or the Myf6 promoter (mature muscle fiber-specific; Myf6-Cre-ER mice) (Brack et al. 2007). Both the Pax7 and Myf6 promoters are tamoxifen-inducible. Crossing the LUSEAP and Cre mice generates animals in which the luciferase enzyme can be expressed in satellite cells or mature muscle fibers upon administration of tamoxifen. This enables tracking of muscle regeneration (as luciferase-expressing satellite cells fuse with muscle fibers) or degeneration (as luciferase-expressing mature fibers are lost) using whole animal imaging. These reporter strains are being crossed to dysferlin-deficient SJL mice in order to study muscle loss and regeneration in dysferlin deficiency.


