                <td valign="top"><p class="section">Past Research Projects - Gene Therapy </p>

                    <p class="home"><strong><font color="#0175C2"><span class="home style2">Gene Therapy with Mini-Dysferlin</span><br>

                    </font></strong>Robert Brown, MGH, Harvard Medical School (Boston, MA)                    

                      <br>

                      <br>

                       <strong>Project Objective</strong><br>

                       Explore dysferlin gene targeting as

                         a strategy towards a cure.<br>

                         <br>

                       The dysferlin protein domains that mediate function are

                       not yet defined and the associated pathway is not well

                       understood. New methods for intravascular and systemic

                       delivery of some viral vectors (e.g. AAV6, AAV8) make

                       gene therapy an attractive option. To overcome the limited

                       size carrying capacity of these promising vectors, it

                       is necessary to generate a partial but functional form

                       of the Dysferlin gene. The three steps are therefore -

                       creating partial proteins, testing to see which one is

                       best and then using that to treat a mouse model for the

                       disease. The approach uses partial dysferlin protein for

                       studies to see if it moves locates and binds to the same

                       areas within the cell as the full protein. In vitro studies

                       will determine if this partial protein retains the same

                       biochemical properties as the full protein and functions

                       like it to repair cell membrane. Finally, the studies

                       will test its ability, when packaged in the muscle-tropic

                       vectors AAV6 and AAV8, to restore membrane repair in vivo

                       in an established Miyoshi mouse model. Finally, the studies

                       will determine the feasibility of and prove the efficacy

                       of gene delivery in mouse models. If successful, the experiments

                       will be repeated in other higher vertebrates before clinical

                       trials in humans.

