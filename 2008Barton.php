                  <strong>Physiological Assessment of Murine Muscles Lacking Dysferlin</strong><strong>&nbsp; &nbsp;</strong><br>

E.R. Barton, Z. Tian, E. Blanco, B. Tatman, and H.L. Sweeney<br><br>

Mouse lines with dysferlin deficiency have become the accepted models for Limb Girdle Muscular Dystrophy 2B and Miyoshi Myopathy (Ho et al, 2004).  They have been utilized to understand the progression of this disease and to evaluate potential therapies to counter the loss of dysferlin.  However, the disease progresses slowly in the mice, inhibiting rapid screening of therapies given the lack of overt pathology.  Histologic characterization has shown that the abdominal muscles of the A/J mouse provide the most severe pathology, yet this muscle (the rectus abdominus) is difficult to use for functional measurements due to its fiber orientation and muscle architecture.  Therefore, the goal of this study is to define a functional signature for the skeletal muscles in the A/J mouse.  Isolated force measurements were performed in the limb muscles (extensor digitorum longus (EDL) and soleus), and the diaphragm from 12 and 36 week old mice (N=5 animals per group).  Because membrane resealing appears to be a primary defect, the susceptibility to eccentric contractile injury was also assessed.  Results of these measurements show that the EDL has normal specific force at both 12 and 36 weeks, whereas the diaphragm has significant and apparent functional deficits.  Immunoblotting for dysferlin in wildtype (C57BL/6) mice revealed that the EDL muscle has little detectable dysferlin compared to other mouse muscles, and the diaphragm has high levels.  This suggests that studies involving the AJ mouse should focus on muscles that normally express high levels of dysferlin where there is apt to be more pathology, instead of those with low dysferlin levels.  We anticipate that these studies will provide a basis for evaluating any potential therapies in terms of muscle strength and fragility.


