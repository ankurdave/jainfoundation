                  <strong><span class="style17">Michael Sinnreich, MD, PhD</span></strong><br>

                  <em>McGill University, Montreal, Canada
</em><br><br>

                  <img src="images/Conf09/Sinnreich.jpg" width="135"><br><br>

                  <strong>Dysferlin: From Bedside to Bench and Back</strong><br>
<br>
Our work on dysferlin was inspired by a LGMD2B family in which two severely affected
sisters where homozygous for a dysferlin null mutation. Their mildly affected compound
heterozygous mother harbored, in addition to one null allele, an in-frame exon-skipping
mutation due to a novel lariat branch point mutation. Despite the lack of a functional C2D
domain, the mother’s internally truncated dysferlin molecule appeared to partially
complement the null mutation, suggesting that certain dysferlin domains may be
dispensable. This observation led us to study the biochemical function of individual
dysferlin C2 domains and to characterize their lipid binding specificity. To better understand
the cellular function of dysferlin, we set out to identify novel protein binding partners. Using
affinity purification followed by liquid chromatography/mass spectrometry, we have identified
a number of proteins in skeletal muscle, which showed association with dysferlin. The
newly identified proteins fall into categories of surface membrane proteins, proteins involved
in cellular trafficking and in protein degradation. Mapping experiments are ongoing to determine which dysferlin domains
are important for these protein interactions. The insight gained will allow the identification of dysferlin domains that would
be dispensable in small recombinantly generated mini-dysferlin molecules, suitable for AAV mediated gene transfer. I
will outline the progress of our laboratory in those studies.</p>

<p><br>

				  <a name="4"></a>

                  <strong><span class="style17">R. Bryan Sutton, PhD</span></strong><br>

                  <em>Texas Tech University, Health Sciences Center, Lubbock, TX</em><br><br>

                  <img src="images/Conf09/Sutton.jpg" width="135"><br><br>

                  <strong>3D Structure of Human Dysferlin: One Piece at a Time</strong><br>
<br>
Dysferlin is a 237kDa multi-domain protein that is expressed at high levels in muscle tissue, and appears to play a critical role in resealing tears in the muscle cell phospholipid membranes. Mutations within the dysferlin gene can result in Limb-Girdle muscular dystrophy (LGMD2B) in humans, presumably resulting from an impaired ability to reseal damaged muscle tissue. While there is a considerable amount of biological data compiled for dysferlin, little structural or biophysical information has been collected. Our long-term plan is to solve the structure of dysferlin from the smallest folding unit (the individual C2 domain) to the largest ensemble of domains. Dysferlin is made up of 7 C2 domains(labeled C2A-C2G), which are evenly distributed throughout the cytoplasmic part of the protein. Interestingly, some of these domains share structural similarity to the Type I C2 domains of synaptotagmin, while others share a topology with Type II C2 domains. The significance of this secondary structure arrangement is unknown. We have also discovered that there is a large central domain that shares significant sequence similarity with glutamine synthetase. If this sequence relationship is accurate, then dysferlin could form into a homo-hexameric structure in the presence of glutamine much like glutamine synthetase. We plan to solve the X-ray crystal structure of each C2 domain and to investigate the putative hexameric region of dysferlin. Each resulting structure will be docked into an EM reconstruction of the entire cytoplasmic region of the protein to yield a pseudo-atomic view of the entire dysferlin protein. This information will be essential to understand how dysferlin functions in the cell, and how to best design therapies to treat the disease.</p>

