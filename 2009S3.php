				<p><span class="style10">Session III: Dysferlin Structure and Function </span><br>


				  <br><br>

                  <strong><span class="style17">Silvère M. van der Maarel, PhD</span></strong><br>

                  <em>Leiden University Medical Center, Leiden, The Netherlands
</em><br><br>

                  <img src="images/Conf09/vanderMaarel.jpg" width="135"><br><br>

                  <strong>Large Scale Proteomic Analysis of Dysferlin Complexes</strong><strong>&nbsp; &nbsp;</strong><br>
Antoine de Morrée1, Paul Hensbergen2, Rune R. Frants1, Silvère M. van der Maarel1
1Department of Human Genetics, and 2Department of Parasitology, Leiden University
Medical Centre, Leiden, The Netherlands<br>
<br>

Mutations in dysferlin were identified as the cause for LGMD2B and Miyoshi Myopathy. In skeletal muscle dysferlin is predominantly found at the muscle membrane where it is believed to play an active role in patch-fusion repair. A dysfunctional membrane repair system was therefore postulated as a pathogenic mechanism for LGMD2B. However, several studies have highlighted that dysferlin is a dynamic protein whose localization depends on the conditions studied. We hypothesize that a comprehensive insight into the dynamics of dysferlin and its complex partners is crucial to understanding dysferlin protein function. Previously we have shown that by making use of two independent affinity binders specific for dysferlin and derived from the llama heavy chain antibody repertoire, we could reproducibly immunoprecipitate dysferlin protein complexes from skeletal muscle. This allowed us to identify two partners in the dysferlin protein complex – AHNAK and calpain 3- as well as their regulation. To further scrutinize the dysferlin protein complex we are currently refining our immunoprecipitation protocols to accommodate the biologically relevant conditions that govern dysferlin behaviour. For this we use myogenic cell lines (C2C12 and IM2) as a model system. As recent studies confirmed the presence of dysferlin on non-skeletal muscle cells we have in parallel begun to identify additional sources of dysferlin protein complexes (cardiomyocytes and monocytes) and are optimising immunoprecipitation protocols and subsequent mass spectrometry analyses for these specific dysferlin sources as well. Thus, by deploying our immunoprecipitation strategy on a large scale we aim to increase our understanding of the dysferlin associated protein complex in a spatiotemporal context.


                </p>
<p><a name="2"></a>

				  <br>

                  <strong><span class="style17">Sandra Cooper, PhD</span></strong><br>

                  <em>Institute for Neuromuscular Research, Children’s Hospital at Westmead, Sydney, Australia</em><br><br>

                  <img src="images/Conf09/Cooper.jpg" width="135"><br><br>

                  <strong>C2C Regulates Stable Anchorage of Dysferlin at the Plasma Membrane.</strong><strong>&nbsp; &nbsp;</strong><br>
Rachel A. Peat1, Frances J. Evesson1,2, Angela Lek1,2, Fabienne Turville1, Marko J.
Nykanen3, Ross A. Boadle3, Russell C. Dale1, Kathryn N. North1,2, Sandra T. Cooper1,2
1Institute for Neuromuscular Research, Children’s Hospital at Westmead, 2Discipline of
Paediatrics and Child Health, University of Sydney, 3Institute of Clinical Pathology &
Medical Research, Millennium Institute, Westmead, Sydney, Austalia.<br>

<br>
Although it is clear that dysferlin is essential for rapid calcium-mediated membrane resealing, very little is known regarding how dysferlin performs this role. It is widely believed that dysferlin functions as a calcium-regulated fusogen at the plasma membrane, playing a central role in vesicular-mediated membrane resealing. Thus, our research has focused on defining the plasma membrane biology of dysferlin, which can then be used as a template to examine the consequences of patient-derived mutations, and as a reference to examine the roles of calcium and C2 domains upon dysferlin trafficking and membrane biology. We have shown that under normal conditions, dysferlin resides at the plasma membrane of cultured myotubes for around 3-4 hours. Our data establishes that dysferlin is a relatively transitory plasma membrane protein; perhaps accounting for its defective membrane localization in the skeletal muscle of many patients with muscular dystrophy. Studies of a severely truncated dysferlin construct, C2F-TM (bearing only the most C-terminal C2 domain and transmembrane domain), reveal that elements within the dysferlin N-terminal domain regulate stable PM anchorage. C2F-TM has a much more transitory life at the plasma membrane of only 1 hour, and is rapidly endocytosed and degraded. If dysferlin is indeed required at the plasma membrane to perform its role in membrane repair, factors or domains influencing dysferlin plasma membrane expression are relevant to its role in membrane resealing. Thus, we have studied a suite of dysferlin expression constructs, each bearing deletion or mutation of various C2 domains, and identified a particular role for C2C in modulation of dysferlin plasma membrane expression. We are now studying the nature of interaction mediated by C2C, and the relationship between the abnormal plasma membrane biology of C2C mutants to their functional role in membrane resealing.
				  
				  
				  
				  
				  
				  
				  </p>

<p><br>

