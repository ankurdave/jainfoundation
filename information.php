<p>The Jain Foundation conference on Dysferlin is dedicated solely to research on dysferlin and LGMD2B / Miyoshi muscular dystrophy. Its goal is to bring scientists together for critical discussions of research results, brainstorming sessions on new research and treatment ideas, and the development of new collaborations.</p>

<h2>Speakers</h2>

<p>Researchers Funded by the Jain Foundation will present their most recent work. Additional speakers are by invitation only. Scientists interested in attending the conference, but not presenting, should contact the Jain Foundation, and will be considered on a case by case basis.</p>

<h2>Posters</h2>

<p>A limited number of abstracts will be selected for poster sessions during the conference. Post-doctoral and graduate students may submit abstracts, following the guidelines below. There is no registration fee, but accepted post-doctoral and graduate students must cover their own travel and lodging costs. Postdoctoral and student attendees are expected to share a room unless they specifically request otherwise. The Jain Foundation will only cover the cost of meals during the conference.</p>

<p>Guidelines for submission:</p>

<ol>
	<li>Abstracts must be related to dysferlin, dysferlinopathy or our goal of a therapy for LGMD2B/MM.</li>
	<li>Abstracts can cover either experimental results OR a proposal for future experiments. (We encourage students who are interested in dysferlin, but have not yet worked on it directly, to present proposals for applying their previous work to the study or treatment of dysferlin deficiency.)</li>
	<li>The deadline for abstract submission is Tuesday, March 31st, 2009. Please email them to <a href="mailto:plavimittal@jain-foundation.org">plavimittal@jain-foundation.org</a>. Abstracts should be no more than one page long.</li>
	<li>You will hear back from us about whether your abstract has been accepted by Friday, April 10th, 2009.</li>
</ol>

