                  <img src="images/conference2008/Jaiswal.jpg" width="135"><br><br><br>
                  <strong>Identification of Cell Wound Healing Pathways Relevant to Muscular Dystrophy</strong><strong>&nbsp; &nbsp;</strong><br>


Jyoti K. Jaiswal1, Arun B. Deora2, Shivaprasad  Bhuvanendran1, Haiteng Deng1 1)The Rockefeller University, 2)Weill Medical College  of Cornell University, New York, NY 10065<br>
<br>

Several myopathies are associated with the inability of wounded muscle cells to heal.  Defects in dysferlin are an underlying feature of such myopathies.  We have shown that the inability to heal wounded cells also occurs in Miyoshi-like myopathy patients with no dysferlin defect.  Thus poor healing of wounded cells may be a feature common to all Miyoshi-like myopathies, raising the possibility that modifiers of the dysferlin-mediated membrane repair pathway or dysferlin-independent membrane repair pathways may be involved in healing of muscle cells.  Identifying such pathway(s) would help in rational design of therapeutic approaches for these myopathies.  We have undertaken a combined cell biological and proteomics approach to identify cellular compartments and molecules involved in healing muscle cells in culture.  A proteomics approach identified annexin A2 (Anx2) as a marker for a compartment potentially involved in cell membrane repair. We find that lack of Anx2 compromised the ability of wounded muscle cells to heal and caused poor translocation of dysferlin to the cell membrane, indicating Anx2 is not merely a marker but also plays a role in healing wounded muscle cells. Real-time imaging of muscle cell healing further supports a role for Anx2 as a modulator of dysferlin-mediated membrane repair. These results validate the utility of our approach for identifying novel regulators of muscle cell membrane repair pathways and provide a potential mechanism by which Anx2 may regulate dysferlin-mediated membrane repair.                </p>

