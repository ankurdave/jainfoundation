				<p><span class="style13">SCIENTIFIC SUMMARY </span><br>

				<br>

				  <a href="2007sci_summary.php" class="style16">&lt;-- back</a> <br>

				  <br><br>

                  <strong>Session II: Dysferlin Gene Therapy</strong><br>

                  <br>

                  The Dysferlin Gene Therapy session discussed recent advances in methods for delivering genes to muscle. The gene therapy approach is similar for most muscle-targeted genes, but dysferlinopathy faces a few specific challenges due to the large size of the dysferlin gene and also due to the repair defect in dysferlin-deficient cells, which could increase the toxicity of some gene delivery methods. <br>

				<br>

				There are two broad categories of gene delivery: viral and non-viral. Adeno-associated virus (AAV) is the most promising viral vector for gene therapy, but it still faces a number of challenges, including the limited DNA packaging capacity of the virion and 

				the immune response to the viral capsid that is illicited in recipients. There are also many non-viral gene delivery methods,  which involve transient generation of pores in the target cell membrane. These pores could be more toxic to cells with a structural or repair defect (such as dysferlin-deficient cells) than to normal tissues.  Whatever the method of delivery, all gene replacement approaches may also trigger an immune reaction in patients to the new transgene product. <br>

				<br>

				Dr. Jude Samulski discussed  work that has led to a clinical trial with AAV in Duchenne Muscular Dystrophy patients. Pre-clinical studies were done in mdx mice (treated with AAV carrying mini-dystrophin) and TO-2 hamsters (treated with AAV carrying delta-sarcoglycan). Treated animals had both increased functional ability and increased lifespan. Dr. Samulski also engineered an improved AAV vector (AAV2.5) by modifying 5 amino acids of the AAV2 capsid protein to match the AAV1 capsid protein, enough to convey muscle tropism to the modified vector. AAV2.5 has a distinct immune profile from either AAV1 or AAV2, so that it can be used even in patients who have received previous AAV treatments. Additional modified vectors are being developed to facilitate repeat treatment of patients. For the Phase 1a trial, 6 DMD patients were injected intramuscularly in one arm  with vector containing mini-dystrophin and in the other arm with empty capsids, in order to clarify whether any observed immune response is due to the capsid or to the transgene product. Data from this trial will soon be available. In preparation for a Phase 1b trial with full limb infusion, AAV carrying the canine mini-dystrophin was infused into a single hindlimb of the GRMD dog model, resulting in restoration of the dystrophin glycoprotein complex to the membrane of muscles in the treated limb. <br>

				<br>

				Dr. Isabelle Richard discussed an approach to overcome the DNA size limitation for packaging into AAV vectors. Multiple AAV genomes that infect the same cell can concatemerize using  inverted terminal repeats at the ends of the genome. If the dysferlin cDNA (a total of 6.9kb) is split into two halves, each of which can fit into an AAV vector, concatemerization may result in the full-length gene being integrated into the recipient chromosome. When two vectors prepared along these lines were tested by IM injection into A/J mice, both full length dysferlin and a smaller product were produced. The effect of this smaller product is not known, and Dr. Richard is currently working to eliminate its expression. She is also in the process of generating an improved mouse model for dysferlin deficiency by back-crossing the A/J mutation onto a Bl6 background. These mice have a slightly more severe phenotype than the original A/J mice. In response to questions following her talk, Dr. Richard mentioned that she is using the C5-12 muscle-specific promoter, which gives a low level of expression that should not have negative effects on the muscle. <br>

				<br>

				Dr. Michele Calos discussed ways to achieve stable expression of genes  delivered by non-viral methods. Genome integration of the transgene is important even in post-mitotic cells, because it often improves the level of expression and because the expression plasmid can otherwise be silenced or lost over time. Her approach is based on co-delivery of a plasmid containing PhiC31 integrase and a plasmid containing the transgene with an attB integration site. Integration facilitated by PhiC31 is site-specific (using pseudo-attP sites in the genome), gives fewer integrated copies of the transgene than does random integration, and has been successful at integrating the full-length dystrophin  gene into mouse muscle after electroporation into hindlimbs. Dr. Calos is in the process of using this integration method for non-viral ex vivo modification of autologous mesoangioblasts from dystrophic mice. In response to questions following her talk, <span id="q_1143c9ec065b782c_1">Dr. Calos mentioned that integration can be targeted to muscle  cells either by expressing the integrase under a muscle-specific  promoter, or by using a DNA delivery method that is highly targeted to  muscle.</span> She has also not observed any immune reaction in mice to the integrase protein. <br>

				<br>

				Dr. Daniel Scherman discussed the electrotransfer method of non-viral gene delivery and its applicability to dysferlin deficiency. Large cells (such as myotubes and fibers) can be permeabilized by a lower electric field strength than small cells (such as satellite cells). Dr. Scherman hypothesized that an even lower E field might be sufficient in the case of dysferlin deficiency because the cell membrane is already somewhat permeable. In wild type mice, electrotransfer into muscle leads to robust local expression of reporter genes. There is some toxicity (nuclear centralization and infiltration from immune cells), which heals within a month. This toxicity could be more serious in the case of dysferlin deficiency, but in the discussion following Dr. Scherman's talk it was mentioned that repair of electropores is calcium-independent and probably does not require dysferlin. Electrotransfer has also been used successfully to deliver dystrophin and lamin alpha2 to diseased muscle. Dr. Scherman proposed that  the localized nature of this technique may be beneficial for delivering muscle-stimulating growth factors (such as IGF-1), which normally act locally. Recently, electrotransfer has been used to deliver the follistatin gene to mdx mouse muscle, resulting in increased muscle mass and improvement in myoblast transplantion to the treated muscle.<br>

				<br>

				During the brainstorming session, an important topic of discussion was how to avoid the immune response in patients both to the viral capsid (in the case of viral delivery) and to the transgene product. Reaction to the transgene product can be severe in patients whose mutations in their endogenous gene have completely prevented its expression. In that case, it may be possible to tolerize  patients to the transgene product prior to administering therapy. This is done in the case of hemophilia, for example. <br>

				<br>

				Gene therapy can be done either in vivo or as ex vivo modification of progenitor cells. If done in vivo, it is important to consider whether the vector or  method used for delivery targets satellite cells as well as mature muscle fibers. Ex vivo modification of progenitor cells faces the same challenges discussed in the Stem Cell Therapy session, including what cell population to use and how to expand the population in culture. In fact, modification of an autologous  population in culture often makes the cells even more difficult to expand than an unmodified, allogenic population. One of the participants suggested avoiding clonal populations for ex vivo modification, because they are particularly hard to expand and  differentiate too quickly. <br>

				<br>

				There was also some discussion of the relative merits of non-viral delivery techniques, particularly with respect to the amount of damage that they produce in recipient cells. While electropores can generally seal on their own, it is not known whether hydropores are similar in size or whether they require cellular repair mechanisms. Finally, it was mentioned that hydrodynamic delivery could potentially be improved by treatment with histamines. 

