                  <strong>Transgenic Overexpression of SERCA1 Mitigates the Dystrophic Phenotype
Observed in the Delta Sarcoglycan Null Mice</strong><br>

Sanjeewa A. Goonasekera, Michelle Sargent, Jeffery D. Molkentin<br><br>

Muscular dystrophy commonly refers to a clinically and genetically heterogeneous group of
degenerative muscle disorders characterized by progressive muscle weakness. Although
the primary defect likely results from a loss of sarcolemmal integrity, the secondary
molecular mechanisms leading to myofiber degeneration is widely debated. One
hypothesis suggests that elevated cytosolic calcium is the initiating event for many
downstream sequelae resulting in myofiber necrosis. In this study we demonstrate the
dystrophic phenotype observed in the delta sarcoglycan null (DSG) mouse is significantly
improved by skeletal muscle-specific overexpression of the SR Ca2+ ATPase (Serca1).
Histological and biochemical markers of muscular dystrophy were significantly reduced in
DSG/Serca1 Tg mice. Specifically, quadriceps, diaphragm and the soleus were analyzed for percent central nucleation
and fibrosis. At 3 months, up to 75% reduction in fibers with central nuclei was observed in DSG/Serca1 Tg mice
compared to DSG mice. A similar decrease in fibrosis was also observed in the same muscle groups. In addition, serum
creatine kinase levels were also significantly reduced in DSG/Serca1 Tg mice compared to the DSG mice. Previous
studies have demonstrated decreased voltage gated calcium transients and prolonged time constant of decay in flexor
digitorum brevis (fdb) fibers isolated from dystrophic animal models. Using a similar approach we demonstrate a
significant increase in the amplitude of action potential-induced calcium release and reduction in the decay time constant
in DSG mice over expressing SERCA1 compared to DSG mice. Our results demonstrate that enhanced SR calcium
ATPase activity improves both biochemical and histological evidence of muscle damage observed in dystrophic muscle
by presumably increasing the net removal of calcium from the cytosol coupled with more efficient sequestration of
calcium in the SR. We are currently investigating the potential application of the Serca1 Tg mouse model to alleviate the
histopathology observed in other models of muscular dystrophy, such as the dysferlin knockout mice. </p>

