                  <strong>Session VI: Gene Therapy</strong><br>

                  <br>

The “Gene Therapy” session focused on the different therapeutic strategies that are being explored to restore dysferlin function in patients with LGMD2B and MM.  Gene delivery strategies include intramuscular injection, systemic delivery via the blood, or ex vivo gene therapy of progenitor cells followed by reimplantation into the patient.  The session also examined different delivery vehicles, ranging from viral delivery to direct injection of DNA.  The purpose of this session was to invoke discussion on potential issues arising from genetic therapy that specifically relate to the dysferlinopathies.  These issues include how to achieve sufficient and sustained dysferlin expression, how to express dysferlin in the right cell types (e.g. immune cells & muscle cells), how to circumvent the issue of the large size of the dysferlin gene, and whether an immune response to the delivery agent (e.g. virus) and/or to the newly expressed dysferlin might be an issue. <br><br>

<strong>Dr. Isabelle Richard</strong> Summary Pending.<br>
<br>


<strong>Dr. Michele Calos</strong> discussed two different strategies to restore dysferlin expression in muscles.  In both strategies, her laboratory takes advantage of the phiC31 integrase system, a site-specific recombinase, to incorporate the dysferlin gene stably into the genome of the recipient cells.  Initial experiments with hydrodynamic injections into the saphenous vein yielded limited expression of GFP in the muscle fibers.  The efficiency of delivery, as measured by luciferace imaging in the mice as well as immunofluorescence staining, was significantly higher when naked DNA was directly injected into the hindlimb muscles and followed by electroporation. The second strategy being used in Dr. Calos' lab is cell therapy transplantation with mesoangioblast cells, which are multipotent, self-renewing stem cells that can differentiate into skeletal muscle, smooth muscle, osteoblasts, adipocytes and cardiomyocytes in vitro and can migrate to sites of muscle degeneration within the body.  Dr. Calos' lab has shown the transfectability of these mesoangioblasts and stable DNA integration mediated by the phiC31 integrase.  They further showed the capacity of the modified mesoangioblasts to undergo differentiation into myotubes in vitro.  Experiments with mdx mice injected with phiC31 Integrase-modified mesoangioblasts in mdx mice showed expression of luciferase in the hindlimb of the mice, and, more importantly, correctly localized dystrophin expression in the muscles.  Future studies will focus on the delivery of dysferlin to dysferlin-null mesoangioblasts, with their subsequent transplantation into dysferlin-null mice.<br>
<br>

<strong>Dr. Nicolas Lévy</strong> presented his work on the identification and characterization of dysferlin mutations.  Primary dysferlinopathies have a large mutational spectrum.  Analysis of the dysferlin gene has revealed no mutational hot spots, with most disease causing mutations being missense or frame-shift mutations, and about 20-25% being nonsense mutations. <br>
Dr. Levy described a patient with atypical and moderate primary dysferlinopathy where the patient still produced a mini-dysferlin protein. In this patient, there is a large multi-exonic genomic DYSF deletion (exons 2 to 40).  The truncated protein is 73 KDa in size, and detectable on the plasma membrane in blood monocytes (by IF, FACS and WB).  This truncated dysferlin is at least partially functional in the patient as well as in a membrane repair assay.  Since the size of the truncated dysferlin’s cDNA is within the packaging-size of AAV-vectors, it might provide a therapeutic alternative for gene therapy to full length dysferlin. Importantly, the partial functionality of this mini-dysferlin molecule demonstrates partial modularity of Wild-type dysferlin, which is an essential pre-requisite for exon-skipping strategies in dysferlinopathies.<br> 
Dr. Levy also talked about the DYSFonCHIP project, where he is using CGH and re-sequencing arrays to identify mutations in DYSF and related partners. CGH is a powerful tool which he showed can be used to detect several types of dysferlin mutations, including the previously described heterozygous large deletion (ex. 2-40), as well as a short homozygous deletion.  Sequence capture arrays detect point mutations and small insertion/deletions. Currently dysferlin mutational analysis is an expensive and time consuming task that often fails to find both pathogenic mutations.  The use of the CGH and Sequence capture arrays could eliminate these issues by making mutational analysis cheap, accurate, and quick.  The use of these arrays has the potential to revolutionize the way mutational analysis is done for dysferlin, as well as many other genes.
