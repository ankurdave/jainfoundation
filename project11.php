                    <p class="home"><strong><font color="#0175C2"><span class="style1">Calcium Regulation</span> <br>

                    </font>Calcium influx as a common mechanism   between Duchenne and Miyoshi myopathy<font color="#0175C2"><br>



                    </font></strong>Jeffery D. Molkentin, Children&rsquo;s Hospital,   University of Cincinnati (Cincinnati OH)

                    <br>

                        <br>



                         <strong>Project Objective</strong><br>

                         Determine if calcium influx itself   is the primary disease initiating stimulus in skeletal muscle fibers   in both Duchenne and Miyoshi myopathy<br>



                          <br>

                          Muscular dystrophy is a disease comprised   of wide ranging mutations in dozens of genes, such as in dystrophin   (Duchenne) or dysferlin (Miyoshi).&nbsp; In recent years it has become   apparent that many of these mutant genes function within a similar pathway   or overall biologic process that appear to safeguard the cell membrane   of an individual muscle fiber.&nbsp; The outer membrane of a skeletal   muscle cell provides a critical barrier function that keeps intracellular   molecules in, and extracellular molecules out, the most destructive   of which is probably calcium.&nbsp;&nbsp; Here we propose that this   barrier function is absolutely necessary to maintain the health of a   skeletal muscle cell, and that the loss of this barrier function permits   calcium entry that initiates myofiber degeneration, leading to muscular   dystrophy.&nbsp; We will evaluate this concept in vivo by generating   genetically altered mice that have greater calcium entry through specific   channels to possibly induce disease, or mice with enhanced calcium clearance   as a means of blocking muscular dystrophy in established mouse models   of disease due to dystrophin and dysferlin deficiencies.<br>



