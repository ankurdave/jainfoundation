                      <strong><font color="#0175C2"><span class="style1">Dysferlin's function in the Sea Urchin Model System </span><br>

                        </font></strong>Steven S. Vogel, NIAAA, National Institutes of Health (Rockville, MD)<br>

                      <br>

                      <strong>Project Objective</strong><br>

                      To determine if anti-sense dysferlin morpholinos can "knock-out" the  sea urchin homologue to dysferlin and thus influence either the repair of  plasma-membrane disruptions and/or membrane fusion events.<br>

                      <br>

                      Limb-Girdle muscular dystrophy-2B and Myoshi Myopathy, another form of muscular  dystrophy, are both caused by mutations in the dysferlin gene. One of the major  road-blocks for developing therapeutic approaches for managing these maladies  is knowledge of the cellular function of the dysferlin gene product. Dr. <span id="st" name="st">Vogel</span>'s group is applying biophysical methods to study dysferlin function in  living cells. They are particularly interested in investigating the  acknowledged role of dysferlin in cell surface wound repair and membrane  fusion. His strategy is to use molecular methods to either knock-down or  over-express dysferlin as well as other proteins of interest in sea urchin  eggs. Developing sea urchin eggs have been extensively used as a model system for  studying early development, calcium triggered membrane fusion, endocytosis, and  membrane wound repair. His group monitors these cellular functions in real-time  using two-photon laser-scanning fluorescence microscopy in both normal  developing embryos and those that have been experimentally manipulated to  under-express or over-express specific proteins.<br>

