                <td valign="top"><p class="section">Research Projects - Dysferlin structure/function </p>

                    <p class="home"><strong><font color="#0175C2"><span class="style1">Structure/function relationship of human dysferlin and its role in Limb-Girdle muscular dystrophy</span><br>

                    </font></strong>R. Bryan Sutton, Texas Tech University Health Sciences Center, Lubbock TX <br>

                        <br>

                         <strong>Project Objective</strong><br>

<br>
In many cases, point mutations within one of the seven C2 domains of dysferlin are sufficient to cause disease. Highresolution structures of each domain will be necessary to understand the etiology of the
mutations that result in LGMD-2B. Structural and biophysical characterization of the different domains of dysferlin will be essential in understanding the function of both wild-type and mutant dysferlin. We are approaching this problem by pursuing two aims. In the first aim, we will clone and characterize the individual
domains of dysferlin. In the second aim, we will analyze the structures of these domains using X-ray
crystallography for the individual domains and electron microscopy (EM) for the full-length
protein. The elucidation of the structure of the C2 domains,GAP3 and the entire molecule will substantially contribute to our understanding of how dysferlin works to repair muscle tissue and how this function is compromised in LGMD. By understanding the structure/function relationship between the various domains of dysferlin, we will be poised to fully understand the disease and perhaps to design potential pharmacologic
treatments for Limb-Girdle muscular dystrophy.
<br>

