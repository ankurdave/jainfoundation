                <td valign="top"><p class="section">Research Projects - Non-invasive Imaging </p>

                    <p class="home"><strong><font color="#0175C2"><span class="style1">Non-Invasive Imaging</span><br>

                    </font></strong>Daniel Stockholm, G&eacute;n&eacute;thon

                    (Evry, France)<br>

                        <br>

                         <strong>Project Objective</strong><br>

                         Demonstrate the feasibility of a non-invasive imaging technique based on scintigraphy for the follow-up of muscle damage in dysferlinopathy<br>

                          <br>

                          Non-invasive imaging techniques are needed to understad the pathophysiology of dysferlinopathy and to following disease progression/regression during clinical trials. In vivo, Evan's blue dye, which is known to bind to albumin in blood, is used as a membrane integrity assay in mouse models. Blood albumin penetrates damaged muscle fibers, which are permeabilized in the muscular dystrophies. After injecting Evans blue dye intraperitoneally, histological assessment of membrane permeability is possible. However, this type of assay is impossible to use in humans due to the toxicity of the dye, and only "post-mortem" animal imaging is possible.

Our new method is based on the use of scintigraphy imaging to follow albumin uptake by muscle. Instead of using the toxic Evans blue dye, albumin is directly labelled with the radioactive tracer technicium, which is widely used in medical imaging, to follow muscle membrane permeability in real time. Technicium labeled albumin is safe to use in both live animals and in humans, and would provide kinetic data on the permeabilisation of muscle fiber membranes at the resolution of the organ level. Furthermore, scintigraphy imaging is highly sensitive and allows quantitative imaging of the biodistribution of the radiopharmaceutical.<br><br>

Preliminary data demonstrates that exogenous human albumin injected intraveanously in mice is detected by immuno-staining in damaged fibers. Albumin preparation for technecium labelling is produced via a partnership with the laboratory of Genetic and Pharmacologic Chemistry (CNRS, UMR 8151). The scintigraphy detection and imaging is obtained in the Bichat faculty's small animal molecular imaging platform thanks to a collaboration with Pr. Philippe Chaumet-Riffaud.<br><br>

We hope to develop a rapid and reproducible non-invasive method for the quantification of muscle membrane permeabilisation in dysferlin deficient mice that can be easily transposable to clinical applications. If confirmed, the project will break technological limitations of muscle imaging and will provide a new tool for understanding the pathophysiology of dysferlinopathy as well as for clinical evaluation.<br>

