                  <strong>Session IV: Membrane Fusion and Repair</strong><br>

         

 <br>This session focused on the molecular mechanisms underlying membrane fusion events—in which dysferlin is hypothesized to be involved—during plasma membrane repair and myoblast fusion. <br><br>

<strong>Jianjie Ma</strong> (USA) discussed his studies on the muscle-specific mitsugumin 53 (MG53) protein and its involvement in sarcolemmal repair. MG53-/- cells cannot effectively reseal their plasma membranes following laser wounding or mechanical damage, and MG53 in wild-type cells rapidly concentrates to the plasma membrane wound site. MG53 does not contain a transmembrane domain, but is likely associated with membranes through its interaction with phosphatidyl serine. Studies performed under Ca2+-free conditions revealed that the local oxidative environment near the injury site is required for recruitment of vesicles, which can subsequently fuse in a Ca2+-dependent manner to seal the membrane wound. Oligomerization of MG53 under oxidative conditions appears to be the signal for vesicle recruitment. Unlike MG53-/- cells, dysferlin-/- cells have normal vesicle accumulation at the wound site, implying that their defect is in a subsequent step of the repair process, such as Ca2+-dependent membrane fusion. Finally, MG53 was found to interact with dysferlin and caveolin-3, suggesting that these three proteins may function as a part of a molecular complex.<br><br> 

<strong>Norma Andrews</strong> (USA) presented her studies on the sequence of membrane cycling events during membrane repair, which is known to involve Ca2+-mediated exocytosis of intracellular vesicles. Previous work from her lab has shown that exocytosis of lysosomal vesicles is involved in the repair process and that the lysosomal marker LAMP-1 can be readily detected on the surface of repaired cells. In addition, formation of endosomes can also be observed in multiple cell types, including C2C12 myoblasts, shortly after membrane wounding by either scraping or treatment with streptolysin O (a toxin that generates stable, protein-lined pores in the membrane). Enhancement of the endocytic process (by disruption of the actin cytoskeleton) accelerates repair, while its inhibition (by cholesterol depletion) blocks the repair process. Dr. Andrews proposed a model in which lysosomal exocytosis adds additional membrane to the plasma membrane and brings about the release of hydrolases that participate in endosome formation. These studies highlight the interplay between exo- and endocytosis in the plasma membrane repair process.<br><br>

<strong>Elizabeth McNally</strong> (USA) presented her research on the protein myoferlin, the closest ferlin homolog of dysferlin, and its involvement in myoblast fusion. Expression studies and characterization of the myoferlin promoter revealed that myoferlin expression is upregulated in response to cardiotoxin-induced muscle damage (Davis et al. 2000; Doherty et al. 2005). Examining the upstream regulatory regions suggests a role for the NFAT and calcineurin pathways in regulating myoferlin expression, presumably in response to Ca2+ influx caused by membrane wounding. Dr. McNally also found that myoferlin interacts with the endocytic recycling protein EHD2 via its C2B domain (Doherty et al. 2008). Myoferlin-null myoblasts are defective in endocytic recycling, and expression of an EHD2 point mutation previously associated with defective endocytic recycling results in myoferlin aggregation and impaired myoblast fusion. Dr. McNally proposed that recycling of plasma membrane receptors involved in signaling events associated with myoblast fusion is mediated by myoferlin and is critical for maintaining the fusion process. 
