                  <strong>Session VII: Emerging Therapies for Dysferlinopathy</strong><br>

                  <br>

 This session discussed an array of approaches for treating dysferlinopathy, including mutation-specific therapies, dysferlin gene therapy, enhancing muscle regeneration, and boosting the stability of the plasma membrane. <br><br>

<strong>Lee Sweeney</strong> (USA) discussed an ongoing clinical trial of PTC124 (Ataluren), a small molecule that promotes ribosomal read-through of premature stop codons. PTC124 was identified after a high-throughput in vitro chemical screen for read-through activity. In mdx mice, it successfully restores dystrophin expression, improves force generation, and lowers CK levels. Dr. Sweeney has now shown that exposure to PTC124 restores dysferlin expression to 20% of normal levels in cultured myoblasts derived from a muscle biopsy from a patient heterozygous for the dysferlin R1905X mutation. Dr. Sweeney also presented a novel assay system to test functional rescue of dysferlin-deficient myotubes: in hypotonic conditions wild type myotubes exhibit membrane blebbing, whereas dysferlin-deficient myotubes do not. Blebbing can be restored in the latter by transferring full-length dysferlin into these myotubes via AAV. Dr. Sweeney found that treating the human myotubes harboring the dysferlin R1905X mutation with PTC124 also restores blebbing, establishing the therapeutic potential of PTC124 for treating dysferlinopathy patients with nonsense mutations. <br><br>

<strong>Joshua Zimmerberg</strong> (USA) discussed the relationship between membrane lipid curvature and the ability of a membrane to repair or resist the formation of tears and pores. Surface tension tends to make membrane pores larger, while line tension tends to make pores smaller. In response to these forces, pores smaller than a critical size will spontaneously close. Increasing the proportion of negative curvature lipids in the membrane increases line tension and favors closure. Dr. Zimmerberg is directly assessing whether changes in the lipid composition of myoblast membranes can alter their ability to tolerate tears and reseal in the absence of dysferlin. Altering lipid composition in vivo through the introduction of specific dietary lipids may provide a means of mitigating the effects of dysferlin deficiency. Dr. Zimmerberg found that dysferlin-deficient myotubes treated with the polyunsaturated fatty acid DHA reseal more quickly than untreated cells as measured by a conductance assay. He hopes to start a human trial to measure exercise induced creatine kinase (CK) levels in normal people taking DHA versus controls to see if the dietary lipid has an effect on spontaneous closure of membrane disruptions. <br><br>

<strong>Isabelle Richard</strong> (France) discussed the use of adeno-associated viruses (AAV) for delivering the dysferlin gene to muscle. To get around the 4.5-kb upper limit to the capacity of the virus, Dr. Richard is using a concatamerization strategy in which the two halves of the dysferlin gene (a total of close to 7-kb) are delivered by two different AAV vectors with specific sequences that allow recombination inside a cell. She has administered the two AAV vectors (using either AAV1 or AAV9) either intramuscularly or intravenously into dysferlin-deficient mice (BLA/J mice, containing the A/J mutation on a C57BL/6 background). Injection into the tibialis anterior results in dysferlin expression detectable by quantitative RT-PCR, in situ hybridization, and western blotting up to 12 months post injection. The transferred dysferlin shows sarcolemmal localization by immunohistochemistry, and a membrane repair assay based on FM 1-43 entry into fibers after laser wounding demonstrates improved repair in AAV-injected dysferlin-deficient mice compared to uninjected mice. Systemic injection also results in detectable dysferlin protein and significantly improves activity level and distance traveled in an assay for locomotor activity one month post-injection. Dr. Richard has also created myoferlin overexpressing transgenic mice,and found that crossing them to dysferlin-deficient mice does not rescue the dysferlin-deficient phenotype. <br><br>

<strong>Simone Spuler</strong> (Germany) discussed a strategy to re-target missense mutant dysferlin that has become trapped in the endoplasmic reticulum (ER) to the sarcolemma. Misfolded proteins are rapidly degraded in the ER via the ubiquitine proteasome system. She used TAT-labeled short 15-mer peptides corresponding to the dysferlin amino acid sequence around the location of each of 3 different missense mutations, versus control peptides with random sequence. She delivered the peptides to C2C12 myoblasts transfected with wild type or mutant GFP-dysferlin. She showed by immunoflouresence (IF) that a small amount of GFP protein found its way to the sarcolemma when she used peptides from the mutated dysferlin sequence or wild type dysferlin sequence but not when she used control nonsense peptides. Dr. Spuler also showed similar results using primary human myoblasts from patients.<br><br>

<strong>Se-Jin Lee</strong> (USA) has previously demonstrated that inhibiting the protein myostatin can increase the muscle mass of both normal and dystrophic mice (Lee 2004). Anti-myostatin antibodies are already being tested in clinical trials as a potential therapy for muscular dystrophy; however, it is unclear whether directly targeting myostatin will be an effective approach in patients. Dr. Lee’s recent work has focused on how myostatin’s interacting protein partners regulate its activity. In particular, Dr. Lee presented genetic studies supporting a role for the BMP-1/tolloid family of metalloproteases in regulating myostatin activity by cleaving the propeptide, which normally maintains myostatin in a latent, inactive state. Dr. Lee is also using genetic studies to try to identify the other ligands that cooperate with myostatin to regulate muscle growth and is attempting to develop novel biologics capable of targeting these ligands.

