                  <strong>Synthesis and Evaluation of Amphiphilic Poly(tetrahydrofuran-b-ethylene oxide)
Copolymers for DNA Delivery into Skeletal Muscle</strong><br>
Catherine Pomel1, Christian Leborgne2, Hervé Cheradame1, Daniel Scherman2, Antoine
Kichler2, Philippe Guegan1
1 Laboratoire Matériaux Polymères aux Interfaces–UMR CNRS7581, Université d’Evry,
91025 Évry, France. 2 Genethon–FRE 3087 CNRS, BP60 91002 Évry, France.<br><br>

Purpose. Amphiphilic triblock copolymers such as the pluronic poly(ethylene oxide-bpropylene
oxide-bethyleneoxide) L64 (pEO13-pPO30-pEO13) have been shown to mediate
more efficient gene transfer in muscle as compared to naked DNA. We were interested in
studying the effect of a chemical change of the central block of pluronic polymers on the
transfection activity. Methods.We synthesized new amphiphilic copolymers in which the
hydrophobic pPO block was replaced by poly(tetrahydrofuran) (pTHF) chains. The resulting
triblock pEO–pTHF–pEO polymers have been characterized by NMR and SEC and assayed for in vitro and in vivo gene
transfer. Results. The animal experiments showed that the new copolymers are able to significantly increase the
transfection efficiency of plasmid DNA after intramuscular injection. Conclusions. These results indicate that the
capacity to enhance plasmid DNA transfection in skeletal muscle is not restricted to pEO–pPO–pEO arrangements.</p>
