				<p><span class="style13">SCIENTIFIC SUMMARY </span><br>

				<br>

				  <a href="2007sci_summary.php" class="style16">&lt;-- back</a> <br>

				  <br><br>

                  <strong>Session IV: Dysferlin Function </strong><br>

                  <br>

                  The Dysferlin Function session discussed our current understanding of the cellular function of the dysferlin protein. Although this is a basic science question, understanding dysferlin's function is essential for any therapy that might  replace this function or maintain cell survival even in the absence of this function. To develop a complete therapy, it is also important to know whether dysferlin has a function in tissues other than muscle, but dysferlin's expression pattern is still incompletely characterized. <br>

                  <br>

                  This session also covered other cellular proteins  that have similar structure to dysferlin and are members of the ferlin family. Other ferlins could potentially substitute for dysferlin in the muscle, but developing a treatment using one of these family members requires understanding in more detail these proteins' functions and expression patterns.<br>

                  <br>

                  Dr. Kevin Campbell discussed experiments that led to the hypothesis and confirmation that dysferlin is involved in the repair of muscle membrane tears  after wounding. Two different assays of wound repair (with wounding accomplished by laser or by passage through a syringe) have demonstrated that  dysferlin is concentrated in the myocyte membrane at sites of newly-repaired damage and that dysferlin-deficient skeletal muscle fibers are unable to repair membrane tears. The laser wounding assay has recently been repeated with dysferlin-deficient cardiac myocytes, which show the same defect in Ca-dependent resealing. If cardiac muscle cells have the same defect as skeletal muscle cells in the absence of dysferlin, it is unclear why cardiomyopathy is not normally a symptom of dysferlin deficiency in patients. Dr. Campbell has recently found that cardiac dysfunction and increased Evans blue dye uptake can be induced by exercise in dysferlin knockout mice more than 1 year old, but the relevance for human patients is not yet known. It may be that dysferlin's role in a process other than membrane damage repair (for example, a role in myoblasts during regeneration) is involved in the phenotypic difference between skeletal and cardiac muscle in the absence of dysferlin.<br>

                  <br>

                  Dr. Eric Hoffman discussed recent mRNA profiling studies  comparing gene expression between different types of muscular dystrophies. In comparing muscle biopsies  from dysferlin-deficient, FKRP-deficient, and normal individuals, he found that expression of certain genes involved in inflammation and vesicle trafficking was uniquely increased in dysferlinopathy.  Dr. Hoffman suggested that an increase in the vesicle trafficing proteins Rab27a and Slp2a (synaptotagmin-like protein a) may indicate a compensatory shift toward alternative vesicle trafficking and membrane fusion pathways in the absence of dysferlin. This increase varied significantly between patients, suggesting that some may be more successful than others in compensating for dysferlin's loss, though the profiling results have not yet been correlated to severity of disease in these patients. Dr. Hoffman also presented data suggesting that during muscular dystrophy, the expression of proteins involved in muscle regeneration pathways does not follow the normal pattern and allows for inappropriate crosstalk between the protiens. Dr. Hoffman hypothesizes that this crosstalk may lead to fibrosis instead of successful regeneration during muscluar dystrophy. Glucocorticoids can affect the timing of expression of these muscle regeneration proteins and may reduce inappropriate crosstalk.<br>

                  <br>

				  Dr. Sandra Cooper discussed studies of dysferlin missense mutants, which are degraded in patient muscle and so cannot perform partial function.  She has studied both patient myoblasts and patient fibroblasts that are triggered to undergo myogenesis by lentiviral delivery of the MyoD gene. Although mutant dysferlin is not detected by Hamlet-1 in muscle biopsies from patients with missense mutations, it is detected in cultured myotubes generated from their fibroblasts. EGFP-tagged dysferlin constructs carrying one of two different patient missense mutations in the C2F domain revealed that one mutant was transported to the membrane of myotubes while the other was not. A variety of C2 domain deletion constructs (some retaining only the C2F domain)  were all successfully targeted to the membrane of cultured myotubes, transported by vesicles from the Golgi. However, in at least one case the loss of  C2 domains affected the persistance of dysferlin at the membrane. Dr. Cooper has also noticed that the Hamlet-1 antibody is not as good at recognizing membrane dysferlin as intracellular dysferlin, and that dysferlin may be able to adopt a confirmation that is not recognized by the Hamlet-2 antibody. These concerns should be kept in mind by anyone using these antibodies. <br><br>

				  Dr. Rita Barresi discussed recent findings that dysferlin in cultured C2C12 myotubes is localized to the T-tubule network, despite its sarcolemmal localization in mature muscle fibers. In the myotubes, GFP-tagged dysferlin constructs lacking various C2 domains all fail to localize properly to  T-tubules, in contrast to the full length protein. Using a membrane wounding and repair assay based on glass bead damage, it was found that wounding of myotubes causes dysferlin to change its localization from the T-tubules to the sarcolemma and accumulate at sites of wounding. Again, none of the tested C2 domain deletion mutants were able to accomplish this translocation. Dr. Barresi also described studies on the effects of Poloxamer 188 (a membrane resealing agent) on the phenotype of dysferlin-deficient mice, but these studies have been slowed by problems determining the correct dose of Poloxamer in animals. In the discussion following her talk, it was mentioned that it is often difficult to distinguish between developing T-tubule membrane and ER membrane. <br>

				  <br>

				  Dr. Rumaisa Bashir discussed studies of other ferlin family members and other proteins associated with dysferlin. Dysferlin and its two closest relatives, myoferlin and Fer1L5, all increase in expression during the transition from myoblasts to myotubes, with Fer1L5 showing the most dramatic increase. These three proteins are found primarily in different types of intracellular vesicles, with differences in density and detergent resistance, but the origins and functional overlap of these vesicles are still unknown. Dr. Bashir has also found a membrane repair defect in  patients with Miyoshi myopathy whose disease is not caused by mutations in dysferlin or in other known membrane repair proteins. She is trying to identify these proteins, which may interact with dysferlin or represent a possible compensatory pathway. <br>

				  <br>

				  During the brainstorming session, one point of discussion was the apparently conflicting results from Dr. Cooper and Dr. Barresi on proper targeting and localization of dysferlin C2 domain deletion constructs. These results could reflect different mechanisms of targeting to the T-tubules versus the sarcolemma -- in which the full length protein is required for initial targeting to the T-tubules, but only the transmembrane domain (and possibly the C2F domain) are required for initial targeting to the sarcolemma. Alternatively, the difference in results may be due to differences between  C2C12 cells and patient-derived cells. Researchers should be cautious in comparing results from cultured and primary cells. <br><br>

				  Another issue discussed in the brainstorming session is the relationship between dysferlin and dystrophin deficiencies. Dr. Campbell mentioned that crossing  transgenic dysferlin-expressing mice with mdx mice does not improve the mdx phenotype. There are a number of possible explanations for this result, including that mdx muscle may already engage in a saturated level of membrane repair, that the membrane ruptures in mdx muscle are too large to be corrected by the dysferlin repair pathway, or that dysferlin-mediated repair cannot take place without some secondary structure that is lost in the absence of the dystrophin glycoprotein complex. <br>

				  <br>

				  It is also important to have a standardized membrane repair assay for work in this field, and this was discussed in many of the talks in both this session and the Membrane Fusion and Repair session. <br>

				  <br>

				  There are still many important unknowns about dysferlin's function. These include:<br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- Do dysferlin-deficient muscle cells have trouble with any other types of exocytosis? <br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- What are all of the other components of the dysferlin membrane repair pathway? <br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- Does dysferlin participate in myoblast fusion?<br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- What is the source of vesicles containing dysferlin or other ferlin family members? <br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- What are the expression levels of the ferlin family members in muscle throughout an &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; individual's lifetime? <br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- What is the function of dysferlin in monocytes and neutrophils? Is dysferlin involved in<br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; immue cell development and does this have any consequences for patients? <br>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- What is the function of dysferlin in other tissues (heart, placenta, blood-brain barrier)?<br>

