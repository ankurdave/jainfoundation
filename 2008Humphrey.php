                  <img src="images/conference2008/Humphrey.jpg" width="135"><br><br><br>
                  <strong>Characterization of Plasma Membrane Lipids in Dysferlin-Deficient Myoblasts.</strong><strong>&nbsp; &nbsp;</strong><br>

Glen W. Humphrey, Ludmila Bezrukov, Elena Mekhedov, Paul S. Blank, Alfred Yergey, and Joshua Zimmerberg
Program on Physical Biology, Eunice Kennedy Schriver National Institute of Child Health and Human Development, NIH, Bethesda, MD
<br><br>

Mutations in the dysf gene coding for dysferlin cause late-onset muscle wasting syndromes in humans and mice (e.g. Miyoshi myopathy and LGMD2B). The muscle wasting disease is thought to be due to loss of a calcium-activated plasma membrane repair process, requiring dysferlin. Dysferlin is a ~200 kDa membrane-anchored protein with six C2 domains, which may serve as a calcium sensor for membrane repair (1, 2). We are exploring modification of plasma membrane fatty acids as a therapeutic approach for dysferlin-deficiency diseases.  Phospholipids containing long chain polyunsaturated fatty acids (PUFA) (i.e. arachidonic acid, 20:4, n-6; docasohexanoic acid, 22:6, n-3) have negative spontaneous curvature, and may promote membrane resealing by increasing line tension and/or decreasing membrane tension. The PUFA content of membrane phospholipids in cultured cells can be increased by adding PUFAs to the medium. In order to determine whether the PUFA content of the membrane has changed, it is necessary to isolate plasma membrane lipids.  Since plasma membrane lipid only represent a small fraction of total muscle cell lipid, we developed a method to isolate membranes from cultured fibroblasts (NIH 3T3) based on physical adhesion of the plasma membrane to solid substrates (3).  Cells adhering to the substrate are lysed in water and washed extensively to remove cellular contents, yielding membrane sheets, which are extracted with organic solvents.  We have employed this method to isolate plasma membrane from cultured dysferlin-deficient mouse myoblasts and fused myotubes.  Membrane lipids were prepared from myoblasts on polylysine-coated glass and from fused myotubes on collagen-coated plastic.  Lipid analysis by mass spectrometry detected major species of PC phospholipids containing saturated (SFA), monounsaturated (MFA) and polyunsaturated (PUFA) fatty acids.  Fragmentation analysis by MALDI-TOF/TOF identified AA-containing species PI 18:0~20:4, and several PUFA-containing species of PC.  Identification of PUFA-containing phospholipids will allow us to measure incorporation of exogenous PUFAs into plasma membrane lipids. 


