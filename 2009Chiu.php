                  <strong>Attenuated Muscle Regeneration is a Key Factor in Dysferlinopathy</strong><br>

Yen-Hui Chiu1, Steven Laval1, Louise Jørgensen1, Mark Hornsey1, Lars Klinge1,2, Richard
Charlton1, Rita Barresi1, Volker Straub1, Hans Lochmüller1 & Kate Bushby1
1Institute of Human Genetics, Newcastle University, UK 2Department of Paediatrics and
Paediatric Neurology, University Medical Centre Göttingen (UMG), Germany<br><br>

Dysferlinopathy is characterized by adult onset, slow progression, high serum CK and a
prominent inflammatory infiltrate. To date, the pathomechanism of dysferlinopathy is still
unclear. Our previous studies have demonstrated that the dysferlin protein translocates
from the cytoplasmic space to the sarcolemma during muscle regeneration, and biopsies of
dysferlinopathy patients show an excess of immature fibers compared to other types of
muscular dystrophy. This indicates that dysferlin might be involved in the process of muscle
regeneration. To test this hypothesis, muscle regeneration was induced by injecting notexin
into the right tibialis anterior (TA) muscles of wild type C57BL/10 and dysferlin deficient
C57BL/10.SJL-Dysf mice. Two days after notexin injection, the injected muscle had been severely damaged in both strains. In wild type animals, the damaged fibers were completely removed and replaced by regenerating fibers at day 7 after notexin injection. In contrast, abundant necrotic fibers were still observed even at 28 days after injury in dysferlin deficient mice. Satellite cell activation and myoblast fusion seemed to be unaffected; however, the number of neutrophils recruited to the site of injury was significantly reduced in dysferlin deficient animals. Since dysferlin is expressed in the
pro-macrophage monocyte lineage, but neither in neutrophils nor inflammatory monocytes, these early changes in inflammatory cell recruitment are likely to be driven by defective myokine secretion. Cytokine assays showed reduced MCP-1 secretion from C57BL/10.SJL-Dysf primary myotubes after stimulating with IFN-? or wounding with saponin.
Altogether, we hypothesized that dysferlin might play a role in muscle regeneration by controlling cytokine secretion.
Without dysferlin, the inflammatory response including neutrophil recruitment was attenuated, resulted in incomplete of
muscle regeneration and cumulatively acted to accelerate the development of muscular dystrophy. </p>

