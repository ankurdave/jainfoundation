                  <strong>Dysferlin-mediated membrane repair protects  the heart from stress-induced left ventricular injury</strong><strong>&nbsp; &nbsp;</strong><br>

Renzhi Han1, Dimple Bansal1, Katsuya  Miyake2, Viviane P. Muniz1, Robert M. Weiss1,  Paul L. McNeil3, Kevin P. Campbell1 </p>

                1Howard Hughes Medical  Institute, Senator Paul D. Wellstone Muscular Dystrophy Cooperative Research  Center, Department of Molecular Physiology and Biophysics, Department of  Neurology, and Department of Internal Medicine, Roy J. and Lucille A. Carver  College of Medicine, The University of Iowa, Iowa City, Iowa 52242; 2Institute  of Molecular Medicine and Genetics, 3Department of Cellular Biology  and Anatomy, The Medical College of Georgia, Augusta, Georgia 30912

                <p>Dilated cardiomyopathy is a life-threatening syndrome that can arise from  a myriad of causes, but predisposition toward this malady is inherited in many  cases. A number of<br>

inherited forms of dilated cardiomyopathy arise from  mutations in genes that encode proteins involved in linking the cytoskeleton to  the extracellular matrix, and disruption of this link renders the cell membrane  more susceptible to injury. Membrane repair is an important cellular mechanism  that animal cells have developed to survive membrane disruption. We have  previously shown that dysferlin deficiency leads to defective skeletal muscle  membrane resealing and muscle necrosis; however, the function of dysferlin in  the heart remains to be determined. Here we demonstrate that dysferlin is also  involved in the membrane repair of cardiomyocytes, and dysferlin deficiency  leads to cardiomyopathy in senescent mice. In particular, stress exercise  disturbs left ventricular function in dysferlin-null mice and increases Evans  Blue Dye uptake in dysferlin-deficient cardiomyocytes. Furthermore, a combined  deficiency of dystrophin and dysferlin leads to early onset cardiomyopathy. Our  results suggest that dysferlin-mediated membrane repair is important for  maintaining membrane integrity of cardiomyocytes, particularly under conditions  of mechanical stress. Thus, our study establishes a novel mechanism underlying  the cardiomyopathy that results from a defective membrane repair due to  dysferlin deficiency.<strong><span class="style17"><br>

