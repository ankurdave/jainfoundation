                  <strong>In Vitro Exon-Skipping: Therapeutic Approaches for Dysferlinopathies</strong><br>
                  Nicolas Wein1, Aurelie Avril2, Martin Krahn1,3, Claire Navarro4, Marc Bartoli1,5, Sebastien<br>
                  Courrier1, France Leturcq6, Luis Garcia2, Nicolas Levy1,3<br>
