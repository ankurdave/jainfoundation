                  2Biotherapies of neuromuscular disorders, Inserm UMRS_974, Paris, France, 3Department<br>
                  of medical genetics, Timone hospital, Marseille, France, 4Stem cell Laboratory, Department<br>
                  of Neurological Science, Fondazione IRCCS Ospedale Maggiore Policlinico, Centro Dino<br>
                  Ferrari, University of Milan, Milan, Italy, 5Genethon, Evry, France, 6Biochemical and<br>
                  molecular genetics, Cassini Laboratory, Paris, France<br><br>
                  A gene therapy approach based on exon-skipping strategy was recently developed for
                  Duchenne muscular dystrophy (DMD). Briefly, Antisense Oligo Nucleotides (AON) blocked
                  the splicing sequences, resulting in the skipping of the targeted exon. To avoid repeated
                  AON administration, the skipping sequences were included in U7-snRNAs and incorporated       in lentivirus or Adeno-Associated Virus. Such approaches have proven to be effective in mice, rats, dogs and monkeys. We hypothesize that this promising strategy could be useful for dysferlinopathies. This assumption is based on the report  of a patient with a very mild phenotype presenting a deletion of dysferlin exon 32 (Sinnreich et al., 2006). According to the UMD-Dysf database, 6 pathogenic mutations localized in exon 32 have been reported to date. Based on these observations, we developed an approach to skip exon 32. After bioinformatics analyses we were able to identify the required splicing sequences. Three different AON targeting these sequences were tested in myoblasts generated from MyoD transduced fibroblasts. Two of them allowed the  expected skipping of exon 32. The corresponding sequences were incorporated in a lentivirus backbone and used to transduced fibroblasts and CHQ cells. In treated cells, two weeks after transduction, we observed a high efficiency of exon 32 skipping. We are currently monitoring the function of the skipped dysferlin by western-blot, immunostaining and membrane repair assay. These encouraging results for therapy of dysferlinopathies will probably benefit from ongoing
                  clinical trials for DMD.</p>
