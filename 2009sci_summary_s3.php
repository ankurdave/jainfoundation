                  <strong>Session III: Dysferlin Structure and Function</strong><br>

                  <br>
This session focused on dysferlin’s function at the molecular level; specifically its structure, interactions with other proteins, lipids and ions, and how these interactions influence its localization and movement within muscle fibers.<br><br>

<strong>Silvère van der Maarel</strong> (The Netherlands) described his ongoing efforts to identify new interaction partners of dysferlin. He has developed anti-dysferlin antibodies using a phage display system of the heavy chain antibody repertoire, taking advantage of the fact that llama antibodies consist of only a heavy chain that can be cloned, tagged, and produced in large quantities in bacteria. These antibodies were used to immunoprecipitate dysferlin and its associated proteins from different tissues and cell lines. A number of known and novel dysferlin interacting proteins were identified by mass spectrometry, including calpain 3, caveolin 3, annexin-1 and -2, AHNAK, and MG-53, as well as many novel interactors that still need to be verified. Bioinformatic analysis of the identified proteins supports the hypothesis that dysferlin acts in a dynamic protein complex depending on the tissue or cell line analyzed. Dr. van der Maarel has also continued to study the interaction of dysferlin with AHNAK, and how calpain-3 regulates this interaction through cleavage of AHNAK.<br><br>

<strong>Sandra Cooper</strong> (Australia) discussed the trafficking and membrane biology of dysferlin and dysferlin truncation mutants. The constructs were tagged with EGFP and His/Myc at the N- and C-termini, respectively, allowing for differential visualization of the intracellular and extracellular domains. Her studies demonstrate that dysferlin requires the Golgi secretory pathway for trafficking to the plasma membrane and that dysferlin remains at the cell surface for several hours. This trafficking pattern may help explain the abnormal localization of dysferlin in many patients with muscular dystrophy. Dr. Cooper also found that dysferlin, and some of the truncation mutants, appear to cluster by light and electron microscopy, and preliminary experiments using FRET suggest that individual dysferlin molecules may associate with one another and form oligomers.<br><br>

<strong>Michael Sinnreich</strong> (Canada) described three different projects that his lab is pursuing on dysferlinopathy. The first is to characterize the lipid binding properties of the C2 domains of dysferlin (Therrien et al. 2009). C2 domains from other proteins can bind to specific phospholipids, often in a manner that is regulated by Ca2+. Dr. Sinnreich demonstrated that the C2A domain of dysferlin does bind phospholipids in a Ca2+-dependent manner but that the other C2 domains do not. Dr. Sinnreich also described his efforts to identify new dysferlin binding partners by combining immunoprecipitation with mass spectroscopy. These studies identified a number of proteins known to interact with dysferlin, including calpain-3, caveolin-3, AHNAK, annexin-1 and -2, and MG-53, as well as a novel interaction with alpha tubulin that is supported by additional biochemical evidence. Finally, Dr. Sinnreich described his efforts to design a functional mini-dysferlin protein for use in gene therapy. The design is based on his finding of a particularly mild phenotype in a patient with a lariat branch point mutation that produces a partially functional mini-dysferlin protein (Sinnreich et al. 2006). <br><br>

<strong>Bryan Sutton</strong> (USA) described his approach to determining the structure of the dysferlin protein, including electron microscopy imaging of the full cytoplasmic portion of dysferlin and X-ray crystallography to determine the atomic resolution structure of the individual C2 domains. Dr. Sutton has re-evaluated the boundaries of each C2 domain using bioinformatics analysis and is generating individual C2 domain expression constructs. The domains that have been expressed so far, C2A and C2F, can be purified from bacteria and are amenable to growing protein crystals for structural analysis. Crystals of C2A are now available and diffract to a very high resolution. Dr. Sutton has not had success using the myoferlin C2A domain NMR structure for molecular replacement of the dysferlin C2A data, suggesting that there are substantial differences between the C2A domains of these two ferlins. The electron microscopy analysis is being done in collaboration with Dr. Tom Walz and is still in the development phase. Finally, Dr. Sutton has found by bioinformatics that the portion of dysferlin between C2C and C2D (containing the ferlin domains) has substantial homology to glutamine synthetase, which is known to form pentamers. 



