                  <strong><span class="style17">John M. Robinson, PhD</span></strong><br>

                  <em>Ohio State University, Columbus, OH</em><br><br>

                  <img src="images/Conf09/Robinson.jpg" width="135"><br><br>

                  <strong>Ferlins in the Human Placenta and Trophoblasts</strong><br>

John M. Robinson, Dale D. Vandré, William E. Ackerman<br><br>

The placenta, an ephemeral organ, facilitates the exchange of solutes and gases between the mother and the fetus. The syncytiotrophoblast (STB) a unique fused-epithelium, forms the interface between maternal blood and the placenta. The STB is produced, and maintained, by underlying mononuclear cells, the cytotrophoblasts (CTB). Proteomics analysis showed that the STB of the human placenta expresses dysferlin (DYSF) and myoferlin (MYOF). Dysferlin is primarily localized to the apical plasma membrane of the STB and is not detectable in the CTBs. Myoferlin is present in the apical plasma membrane of the STB, but is also detected in intracellular compartments in the STB and in CTBs. Freshly isolated CTBs spontaneously fuse and form syncytia; DYSF expression is upregulated in the syncytia, while non-fused mononuclear CTBs do not express DYSF. The BeWo cell line (a surrogate for the CTB) also expresses DYSF following forskolin-induced cell fusion, while non-fused BeWo cells do not express this protein. On the other hand, MYOF is constitutively expressed in BeWo cells. Thus, BeWo cells recapitulate the expression patterns of DYSF in the STB and CTB, and are therefore a useful model system to study the function of DYSF and MYOF in the placenta. Lentivirus-based short hairpin RNA (shRNA) technology was used to interfere with DYSF and MYOF RNAs and knock down the expression of these ferlins. Stable cells lines lacking DYSF and MYOF behave normally with regard to forskolin-induced cell fusion, indicating that neither protein is required for fusion of these cells. In a pilot study, we find that DYSF, but not MYOF, expression is depressed in placentas from pregnancies with severe preeclampsia. This disease of pregnancy is associated with increased shedding of apical plasma membrane microparticles from the STB. This result suggests that reduction in DYSF expression leads to increased instability of the STB plasma membrane.</p>

