				<p><span class="style10">Session IX: Clues from Other Ferlins and Myopathies </span><br>
				  
				  
				  <br>
				  <br>
				  
				  <strong><span class="style17">Dennis G. Drescher, PhD</span></strong><br>

                  <em>Wayne State University School of Medicine, Detroit, MI</em><br><br>

                  <img src="images/Conf09/Drescher.jpg" width="135"><br><br>

                  <strong>The Molecular Structure of Otoferlin Reflects its Function at the Hair-Cell Synaptic
Complex</strong><br>
Dennis G. Drescher, Neeliyath A. Ramakrishnan, Marian J. Drescher
Departments of Otolaryngology and Biochemistry-Molecular Biology, Wayne State
University School of Medicine, Detroit, MI<br>

<br>

Otoferlin, implicated in vesicular fusion in the auditory sensory hair cell, is a member of the homologous mammalian ferlin gene family, with six Ca2+-binding C2 domains (A-F, long form) and a C-terminal transmembrane region. Of all the ferlin family members, only otoferlin and dysferlin are associated with human disease. Mutations in the OTOF gene cause nonsyndromic recessive deafness, DFNB9, characterized by auditory neuropathy/auditory dyssynchony with absent auditory brainstem responses but normal otoacoustic emissions. Otoferlin localizes to ribbon-associated synaptic vesicles at the base of inner hair cells, where it may be the major synaptic-complex Ca2+ sensor -- although other C2 Ca2+-sensing proteins, such as synaptotagmins, may participate as well. Otoferlin is also found in the hair-cell apical zone, co-localizing with Golgi membranes, implicating otoferlin as part of a recycling trans-Golgi network. In the present studies, we generated otoferlin fusion proteins containing two of the same amino acid substitutions detected in specific DFNB9 human deafness forms (Pro1825Ala in C2F and Leu1011Pro in C2D). Using binding analyses employing surface plasmon resonance, pull-down, and yeast two-hybrid methodology, we measured interaction of C2 domains with the main SNARE complex proteins of the hair cell. Native otoferlin C2F bound syntaxin 1A and SNAP-25 in a Ca2+- dependent manner. The latter interactions were greatly diminished for the C2F Pro1825Ala mutation, possibly due to a reduction in tertiary structural change induced by Ca2+, for the mutated C2F compared to the native C2F. Further, otoferlin C2D bound the hair-cell Ca2+ channel Cav1.3 II-III loop, in a Ca2+-dependent manner, and the Leu1011Pro mutation in C2D rendered this binding insensitive to Ca2+ and considerably diminished. Since mutations in the otoferlin C2 domains that cause deafness in humans impair otoferlin’s ability to bind syntaxin, SNAP-25, and the Cav1.3 Ca2+ channel, these interactions may mediate regulation by otoferlin of hair-cell synaptic exocytosis critical to inner-ear haircell function.</p>
				
