                  <strong><span class="style17">Rumaisa Bashir, PhD</span></strong><br>

                  <em>University of Durham, Durham, UK</em><br><br>

                  <img src="images/Conf09/Bashir.jpg" width="135"><br><br>

                  <strong>Candidate Gene Analysis of the MMD3 Region on Chromosome 11p14-12</strong><br>

Gareth Marlow, Khalil Saleki, Usha Ramachandran, Rumaisa Bashir<br><br>

Miyoshi myopathy (MM) is an autosomal recessive muscular dystrophy characterized by distal muscle weakness at disease onset and a marked elevation of serum creatine kinase levels. One form of Miyoshi myopathy shares allelic heterogeneity with limb-girdle muscular dystrophy type 2B (LGMD2B) and is linked to dysferlin mutations. The identification of the dysferlin gene had identified genetic heterogeneity in MM. It is now recognized that not all families with similar clinical features as dysferlin MM are linked to the dysferlin locus and a second MM locus (designated MMD2) has been assigned to chromosome 10p1. Dysferlin and non-dysferlin MM patients show similar clinical phenotypes and in one family with non-dysferlin MM we have previously demonstrated defective membrane repair and shown that the lysosomal and enlargeosomal repair pathways are not disrupted2. These findings have highlighted that the genes responsible for non-dysferlin MM may encode proteins that function in the dysferlin membrane repair pathway. We have undertaken a genome SNP analysis of selected non-dysferlin MM families, which have been excluded for dysferlin and MMD2 involvement. In two such non-dysferlin MM families including the family linked with defective membrane repair we have mapped the disease gene to a region on chromosome 11p14-12. The MMD3 region on chromosome 11 overlaps with another autosomal recessive muscular dystrophy, designated LGMD2L3, and highlights the potential allelic heterogeneity of MMD3 and LGMD2L. The analysis of candidate genes mapping to chromosome 11p14-12 and the progress in the identification of the MMD3 gene will be described.<br>
1. Linssen, W.H.J.P. et al. (1998) Neuromuscular Disord., 8: 317-320.<br>
2. Jaiswal, J. et al. (2007) Traffic, 8: 77-88.<br>
3. Jarry, J. et al (2007) Brain, 130:368-380.</p>
