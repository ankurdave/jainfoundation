                    <p class="home"> <strong><font color="#0175C2">Clinical Trial Information </font></strong><br>

                      <br>

Dear Patients,<br>

<br>

Listed below are ongoing clinical trials. As you will notice, most of these trials  are directed towards other forms of muscular dystrophy (the majority are for Duchenne Dystrophy). It is important to understand that success in the trials below will directly impact progress towards a clinical trial for other forms of muscular dystrophy. Our goal is to expedite and plan for clinical trials for LGMD2B/Miyoshi so that we can initiate these as soon as possible, if we hear promising results from any of the trials below. There are two major steps as we plan for LGMD2B/Miyoshi specific trials:<br>

<br>

 1. Conduct all background experiments in mouse models, construct appropriate gene vectors, and outline the strategy for the trial.<br>

 <br>

 2. Recruit patients who have the mutations that are required for the specific kind of trial.<br>

<br>

We cannot take the second step without help from you. This is a very rare disease, so finding enough patients with appropriate mutations within this small group is next to impossible if you do not help us. Therefore, please help us by <a href="register.php" class="style2">registering</a>, telling everyone you know with the disease about our Foundation, and following up your diagnosis with a mutation analysis of your dysferlin gene. Please share this information with us and keep in touch with us through the process.<br>

<br>

Rest assured that we are doing what we can to implement the first step. In addition to funding scientific research projects related to the trials below, we are also funding research that will, we hope, eventually lead us to other strategies to combat this disease.<br>

<br> 

Let's work together to find a cure for this disease.

<br>

