                  <strong>Session IX: Clues from Other Ferlins and Myopathies</strong><br>

                  <br>


This session included talks on the function of ferlin family members in non-muscle tissues and on the identification of a gene responsible for a form of non-dysferlin Miyoshi Myopathy.<br><br>

<strong>Dennis Drescher</strong> (USA) discussed the role of otoferlin in the ear. Otoferlin and dysferlin are both members of the ferlin protein family, which also includes myoferlin, Fer1L4, Fer1L5, and Fer1L6. Patients with otoferlin mutations have profound deafness (DFNB9) but have normal otoacoustic emissions (Yasunaga et al. 1999, Tekin et al. 2005). Otoferlin is strongly expressed in inner hair cells of the ear and plays a role in exocytosis in the synaptic region between the hair cells and the acoustic nerve. Dr. Drescher's lab has studied the interaction of otoferlin with SNARE proteins through two of its C2 domains (C2F and C2D) (Ramakrishnan et al. 2009; Drescher et al. 2009). The C2F domain of wild-type otoferlin exhibits a calcium-dependent interaction with Syntaxin-1 and SNAP-25, and the C2D domain exhibits calcium-dependent binding to the calcium channel Cav1.3. Dr. Drescher's lab engineered otoferlin with known pathological mutations in the C2D and C2F domains (Leu1011Pro and Pro1825Ala, respectively) and demonstrated that the calcium-dependent interactions were inhibited by these mutations. <br><br>

<strong>John Robinson</strong> (USA) presented studies of dysferlin and myoferlin in the human placenta. Dysferlin and myoferlin are both highly expressed in purified tissue from the syncytiotrophoblast, a large polynuclear cell structure at the boundary between maternal blood and the fetus that is formed by fusion of fetal cytotrophoblasts (mononuclear precursor cells). Trophoblastic cell lines express myoferlin, but do not express dysferlin until they are induced to fuse. Knockdown of either dysferlin or myoferlin in one of the trophoblastic cell lines (BeWo cells) did not disrupt fusion. In the syncytiotrophoblast, there is a turnover of nuclei in which dead nuclei are removed though exocytosis. Dr. Robinson hypothesizes that high levels of dysferlin and myoferlin may be needed for extensive membrane remodeling due to the high rate of exocytosis. In collaboration with Dr. Paul McNeil, he is testing whether trophoblastic cell lines show a membrane repair defect in the absence of dysferlin. Dr. Robinson also found that expression of dysferlin was reduced by about 50% in placentas associated with pre-eclampsia, a disease of pregnancy in which the placenta causes health problems to the mother. <br><br>

<strong>Rumaisa Bashir</strong> (UK) discussed two other genetic loci, in addition to dysferlin, that give rise to the clinical diagnosis of Miyoshi myopathy (MM). One, designated MMD2, is located on Chromosome 10 and was identified in a study of patients in the Netherlands (Linssen et al. 1998). The gene responsible for MMD2 has not yet been identified. The second is the MMD3 locus, located on Chromosome 11, which overlaps a genetic locus for an autosomal recessive form of LGMD (LGMD2L) (Jarry et al. 2007). Dr. Bashir demonstrated that cells from MMD3 patients have defective membrane repair (Jaiswal et al. 2007). In two MMD3 families, homozygous pathogenic mutations were found in TMEM16E, a member of the TMEM16 (Anoctamin) gene family. Mutations in TMEM16E are also responsible for LGMD2L and autosomal dominant gnathodiaphyseal dysplasia (GDD1), which causes bone fragility (Tsutsum et al. 2004). From studies of other TMEM16 proteins (Hartzell et al. 2009; Pifferi et al. 2009), TMEM16E is predicted to function as a calcium-activated chloride channel. Dr. Bashir hypothesizes that during repair of membrane tears, TMEM16E may function as a counter-ion channel following calcium influx to maintain charge neutrality for efficient exocytosis.

