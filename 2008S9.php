                  <strong>Development of a Drosophila Model for Studying Ferlin Function and Dysfunction</strong><strong>&nbsp; &nbsp;</strong><br>

M. K. Smith, K. Wilson, and B.T. Wakimoto,<br><br>

Fundamental insights into the function of ferlin proteins have been obtained through studies of mutations and gene disruptions in animal models.  This was first demonstrated by Ward and colleagues in their studies of fer-1, the C. elegans gene and founding member of the ferlin gene family.  Similar to the C. elegans story, studies of male sterile mutations led to the discovery of the Drosophila ferlin gene in the laboratory of M. Yamamoto and in our laboratory.  Drosophila has only a single ferlin gene, denoted misfire (mfr), but it exhibits complex regulation at the transcriptional and post-transcriptional levels.  Analysis of a spectrum of missense and truncation mutations indicates the production of multiple protein isoforms and reveal a role for specific isoforms in three developmental processes: sperm activation during fertilization, egg patterning during oogenesis, and progression of early embryogenesis.  Missense mutations have been informative for identifying important domains of the protein.  We have also observed unanticipated detrimental effects of certain truncation mutations which may be informative for understanding the cellular processing of normal and mutant Ferlins.  Current efforts are aimed at further developing D. melanogaster as a model system for understanding the complex regulation of Ferlins and for discovery of interacting components affecting the processing, function, and dysfunction of Ferlin family members.   	  </p>


