                  <strong>Dysferlin and Myoferlin in the Human Placenta and in Trophoblastic Cells</strong><strong>&nbsp; &nbsp;</strong><br>

<br>

We identified dysferlin (DYSF) and myoferlin (MYOF) as placental proteins by proteomics profiling of a highly enriched fraction of the apical plasma membrane of the syncytiotrophoblast (STB) of the human placenta.  The STB forms the interface between maternal blood and the placenta itself and is crucial for placental function.  The presence of DYSF and MYOF in the placenta was validated by immunoblot assays and by immunofluorescence microscopy.  The major site for expression of these proteins in the placenta is the apical plasma membrane of the STB with lesser amounts in the basal plasma membrane.  Quantitative immunofluorescence microscopy showed that first trimester placenta had about 3 times more immunoreactivity for DYSF than did the full-term placenta.  Cytotrophoblasts (CTB), the precursor cells that form the STB, did not display immunolabeling for DYSF in situ.  When CTBs, isolated from full-term placentas, were maintained in cell culture they were DYSF negative until after spontaneously fusing to form syncytial structures.  Thus the in vitro results appear to recapitulate the in vivo situation.  Additionally, caveolin was not detectable in the STB.  BeWo cells, a trophoblastic cell line, are often used as surrogates for CTBs since they can be induced to fuse following treatment with forskolin.  We find that control-mononuclear BeWo cells do not express DYSF.  However, following forskolin treatment and fusion of BeWo cells, DYSF is expressed.  Other trophoblastic cell lines, JEG-3 and JAR, also show an increase in DYSF in fused cells.  However, these cells do not fuse as well as BeWo cells and are not as useful as models for CTB function.  Myoferlin was detected in each of these trophoblastic cell lines.  Additionally, MYOF was present in control BeWo, JEG-3, and JAR cells and was not modulated by forskolin treatment.  We have used lentiviruses carrying expression cassettes that encode small hairpin RNA (shRNA) targeting DYSF to generate stable BeWo cell lines (LV-BeWo) that do not upregulate DYSF expression in response to forskolin-induced cell fusion.  We find that DYSF expression was not detectable in the five LV-BeWo cell lines that we have made.  The LV-BeWo cells do however fuse when treated with forskolin.  The parental BeWo and LV-BeWo cells express MYOF in both control and forskolin-treated cells.  BeWo cells appear to be a good model for CTBs and for the study of DYSF and MYOF.				  </p>
