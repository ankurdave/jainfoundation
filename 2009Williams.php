                  <strong>Effect of Gender and Mutation Type on Age of Clinical Onset in Dysferlinopathy</strong><br>
                  Bradley A. Williams, Esther Hwang, Laura E. Rufibach<br><br>
                  Dysferlinopathy typically involves onset of clinical symptoms in adolescence or early  adulthood, but only rarely in childhood. The factors which influence the age of onset and  rate of progression are not currently known. For example, gender could be playing a role  based on anecdotal reports that males are more severely affected in mouse models of dysferlinopathy. In addition, a recent study reported a significantly later age of onset for dysferlinopathy patients with missense mutations, contrary to earlier analyses which did not find a genotype-phenotype correlation. To address these issues, we analyzed the clinical history of a large number of dysferlinopathy patients to determine the effect of gender and type of mutation on the age of onset of clinical symptoms. The Jain Foundation patient  registry currently includes nearly 100 patients in whom dysferlinopathy has been confirmed  by mutational analysis or protein deficiency. Additionally, our analysis included patients confirmed at a genetic or protein level reported in previously-published studies. The total cohort includes over 250 patients with confirmed mutations and  over 350 diagnosed at either a genetic or protein level. The average age of onset is similar between females (22.0  years) and males (21.5 years). Females, however, are much more likely to have symptoms in early adolescence: over 30% of females, but only 15% of males, have symptoms by age 16. Conversely, almost 50% of male patients, but only 26% of females, have onset in the age range of 17-20. These observations suggest that the onset of symptoms may be related to some aspect of maturation. We found a small, but noticeable effect of mutation type. Although patients with either type of mutation can have an early or late onset of symptoms, a statistically-significant majority of patients with onset after age 25 have at least one missense mutation. Also, most patients (10/12) with onset after age 40 are female. In conclusion, our data indicate that both gender and type of mutation influence the age of onset in dysferlinopathy. Extension of this study to more patients is expected to clarify the statistical significance of some of our observations.</p>
