                      <strong><font color="#0175C2"><span class="style1">Dysferlin in Apoptosis </span></font><font color="#0175C2"><br>

                      </font></strong>Christina Jamieson, University of California at Los Angeles (Los Angeles CA) <br>

                      <br>

                      <strong>Project Objective</strong><br>

                      Identify the role of dysferlin in apoptosis in lymphocyte and muscle   cell development<br><br>

Apoptosis is the process   of programmed cell death in which the cell is dismantled and cleared   without damage to the surrounding tissue. It is well established that   defects in T cell apoptosis can lead to immune defects such as autoimmune   diseases; however, the role of apoptosis in muscle development and homeostasis   is less understood. Using genome-wide   DNA microarray gene expression profiling, we found that glucocorticoids   activated expression of dysferlin in CD4+ T lymphocytes during apoptosis. In the absence of dysferlin, apoptosis signals may   trigger a defective process that instead results in necrosis, that is,   uncontrolled cell rupture and release of toxic cell contents causing   damage and inflammation in the surrounding tissue. We are using mouse   muscle, mesenchymal stem cells and T cell development models as well   as human muscle and lymphoid cells to elucidate the molecular mechanism   of dysferlin function in apoptosis and the potential defect in its absence.   We will determine if this is a specific response to glucocorticoids   or part of a general pathway for apoptosis. Our aim is to identify novel   targets for molecularly targeted therapy that restore the apoptosis   pathway in dysferlin-deficient cells.<br>

