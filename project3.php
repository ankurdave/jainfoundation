                    <p class="home"><strong><font color="#0175C2"><span class="style4">CD55 Modulation</span><br>

                    </font></strong>Tejvir Khurana, University of Pennsylvania

                    (Philadelphia, PA)<br>

                        <br>

                         <strong>Project Objective</strong><br>

                         Identify activators of CD55 and use them to analyze

                         damage inhibition<br>

                          <br>

                          Different muscle groups are affected differently in

                           dysferlinopathies. Some muscle groups are not involved

                           at all. This aspect of the disease offers clues leading

                           to cure if the molecular driver of this difference

                           is identified. Data from others has shown that CD55

                           is down regulated in affected muscles in Miyoshi patients.

                           CD55 plays a role in inhibiting complement-mediated

                           damage caused to muscle it is possible that up regulation

                           of this factor will help prevent damage to muscle.

                           The human CD55 promoter gene is cloned and an assay

                           is being developed using stable cell lines where we

                           can screen and test several small molecule and drug

                           screening to identify an activator of CD55.<br>

