                  <em>Laboratory of Dr. Nicolas Lévy, Neuromuscular Disorders and Laminopathies, Inserm U491</em><em></em><br><br>

                  <img src="images/conference2008/Wein.jpg" width="135"><br><br><br>

                  <strong>Screening of and Therapeutic Approaches in Dysferlinopathies Using Peripheral Blood Monocytes </strong><strong>&nbsp; &nbsp;</strong><br>

Nicolas Wein1, Martin Krahn1,2, Sebastien Courrier1, Veronique Labelle2, Patrick Nusbaum3, France Leturcq3, Pierre Cau1 and Nicolas Levy1,2
1Team 5: neuromuscular disorders and laminopathies, Inserm U910, Marseille, France
2Department of medical genetics, Timone hospital, Marseille, France
3Biochemical and molecular genetics, Cassini Laboratory, Cochin hospital, Paris, France


 <br><br>
 Dysferlinopathies  are autosomal recessive muscular dystrophies caused by <em>DYSF</em> mutations, which lead to reduced or a complete lack of dysferlin.  &nbsp;Diagnosis is first based on the clinical,  biological (CKs) and skeletal muscle CT-scan or MRI analyses. &nbsp;The second diagnosis step consists of western-blot  analysis of proteins extracted from muscle biopsy, or blood monocytes, which express  dysferlin. &nbsp;Noteworthy, monocytes  represent a cellular model  easier to obtain than muscle biopsy samples, in particular for patients  suffering from muscular dystrophy. <br>
In regard to the  accessibility of monocytes, whole blood flow cytometry with antibodies directed  against dysferlin appear to be a promising approach. &nbsp;We evaluated detection of dysferlin by FACS  using three different antibodies (Hamlet-1 which recognizes amino-acids  1999-2016; Hamlet-2: aa349-366; and sc-16634: internal region of dysferlin).  Four patients were subjected to this analysis. &nbsp;In two patients carrying mutations predicted  to lead to truncated dysferlin that lacks the transmembrane domain (homozygous  c.1392dupA - p.Asp465ArgfsX9; compound heterozygous: intron33:c.3703-1G&gt;A  and c.490G&gt;T - p.Gly164X), we obtained no signal. &nbsp;These results suggest that truncated dysferlin  is either not produced, or degraded. &nbsp;In  two other patients predicted to produce dysferlin protein containing its transmembrane  domain, (compound heterozygous c.1157_1168delTCCGGGCCGAGG - p.Phe386_Asp390delinsTyr  and c.1663C&gt;T - p.Arg555Trp; and a patient with a homozygous 40 exons  deletion), a fluorescent signal was detected in monocytes. &nbsp;These results have been confirmed by western-blot  and immunofluorescence analysis on monocytes. &nbsp;Additional patients have been recruited, and  the corresponding FACS analysis on their monocytes are ongoing. &nbsp;The possible usefulness of this flow cytometry  analysis in routine diagnosis will be discussed. &nbsp;<br>
Monocytes also  represent a possible <em>ex-vivo</em> model  for the evaluation of different therapeutic approaches, including the possible  efficiency of pharmacological compounds. &nbsp;Stop-codon readthrough using aminoglycosides  has been shown to be at least be partially efficient in <em>mdx </em>mice, and in human genetic disorders caused by nonsense  mutations. &nbsp;With the objective of  identifying readthrough-efficient gentamycin batches for further use in patient  monocytes, we conducted preliminary tests on a fibroblast cell line, which does  not express <em>DYSF</em>. &nbsp;Therefore, we selected 2 nonsense mutations  associated with dysferlin deficiencies as observed in patients. &nbsp;We constructed <em>DYSF</em>-cDNA-reporter-vectors carrying the corresponding mutations,  and transfected them into wt fibroblasts. &nbsp;Eight different gentamycin batches were evaluated  at different concentrations for their readthrough efficiency. &nbsp;Unfortunately, no evidence of readthrough was  obtained. &nbsp;We intend to test additional  compounds, including PTC124, using the same strategy.
