				<p><span class="style10">Session VIII: Lessons From the Clinic - Clues to a Cure</span><br>
				  
				  
				  <br>
				  <br>
				  
				  <strong><span class="style17">Isabel Illa, MD</span></strong><br>
				  
				  <em>Hospital Santa Creu i Sant Pau, Universitat Autònoma, Barcelona, Spain</em><br>
				    <br>
				  
				    <img src="images/Conf09/Illa.jpg" width="135"><br>
				    <br>
				  
				    <strong>Dysferlin Myopathies: Preliminary Approach to Disease Progression</strong><br>
				  Carmen Paradas, Eduard Gallardo, Isabel Illa<br>
                    <br>
				  
				  Background: Dysferlin myopathies present with several phenotypes and with different degrees of clinical deterioration. Descriptions of its clinical course are based on crosssectional and retrospective studies. Attempts to correlate the degree of clinical deterioration with different mutations in the dysferlin gene (DYSF) have failed. Patients: We conducted a quantitative study of the natural history of 5 limb girdle muscular dystrophy 2B (LGMD2B) and 4 Miyoshi myopathy (MM) patients, over an 18-month follow-up period who fulfilled a series of previously established criteria (age, clinical status, previous treatments). Methods: We performed the following standardized protocol every 3 months: a) a 13-point scale modification of the Medical Research Council to grade 15 muscle groups (MMT) b) a Quantitative Muscle Testing on 16 muscle groups (QMT) and c) the Rankin and Rotterdam scale, and d) three timed tests (stand up from a chair, climb and go down 12 steps and walk 10 m). Also, at the beginning and end of the study we performed: a) quantification of serum creatine kinase, b) electrocardiogram and pulmonary function tests and c) magnetic resonance imaging (MRI) of lower limbs. Results: We detected a significant decline in composite MMT score (CMMT) (p= 0,007) only in the global group but not in any of the 15 movements individually. We detected a significant decline in the final knee flexion score with QMT (p< 0.008) again when the group was considered globally, however we did not find any other significant change for any movement by QMT in individual patients. No significant worsening was observed in any of the functional tests, nor in respiratory and cardiac function. The MRI detected measurable deterioration changes in the biceps femoris and tibial posterior muscles (p< 0.05), that showed a slower deterioration during the follow-up. No association with age, age at onset, duration of symptoms or the phenotype was found. No improvements were observed in any of the tests performed. Conclusions: This preliminary study establishes a similar deterioration of the quantified measurements of progression in dysferlinopathies over an 18 months period regardless of the phenotype, and points out muscle MRI as a reliable qualitative and quantitative method for future studies.</p>
				<p><a name="2"></a>

				  <br>

                  <strong><span class="style17">Kate Bushby, MD</span></strong><br>

                  <em>Institute of Human Genetics, University of Newcastle, UK</em><br><br>

                  <img src="images/Conf09/Bushby.jpg" width="135"><br><br>

                  <strong>Dysferlinopathy - Newcastle Update</strong><br>
Yen-Hui Chiu, Mark A. Hornsey, Lars Klinge, Louise H. Jorgensen, Steven H. Laval,
Richard Charlton, Rita Barresi, Volker Straub, Hanns Lochmuller, Kate Bushby <br>
<br>

Newcastle serves as the diagnostic centre for limb-girdle muscular dystrophies for all of UK. Mutation analysis for dysferlin has come on line in the past year, having been previously contracted to the lab of Simone Spuler in Germany. A recent review of 36 confirmed UK patients has allowed us to identify 17 novel mutations, with no hotspots in the British population. 61% of our cohort presented with LGMD2B, perhaps reflecting a bias in the referral group into our LGMD service. 25% of patients had onset of the disease before age of 13 years. By contrast to other forms of LGMD, 53% of this group reported having been very active and sporty in childhood, indicating that a truly presymptomatic period may exist in this condition. As part of this review of dysferlinopathy, we observed a higher than expected proportion of immature muscle fibres in patient muscle, and moved on to study the process of regeneration in dysferlinopathy in more detail. By contrast with control animals, the SJL/BL10 Dys-/- model and the Campbell dysferlin knock out both show attenuation of the regeneration response which is associated with a delay in neutrophil recruitment. This poor neutrophil response can also be shown in response to needle wounding. Repeated cycles of regeneration are associated with increased muscle pathology. Our findings suggest that dysferlins role in membrane repair may be accompanied by a role in secretion of chemokines responsible for attracting neutrophils to damaged muscle. Further definition of this pathway is necessary, but already this system provides a way to measure response to potential treatment strategies. Preclinical work on dysferlinopathy cannot take place in isolation from the considerations of trial readiness for this patient group. The TREAT-NMD network of excellence, co-ordinated from Newcastle, offers a collaborative platform for the development of unified registries, validation of outcome measures and a network of trial sites who could contribute to natural history studies and trials.</p>


<p>

				  <a name="3"></a><br>

                  <strong><span class="style17">Nicolas Lévy, MD, PhD</span></strong><br>

                  <em>Hôpital Timone Enfants, APHM, Marseille, France</em><br><br>

                  <img src="images/Conf09/Levy.jpg" width="135"><br><br>

                  <strong>From Patient Diagnosis to Therapeutic Strategies</strong><br>
Main collaborators in-house: Martin Krahn1,2, Marc Bartoli2, Nicolas Wein2, Pierre Cau1,2.
1Département de Génétique Médicale et de Biologie Cellulaire, Hôpital Timone Enfants, APHM,
Marseille, France; 2INSERM UMR910, Université de la Méditerranée, Faculté de Médecine de
Marseille, France<br>
Main collaborations on the project: Isabelle Richard3; Christophe Béroud4; Aurélie Avril5, Luis Garcia5;
Vincent Mouly5, Gillian Butler-Browne5; France Leturcq6 ;J. Andoni Urtizberea7 and the French Network
on Dysferlinopathies
3Genethon Laboratories, Evry, France; 4CHU de Montpellier, INSERM U827, and Université Montpellier
1, Montpellier, France; 5Institute of Myology, AFM, Hôpital Pitié-Salpêtrière, Paris, France; 6Service de
Biochimie Génétique, Hôpital Cochin, AP-HP, Paris, France; 7Hôpital Marin, AP-HP, Hendaye, France<br>
<br>

Accurate molecular diagnosis is a major problem in dysferlinopathies. In 10 to 20% of patients, only one mutation is detected, while an additional 10% of patients have no identified mutations. This problem is the result of either insufficient mutation detection rates, and/or the involvement of other genes, particularly those encoding interacting partners of dysferlin. To address these problems, we developed tools for more accurate molecular diagnosis. First, we developed a MLPA® technique and CGH-array chips, to detect large deletions and insertions within the DYSF gene. Second, a sequence capture chip for deep intronic sequencing of the DYSF gene was created. Finally, to identify mutations in other genes potentially involved in dysferlinopathy-like disorders, we designed CGH-array chips spotted with probes to analyse candidate genes chosen from data mining. This large-scale analysis is predicted to dramatically increase the efficiency of molecular diagnosis in dysferlinopathies. The molecular diagnosis of patients can provide valuable clues to new therapeutic approaches. Last year we presented a mini-dysferlin molecule, identified in a patient with a moderately severe dysferlinopathy, that is functional for membrane repair when delivered by AAV vectors into muscle. We have continued to test and refine this mini-dysferlin, by in vivo transfer into dysferlin deficient mice, and by testing additional constructs with more C2 domains to allow for increased physiological interactions between the mini-dysferlin and known partner proteins. Exon skipping is another promising therapeutic approach where there is a natural proof of concept; a patient with an in-frame deletion of exon 32 and a very moderate phenotype was reported by Sinnreich et.al, (Neurology, 2006). In our cohort, seven patients have premature termination codons in exon 32. We used bioinformatics analysis to identify the best targets for exon skipping, and transfected 2'O-Methyl oligonucleotides into patient myotubes trans-differentiated from fibroblasts. After 48 hours, we show skipping of exon 32 by RT-PCR. We then constructed lentiviral vectors targeting exon 32, and observed a skipping efficiency of about 50%. We are now testing antisense sequences under the control of U7 snRNA delivered by AAV vectors (preferred for clinical use) in human dysferlin deficient myotubes carrying a premature termination codon in exon 32.</p>

<p><a name="4"></a>

				  <br>

                  <strong><span class="style17">Eric P. Hoffman, PhD</span></strong><br>

                  <em>Childrens National Medical Center, Washington, D.C.</em><br><br>

                  <img src="images/Conf09/Hoffman.jpg" width="135"><br><br>

                  <strong>Modifiers of Muscular Dystrophy: How Do Exercise Regimens or Genetic Modifier
Loci Alter Disease Progression?</strong><br>
Eric Hoffman1, Micaela Iantorno1, Tina Duong1, Akanchha Kesari1, Luisa Piva2, Luca Bello2,
Susan Sparks3, Robert Leshner1, Craig McDonald4, Avital Cnaan1, Kanneboyina Nagaraju1,
Corrado Angelini2, Elena Pegoraro2, and the CINRG Investigators
1Research Center for Genetic Medicine, Childrens National Medical Center, Washington,
DC. 2Neurology, University of Padova, Padova Italy. 3Medical Genetics, Carolinas Medical
Center, Charlotte, NC. 4Physical Medicine and Rehabilitation, UC San Diego, San Diego,
CA <br>
<br>

Clinical heterogeneity in presentation and progression are well-recognized in dysferlin deficiency. Despite a shared biochemical defect, muscular dystrophy patients can show early or late onset, rapid or slow progression, with distal or proximal weakness predominating. Modifiers of disease severity include environmental or physiological effects (such as activity and exercise), as well as genetic modifiers (polymorphisms in non-disease genes that effect disease presentation and progression). Here, we report pre-clinical studies of quantified exercise in dysferlin-deficient mouse models, and identification of genetic modifiers in two cohorts of Duchenne muscular dystrophy patients (CINRG cohort, Padova cohort). 17 week old A/J mice (n=40) were given 21 wks of exercise interventions: Untreated (NT; n=10), Horizontal Treadmill (HT; n=10), Downhill Treadmill (DT; n=10) and Voluntary Wheel (VW; n=10). Treated mice underwent 48 exercise sessions: HT and DT ran for 15 min at 4 m/min followed by another 15 min at 6 m/min, twice a week. VW mice were placed in voluntary wheel 3 times/week for 24 hour sessions. Baseline (17 weeks), midpoint (28 weeks) and endpoint (38 weeks) measures were taken to assess the efficacy of these exercise regimes in improving muscle function. Muscle function was assessed using behavioral (grip strength and rotarod), physiological (muscle force), biochemical (CK assay) and histological parameters (inflammation, degeneration and regeneration). Results showed relatively mild yet statistically significant improvements of behavioral and physiological endpoints in A/J mice with training. Improvements were greatest at the midpoint (28 wks) coinciding with the known histological onset of disease in AJ mice. We are currently assessing biochemical and histological data to provide more insight on the effects of exercise stressors on the physiological capabilities of muscle strength and function. <br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Genetic modifiers are polymorphisms in genes that modify the progression of muscular dystrophy. Identification of validated loci may identify new therapeutic targets. Large numbers of well characterized patients are typically required to enable identification of genetic modifiers, and this is currently impossible for LGMD2B/Dysferlin. However, Duchenne muscular dystrophy (DMD) is more frequent, and genetic modifiers of DMD are likely to be relevant to LGMD2B. We present the first validated genetic modifier of DMD, using longitudinal natural histroy cohorts followed in Padova Italy (n=148), and the UC Davis/CINRG Duchenne Longitudinal Study (n=340). A haplotype in osteopontin (SPP1) is seen to significantly alter disease progression. Osteopontin is an immune modulator, and associated with connective tissue homeostasis. Osteopontin is dramatically elevated in dysferlin deficiency, calpain deficiency, and DMD; three disorders associated with failed regenerative processes. We hypothesize that osteopontin becomes a new therapeutic target in the muscular dystrophies, and this is supported by recent data showing that osteopontin modulates T regulatory cells in muscle, and double knockouts showing improved phenotypes (Sylvia Vetrone, Melissa Spencer; JCI, in press).</p>

