                        <br>

                         <strong>Project Objective</strong><br>

                         Characterize and explore ferlin members as potential

                         substitutes to dysferlin function<br>

                          <br>

                          There are at least five ferlin genes in the mammalian

                           genome. The ferlin protein family ins incompletely

                           characterized at present. It is possible that one

                           or more of these may be able to substitute for dysferlin.

                           Myoferlin is highly related to dysferlin by sequence

                           homology. Myoferlin is significantly expressed in

                           myoblasts and then downregulated in mature myotubes

                           and mature muscle. The project explores if increased

                           myoferlin expression functionally is able to functionally

                           replace dysferlin.<br>

