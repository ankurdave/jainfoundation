                  <strong>Non-Viral Gene and Cell Therapy for Dysferlin Deficiency </strong><strong>&nbsp; &nbsp;</strong><br>

W.E. Jung, A. Keravala, L.E. Woodard, and M.P. Calos<br><br>

We are developing two strategies to restore dysferlin to muscles, utilizing mouse models deficient in dysferlin.  In one strategy, plasmid DNA encoding dysferlin is delivered to muscle fibers.  In the other strategy, autologous mesoangioblast stem cells are corrected by transfection of the dysferlin gene.  Both strategies employ phiC31 integrase, a site-specific recombinase, to incorporate the dysferlin gene stably into the genome of recipient cells.  Success in these proof-of-principle experiments will move us toward clinical application of one or both of these strategies.
Experiments with direct DNA delivery in mouse hindlimbs revealed that hydrodynamic injection into the saphenous vein yielded limited delivery to muscle fibers.  By contrast, intramuscular DNA injection followed by electroporation provided robust delivery into muscle fibers.  Both methods were well tolerated in 6 – 12 week old A/J mice.  We detected sequence-specific integration of the donor plasmid into genomic DNA in the hindlimb muscle after electroporation.  We have constructed a plasmid encoding mouse dysferlin cDNA, as well as luciferase, controlled by muscle-specific creatine kinase promoters.  We are introducing this plasmid, along with integrase, into muscles of dysferlin-deficient mice and are monitoring dysferlin expression.
Our experiments with mesoangioblast stem cells utilize mouse mesoangioblasts isolated in the laboratory of Dr. Suzanne Berry.  We showed that the mesoangioblasts can be transfected efficiently (~76%) with Lipofectamine Plus.  After integration, the mesoangioblasts were capable of differentiating into fibers upon co-culture with C2C12 cells.  Luciferase signal was detectable after intramuscular injection of CMV-luciferase-integrated cells in mice.  We are currently following dysferlin signal after injection of normal C57/BL mesoangioblasts into dysferlin knock-out mice, and mesoangioblasts are being isolated from A/J mice.
				  
				  </p>

