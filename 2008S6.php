				<p><span class="style10">Session VI: Gene Therapy </span><br>


				  <br><br>

                  <strong><span class="style17">Isabelle Richard, PhD</span></strong><br>

                  <em>Laboratoire Dystrophies des Ceintures, Généthon-CNRS
richard@genethon.fr
</em><br><br>

                  <img src="images/conference2008/Richard.jpg" width="135"><br><br>

                  <strong>AAV-Mediated Transfer of the Dysferlin Gene Using a Dual Vector Strategy</strong><strong>&nbsp; &nbsp;</strong><br>

<br>

Deficiency in dysferlin presents as two major phenotypes: Limb Girdle Muscular Dystrophy type 2B (LGMD2B) and Miyoshi Myopathy (MMD1).  Dysferlin is known to participate in membrane repair, providing a preliminary understanding of the pathophysiological mechanism in the diseases.  Considering the recessive nature of dysferlinopathies, a possible therapeutic strategy is gene transfer.  However, the size of the dysferlin cDNA (nearly 7 kb) prevents its direct incorporation in Adeno Associated Virus (AAV) vector, the best vector for transfer in muscle to date.  To bypass this limitation, we applied an approach based on the concatemerization property of the AAV vector.  We split the dysferlin cDNA at the exon 28/29 junction and cloned it into two AAV2/1 vectors.  We evaluated the efficiency of these vectors to reconstitute dysferlin after intramuscular and intravascular injection in a murine model for LGMD2B.  We observed efficient and stable full-length dysferlin expression in muscle at the RNA and protein levels.  We also showed that expression of the transgene is associated with restoration of the membrane repair potential.

                </p>
