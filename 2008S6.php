                  <strong>Partial Functionality of a Mini-Dysferlin Molecule Identified in a Patient Affected with Moderately Severe Primary Dysferlinopathy </strong><strong>&nbsp; &nbsp;</strong><br>

In collaboration with Isabelle Richard’s lab and Paul McNeil’s lab<br><br>

Primary dysferlinopathies are a heterogeneous group of autosomal recessive muscular dystrophies, caused by mutations in the large-sized DYSF gene encoding dysferlin.  Dysfunction of dysferlin causes deficient sarcolemmal repair and leads to muscle fiber degeneration.  In a patient born from a distantly consanguineous union, presenting with moderately severe primary dysferlinopathy, we identified a homozygous multi-exonic internal deletion using genomic, transcriptional and F.I.S.H. analyses.  Currently, this patient presents with proximo-distal weakness, but is still ambulant without a cane at 41 years.  Bioinformatic analysis of putative Open-Reading-Frames within the resulting, deleted transcript predicts a possible translation into a truncated protein maintaining two C2 domains and the transmembrane domain.  While muscle biopsy samples were not available, dysferlin protein analyses on monocytes obtained from the patient evidenced expression of a truncated molecule at the expected size, which localizes to the plasma membrane.  Ectopic expression of the predicted Open-Reading-Frame using transfection into fibroblasts confirmed targeting to the plasma membrane.  To further evaluate the functionality of this mini-dysferlin, we constructed an AAV-vector containing the predicted Open-Reading-Frame identified in the patient under a muscle specific promoter.  Intra-muscular injection of the mini-DYSF-AAV vector in wild-type and dysferlin-deficient mice led to high-level expression of the truncated protein.  Using a membrane repair assay, based on membrane wounding of transduced isolated muscle fibers with a two-photon laser-scanning microscope, we demonstrated that the mini-dysferlin efficiently reseals the damaged sarcolemma.  Therefore, this mini-dysferlin is at least partially functional.  Importantly, this demonstrates the modularity of dysferlin.  In Duchenne muscular dystrophy, the identification of functional mini-dystrophins led to the development of therapeutic strategies such as gene transfer of mini-dystrophin and exon-skipping approaches.  Our findings constitute a prerequisite for similar therapeutic strategies for a subset of primary dysferlinopathies. </p>


