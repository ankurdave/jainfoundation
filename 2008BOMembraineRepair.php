<strong>Membrane Repair</strong><br><br>

Moderators:  Doug Albrecht, Brad Williams  <br><br>
Participants:  Norma Andrews, Rumaisa Bashir, Bob Brown, Glen Humphrey, Jyoti Jaiswal, Nicholas Keep, Srinagesh Koushik, Paul McNeil, Anna McNeil, Jeff Molkentin, Isabelle Richard, John Robinson, Joseph Roche, Christian Therrien, Mohan Viswanathan, Steve Vogel, Barbara Wakimoto, Woody White, Josh Zimmerberg<br><br>

1.	What are the different mechanisms a cell uses to reseal its membrane?  <br>
•	Ca++ dependent vs Ca++ independent repair, how do they differ?<br>
•	Does the size of the hole make a difference and is this important from a pathological standpoint?<br>
•	What are the various roles of exocytosis and endocytosis in membrane repair?  Are they linked? <br>
•	What is the origin of the intracellular vesicles used in membrane repair?  What happens to them when repair is defective?  <br>
•	Are there different repair mechanisms in different cell types?  <br><br>

2.	What is Dysferlin’s role in membrane repair?<br>
•	Is dysferlin directly involved in membrane fusion events?<br>
•	What other proteins work with dysferlin in the membrane repair process? <br>
•	Do dysferlin deficient cells have other processes that are impaired or other problems that involve membrane fusion?  <br><br>

3.	Which Assays are effective for measuring membrane repair and what are their limitations? <br> 
•	Laser wounding<br>
•	Glass beads<br>
•	Osmotic pressure<br>
•	Scraping<br>
•	Detergents<br>
•	Protein pores (bacterial toxins, Complement/Membrane Attack Complex)<br>

