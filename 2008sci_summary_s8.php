                  <strong>Session VIII: Ferlin Structure and Function</strong><br>

                  <br>
This session addressed the comparative expression patterns, folding structure, and interaction partners of ferlin family member proteins.  There are six human ferlins, which can be separated into two groups based on homology and conserved domain structure.  The proteins dysferlin, myoferlin, and FER1L5 comprise one of the two homology groups, with a distinguishing feature being the presence of a DysF domain of unknown function in the central region of the protein.<br><br>

<strong>Dr. Rumaisa Bashir</strong> (University of Durham) presented studies comparing the expression patterns of the three DysF-containing ferlins.  Using antibodies raised against myoferlin and FER1L5, Dr. Bashir’s laboratory found that all three proteins show co-localization in C2C12 myotubes, and that dysferlin and FER1L5 partially co-localize in adult muscle.  To prompt discussion on whether dysferlin may also have a role in signaling, Dr. Bashir reported that she has found nuclear localization of all three DysF ferlins during early myogenesis.  Other experiments, however, showed that these three proteins are <em>not</em> early response genes during myogenesis.  Cell lines deficient in myoferlin and FER1L5 were created and found to have delayed myoblast fusion, which has also been reported for dysferlin.  Dr. Bashir also briefly discussed another project in her laboratory, in which she is analyzing two genetic loci from families who show symptoms similar to Miyoshi myopathy but who do not have dysferlin deficiency.  Identification and study of the proteins involved in these cases may identify interaction partners of dysferlin and/or dysferlin-independent alternative repair pathways.<br><br>

<strong>Dr. Silvère van der Maarel</strong> (Leiden University Medical Center) reported his work on identifying interaction partners of dysferlin.  Much of his effort has been directed to developing appropriate antibodies for these proteins, and he described in detail the use of phage display-derived antibodies from llamas.  Although it has been known for some time that calpain-3 (the protein that is defective in LGMD2A) can sometimes show secondary deficiency in cases of primary dysferlin deficiency, the relationship between the two proteins has been unclear.  Dr. van der Maarel’s laboratory has found that dysferlin binds to the large protein AHNAK, and that calpain-3 cleaves AHNAK, which disrupts the association of AHNAK and dysferlin.  Dr. van der Maarel also briefly discussed the expression of dysferlin in different cell types and noted that dysferlin is highly expressed in Purkinje cells of the cerebellum.  This might imply that some aspects of the phenotypes of the mouse models of dysferlin deficiency, for example, the animals’ inability to balance on a rotating rod, might be an effect of coordination as well as strength.<br><br>

<strong>Dr. Nicholas Keep</strong> (Birbeck University of London) discussed the folding structure of the DysF domain of dysferlin and myoferlin.  The DysF domains in these proteins are nested, resulting from an insertion of one DysF domain inside another.  Dr. Keep’s laboratory determined the structure of the inner DysF domain of myoferlin.  The amino acid sequences of this region are very similar for dysferlin and myoferlin, and for technical reasons the myoferlin structure was easier to determine experimentally.  The structure was determined by first creating a soluble protein fragment of the appropriate region of the protein (just the inner DysF domain), and then using nuclear magnetic resonance (NMR) imaging of 1H, 13C and 15N (the latter two using isotopically-labeled amino acids).  The wide distribution of 1H-15N peaks indicated that the protein was folded.  By assigning each atom to a particular chemical shift, constraints on distances between the atoms can be used to obtain a computed protein structure that is consistent with the NMR spectra.  <br><br>

The major feature of the structure obtained for the DysF domain of myoferlin is a beta sheet which has a “stack” of tryptophan and arginine residues (a W/R stack).  This stacking occurs in some otherwise unrelated proteins, such as the cytokine receptor and the thrombospondin repeat.  Various missense mutations located in this DysF region and known to be pathological were mapped onto the structure to determine their likely effect on folding structure or charge distribution, and many of them are in locations where they would disrupt the W/R packing.  Analysis of the charge distribution of the DysF domain indicates there are regions that may bind metals, but not calcium or magnesium.  It was found that addition of zinc, nickel, or copper caused the protein to precipitate. 

