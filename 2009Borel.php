                  <strong>Characterisation of Size and Site of Membrane Tears Occurring After Damage in
Dysferlin-null Mice</strong><br>
Perrine Borel1, Jessica Petiot1, Joseph A. Roche2, Robert J. Bloch2, Daniel Stockholm1<br>
1G&eacute;n&eacute;thon, &Eacute;vry, France, 2Department of Physiology, School of Medicine, University of<br>
Maryland, Baltimore, MD 21201. USA.<br><br>

Membrane tears in dysferlin deficient muscle fibers are the effects of a physiologic
phenomenon occurring after a mechanic stress like eccentric exercise. Membranes rips can
either lead to fiber necrosis or repair but basic questions still remain on the precise
characteristics of the membrane tears. Therefore, we have started to investigate different
methods of characterizing the size and location of membrane tears in BlaJ models.
In this work, we explore three different techniques inducing membrane damage in dysferlin
deficient mice compared to wild type mice using fluorescence microscopy both on
histological sections and muscle in situ in living mice. In order to characterise the size of the
holes, we use for each technique a battery of fluorescent-tagged dextrans of different sizes in an exclusion assay. The
first injury protocol consists in a downhill run on a treadmill. Preliminary data were obtained showing that a
permeabilisation of a few fibers of the lower limb and pelvis muscles could be verified by the entry of fluorescent
dextrans. The size of the membrane tears generated after such exercise can be estimated thanks to the higher size of
dextrans able to diffuse through the holes that seems to be near 10kDa. The second injury protocol uses a dedicated
chamber enabling stretching and isometric contraction of isolated EDL muscle with a real-time confocal imaging. This
device allows the monitoring of the dynamics of tearing events occurring immediately after the injury. The third and last
injury protocol consists of the large strain injury protocol of Roche, Lovering, and Bloch (Neuroreport 19 (2008) 1579-
84.), which should give information about the synchronisation between injury and membrane tears, as well as the
evolution of the shape and location of membrane ruptures under conditions close to eccentric exercise. </p>
