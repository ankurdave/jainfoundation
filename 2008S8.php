				    <strong>Nuclear Trafficking of Dysferlin, Myoferlin and FER1L5 in Muscle Cells</strong><strong>&nbsp; &nbsp;</strong><br>
				  
                    <br>
				  
				  The ferlin protein family has emerged following the identification of the dysferlin gene.  There are six mammalian ferlin proteins subgrouped in two functional groups based on their sequence homologies and molecular features of their C2 domains.  Myoferlin and FER1L5 which are subgrouped with dysferlin share sequence similarities, high muscle gene expression and overlapping subcellular distribution in muscle cells highlighting a role for these ferlins in muscle membrane fusion.  These ferlins also contain the novel DysF domain and have thus been classed as the DysF ferlins.  To assess whether myoferlin and FER1L5 are compensatory proteins of dysferlin we have pursued investigations aimed at understanding the muscle cell biology of the DysF ferlins which presently is poorly understood.  Examination of the distribution of the DysF ferlins during C2C12 myoblast fusion has revealed that dysferlin, myoferlin and FER1L5 traffic to the nucleus.  This data coupled with the emerging evidence that dysferlin and myoferlin may function in membrane remodelling through signalling pathways has prompted us to examine the nuclear trafficking of the DysF ferlins in muscle cells in more detail. In this talk we will present data on the nuclear solubility profiles of the DysF ferlins, their co-localization in muscle cells and whether the DysF ferlins are early markers of muscle regeneration.				  </p>
