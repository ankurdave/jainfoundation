                  <strong>Comprehensive Analysis of the Dysferlin Protein Complex in Skeletal Muscle</strong><strong>&nbsp; &nbsp;</strong><br>

<br>

Dysferlin is proposed to play a role in patch fusion repair of the skeletal muscle membrane after damage.  It is becoming increasingly apparent that dysferlin may also be involved in membrane maintenance and synthesis.  Nevertheless, a comprehensive understanding of the dysferlin protein complex in health and disease is not available.  
We aim to unravel the spatiotemporal composition of the dysferlin protein complex in adult skeletal muscle and in differentiated myotubes in cell culture.  To this end we have selected two phage display derived heavy chain antibody (HCAb) fragments against dysferlin.  These two antibody fragments, F4 and H7, were selected from a non-immune library representing the llama heavy chain antibody repertoire.  All camelid species have two IgG repertoires, one conventional repertoire and one heavy chain repertoire in which the IgG molecules are composed of a dimer of heavy chains and which are devoid of light chains.  This heavy chain repertoire is eminently suited for phage display and typically yields 15-20 kDa large high affinity protein binders that are very stable, soluble and can be used in a variety of conventional and non-conventional immunological applications.
Employing F4 and H7 in immunoprecipitation experiments of skeletal muscle, we showed that calpain 3, the Ca2+ dependent cysteine protease of skeletal muscle which is often secondarily reduced in patients with a dysferlinopathy, is in complex with dysferlin.  Also, we showed that AHNAK, a giant protein implicated in a wide range of cellular activities including plasma membrane enlargement and repair, is in complex with dysferlin in a calpain 3-dependent manner.  We are currently systematically using IP followed by mass spectrometry to identify novel partners of dysferlin under different conditions.  We expect this strategy to contribute to our understanding of the function and regulation of the dysferlin protein complex in skeletal muscle. 
				  </p>

