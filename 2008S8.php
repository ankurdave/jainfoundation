                  <strong>The Atomic (NMR) Structure of the Inner DysF Domain of Myoferlin and the Interpretation of Dysferlin DysF Mutations</strong><strong>&nbsp; &nbsp;</strong><br>

<br>

The ferlins are large proteins characterised by multiple C2 domains and a single C-terminal membrane spanning helix.  However, there is sequence conservation in some of the ferlin family in regions outside the C2 domains.  In one annotation of the domain structure of these proteins, an unusual internal duplication event has been noted where a putative domain is inserted in between the N and C terminal parts of a homologous domain.  This domain is known as the DysF domain.  We present the solution structure of the inner DysF domain of the dysferlin paralogue, myoferlin, determined by multi-dimensional heteronuclear nuclear magnetic resonance spectroscopy (NMR).  This shows that the DysF domain has a unique fold held together by stacking of arginine and tryptophan.  This work confirms that the sequence homology corresponds to a folded domain and defines the domain boundaries for protein interaction studies.  A significant number of clinical mutations in dysferlin map to the DysF domains particularly the inner DysF domain.  These include some of the most commonly reported disease-causing mutations in the Leiden database- R959W (reported 22 times) and W999C (reported 15 times).  In the light of our structure we propose that these mutations would lead to the DysF domain not being able to fold, either making the domain non-functional or exposing dysferlin to degradation by unfolded protein degradation pathways.  </p>


