				<p><strong>GENERAL SUMMARY </strong><br><br>

				  The Third Annual Dysferlin Conference, held from June 2-5, 2009 in Boston, brought together leading scientists to discuss recent research and progress towards a therapy for Limb Girdle Muscular Dystrophy 2B (LGMD2B) and Miyoshi Myopathy, which are caused by mutations in the protein dysferlin.<br>

				  <br>

An important goal of the Dysferlin Conference is to bring together and facilitate discussion between the leaders of different fields that may contribute to the development of a therapy or cure:<br>

				  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- clinician scientists who see dysferlin patients and study their genetic mutations;<br>

				  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- researchers who study the role of the dysferlin protein and how it's absence causes this<br> 
				  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; disease;<br>

				  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- experts in the processes of membrane fusion and repair (in which dysferlin is now <br>

				  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;known to participate); and <br>

				  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- leaders in emerging muscle-targeted and gene-targeted therapies (including gene <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;therapy, stem cell therapy, muscle regeneration, stop-codon skipping, and exon <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;skipping).<br><br>

				  The 2009 meeting achieved this goal through the participation of 37 invited speakers, and 41 poster presenters representing their fields in lively discussions throughout the conference. Many new collaborations were formed, and established collaborations were strengthened. <br>
				  <br>

				  The Jain Foundation implemented a number of unique initiatives designed to stimulate discussion and encourage collaboration at the conference.  Two of these initiatives are listed below:<br>

				  <br>

				  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1)   Formal lunch seating grouped individuals by theme to help facilitate discussion. <br>
				  <br>

				  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2) Investigators funded by the Jain Foundation were asked to develop a flow chart outlining the path their project will ideally take towards a therapy for LGMD2B/Miyoshi, including the new materials, technologies, and basic information that they will need along the way, and to highlight their progress along that path so far.  These flow charts were included in the conference booklet to encourage constructive discussion of each funded project.	  <br><br>

