				    <strong>LOOKING AHEAD: </strong><br>

				    <br>

				    We are very pleased with the scientific progress that was presented during the Third Annual Dysferlin Conference.  It was clear that the dysferlin field is beginning to take off in a number of new directions and that some of our assumptions about dysferlin and LGMD2B need to be revisited.  The presentation of such a variety of high-quality new data on dysferlin and LGMD2B made for an exciting meeting with many lively discussions, new ideas, and new collaborations.<br><br>

In 2010, we plan to have another conference of similar size and format in order to facilitate the same level of discussion and maintain the intimate feel and scientific focus of the meeting.  In the meantime, we hope that all researchers working on dysferlin will take to heart the closing comments of our founder, Ajit Jain, and endeavor to accelerate the pace of dysferlin research and make the most of their ideas by welcoming collaborations and sharing and using all of the resources available in the field.
