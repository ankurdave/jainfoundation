                  <strong>Gadofluorine M is Suitable to Study Dysferlin-Deficient Muscular Dystrophy with Magnetic Resonance Imaging  </strong><strong>&nbsp; &nbsp;</strong><br>

Saskia Schmidt, Volkmar Gross, Lutz Lüdemann, Simone Spuler

 <br><br>
Dysferlin-deficient muscular dystrophy (DysfMD) is characterized by a defect in sarcolemmal wound healing with scant inflammation in the face of progressive muscle weakness.  Noninvasive methods to follow this and other muscle diseases are not established; however, high-field magnetic resonance imaging (MRI) with gadolinium-based contrast agents offers new possibilities. 
We used the SJL/J mouse model to study and compare the utility of Gadofluorine M and Gadomer in monitoring the course of DysfMD in SJL/J mice (n=24) and C57BL/6 control mice (n=24).  We studied animals at age 15 (young) or 30 (old) weeks in a 7.0 Tesla animal scanner using dynamic imaging with an inversion-prepared, steady-state, free-processing sequence, before, during, and after Gadofluorine M at 2 µmol or Gadomer at 4 µmol intravenously.  We used carbocyanine-labeled Gadofluorine M for histological visualization of contrast agent.  All experiments were approved by the local boards (Reg G009905).  
This is the first report to show that Gadofluorine M was significantly enriched in skeletal muscle of aged DysfMD mice, compared to controls, while Gadomer provided no discrimination.  These differences were present with regards to uptake velocity ( ±SEM sec-1 : 0.004± 3e-4 vs 0.002± 3e-4 , p<0.05 ) and uptake capacity ( ±SEM: 0.035±0.004 vs 0.019±0.004, p<0.05).  Labeled Gadofluorine M was identified within the muscle cells. 
Gadofluorine M-enhanced MRI may be of value in monitoring DysfMD disease progression in this animal model and could prove to be a useful tool in humans to follow the course of chronic muscle diseases.
