                  </span><strong>Myostatin inhibitors and  muscle regeneration&nbsp;  &nbsp;</strong><br><br>

                  Myostatin,  a member of the TGF-&beta; superfamily, is an endogenous inhibitor of muscle  growth.&nbsp; Its structure and function are highly conserved across species  including in humans.&nbsp; In the absence of myostatin, muscle growth is  stimulated, at least partially through the disinhibition of muscle progenitor  cells.&nbsp; Previously, we have shown that in the absence of myostatin, muscle  regeneration is enhanced in both acute and chronic models of muscle injury and  disease.&nbsp; For example, in the mdx mouse model of muscular dystrophy, muscle  size and strength were increased while muscle fibrosis was decreased in the  absence of myostatin.&nbsp; Following acute injury with cardiotoxin, muscle  regulatory factors are turned on earlier, resulting in enhanced regeneration in  the absence of myostatin.&nbsp; Recently, we  have found that myostatin directly regulates fibrosis formation in  muscle.&nbsp; This work arose from our early observations that the diaphragm of  mdx/mstn null animals had markedly less fibrosis than their mdx  counterparts.&nbsp; In order to

determine if this observation was secondary  to myostatin&rsquo;s regulation of muscle progenitor cells and regeneration or to a  direct effect on muscle fibroblasts, we studied the proliferation of muscle  fibroblasts in vivo and in vitro in the presence of myostatin and myostatin  inhibitors.&nbsp; These unpublished studies, to be presented at the Jain  Foundation meeting, suggest that myostatin directly activates muscle fibroblast  proliferation through known signaling pathways.&nbsp; The implications of our  results are that myostatin inhibitors may be useful in stimulating regeneration  and reducing fibrosis in muscle disease. Several myostatin inhibitors have been  developed and one such inhibitor, MYO-029, developed by Wyeth, Inc., has  entered clinical trials in muscular dystrophy. &nbsp;Since myostatin inhibition will not address  the underlying pathophysiology of various muscular dystrophies, we anticipate  that ultimately, such agents will be used in conjunction with therapies such as  gene therapy or stem cell transplantation to reduce fibrosis and increase  efficacy.</p>

