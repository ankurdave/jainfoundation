                  <strong>Session III: Membrane Fusion and Repair</strong><br>

                  <br>

This session focused on research into the basic cellular mechanisms for repairing membrane tears.  Dysferlin is required for membrane repair, but its specific role in the repair process—a process that is itself poorly understood—is unknown.  Additional studies of the basic mechanisms of repair will help define dysferlin’s function and generate new ideas for therapies.  For example, identification of other proteins that participate in repair or alternate (non-dysferlin-dependent) repair pathways may suggest candidate proteins that could compensate for the absence of dysferlin.  Knowledge of other cellular processes involved in or dependent on repair of membrane tears may help identify additional downstream effects of dysferlin deficiency that could contribute to pathology.  Finally, a more detailed understanding of the steps of repair will also aid development of high-throughput assays to screen for membrane repair-enhancing drugs.<br><br>

<strong>Dr. Paul McNeil</strong> (Medical College of Georgia) discussed his ongoing efforts to identify proteins that are required for repair of membrane tears in addition to dysferlin.  He had previously shown that the dysferlin-interacting protein annexin A1 is required for repair in fibroblasts and has now extended his experiments to myotubes.  He found that both annexin A1 and annexin A2 concentrate at the site of membrane damage after laser wounding.  He also found that C2C12 myotubes with annexin A1 function disrupted (by expression of a dominant negative annexin A1 mutant or by siRNA knockdown) and myotubes from annexin A1 knockout mice cannot repair laser-induced wounds, similar to dysferlin-deficient myotubes.  Some of the unrepaired cells in culture appear to undergo rapid contraction and membrane blebbing after wounding.  It is unclear whether this happens physiologically, but it may be a sign of changes that could prevent repair-deficient cells from pursuing alternate, delayed mechanisms of repair (such as activating another repair pathway) after repair fails initially.  Dr. McNeil mentioned during the question period that significant membrane blebbing sometimes occurs even when cells successfully repair, if the original damage was particularly severe.  <br><br>

Dr. McNeil also discussed his progress in developing a high-throughput assay to monitor repair of membrane tears.  The limiting factor for such a membrane repair assay has been the method used to generate reproducible damage in a high-throughput manner.  Dr. McNeil has developed a tool to damage cells in a 96-well plate by simultaneously scraping cells off the bottom of each well and then transferring the damaged cells in suspension to a new 96-well plate.  The entire process for each plate takes about 10 minutes by hand.  The cells are stained with two different dyes, one retained by cells that successfully repair the damage and one retained only in dead cells, and the ratio of repaired-to-dead cells gives a reproducible measure of the cells’ ability to reseal.
<br><br>

<strong>Dr. Steven Vogel</strong> (National Institutes of Health) discussed recent experiments on the function of a sea urchin homolog of dysferlin.  Sea urchin eggs have long been used as a model system to study membrane repair.  Dr. Vogel is now using early sea urchin embryos, rather than eggs, to study the role of ferlins in the sea urchin.  He identified a sea urchin ferlin with similarity to dysferlin and found that morpholino-mediated knockdown of its expression (in one cell of 2-cell stage embryos) inhibits the cytokinesis step of cell division, which is thought to require endocytosis.  He also found reduced uptake of FM1-43 dye into cells with sea urchin ferlin knocked down, suggesting that endocytosis is inhibited.  These observations could result from a primary defect in endocytosis or from a primary defect in exocytosis that subsequently inhibits compensatory endocytosis.  Dr. Vogel has now confirmed in preliminary experiments that knockdown of sea urchin ferlin in embryonic cells also inhibits membrane repair.

While monitoring Ca2+ levels inside wild type embryonic cells using a fluorescent reporter, Dr. Vogel observed occasional flashes of high intracellular Ca2+ that became much more frequent shortly before cell division.  He also found that wounding a single cell in an embryo causes a flash of high intracellular Ca2+ that is subsequently observed in neighboring unwounded cells.  These Ca2+ spikes observed in unwounded cells are inhibited by cadmium (a Ca2+ channel blocker) and occur even in cells of nearby embryos in the culture, suggesting that they are triggered by a soluble signal.  Dr. Vogel has found that ATP applied onto the cells can mimic these flashes.  He is continuing to study this Ca2+ signaling to determine the nature of the soluble signal and whether transmission of the signal is blocked by knockdown of the sea urchin ferlin.
<br><br>

<strong>Dr. Norma Andrews</strong> (Yale University School of Medicine) discussed her recent work on the sequence of membrane cycling events during membrane repair, which is known to involve Ca2+-mediated exocytosis of intracellular vesicles.  She had previously shown that lysosomes participate in exocytosis during the repair process and has now confirmed this result in muscle cells, showing that inhibition of lysosomal exocytosis in C2C12 myoblasts using bromoenol lactone interferes with repair.  She also presented her recent finding that newly-formed endosomes can be observed in multiple cell types, including C2C12 myoblasts, shortly after membrane wounding by either scraping or treatment with streptolysin O (a toxin that generates a protein-lined pore in the membrane).  Inhibition of lysosomal exocytosis prevents the formation of these endosomes, suggesting that they are the result of compensatory endocytosis after exocytosis.  Enhancement of this endocytic process (by disruption of the actin cytoskeleton) accelerates repair, while inhibition (by cholesterol depletion) blocks it, which led Dr. Andrews to hypothesize that these endosomes contain the tear or pore from the plasma membrane and carry it into the cell.  If compensatory endocytosis is indeed required for repair, it is possible that dysferlin’s primary role could be in endocytosis rather than exocytosis.  <br><br>

Dr. Andrews also discussed her work on another protein, synaptotagmin VII (SytVII), that she has found to be involved in membrane repair in muscle.  The synaptotagmins are a family of transmembrane proteins similar to dysferlin (with two C2 domains instead of seven), and synaptotagmin I is known to mediate Ca2+-dependent exocytosis in nerve cells.  Dr. Andrews found that SytVII is expressed in muscle and localizes to lysosomes.  SytVII knockout mice have progressive loss of muscle strength coupled with inflammatory cell infiltration and fibrosis in skeletal muscle and skin, similar to the symptoms of dysferlinopathy, and fibroblasts from these mice have defective membrane repair.  Additional work is needed to determine whether dysferlin and SytVII function in different repair pathways, both required for muscle cell maintenance, or whether they have different functions in the same repair pathway.

