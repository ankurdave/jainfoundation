                  <strong>Calpain 3 is a Modulator of the Dysferlin Protein Complex in Skeletal Muscle </strong><strong>&nbsp; &nbsp;</strong><br>

Antoine de Morrée1, Yanchao Huang2,  Alexandra van Remoortere3, Kate Bushby4, Rune R. Frants1, Johan den Dunnen1, Silvère M. van der Maarell
1 Center for Human Genetics, Leiden University Medical Center, Leiden, The Netherlands  2 Netherlands Vaccine Institute, Bilthoven, The Netherlands 3 Department of Parasitology, Leiden University Medical Center, Leiden, The Netherlands  4 Institute of Human Genetics, International Centre for Life, Newcastle-upon-Tyne, UK
 <br><br>

Muscular dystrophies (MD) comprise a genetically heterogeneous group of degenerative muscle disorders characterized by progressive muscle wasting and weakness.  Two forms of limb-girdle muscular dystrophy (LGMD), 2A and 2B, are caused by mutations in calpain 3 (CAPN3) and dysferlin (DYSF), respectively.  CAPN3 may be involved in sarcomere remodelling, and DYSF is proposed to play a role in membrane repair.  In LGMD2B patients a secondary reduction of CAPN3 can be observed.  Using specific antibodies selected from a non-immune llama VHH phage display library we have previously shown that dysferlin interacts with calpain-3, suggesting a molecular link between the two disease mechanisms of LGMD.
A second protein that was identified in the DYSF protein complex is AHNAK. AHNAK is a 700 kDa protein found on enlargeosomes.  It is involved in subsarcolemmal cytoarchitecture and implicated in membrane enlargement and repair.  AHNAK can directly interact with DYSF, and redistributes to the sarcolemma in regenerating skeletal muscle.  
The coexistence of CAPN3 and AHNAK in the DYSF protein complex and the presence of proteolytic cleavage fragments of AHNAK in skeletal muscle led us to investigate whether AHNAK can act as a substrate for CAPN3.  Here we present evidence that AHNAK can be cleaved by CAPN3 and show that AHNAK is lost in cells expressing active CAPN3.  Conversely, AHNAK accumulates when calpain 3 is defective in skeletal muscle of calpainopathy patients.  Moreover, we demonstrate that AHNAK fragments cleaved by CAPN3 have lost their affinity for DYSF.  Thus, our findings suggest interconnectivity between both LGMD2A and LGMD2B by revealing a novel physiological role for CAPN3 in regulating the dysferlin protein complex.
