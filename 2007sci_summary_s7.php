				<p><span class="style13">SCIENTIFIC SUMMARY </span><br>

				<br>

				  <a href="2007sci_summary.php" class="style16">&lt;-- back</a> <br>

				  <br><br>

                  <strong>Session VII: Immune Involvement and Cell Death </strong><br>

                  <br>

                  The Immune Involvement and Cell Death session discussed the mechanisms by which damaged muscle cells die in dysferlin deficiency. As in other muscular dystrophies, the absence of dysferlin causes an accumulation of damage to the muscle cell membrane, in the form of membrane tears. The mechanism by which these tears lead to cell death is unknown, but one possibility (discussed below) is that increased calcium influx into the cell activates proteins involved in cell death pathways. 

                  Dysferlin deficiency is distinct from other dystrophies in that it is often also associated with a high level of muscle inflammation. Inflammation is a response to cellular injury and death, but it may also mediate a significant amount of death and cause additional damage to neighboring cells, in a positive-feedback loop that accelerates muscle wasting.  <br>

                  <br>

                  Reducing inflammation or preventing the death of muscle cells, even after injury, are potential therapeutic strategies because they may break this feedback loop and may also reduce muscle fibrosis. Another possible strategy is to shift dying cells from necrotic to apoptotic pathways in order to reduce muscle inflammation. <br>

				<br>

				Dr. Simone Spuler discussed methods for reducing immune-mediated cell death in dysferlin deficiency. She has found that expression of CD55 (a complement regulator) is significantly reduced in the skeletal muscle of SJL/J mice and LGDM2B patients compared to controls. Dysferlin-deficient myoblasts from LGMD2B patients were found to have increased susceptibility to complement-mediated lysis relative to normal human myoblasts, suggesting that blocking complement activity could reduce muscle cell death in this disease. In addition, A/J dysferlin-deficient mice, which lack complement factor 5 (C5),  have a milder phenotype than SJL/J mice. Dr. Spuler treated SJL/J mice with anti-C5 and found significantly reduced muscle damage in the treated mice. (A control IgG1 antibody also caused some improvement, and she would like to follow up on that observation as well.) Dr. Spuler has already treated three LGMD2B/MM patients with IVIG to block complement activity and seen some improvement in muscle strength, but IVIG is a heterogeneous preparation with many different effects, so she suggests that a monoclonal antibody (such as anti-C5) would be a better treatment option. In the discussion following Dr. Spuler's talk, it was suggested that  a good test of her hypothesis would be to provide C5 to A/J mice to see if the severity of their phenotype increases. <br>

				    <br>

				  Dr. Kanneboyina Nagaraju discussed characteristics of the unusual immune response seen in dysferlin deficiency, which is marked by mononuclear cell infiltration and the presence of complement/MAC even on non-necrotic muscle fibers. Although dysferlin deficiency is sometimes mis-diagnosed as polymyositis, auto-antibodies are not found in dysferlin-deficient patients. Monocytes/macrophages have increased phagocytic activity in SJL/J mice relative to controls (though there is not as much difference in humans), and suppression of dysferlin in a macrophage cell line increases phagocytic activity. Expression levels of inflammatory mediators, including caspase-1 and NALP3, are often elevated in muscle from LGMD2B patients. Dr. Nagaraju also encouraged collaborative use of the CNMC preclinical drug testing facility, dedicated to testing muscular dystrophy models. This facility performs standardized tests of muscle function, including treadmill exercise, Rota-rod testing, grip strengthing, MRI and ultrasound imaging. <br>

				    <br>

				  Dr. Jeffery Molkentin discussed the role of calcium in muscle cell death pathways. Both necrosis and apoptosis can be triggered by rupture of mitochondria after a rise in intracellular calcium levels and formation of a pore complex in the mitochondrial membranes. When one component of this pore complex (cyclophilin D, encoded by Ppif) is knocked out in mice, cell death after peroxide damage is reduced. Dr. Molkentin hypothesizes that damage to the sarcolemma in muscular dystrophies allows unregulated calcium influx and also triggers enhanced influx of calcium through membrane channels, leading to mitochondrial calcium overload. He has found evidence of swollen mitochondria in both delta-sarcoglycan and laminin alpha-2 knockout mice. Crossing delta-sarcoglycan knockout and Ppif knockout mice rescues the muscle loss normally associated with sarcoglycan deficiency, increases force generation, reduces inflammation, and improves histological characteristics of the muscle. Muscle membrane damage is still seen, demonstrating that the cyclophilin D pathway acts downstream of the damage. In the discussion following Dr. Molkentin's talk, it was suggested that inhibitors (or inducible knockouts) of cyclophilin D or other pathway members should be tested to ensure that the beneficial effect is not due to absence of cyclophilin D before birth. <br>

				    <br>

				  Dr. Christina Jamieson discussed steroid hormone receptors and their role in the immune system.  Although glucocorticoids are not effective at treating dysferlin deficiency, other steroid pathways could have a beneficial effect. Activated androgen receptor, for example, enhances myogenic differentiation of stem cells and helps build muscle. Dr. Jamieson plans to screen for androgen receptor modulators that promote muscle-building even in the absence of dysferlin. She has also studied activated glucocorticoid receptor, which leads to immune suppression by repressing cytokine production and by triggering apoptosis in T lymphocytes. During the late stages of dexamethasone-induced T cell apoptosis, she found that dysferlin expression was upregulated, along with that of cytoskeletal remodeling and vesicle trafficking proteins. This timing of expression coincides with membrane blebbing and the fusion of internal vesicles with the plasma membrane. Dr. Jamieson hypothesizes that apoptosis may be defective in dysferlin deficiency. Defective apoptosis during T cell development can result in autoimmune diseases later in life, and impaired apoptosis might account for the fact that glucocorticoids are not effective in this disease. It is still unknown what role apoptosis plays in normal muscle. <br>

				    <br>

				  During the brainstorming session, there was much discussion of potential complement inhibition therapies and IVIG. The three dysferlin-deficient patients who were treated with IVIG by Dr. Spuler had all been tested for the presence of MAC proteins in their muscle biopsies prior to treatment, and Dr. Spuler believes that IVIG will only be beneficial in patients who show MAC binding to non-necrotic muscle fibers. Although IVIG is commercially available, it was recommended that patients undergo this screening before trying the drug. It was also mentioned that complement inhibition may be a particularly useful strategy for this disease if dysferlin-deficient cells are more susceptible to complement because of their inability to repair membrane pores. Though the complement-generated pores are different from normal damage in that they are ringed by proteins, there is some evidence that these pores can be repaired by calcium-dependent mechanisms because complement activity is often sub-lethal in the presence of calcium. <br>

				  <br>

				  Another point of discussion was how to clarify the many different effects of the immune response in dysferlin deficiency. Inflammation may cause a significant amount of damage to muscle cells, but the inflammatory response is also an important mediator of reconstruction and repair of muscle tissue after damage. Anti-IL-1 therapy was suggested as a way to reduce the pro-inflammatory response while retaining the muscle reconstruction phase of the immune response. Because it is complicated to predict the changes in pathology that may result from immune modulation or suppression strategies, Dr. Nagaraju suggested transplanting muscle from dysferlin-deficient mice into nude mice to test the effects of a lack of T cell responses. 

				  Another possibility is to cross dysferlin-deficient mice with immune-deficient mice and then characterize the resulting phenotype. <br>

				  <br>

				  Questions were also asked about possible cardiac involvement in dysferlin deficiency, and Dr. Spuler recommended that LGMD2B/MM patients be screened for reduced heart rate (elevated RR interval) and rigorously treated for any abnormality. <br>

