<p>For local attendees, we have negotiated a full day of parking (7-10 hours) for the self-park rate of $7/day with the first three hours complimentary. To receive this rate, please use the self-park and bring your parking ticket to the conference.  We will stamp the ticket so that at exit, the special rate will be honored.</p>



<p><strong>Hotel Charges</strong></p>

<p>The Jain Foundation has reserved a guaranteed block of rooms for the conference for the nights of September 11, 12, and 13, 2010. 

In addition, if you need lodging outside of these 3 nights, the conference rate has been reservered for 3  days pre and post the conference (September 8-17).

The conference room rate is $142 USD/night + 14.5% tax for single or double occupancy.  A third or fourth person in the room is $25 per night extra for each additional person. </p>




