                  <strong>Session X: New Tools for Studying Dysferlin </strong><br>

                  <br>

This session highlighted technological advances that may improve the study of dysferlin or muscular dystrophies. <br><br>

<strong>Robert Brown</strong> (USA) presented his development of a C2C12 cell line that stably expresses shRNAs that suppress dysferlin expression. However, injuring these cells using detergents or osmotic shock and monitoring release of creatine kinase or luciferase has not demonstrated a significant difference in membrane repair compared to wild type C2C12 myotubes. Gene expression analysis of these dysferlin-deficient C2C12 cells indicates that a number of genes involved in myogenesis and regeneration are upregulated. Dr. Brown also discussed a collaboration with Dr. Jerry Mendel to develop mini-dysferlins that can be used in AAV gene therapy.  They have also recently begun to explore delivery of full-length dysferlin to the muscles of mice, based on a recent report that AAV5 can package larger genomes than other AAVs. <br><br>

<strong>Paul McNeil</strong> (USA) discussed his progress on developing a high-throughput assay to monitor repair of membrane tears. He developed a tool to damage cells in a 96-well plate by simultaneously scraping cells off the bottom of each well and transferring the damaged cells in suspension to a new 96-well plate. The cells are stained with two different dyes, one retained by live cells that successfully repair their membranes and one retained only in dead cells. The live/dead cell ratio is a reproducible measure of the cell population’s ability to reseal. The entire process for each plate takes about 10 minutes by hand. This cell scraping technique works well on a variety of mononuclear cell types, but applying it to myotubes has proven difficult and may require further modifications to the protocol, such as application of drugs that inhibit muscle contraction. Dr. McNeil has surprisingly not found the same membrane repair defect in cultured dysferlin-deficient myotubes (derived from two different dysferlin-deficient cell lines) that he observes in isolated myofibers from dysferlin-deficient mice. In contrast, myotubes derived from primary dysferlin-deficient human myoblasts have been shown to have defective repair, suggesting that membrane repair may be influenced by the immortalization process or by differences between species. <br><br>

<strong>Daniel Stockholm</strong> (France) is attempting to non-invasively monitor the membrane permeability of muscle fibers in vivo by labeling albumin with the radioactive tracer Technetium-99m.  However, he found that the background levels of circulating Technetium-99m in the blood were too high to reliably detect an increase in the amount of Technetium-99m in the skeletal muscle of dystrophic mice. Audience members suggested that he try Technetium-99m alone rather than bound to albumin, as this may change its clearance properties. Dr. Stockholm is also using live photonic imaging of mouse skeletal muscle to follow the pathological events that occur after large strain injury, in collaboration with Dr. Robert Bloch. He monitors the entry and distribution of fluorescently labeled dextrans of various sizes into muscle fibers during and after injury. Preliminary results reveal patterns of dextran infiltration that suggest a segmental model of muscle fiber degeneration. 

