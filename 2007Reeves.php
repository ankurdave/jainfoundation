                  <img src="images/conference/52-Reeves.jpg" width="135">                  <br><br><br>

                  <strong>Quantitative proteomic analysis of the acute response of muscle to Prednisone</strong>

                <br>

                Erica Reeves, Yetrib Hathout, Eric P. Hoffman

                <br>

                <br>

                <strong>Inroduction:</strong> Differential  diagnosis of the inflammatory myopathies from the muscular dystrophies has  previously been considered to be relatively straightforward. However,  dysferlin-deficiency (LGMD2B; Miyoshi) is a dystrophy that is frequently  mis-diagnosed as polymyositis due to more acute presentation and associated  inflammation on muscle biopsy. Differential diagnosis is particularly  important, as inflammatory myopathies respond well to corticosteroids, while  dysferlin patients show more variable response and can worsen. This may  indicate that inflammation is not the primary cause of muscle fiber damage  degeneration and that there is an important difference in inflammatory cell  involvement in patients with dysferlinopathies compared to those with DMD where  corticosteroids have a beneficial response. The goal of this research is to  analyze the acute downstream proteomic signaling networks of corticosteroids  and anabolic steroids

in muscle cells in normal muscle. Through a  complete understanding of both signaling and transcriptional pathways induced  by steroids in muscle, more targeted and effective therapeutics could be  developed.&nbsp;<strong>Methods:</strong> C2C12 cells were grown in DMEM medium where Arg  and Lys were replaced by doubled labeled 13C6,  15N-Arg and 13C6, 15N2-Lys.  In parallel the same amount of C2C12 cells were grown to confluence in  unlabeled medium. Unlabeled myotubes were treated with a pharmacological dose  of Prednisone and incubated at various time points (5min, 15min, and 30min).  Treated unlabeled cells were mixed in equal amounts to labeled untreated  control cells. Cytosolic and nuclei fractions were prepared. Each subcellular  fraction was further fractionated and analyzed by LC-MS/MS. The ratio of the  monoisotopic peak heights of the labeled to unlabeled peptides was used for  quantitation. <strong>Results:</strong> Multiple pathways were significantly altered due  to Prednisone treatment in a time dependent manner. Proteins involved in gene  expression and protein synthesis were seen to be significantly altered.  Translocation between the cytosol and nucleus of proteins such as eukaryotic  translation elongation factor 1 alpha 2 (EEF1A2), crystalline, alpha B (CRYAB),  and Y box binding protein 1 (YBX1) were seen. EEF1A2 promotes the GTP-dependent  binding of aminoacyl-tRNA to the A-site of ribosomes during protein  biosynthesis. CRYAB belongs to the small heat shock (HSP20) family and is  involved in apoptosis. YBX1 contributes to the regulation of translation by  modulating the interaction between the mRNA and eukaryotic initiation factors.  None of these proteins have been previously shown to be involved in  glucocorticoid (GC) signaling. Numerous other proteins appear to be  translocating from other subcellular fractions to the cytosol and nucleus,  including ANXA1 and ANXA2, both of which have been shown to be involved in GC  signaling. Overall, multiple pathways appear to be activated during this acute  time period, independent of the classical steroid transcriptional pathway. <strong>Future  Aims:</strong> Future studies are to take this same experimental approach and apply  it to mdx muscle cells and SJL-dysf cells and to generate a working model of  the differential cellular response of dystrophic muscle to corticosteroids in  hopes to better understand the molecular mechanisms of the diseases. </p>

