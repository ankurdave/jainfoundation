<strong>Stem Cells</strong> <br><br> 

Moderators:  Plavi Mittal, Nilah Monnier  <br>
Participants:  Gayathri Balasundaram, Beth Barton, Michele Calos, Katie Maguire, Michele Maxwell, Terry Partridge, Tom Rando, Morayma Reyes, Jennifer Shadrach, Yvan Torrente, Amy Wagers<br><br>

1.	Does the late age of onset of symptoms reflect exhaustion of stem cell regenerative capacity? <br>
•	Is it possible to compare stem cell numbers in patients and non-patients?<br>
•	Would stem cells from pre-symptomatic animals help symptomatic animals?<br><br>

2.	Does dysferlin have any role in stem cell function?<br>
•	Is dysferlin expressed in stem cells? Is it expressed differently in different kinds of cells?<br>
•	Does dysferlin play a role in stem cell fusion to muscle fibers or stem cell proliferation? <br><br>

3.	How can we determine the best stem cells for therapy?<br>
•	CD133+, MAPCs, Mesoangioblasts, etc?  Will we need to take each of these types into human trials, or is there an effective way to compare them pre-clinically?<br>
•	Can these stem cells also" seed" the immune system if it turns out that dysferlin has a role in the immune system?<br><br><br>

<span class="style13">The discussion yielded several experiments that could help address some of the questions listed above. These experiments have been organized into several discrete projects that are listed below.</span><br>
<br>

<strong>Generation of chimeric mice by muscle stem cell transplantation</strong><br>
This project involves transplanting muscle stem cells from control mice into the muscle of dysferlin-deficient mice (and vice versa) and monitoring the cells’ engraftment potential (both with and without muscle injury), in order to determine whether the environment in dysferlin-deficient mice (rather than a defect in dysferlin-deficient stem cells themselves) contributes to inefficient regeneration.  This project should be repeated for each stem cell type.
Project participants include: Amy Wagers, Morayma Reyes, Yvan Torrente<br><br>

<strong>Determining the lineages of stem cells that are naturally recruited to dysferlin-deficient muscle</strong><br>
This project looks for the presence of cells with unusual surface markers (indicating that they represent lineages not normally found in muscle) in muscle samples from dysferlin-deficient mice (both pre- and post-symptomatic) and patients.  The presence (or an increase in number) of such cells in dysferlin-deficient muscle versus controls would suggest a deficiency in the regeneration capacity of dysferlin-deficient muscle stem cells, compensated for by recruitment of other stem cell lineages to muscle.<br><br>

<strong>Bone marrow transplantation from normal to dysferlin-deficient mice</strong><br>
This project involves transplanting either the bone marrow alone or bone marrow and muscle stem cells from the same donor to dysferlin-deficient mice, in order to reduce immune rejection and to investigate the potential contribution of dysferlin-deficient immune cells to pathology.
Project claimed by: Morayma Reyes<br><br>


