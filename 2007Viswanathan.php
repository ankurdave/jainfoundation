                  <img src="images/conference/50-Viswanathan.jpg" width="135">                  <br><br><br>

                  <strong>Identification of chemical and genetic suppressors  of <em>Caenorhabditis elegans</em> <em>fer-1</em></strong><br>

                <br>

                <em>Caenorhabditis  elegans </em>FER-1  is the founding member of the ferlin family of transmembrane proteins that  includes human dysferlin, which is mutated in patients with Limb-Girdle  Muscular Dystrophy 2B (LGMD2B) and Miyoshi Myopathy (MM). <em>C. elegans</em> <em>fer-1</em> null mutants are defective in spermatocyte maturation and as a result,  hermaphrodite worms are incapable of self-fertilization and are sterile.  Importantly, a striking conservation of ferlin family function at the cellular  level underlies this disparity in defects at the organismal level.&nbsp; Loss  of <em>fer-1</em> function in worms or dysferlin function in mammals both result  in the failure of newly formed cytosolic vesicles to fuse with the existing  plasma membrane. In mammalian skeletal muscle cells, this defect prevents membranous  vesicles from sealing the plasma membrane ruptures caused by normal activity,  resulting in progressive muscular degeneration. In <em>C. elegans</em> spermatocytes loss of functional FER-1 protein prevents the normal fusion of  specialized vesicles known

as membranous organelles to the plasma  membrane, resulting in incomplete sperm maturation, sperm motility defects, and  infertility. The fact that FER-1 and dysferlin regulate vesicular membrane  fusion events in different cell types in vastly diverged species suggests that  both have likely evolved to regulate a conserved biological mechanism under  specialized cellular circumstances. This conservation in biological function  provides the rational basis for the use of <em>C. elegans</em> <em>fer-1</em> mutants as a model for human dysferlinopathy. We are conducting both forward  genetic and chemical screens in <em>C. elegans</em> in order to identify novel  therapeutic targets and potential drug therapies for LGMD2B and MM. Genetic  suppressors can help identify constituents of previously unknown biological  pathways relevant to dysferlinopathy and have potential as new therapeutic  targets, while chemical compounds that restore fertility hold promise as  therapeutic drug candidates for LGMD2B and MM patients.</p>

