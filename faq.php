                        <strong>

                        What is muscular dystrophy?</strong><br>

Muscular dystrophy refers to a group of diseases that produce muscle weakness.

Muscular dystrophies all have in common the characteristic that they involve

abnormalities of the muscle cells themselves, rather than of the nerves that

control the muscles. Also, all muscular dystrophies are caused by genetic mutations.

Mutations are not communicable but can be inherited, with different inheritance

patterns depending on the particular form of muscular dystrophy. In general,

muscular dystrophies cause progressive weakness, although there are a few exceptions.

The most common, and best known, type of muscular dystrophy is Duchenne Muscular

Dystrophy, which causes symptoms starting in childhood and almost exclusively

affects males, because the responsible gene is located on the X chromosome. Some

other forms of muscular dystrophy affect both genders equally and cause symptoms

that first appear in adolescence or adulthood.<br>

<a href="#top" class="top">&raquo;top</a><br>

<br>

<a name="2" class="faq" id="2"></a><br>

<strong>What is Miyoshi myopathy?</strong><br>

Myopathy simply means &#8220;muscle disease.&#8221; Miyoshi myopathy (MM) is

a form of muscular dystrophy which was first described in the medical literature

by Miyoshi in 1967. Although first identified in Japan, it occurs worldwide.

Miyoshi is caused by defects in the gene for the protein dysferlin.<br>

<br>

Miyoshi myopathy is in the category of distal muscular dystrophies, which means

that the muscles most strongly affected are in the calves, forearms, hands, or

feet. Most forms of muscular dystrophy preferentially affect proximal muscles

(those in the hips, thighs, or shoulders), so those that don't follow that pattern

were grouped together for classification purposes. There are many forms of distal

myopathy, which don&#8217;t have anything in common on a genetic or biochemical

level.<br>

<br>

Miyoshi myopathy is distinctive among the distal myopathies in several respects:<br>

<br>

&#8226; It has a recessive inheritance pattern. This means that in most cases

there is

no family history.<br>

<br>

&#8226; The first muscles to be affected are typically the gastrocnemius (the

calf muscles

in the back of the legs used to stand on tiptoe).<br>

<br>

&#8226; Onset of symptoms generally occurs between the ages of 15 and 30. There are exceptions

to this age range: genetically confirmed cases have been reported in the literature

with onset as late as age 73.<br>

<br>

&#8226; Levels of CK (creatine kinase, a muscle enzyme) in the blood are very high. Values

typically found in Miyoshi patients are several thousand, compared to a normal

reading of 100 or less. This is typical of some other muscular dystrophies but

not most of the distal forms.<br>

<br>

&#8226; Muscle biopsy is distinctive. Most distal myopathies produce something called &quot;rimmed

vacuoles&quot; in muscle biopsies. Miyoshi usually does not, although there are

a few reports of genetically confirmed cases which show rimmed vacuoles (although

these are usually not prominent) in biopsies. The biopsies do show indications

of a muscle disease, including degeneration/regeneration of fibers. Also, muscle

biopsies often show evidence of inflammation.<br>

<br>

&#8226; There are typically no symptoms of neuropathy, and tests of nerve conduction

generally yield normal results.<br>

<a href="#top" class="top">&raquo;top</a><br>

<br>

<a name="3" class="faq" id="3"></a><br>

<strong>What is Limb-Girdle Muscular Dystrophy?</strong><br>

Limb-girdle muscular dystrophy refers to a group of diseases (not a single disease)

which, long before the era of molecular biology and genetic engineering, were

lumped together because they shared some common symptoms. The name refers to

the fact that the first muscles to show symptoms generally are those around the

shoulders and the hips. Beyond that, the different limb-girdle dystrophies show

very little in common in terms of inheritance pattern, age of onset, severity

of symptoms, prognosis, genetic mutation, or biochemical abnormality in the muscle

cells. At the current time, a number of forms of limb-girdle muscular dystrophy

have been tracked to specific locations in the human genome. Many of the specific

proteins involved have been identified.<br>

<br>

A diagnosis of limb-girdle muscular dystrophy without specifying the particular

type is not really a diagnosis at all. Tests do not yet exist to identify every

type of LGMD, but at a minimum a patient can be tested for the forms for which

tests do exist. Also, some forms of LGMD are difficult to distinguish clinically

from Becker muscular dystrophy (caused by a mutation of the dystrophin gene on

the X chromosome), so dystrophin testing is also typically done.<br>

<br>

The classification scheme of the limb-girdle dystrophies is as follows. The diseases

are specified as LGMDNL, where N is a number which designates the inheritance

pattern (1=dominant, 2=recessive). The L is a letter assigned in alphabetical

order within each inheritance pattern, following the chronological order in which

the locations of specific mutations were discovered. So LGMD2B, the specific

type of limb-girdle caused by a dysferlin gene mutation, is the second recessive

type to have its genetic locus identified.<br>

In general, the clinical features described for Miyoshi myopathy above also apply

to LGMD2B, except that the initial muscle involvement is different (hip muscles,

rather than calf muscles).<br>

<a href="#top" class="top">&raquo;top</a><br>

<br>

<a name="4" class="faq" id="4"></a><br>

<strong>What causes LGMD2B/Miyoshi?</strong><br>

LGMD2B and Miyoshi are both caused by inherited genetic mutations in the gene

for dysferlin. They are recessive conditions, so patients with these diseases

must have two defective copies of the dysferlin gene. There are many different

mutations that disrupt the function of dysferlin, and each patient usually has

different mutations in each of his/her two copies of the gene. People with only

one defective gene do not have symptoms.<br>

<br>

An individual may have genetic defects in the dysferlin gene for the following

reasons:<br>

<br>

&#8226; Inheritance of a defective gene from each parents (autosomal recessive

inheritance)<br>

<br>

&#8226; Sporadic mutations<br>

<br>

We do not yet understand why defects in the same gene give rise to both Miyoshi

myopathy and LGMD2B, with their slightly different clinical manifestations. In

most cases the defect produces some symptoms from both conditions.<br>

<a href="#top" class="top">&raquo;top</a><br>

<br>

<a name="5" class="faq" id="5"></a><br>

<strong>What is autosomal recessive inheritance?</strong><br>

A single defective dysferlin gene cannot cause LGMD2B/Miyoshi. For the disease

to occur, both copies of the dysferlin gene must be defective.<br>

<br>

Dysferlin is autosomal. This means that it is not a sex-linked gene, and both

males and females are affected equally. One copy of the gene is inherited from

each parent. If one good copy and one defective copy are inherited, the single

good copy is sufficient for normal muscle function and the individual is only

a carrier of the disease. If both parents are carriers, each child has a one

in four (25%) chance of inheriting two defective copies of dysferlin and developing

LGMD2B/Miyoshi.<br>

<a href="#top" class="top">&raquo;top</a><br>

<br>

<a name="6" class="faq" id="6"></a><br>

<strong>What is dysferlin?</strong><br>

Dysferlin is a protein which, when mutated or absent, causes at least two forms

of muscular dystrophy. Both Limb-Girdle Muscular Dystrophy type 2B and Miyoshi

distal myopathy have been shown to arise from mutations in the gene which codes

for dysferlin. Dysferlin has no common features with any other human protein

known at the time of its discovery (mid 1998), but is similar to a protein in

the nematode worm C. elegans. The C. elegans protein is necessary for reproduction

(although lack of dysferlin in humans does not appear to cause any symptoms except

muscle weakness) and so it was called fer-1 for &quot;fertile&quot;. The name

of the human protein, &quot;dysferlin&quot; was coined by the discoverers to

acknowledge its role in muscular dystrophy and its similarity to the C. elegans

gene. Since the discovery of dysferlin, five closely related human proteins have been characterized.<br>

<a href="#top" class="top">&raquo;top</a><br>

<br>

<a name="7" class="faq" id="7"></a><br>

<strong>Why do mutations in dysferlin cause muscle weakness?</strong><br>

                      Not much is known about this question. Recently, studies

                        have shown that the dysferlin protein is involved in

                        repair of the cellular membrane. Because muscle fibers

                        repeatedly stretch and contract, they develop small tears

                        in their outer membranes that must be repaired. Muscle

                        is also a regenerative tissue, and muscle cells fuse

                        together to form fibers throughout life. Both of these

                        processes require restructuring of the cellular membrane.

                        It is not known exactly how dysferlin participates in

                        these processes, or which process is most affected by

                        a lack of dysferlin. Understanding the biological role

                        of dysferlin, and the problems which result from its

                        absence, is a high priority in current research into

                        LGMD2B/Miyoshi.<br>

                        <a href="#top" class="top">&raquo;top</a><br>

                        <br>

                        <a name="8" class="faq" id="8"></a><br>

                        <strong>What is the difference between a gene and a protein?</strong><br>

                      A gene is a segment of DNA containing instructions for

                        manufacturing a certain protein in the cell. Proteins

                        are essentially chains of building blocks called amino

                        acids, which are obtained (in animals) from protein-containing

                        foods. An animal breaks apart proteins from food into

                        these building blocks, then reassembles them in a different

                        order to make its own proteins. There are virtually no

                        biological functions which are not dependent in some

                        way on proteins. Each gene tells the cell how to put

                        together the building blocks for one specific protein.<br>

                        <br>

                      The terminology can be somewhat confusing. Dysferlin is

                      a protein, and &quot;the dysferlin gene&quot; means &quot;the

                      gene which contains the instructions for producing the

                      dysferlin protein.&quot; The actual name for this gene

                      is Dysf.<br>

                      <br>

                      Usually, when a protein is not properly produced, it is

                      because there is some mutation in the gene which contains

                      its instructions. When the instructions are wrong, the

                      defective protein will not carry out its normal function

                      in the cell. It may do something different than it was

                      intended to do, or it may not function at all.<br>

                      <a href="#top" class="top">&raquo;top</a><br>

                      <br>

                      <a name="9" class="faq" id="9"></a><br>

                      <strong>What is the difference between a genotype and a phenotype?</strong><br>

  A genotype refers to the genetic characteristics of an organism. A phenotype

  refers to the physical characteristics. For example, having blue eyes (an autosomal

  recessive trait) is a phenotype; lacking the gene for brown eyes is a genotype.

  Dysferlin deficiency will only cause muscle weakness (phenotype) if a person

  has two defective copies of the dysferlin gene (genotype). The genotype of

  having two defective dysferlin genes is associated with two different phenotypes,

  the symptoms of LGMD2B or of Miyoshi myopathy.<br>

                    <a href="#top" class="top">&raquo;top</a>

					

