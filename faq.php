					<a name="10" class="faq" id="10"></a><br>

					<strong>Are any other muscular dystrophies caused by mutations

                      in the dysferlin gene?</strong><br>

                      There is a condition referred to

                      as &quot;distal anterior compartment myopathy&quot; (DACM)

                      which is very similar to Miyoshi myopathy except that the

                      first muscles affected are the muscles in the front of

                      the calf, rather than in the back. There also have been

                      some patients found to have dysferlin gene mutations whose

                      symptoms do not precisely match the clinical features of

                      MM, LGMD2B, or DACM. A more general term for all muscular

                      dystrophies caused by mutations in dysferlin is &#8220;dysferlinopathy&#8221; (see

                      next question).<br>

                        <a href="#top" class="top">&raquo;top</a><br>

                        <br>

                        <a name="11" class="faq" id="11"></a><br>

                        <strong>What is dysferlinopathy?</strong><br>To what extent is the clinical

                        classification of LGMD2B and Miyoshi myopathy really

                        meaningful? How different do two patients' symptoms have

                        to be before they are diagnosed as having different,

                        rather than identical, diseases? The current categorization

                        (done mostly for historical reasons) is somewhat arbitrary

                        now that the genetics are understood.<br>

                        <br>

                        Another classification approach, based on genetics, groups

                        these conditions all into one disease, called &quot;dysferlinopathy&quot; (literally,

                        a disease involving dysferlin). The symptoms of dysferlinopathy

                        (for reasons which are not yet understood) vary significantly

                        between individuals.<br>

                        <a href="#top" class="top">&raquo;top</a><br>

                        <br>

                        <a name="12" class="faq" id="12"></a><br>

                        <strong>If I have LGMD2B/Miyoshi, will my children inherit it?</strong><br>

  All of the muscle diseases associated with dysferlin deficiency have an autosomal

  recessive inheritance, so a person will only have symptoms if he/she inherits

  a mutated version of the dysferlin gene from both parents. In the absence of

  a spontaneous mutation, the children of someone with LGMD2B/Miyoshi will only

  develop symptoms if they inherit a second mutated dysferlin gene from the other

  parent. Based on the incidence of these conditions in the general population,

  there are probably only about 3-5 people out of every thousand who have one

  copy of a mutated dysferlin gene (carriers). So, the chance of children inheriting

  these conditions is quite small unless the other parent has a family history

  of these conditions or shares a common ancestry with the affected parent.<br>

  <br>

  If both parents have one copy of a mutated dysferlin gene, each child has a

  25% chance of getting both defective copies and thus the disease. However,

  for each child of this same couple there is also a 50% chance of being a carrier

  of the defective gene, meaning that he/she has a single mutated copy of the

  gene but does not show any symptoms. Genetic testing in families with recessive

  disorders is sometimes advisable in order to detect carriers. It might also

  be good to determine whether the potential spouses of carriers or affected

  individuals are also carriers. Determination of a person's carrier status requires

  testing his/her DNA (rather than antibody testing for the dysferlin protein),

  since a carrier still properly expresses the protein.<br>

  <a href="#top" class="top">&raquo;top</a><br>

  <br>

  <a name="13" class="faq" id="13"></a><br>

  <strong>What is the prognosis and rate of progression for LGMD2B/Miyoshi?</strong><br>

  The rate of progression of LGMD2B/Miyoshi is fairly slow compared to most other

  types of muscular dystrophy. Among the identified forms of LGMD, type 2B is

  one of the more slowly progressive forms. There have been some reports that

  Miyoshi has a slower progression than LGMD2B, while other reports indicate

  that the two are similar. This may reflect a difference in the initial muscles

  affected, since weakness in the calf muscles has less impact on a patient's

  function than weakness in the hip muscles. However, the reported rates of progression

  and symptoms do vary significantly, which may be due to differences in the

  type of mutation or other genetic differences between populations studied.

  There have not been any reports of cardiomyopathy (dystrophy of the heart muscle)

  in either LGMD2B or MM. Nor have there been any reports that either condition

  significantly shortens life expectancy.<br>

  <a href="#top" class="top">&raquo;top</a><br>

  <br>

  <a name="14" class="faq" id="14"></a><br>

  <strong>How is LGMD2B/Miyoshi diagnosed?</strong><br>

  Diagnosis is generally made based on the following information:<br>

  <br>

&#8226; A detailed patient and family history (a 3 generation pedigree)<br>

  <br>

  &#8226; The source of the muscle weakness (nerve or muscle)<br>

<br>

  &#8226; A detailed examination looking for specific symptoms of LGMD2B or Miyoshi,

  as described above<br>

  <br>

  &#8226; Muscle biopsy<br>

  <br>

  &#8226; Blood testing<br>

  <br>

  &#8226; Genetic testing (sequencing the dysferlin gene to identify disease

  causing mutations)<br>

