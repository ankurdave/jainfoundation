                  <p><strong>Arrival</strong></p>
<p> Conference attendees should plan to fly into Puerto Rico via the Luis-Munoz Marin International airport.&nbsp;  Transportation to and from the airport is  provided by a shuttle from the hotel arranged by and paid for by the Jain  Foundation.&nbsp; Spouses and children traveling with the attendee can avail this service at the rate of $45 per person round trip, paid directly to the hotel.&nbsp; Please look for the &quot;Jain  Foundation Inc&rdquo; sign.&nbsp; Travel time between the airport and hotel is approximately 50 minutes.&nbsp; Rental cars are available at the airport as well as at the hotel (Avis) and can be arranged by the attendee at his/her expense. </p>
