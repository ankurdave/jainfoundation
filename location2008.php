<br>
 

<p><strong>Cell Phones &amp; Electronic Devices</strong></p>

<p>Many US cell phone companies offer service in Puerto Rico.&nbsp;  Please call your cell phone company before leaving for Puerto Rico to find out the specific policy in your case.</p>

<p>Puerto Rico   uses the same voltage (120 Volts) and the same outlets/plugs as the United States.</p>

<p>&nbsp;</p>

<p><strong>PowerPoint Presentations</strong></p>

<p>All speaker presentations must be loaded onto  the official conference laptop computer.&nbsp; Speakers are required to provide an electronic copy of their slides to Laura Rufibach, <u>the evening, before their presentation</u>.&nbsp; Presentations can be sent via email  (lrufibach@jain-foundation.org), or transferred via a USB stick.</p>

<p>&nbsp;</p>





<p><strong>Hotel Charges</strong></p>
<p>The Jain Foundation has reserved a block of rooms at the Rio Mar, therefore it is imperative that you tell us your arrival and departure dates, so that we can give the hotel an accurate count of the number of rooms that will be needed.  Do not book your own room at the Rio Mar, as this will result in double booking.  The room rate is $149/night + 21% (tax and resort levy) for single or double occupancy. &nbsp;A third person in the room is $40 per night additional (children under 18 are free). &nbsp;This special rate is available to conference attendees for 3 days before and after the meeting (the nights of June 11-20). &nbsp;However the availablity of rooms before and after the meeting is not guaranteed, so the sooner we hear from you, the more likely that they will be available. &nbsp;In addition to the room rate, a housekeeping gratuity of $2.50 per room per day will apply, as well as a one-time porterage fee of $7.00 per person. &nbsp;Rooms will be assigned from the Jain Foundation block on a first come first served basis upon arrival. &nbsp;Attendees have the option to pay an additional $50.00 for a "run of ocean" upgrade.</p>
<p>Participants  will be responsible for any incidental costs associated with their room &ndash; this includes parking ($15/day), phone calls, food/beverages taken from or ordered to the room, dry  cleaning, pay-per-view movies, etc.</p>

