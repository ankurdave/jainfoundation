                  <strong>Oligonucleotide-Based Editing of the SJL Mouse Dysferlin Gene Mutation </strong><strong>&nbsp; &nbsp;</strong><br>

<br>

Limb Girdle Muscular Dystrophy 2B and Miyoshi Myopathy (LGMD2B/MM) are diseases of muscle caused by mutations in the dysferlin gene, which encodes a large membrane associated protein important for cellular membrane repair and cell fusion. The SJL/J mouse is a naturally occurring model for LGMD2B in which a tandem repeat within an intron has been deleted and disrupts a splice acceptor site within an intron/exon junction. The mutation results in the deletion of an exon and instability of the dysferlin protein. Gene editing is a method of gene therapy in which synthetic modified single-stranded oligonucleotides are designed to alter single bases in mutant genes. Gene editing approaches as a treatment for other forms of muscular dystrophy have yielded promising results in mice, but data show that an ex vivo alteration results in higher frequencies than when oligonucleotides are introduced in vivo. Here, we describe an ex vivo targeted gene editing approach to alter a single base and restore proper splicing of the dysferlin gene in muscle precursor cells of SJL/J mice. We show the alteration of the mutant dysferlin gene by single-stranded oligonucleotides using a sensitive ARMS PCR assay, which is capable of distinguishing the mutant from the altered form of the gene. Quantitative RT PCR data show that cells treated with oligonucleotides transcribe mRNA containing the previously deleted exon. Thus, the restoration of dysferlin protein expression can occur in muscle progenitor cells using the gene editing technique and when combined with progenitor cell transplantation may serve as a feasible therapeutic approach for the treatment of LGMD2B. 
