                <td valign="top"><p class="section">Past Research Projects - Repair-Mediating Drugs </p>

                        <p class="home"><strong><span class="style2"><font color="#0175C2">Regenerative Capacity of Muscle </font></span><font color="#0175C2"><br>

                    </font></strong>Amy Wagers, Joslin Diabetes Center, Harvard

                    Medical School (Boston, MA)<br>

                        <br>

                         <strong>Project Objective</strong><br>

                         Explore whether exposure to a normal systemic environment

                         influences dysferlin-deficient disease progression<br>

                          <br>

                          Progressive decline in the regenerative capacity of

                           skeletal muscle is part of normal aging. Previous

                           work using parabiosis -- the surgical joining of two

                           animals so that they develop a common, anastomosed

                           circulatory system -- has shown that skeletal muscle

                           regenerative capacity in aged mice can be improved

                           by exposure to the systemic environment of young mice.

                           This project will use parabiosis between young wildtype

                           and dysferlin-deficient mice, at different stages

                           of muscle degeneration, to explore whether muscle

                           regeneration in diseased mice can also be improved

                           by exposure to a wildtype systemic environment. This

                           project may lead to identification of blood-circulating

                           factors that improve symptoms of dysferlinopathy.<br>

						  
						  
