                      <br>

                      <strong>Project Objective</strong><br>

Identify muscle-specific SARMs (Selective Androgen   Receptor Mediators) for therapeutic use in LGMD2B/MM<br>

<br>

Aside from avoiding   strenuous exercise to prevent further muscle damage there is no treatment   or cure for LGMD2B/MM. Glucocorticoid (steroid) hormone-treatment suppresses   inflammation and damage in other muscular dystrophies and muscle inflammatory   diseases but it is <u>not</u> effective in reducing muscle damage in   LGMD2B/MM patients and can even make it worse. Androgens are well-known stimulants of   muscle development and promoters of cell survival. Using mouse and human   models of muscle development we are analyzing the muscle-building mechanisms   of androgen receptor agonists in the presence and absence of dysferlin.   We are also establishing screens to identify SARMS (Selective androgen   receptor mediators) that are preferentially muscle building with minimal   side-effects as protective therapy against dysferlinopathy.<br>

