                  <strong>Plasma Membrane Repair Requires ERK-dependent Microvesicle Shedding</strong><br>

Peter A Keyel1, Lyussiena Loultcheva2, Robyn Roth2, John E. Heuser2, Wayne M.
Yokoyama1
1Howard Hughes Medical Institute and Rheumatology Division, Department of Medicine and
2Department of Cell Biology and Physiology, Washington University School of Medicine, St
Louis, MO 63110<br><br>

Dysferlin promotes membrane repair in muscle cells. However, even nucleated cells lacking
dysferlin actively repair damage to the plasma membrane caused both by mechanical tears
and the cholesterol-dependent toxin streptolysin O (SLO). Furthermore, various dysferlinnegative
cell types show differing sensitivities to SLO in a cholesterol-independent manner,
suggesting that the repair mechanism can be actively modulated. Understanding these
differences in membrane repair and how these repair mechanisms are modulated may allow
their utilization to treat diseases arising from defects in membrane repair proteins. Current
models suggest that endocytosis is the primary mechanism through which membrane repair is accomplished in toxinexposed
cells. Herein, we show that when cells are treated with SLO, a fraction of the toxin is not cleared from the cell
surface, even in cells that have successfully repaired the damage. We further show that resealing occurs under
conditions where endocytosis was blocked, suggesting endocytosis is not necessary for membrane repair. Using electron
microscopy, we demonstrate that this surface-accessible SLO forms pores, but is rapidly segregated into plasma
membrane protrusions. These protrusions are ultimately shed, and are enriched in GPI-anchored proteins and SLO, but
devoid of internal membrane markers such as Lamp1, consistent with plasma membrane-derived microvesicles. To
determine the molecular events necessary for microvesicle shedding in response to SLO, we used a panel of chemical
inhibitors. Although p38 MAP kinase inhibitors demonstrated no effect, the MEK inhibitors PD98059 and U0126 both
increased sensitivity to SLO without increasing toxicity on their own. These data implicate the MAP kinase pathway in
microvesicle generation. We conclude that plasma membrane repair functions via microvesicle shedding following the
rapid activation of ERK. </p>

