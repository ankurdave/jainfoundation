                  <strong>One Year Old Dysferlin-null Mice Recover from Physiological Injury Similar to Three
Month Old Dysferlin-null Mice</strong><br>
Lisa W. Ru, Joseph A. Roche, Renuka Roche, Robert J. Bloch<br><br>
Background: We have reported that 3 month old, dysferlin-null A/J mice show delayed
recovery after injury by large-strain lengthening contractions, and that the delay is due to
the need for myogenesis to replace lost fibers. Here, we compare the recovery of 1-yr old
A/J and control, A/WySnJ mice, from large-strain injury, and from the same injury repeated
a second time. We hypothesized that recovery in older A/WySnJ mice would be similar to
young mice, but that 1-yr old A/J mice would recover more slowly than young A/J mice, and
that recovery from re-injury would be even more delayed.
Methods: We induced injury by lengthening the ankle dorsiflexors of A/J and A/WySnJ
mice (male, 12-13 months old) through 90 degrees of plantarflexion during a maximal tetanic
contraction. We measured contractile torque before injury and at different times after injury, when we harvested TA
muscles to assess histological and biochemical changes. We also reinjured some A/J and A/WySnJ animals to test the
effects of repetitive injury on recovery
Results: Uninjured TA muscles of 1-yr old A/J mice produced lower and more variable torques than controls. As in young mice, 1-yr old A/J mice were not more susceptible to initial injury than A/WySnJ mice. Torque recovered to preinjury levels in 1-yr old mice within 7 (A/WySnJ) or 14 days (A/J). Recovery from a second large-strain injury was temporally similar to recovery from the first injury in both A/J and A/WySnJ mice. Conclusion: One year old A/J mice are weaker than A/WySnJ mice but do not differ significantly in their response toinjury, compared to young animals. One yr old A/WySnJ mice also respond to injury and to re-injury like youngeranimals. Our results suggest that 1-year old A/J mice regain contractile function after injury by generating new muscle fibers as effectively as young A/J mice.</p>
