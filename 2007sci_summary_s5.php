				<p><span class="style13">SCIENTIFIC SUMMARY </span><br>

				<br>

				  <a href="2007sci_summary.php" class="style16">&lt;-- back</a> <br>

				  <br><br>

                  <strong>Session V: Membrane Fusion and Repair </strong><br>

                  <br>

                  The Membrane Fusion and Repair session focused on the cellular process of membrane repair and on therapeutic prospects for improving or restoring this process even in the absence of dysferlin. It might be possible to trigger other cellular fusion proteins to substitute for the function of dysferlin in membrane repair. Some candidates include other ferlin family members, or other C2 domain proteins such as synaptotagmins. Another possibility is to identify drugs that stabilize the muscle membrane, making it less likely for tears to form, or that facilitate membrane fusion even in the absence of cellular fusion proteins. An example currently being tested is the drug Poloxamer 188. <br>

                  <br>

                  To screen for drugs or genetic modifiers of dysferlin, it is essential to have both in vitro and in vivo assays of membrane repair. In vitro assays are complicated by the question of how to physiologically and reproducibly damage cells, while in vivo assays are complicated by the necessity of using non-human homologs of dysferlin, whose function may differ somewhat from that of human dysferlin.<br>

                  <br>

                  Dr. Paul McNeil discussed various methods for damaging cells in vitro to trigger calcium-dependent repair -- these include laser wounding, impaling cells with a microneedle, and scraping cells off the bottom of a dish using a pipette. Cell scraping is a good initial screen to test for cell survival after wounding, while the laser assay allows detailed monitoring of membrane tears as they are resealed. The laser wounding assay has recently been used to test fibroblasts in which one of the calpain subunits is knocked out, and has demonstrated that calpains (like dysferlin) are required for calcium-dependent membrane resealing. Calpains may be involved in cytoskeletal remodeling at the site of injury. Similarly, HeLa cells in which the function of annexin A1 is blocked are unable to reseal after laser wounding. Annexins are known to interact with dysferlin and concentrate at sites of membrane disruption. Further studies may clarify the roles of these three proteins in the repair process. Finally, Dr. McNeil discussed preliminary data that overnight incubation with glutamine enhances membrane resealing after laser wounding of wild type HeLa cells. Depending on the mechanism of this enhancement, glutamine or one of its downstream targets could potentially improve the resealing phenotype of dysferlin-deficient cells. <br>

                  <br>

                  Dr. Joshua Zimmerberg discussed the relationship between membrane lipid curvature and the ability of a membrane to repair or resist the formation of tears (or pores). Surface tension tends to make membrane pores larger, while line tension tends to make pores smaller. In response to these forces, pores smaller than a critical size will spontaneously close. Surfactants such as Poloxamer 188 decrease surface tension,  increasing the critical size and making it more likely that membrane tears will spontaneously close. Another way of making closure more likely is to  increase the proportion of negative curvature lipids in the membrane, which increases line tension. A biological example of this effect is in the life cycle of malaria, a pathogen that exits red blood cells by creating pores in two membrane layers. A positive curvature lipid (LPC) promotes the replication of malaria because it makes pore formation and expansion easier. Dr. Zimmerberg also discussed the process of membrane fusion, which involves hemifusion (stalk formation) followed by expansion into a fusion pore. The hemifusion step is facilitated by lipids with negative curvature, while the fusion pore expansion step is facilitated by lipids with positive curvature that increase surface tension. C2 domain proteins or other proteins may also help increase surface tension around the pore. In response to questions following his talk, Dr. Zimmerberg mentioned that cholesterol has negative curvature but is often associated with other molecules with positive curvature, so that its effect on membrane curvature is hard to determine. One interesting question for the future is whether it may be possible to target lipids to specific vesicles or rafts in a cell. <br>

                  <br>

				  Dr. Steven Vogel discussed the sea urchin model system for studying calcium-dependent membrane fusion. Sea urchin eggs have long been used to study exocytosis, and recently Dr. Vogel has made use of fertilized sea urchin eggs at the two cell stage by injecting one of the two cells with a gene expression construct or with anti-sense morpholinos to knock down expression of a target gene. As the embryo develops, the uninjected half serves as a control for the injected half, and the laser wounding and repair assay (or other assays of endo- or exocytosis) can be performed on cells from both halves of the embryo. Sea urchins have a dysferlin homolog, whose function can be tested by this method. Dr. Vogel also discussed past experiments on exocytosis in sea urchin eggs, which revealed that the fusion step of repair happens too quickly for binding partners in both membranes to have time to meet. It may be that vesicles are already docked at the membrane prior to fusion, or that fusion is carried out by proteins present in only one of the membranes. This differs from the SNARE model of membrane fusion (requiring interaction between proteins in both membranes), which Dr. Vogel hypothesizes may be responsible for constitutive exocytosis but is too slow to give the rapid fusion response seen in repair. <br>

				  <br>

				  Dr. Samuel Ward discussed the role of the dysferlin homolog Fer-1 in <em>C. elegans</em>, where its known function is in sperm motility. <em>C. elegans</em> sperm move by pseudopod extension, which requires fusion of membranous organelles (MOs) with the plasma membrane at the site of extension. In normal worms, the MOs maintain stable fusion pores with the plasma membrane; while in Fer-1 mutant worms, the MOs dock at the membrane but do not fuse. These docked MOs have a characteristic appearance in electron microscopy images, with the tip of the vesicle (closest to the membrane) protruding from a protein collar. A possibly important functional difference between Fer-1 and human dysferlin is that Fer-1 is initially located in the membrane of the MOs, rather than in the plasma membrane (though it is deposited in the membrane after fusion). Dr. Ward also discussed the Fer-1 C2 domains, each of which is more conserved across the different ferlin family members than it is similar to the other C2 domains, suggesting that the domains have distinct biological roles. In <em>C. elegans</em>, unlike in humans,  missense mutations of Fer-1 are observed to cluster in the C2 domains. In response to questions following his talk, Dr. Ward mentioned that the identity of the protein making up the vesicle collar is unknown, but is not believed to be dynamin. <br>

				  <br>

				  Dr. Steven L'Hernault discussed the maturation of <em>C. elegans</em> MOs and the potential for using <em>C. elegans</em> as a model system for dysferlin function. He has identified proteins required for formation of MOs and has found that these vesicles become acidified prior to fusion with the plasma membrane, both results suggesting that the MOs are similar to lysosomes in human cells. The acidification is also interesting given that many viral fusion proteins are believed to be triggered by pH changes. As a demonstration of the potential for using <em>C. elegans</em> as a model system, Dr. L'Hernault has recently screened for suppressors of two different Fer-1 mutants by a self-fertility assay. The suppressors are located in the same chromosome as Fer-1 and another ferlin-like protein (T05E8.1), of which a targeted knockout is now available. Finally, Dr. L'Hernault discussed the potential for genetic studies in<em> C. elegans</em>, an organism that has traditionally been very difficult to engineer. The laboratory of Dr. Stephen Hauschka has recently had some success engineering <em>C. elegans</em> using a low copy number integration technique. RNAi is not normally successful but can work with certain genes, and manipulating introns is possible.<br>

				  <br>

				  During the brainstorming session, it was pointed out that of all the different methods of wounding cells, it is still unknown which type of damage is most similar to natural damage in the muscle. There may be some broad classifications of damage, since certain types of damage require calcium-dependent repair mechanisms and other types don't. (Dr. McNeil mentioned electroporation as an example of the latter -- the pores created by electroporation are  small enough that they presumably reseal on their own.) If all repair based on calcium-dependent exocytosis relies on the same fusion machinery, then any type of damage that triggers calcium-dependent repair should be a possible assay for the role of dysferlin. <br>

				  <br>

				  Another issue discussed in the brainstorming session was whether fibroblasts can be used as a cell type for membrane repair assays in the absense of dysferlin, since they are easier to collect from patients than muscle. This depends on the expression of dysferlin in fibroblasts, which turns out to be a controversial question. Dr. Rumaisa Bashir has found that dysferlin is expressed in fibroblasts, while Dr. Nicolas Levy has found the opposite. It may be that dysferlin is expressed in fibroblasts from only certain parts of the body, but this needs to be more rigorously tested.<br>

				  <br>

On the question of whether dysferlin deficient cells have trouble with any other types of exocytosis, it was noted that there is no evidence of any insulin abnormality in dysferlin deficient patients. Fusion of vesicles containing glucose transporters with the plasma membrane is another function of muscle cells that could potentially be affected by dysferlin, but this has not yet been tested. <br>

				  <br>

				  Finally, there was discussion of Dr. Ward's observation that Fer-1 is cleaved into two smaller forms in <em>C. elegans</em> sperm cells. This cleavage could simply a product of degredation, or it could occur during maturation of the protein. It was pointed out that many viral fusion proteins require cleavage into two smaller subunits before they can carry out fusion. It is possible that Fer-1 (and dysferlin) must also be primed for fusion by some cleavage reaction. More information is needed about these Fer-1 subunits, and whether the same can be seen in the case of dysferlin, to determine whether this cleavage is biologically relevant. <br>

