				<p><span class="style10">Session VII: Compensating for Dysferlin Deficiency </span><br>


				  <br><br>

                  <strong><span class="style17">Se-Jin Lee, MD PhD</span></strong><br>

                  <em>The Johns Hopkins University School of Medicine 

</em><br><br>

                  <img src="images/conference2008/Lee.jpg" width="135"><br><br>

                  <strong>Regulation of Muscle Growth by Myostatin</strong><strong>&nbsp; &nbsp;</strong><br>

<br>

Myostatin is a secreted signaling molecule that plays an important role in the regulation of skeletal muscle growth.  Genetic studies in mice, cattle, sheep, dogs, and humans have demonstrated that myostatin normally functions to limit muscle mass.  Moreover, agents capable of blocking myostatin activity have been shown to cause significant increases in muscle growth and muscle strength when administered to normal adult mice.  These findings have suggested the possibility that such agents could have utility in clinical settings in which increasing muscle mass and strength may be beneficial.  The overall effort of my laboratory has been to attempt to elucidate the mechanisms by which myostatin signals to target cells and by which myostatin activity is regulated in order to develop the most optimal strategies for targeting this pathway for clinical applications.  My presentation will focus on our recent efforts addressing the regulation of myostatin by extracellular binding proteins.  We believe that these findings have important implications for the development of novel therapies exploiting the biological activity of myostatin and related proteins for diseases such as muscular dystrophy.
                </p>
<p><a name="2"></a>

				  <br>

                  <strong><span class="style17">Joshua Zimmerberg, MD PhD</span></strong><br>

                  <em>NICHD/National Institutes of Health
joshz@helix.nih.gov</em><br><br>
<img src="images/conference2008/Zimmerberg.jpg" width="135"><br><br>

                  <strong>Understanding and Modifying the Dysferlin-Independent Pathway of Muscle Cell Repair</strong><strong>&nbsp; &nbsp;</strong><br>

Glen Humphrey, Paul Blank, Ludmilla Bezrukov, Elena Mekhedov, Kanneboyina Nagaraju*, and Joshua Zimmerberg
Program on Physical Biology, Eunice Kennedy Schriver National Institute of Child Health and Human Development, NIH, Bethesda, MD; * Research Center for Genetic Medicine, Children's National Medical Center, Washington, DC
<br><br>

Since the thermodynamic energetics for plasma membrane stability is dominated by the physico-chemical properties of the phospholipid bilayer membrane it comprises, we have been investigating the possibility that a putative dysferlin-independent pathway for plasma membrane resealing is altered by lipid composition.  The critical radius hypothesis for lipidic pore lifetime is the current model for membrane breakdown.  Critical radius depends upon 1) membrane monolayer spontaneous curvature, which is dependent upon lipid composition; and 2) membrane surface tension.  We have selectively passaged a primary culture from the dysferlin deficient AJ mouse and obtained a pure myoblast culture without fibroblast contamination.  These myoblasts in culture will fuse together to make myotubes.  Presently, this culture is well behaved and available for use as a dysferlin-deficient cell line.  Experiments to isolate plasma membrane for lipid analysis from this cell line are currently underway, using C2C12 myoblasts/myotubes as controls.  Cell membrane repair times for both AJ and control myoblasts/myotubes are estimated using lipid dye entry following membrane disruption by multi-photon excitation, in anticipation of measuring the effect of changing lipid composition on membrane repair.  Ultimately, if successful, we will explore the clinical possibility that supplementing dietary lipids can alter muscle cell plasma membrane phospholipid tails in ways that would increase resealing and  prevent or limit the duration of membrane rupture. 
				  </p>

<p>

				  <a name="3"></a><br>

                  <strong><span class="style17">Mohan Viswanathan, PhD</span></strong><br>

                  <em>Cambria Pharmaceuticals Inc
</em><br><br>

                  <img src="images/conference2008/Viswanathan.jpg" width="135"><br><br>

                  <strong>Identification of Potential LGMD2B/MM Therapeutics Utilizing a Caenorhabditis Elegans Model of Dysferlin Deficiency</strong><strong>&nbsp; &nbsp;</strong><br>

George Spracklin and Mohan Viswanathan
<br><br>

Loss-of-function mutations in the human gene dysferlin cause LGMD2B and MM while similar mutations in the homologous C. elegans gene fer-1 result in sterility.  Interestingly, there is striking conservation of protein function at the cellular level that underlies this disparity in phenotypic defects at the organismal level.  In mammalian skeletal muscle cells, loss of dysferlin function prevents membranous vesicles from sealing plasma membrane ruptures caused by normal activity, resulting in progressive muscular degeneration.  Likewise, loss of functional FER-1 protein in C. elegans prevents the obligate fusion of specialized vesicles known as membranous organelles to spermatocyte plasma membrane preventing spermatocyte maturation and motility.  The fact that FER-1 and dysferlin regulate vesicular membrane fusion events in different cell types in vastly diverged species suggests that both have likely evolved to regulate a conserved biological mechanism under specialized cellular circumstances.  We have exploited this conservation in biological function by utilizing C. elegans fer-1 mutants as a model for human dysferlin deficiency. 
We have implemented a high-throughput in vivo screen for small molecules capable of restoring fertility to a temperature-sensitive C. elegans fer-1 mutant strain.  We have identified a number of hit compounds and from these a number of structurally related active classes.  Currently, we are developing and implementing additional assays both in C. elegans and in cultured mammalian muscle cells to better characterize the effects of these compounds upon membrane fusion and repair.  Subsequently, we will develop structure-activity relationships among the various chemistry classes as the next step in the preclinical development of therapeutics for LGMD2B and MM. </p>

<p><a name="4"></a><br>
<strong><span class="style17">Kanneboyina Nagaraju, DVM PhD</span></strong><br>

                  <em>Children’s National Medical Center
knagaraju@cnmcresearch.org

</em><br><br>

                  <img src="images/conference2008/Nagaraju.jpg" width="135"><br><br>

                  <strong>Phenotyping of Dysferlin Deficient Mice: Functional and Behavioral Methods</strong><strong>&nbsp; &nbsp;</strong><br>

<br>

Phenotyping of mouse models of muscular dystrophies is critical not only to understand disease pathogenesis but also for pre-clinical efficacy and toxicity evaluations.  Even though promising therapeutic interventions for muscular dystrophy are steadily increasing, there is no consensus regarding the most appropriate pre-clinical trial design and end-points (clinical, functional, histological, and biochemical) for muscular dystrophy mouse models.  International efforts are currently in progress under the auspices of the Wellstone muscular dystrophy center of Washington DC and TREAT-NMD to identify and establish specific, sensitive and reproducible methods and draft standard operating procedures for each method to evaluate phenotype and assess drug efficacies in dystrophy mouse models.  The murine preclinical and phenotyping facility at Children’s National Medical Center is a state-of-the-art facility dedicated to muscular dystrophy mouse models.  We have recently evaluated the phenotype of two dysferlin deficient (SJL and AJ) models using various functional, behavioral, and histological assessments.  The presentation will discuss some of the advantages and disadvantages of these assessments in these mouse models.  These preclinical and phenotyping efforts would significantly strengthen and accelerate the pace of human clinical trials for LGMD2B.</p>

