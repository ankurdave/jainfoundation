                  <img src="images/conference/32-Bartoli.jpg" width="135"><br><br><br>

                  <strong>Could myoferlin compensate dysferlin deficiency  in LGMD2B?</strong><strong>&nbsp; &nbsp;</strong><br>

Marc Bartoli, Carinne Roudaut, Nathalie  Bourg, William Lostal and Isabelle Richard<br><br>

Myoferlin (MYOF) was identified along with  dysferlin as member of the Ferlin family, proteins homologous to FER1, a  protein involved in membrane fusion in C.elegans. Myoferlin mRNA was highly  expressed in cardiac muscle and to a lesser degree in skeletal muscle. Within  the cell, myoferlin was associated with the plasma membrane but, unlike  dysferlin, myoferlin was also associated with the nuclear membrane. Due to the  homology and the fact that both proteins were localized in the membrane of  skeletal muscle, myoferlin has been thought to be a good candidate for  compensatory therapy for the muscular dystrophy LGMD2B. To investigate this  hypothesis, we engineered a model animal overexpressing myoferlin. A transgene  encoding the mouse myoferlin was inserted in the unique HPRT locus in the X  chromosome by homologous recombination using the speedy mouse&copy;  technology. This strategy allowed us to analyse only one clone. Myoferlin expression was driven by the strong muscle  specific CAG promoter (the chicken skeletal alpha-actin gene promoter). The  transgenic newborn pups are healthy, showing no gross developmental  abnormalities compared with control littermates. Those mice were fertile,  produced normal litter size and have a normal lifespan. Currently, we are  characterizing transgenic mice by histological and immunological approaches to  quantify the level of overexpression and monitor the absence of deleterious  effect. Next, we will cross those mice with dysferlin deficient mice (A/J in a  BL/6 genetic background). Mice resulting from this breeding will be  characterized at histological, strength and molecular level. If overexpression  of myoferlin can alleviate the dystrophic phenotype in dysferlin deficient mice,  we will develop a screen to test different drugs that could increase the level  of expression of the myoferlin gene. Myoferlin model characterization and  preliminary result on crossed animals will be presented.

