                  <strong>A strategy based on splicing between concatemerized  AAV vectors for reversal of dysferlin deficiency by gene transfer</strong></span><br>

                  <br>

Dysferlinopathies  are caused by mutations in the <em>DYSF</em> gene encoding the Dysferlin protein  and include three clinically distinct diseases, namely Limb-girdle muscular  dystrophy type 2B (LGMD2B; OMIM 253601), the distal Miyoshi myopathy (MM; OMIM  254130), and a more recently identified and rare phenotype, the Distal anterior  compartment myopathy (DACM; OMIM 606768) (Illa  et al., 2001) (Ueyama et al., 2002). The dysferlin protein is a 237 kDa protein  composed of one C-terminus transmembrane domain and a very long N-terminus  cytosolic domain containing 6 C2 domains and a specific central domain (the  dysferlin domains) whose function is not yet elucidated. In adult skeletal  muscle fibers, dysferlin plays a key role in sarcolemma repair, as observed in  human and mice dysferlin deficient muscles (Bansal et al., 2003). In humans,  DYSF is located at the 2p13 chromosomal region and belongs to the category of  large genes

since it is composed of 55 exons spanning 150  kb of genomic DNA and is transcribed as a 6.5 kb mRNA (Bashir et al., 1998; Liu  et al., 1998). Considering the  recessive nature of dysferlinopathy, a sensible therapeutic strategy is by gene  transfer. The best vectors to date for transfer in muscle are derived from  Adeno Associated Virus (AAV). However, the size of the dysferlin cDNA (nearly 7  kb) prevents its direct incorporation in an AAV vector. To bypass this  limitation, we are testing an approach based on trans-splicing (Tahara et al.,  2004; Xu et al., 2004). This strategy uses two independent AAV vectors, one  carrying the 5&rsquo; part of the cDNA together with an intronic sequence bearing a  donor splice site and the other an intronic sequence with an acceptor splice  site followed by the 3&rsquo; part of the cDNA. In this strategy, the protein  expression is obtained after co-administration of the two vectors,  concatemerization of the viral genome and splicing between the two splicing  sites. For the proof-of-principle of our strategy,  we chose to split the dysferlin coding sequence at the level of the 28th  intron. Two AAV vectors were constructed: a 5&rsquo; vector carrying exon 1 to 28 of  the dysferlin cDNA and a complementing 3&rsquo; vector carrying the remaining  dysferlincDNA. As a first step, we verified that the 5&rsquo; and 3&rsquo; vectors  together produced the human dysferlin protein following cotransfection of 293  cells. The approach was then tested <em>in vivo</em> on dysferlin deficient mice  by intra muscular injection. Injected muscles were processed for expression  analysis at the RNA level using Taqman quantitative assays and by western blotting.  A substantial level of expression was obtained. An effort is now underway to  investigate the efficiency of the approach at the biochemical and functional  levels. &nbsp;</p>

