                  <strong>What We Learned, Didn’t Learn, and Might Still Learn from the MYO-029 Trial of Myostatin Inhibition in Adult Muscular Dystrophy</strong><strong>&nbsp; &nbsp;</strong><br>

<br>

Within the past year, the results of the Phase I/II trial of a neutralizing antibody to myostatin in adult muscular dystrophy have been analyzed and published.  This was the first clinical trial to include many of the limb girdle muscular dystrophies, including dysferlinopathies.  The trial was a double-blind, placebo-controlled, multinational, randomized study of MYO-029 over 6 months plus a 3 month follow up.  MYO-029 had good safety and tolerability with the exception of cutaneous hypersensitivity reactions at higher doses.  The study was not powered to evaluate for efficacy and no statistically significant increases in muscle mass, strength or function were found.  Consistent trends in muscle fiber diameters and muscle mass were observed.  Analysis of the data as a whole as well as individual data points, suggest important ways in which endpoints could be refined for future clinical trials in dysferlinopathies as well as other adult muscular dystrophies. </p>

