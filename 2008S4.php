                  <strong>Analysis of the DYSF Mutational Spectrum: Novel Insights and Novel Tools for Large-Scale Routine Diagnosis of Primary Dysferlinopathie</strong><strong>&nbsp; &nbsp;</strong><br>

<br>

The identification of deleterious mutations in patients affected with dysferlinopathy is an essential initial step, which constitutes a pre-requisite for accurate genetic counselling, follow-up and possible inclusion in future therapeutic clinical trials. 
As the French national reference centre for molecular diagnosis of primary dysferlinopathies, we received to date more than 220 patient samples for genomic mutational screening.  Disease-causing mutations identified in this cohort correspond to more than 25% of all DYSF mutations reported to date. Genomic mutational screening allowed for the identification of both disease-causing mutations in approximately 70% of patients. We are currently developing several complementary analyses with the aim of exhaustive genomic mutational analysis.
For the compilation and interactive analysis of mutational data, we developed a Locus-Specific database, the Universal Mutation Database for Dysferlin (UMD-DYSF, in collaboration with Christophe Béroud, Montpellier, France).  This database includes several bioinformatic tools which allow for the statistical analysis of mutational data.  We precisely analysed the mutational spectrum in 134 patients, included based on clinical suspicion of primary dysferlinopathy and/or dysferlin protein deficiency identified on muscle biopsy samples.  Most of the identified mutations are predicted to produce a truncated protein or one amino-acid substitution, but we also report a high proportion of nonsense mutations as compared to previous studies.  The database also includes specific algorithms for the prediction of a pathogenicity score, for newly identified intronic, missense- or isosemantic-exonic sequence variants, and is therefore a valuable bioinformatic support for genetic diagnosis in primary dysferlinopathies. 

                </p>
