                  <strong>Prognostic Factors in Dysferlinopathy Progression</strong><strong>&nbsp; &nbsp;</strong><br>

C. Angelini, C. Borsato, R. Padoan, M. Fanin, E. Pegoraro<br><br>

LGMD2B or dysferlinopathy is due to a mutation in dysferlin, a protein involved in membrane repair.  Dysferlinopathy might present with different clinical phenotypes such as a distal or proximal myopathy or a cramp/myalgia syndrome with hyperCKemia.  The disease progression is highly variable in patients from the same family, implying that epigenetic factors such as muscle fatigue, autophagy and physical activity, that produce different types of either mechanical or inflammatory stress, could determine a different muscular injury and recovery.  We investigated whether strenuous physical activity before onset might influence the disease clinical progression in 11 cases.  We also analysed by quantitative histopathology 25 cases and evaluated MHC1 molecules and markers of degeneration/regeneration.  Only patients with a defined molecular diagnosis of LGMD2B were included, all patients were evaluated and graded with a modified Gardner–Medwin and Walton clinical scale and statistically analyzed by Cox multivariate and Log rank.  
We divided the patients in two groups:  five of 11 patients never performed regular physical activity, six performed competitive physical activity until disease started (at least 3 hours/week for 2 years of continuous physical training).  Six /11 patients (mean age: 25 years) did physical activity in a series of agonistic sports (soccer, basket, volleyball and swimming) before the disease onset; five of 11 patients (mean age: 31 years) never performed any sport.  Sportive patients presented a more rapid clinical progression and early loss of limb muscle strength.  Time to reach stage (loss of Gowers) of modified Gardner-Medwin and Walton scale was 5.75 years in sportive patients and 10 years in non-sportive patients (Cox test: p=0.11; Log rank: p=0.09).  In agreement with our previous study an inflammatory response (Fanin M, Angelini C, Neuropathol. Appl. Neurobiol. 28;461-470, 2002) was seen in most cases.  The highest rate of degeneration /regeneration and acute inflammation was found in patients younger than 30 years:  clinicians should be aware of these inflammatory reactions and avoid unnecessary steroids; in 2 patients an immunomodulatory treatment with IVIG for 6 months was unsuccessful. 
All LGMD2B patients showed a progressive loss of muscle function but patients that performed physical activity had a significantly more rapid clinical evolution and this should be considered as a negative prognostic factor, which might influence disease progression. 
				  
				  
				  
				  
				  
				  
