                  <strong>Session II: Dysferlinopathy and the ImmuneSystem</strong><br>

                  <br>

                  This session addressed the role of dysferlin in the immune system and highlighted studies that suggest a more direct involvement of immune system dysfunction in dysferlinopathy.  While an inflammatory response to damaged muscle is expected, certain observations—such as the robust expression of dysferlin in immune cells, the unusually marked inflammation associated with the disease, and the development of autoimmunity in mouse models of dysferlinopathy—have raised the possibility that an aberrant immune response may play a more active, rather than passive, role in the progression of LGMD2B/Miyoshi.<br><br>
				  
<strong>Dr. Eric Hoffman</strong> (Children’s National Medical Center) presented his recent studies on a possible mechanism behind the aggressive inflammatory response seen in many dysferlinopathy patients.  Work carried out in collaboration with Dr. Kanneboyina Nagaraju has uncovered enhanced phagocytotic activity in dysferlin-deficient murine monocytes and macrophages, as well as upregulation of multiple vesicular trafficking proteins in these cells.  Investigation of how these immune cells interact with and are recruited to damaged myofibers has revealed a specific induction of the Rab27A/Slp2a vesicle trafficking pathway in dysferlinopathy but not in two other dystrophies, LGMD2I and Becker’s muscular dystrophy (BMD).  These results suggest a possible hypothesis: that this alternative vesicle trafficking pathway is induced to compensate for the absence of dysferlin, but that this mode of compensation is also accompanied by abnormal release into the extracellular environment of vesicular contents that serve as paracrine signals for the recruitment of cytotoxic T-cells and natural killer cells, leading to inflammasome formation and fiber death.  Components of the inflammasome are increased in the dysferlin-deficient SJL mouse model and LGMD2B/MM patients, and this finding appears to be specific to dysferlin deficiency.  Dr. Hoffman also briefly mentioned that the reason why prednisone does not benefit dysferlinopathy patients may not have to do with its anti-inflammatory activity but rather with the fact that this steroid helps re-synchronize regeneration in muscle, a process that is already in place and not excessively affected in LGMD2B/Miyoshi patients.  Overall, Dr. Hoffman’s work underscores the contribution of aberrant immune responses to dysferlinopathy, and suggests that therapeutic strategies to intervene these responses should be explored.  <br><br>

<strong>Dr. Simone Spuler</strong> (Charité University Medicine Berlin) discussed the involvement of the complement system in disease pathology associated with dysferlin deficiency.  Complement is a major effector of the humoral immune response and consists of a group of serum proteins that generate a cytolytic membrane attack complex (MAC).  Dr. Spuler’s studies have shown that dysferlin deficiency is associated with absence of the complement protection factor DAF1/CD55 from the surface of skeletal muscle fibers in mouse models as well as LGMD2B/MM patients, which may make the fibers more prone to membrane attack via the complement cascade.  This hypothesis is supported by Dr. Spuler’s findings of MAC deposition on dysferlin-deficient muscle fibers and the susceptibility of dysferlin-deficient myotubes to complement-mediated lysis.  Furthermore, the administration of anti-C5 antibodies to antagonize complement activity in dysferlin-deficient mice led to reduction of muscle fiber damage, and IVIG therapy in three LGMD2B/MM patients led to muscle strength improvement and decreased levels of MAC.  These results indicate that inhibition of complement activity should be explored as a therapy for LGMD2B/MM.  <br><br>

<strong>Dr. Christina Jamieson</strong> (University of California, Los Angeles School of Medicine) presented data suggesting that dysferlin may be involved in T-cell apoptosis.  Gene expression profiling of murine T-cells treated with the glucocorticoid dexamethasone revealed that dysferlin mRNA, along with that of some synaptotagmins, is upregulated during apoptosis.  This induction of expression was abolished by inhibiting apoptosis using TCR-mediated survival signaling.  Another apoptotic stimulus, gamma irradiation, also induced expression of dysferlin mRNA and protein in the mouse thymus, albeit with different kinetics than was observed for dexamethasone treatment.  Comparison of irradiation-induced apoptosis in wild type C57BL/6 versus dysferlin-null A/J mice showed that CD8+ T-cells were more resistant to apoptosis in dysf-null mice than wild type.  Overexpression of mouse dysferlin cDNA in T-cells resulted in a 50% reduction in viability even without dexamethasone treatment.  Taken together, these data suggest that induction of dysferlin expression in T-cells promotes apoptosis.  Dr. Jamieson hypothesized that dysferlin may be involved in the vesicle trafficking and membrane remodeling steps associated with late-stage apoptosis, such as membrane blebbing, and that the absence of dysferlin may lead to aberrant apoptosis in T-cells.  She also briefly discussed the effect of glucocorticoids on muscle development, and showed that dysferlin is also induced in C2C12 cells upon dexamethasone treatment.  She speculates that the inability to upregulate dysferlin may contribute to the ineffectiveness of glucocorticoid treatment for LGMD2B/MM patients, in contrast to other MD patients. 
