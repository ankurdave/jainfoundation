				<p><span class="style13">SCIENTIFIC SUMMARY </span><br>

				<br>

				  <a href="2007sci_summary.php" class="style16">&lt;-- back</a> <br>

				  <br><br>

                  <strong>Session VI: Other Therapeutic Strategies </strong><br>

                  <br>

                  The Other Therapeutic Strategies session discussed a variety of different approaches to muscle disease therapy that do not fall into the categories of gene therapy or stem cell therapy. This session included therapies designed to modify the effect of specific genetic mutations in dysferlin, to replace the function of dysferlin by inducing expression of other cellular proteins, and to increase endogenous levels of muscle regeneration. 

                  <br><br>

				Dr. Lee Sweeney discussed an ongoing clinical trial of PTC124, a small molecule that promotes ribosomal read-through of premature stop codons. (Aminoglycoside antibiotics such as gentamicin can also cause read-through, but have significant toxicity.) PTC124 was identified after a high-throughput in vitro chemical screen for read-through activity. In mdx mice, it successfully restores dystrophin expression, improves force generation, and lowers CK levels. Phase 1 studies in humans revealed no significant toxicity or side effects. Phase 2 studies have been carried out with Cystic Fibrosis and Duchenne MD patients. Dr. Sweeney suggests that these two conditions represent distinct efficacy tests because in CF the relevant protein has a short half-life but not much is needed to correct the disorder, while in DMD the relevant protein has a long half-life but  much more   is needed. Cultured myotubes from all DMD patients were tested with PTC124 and showed dystrophin expression in vitro. During the trial, a higher dose of the drug was required for DMD patients, because it turned out that children clear the drug  faster than adults. After 4 weeks of treatment, dystrophin could be detected in muscle biopsy from about half of the DMD patients. A Phase 3 trial will begin later this year. Dr. Sweeney is now evaluating the ability of PTC124 to read through the dysferlin R1905X mutation in an artificial in vitro system.<br><br>

				In discussion of this approach, an important question was whether and how to screen patient cells for efficacy before enrolling them in a trial of PTC124. One feature of PTC124 is that it inserts a substitute amino acid in place of the premature stop codon (usually a missense substitution). In the case of dysferlin, missense mutations are often detrimental to protein function, so it may be necessary to test for protein function after read-through of each patient's specific mutations. Depending on the stop codon, PTC124 has preferences for certain substitute amino acids. Dr. Sweeney is studying the R1905X dysferlin mutation because in that case the most likely substitute amino acid is arginine (R), so a significant percentage of  full-length protein may be produced after read-through without any missense substitution. Dr. Sweeney also recommended testing patient cells with PTC124 directly, without first testing the efficacy of gentamicin at promoting read-through of their mutations. <br>

				<br>

				Dr. Kathryn Wagner discussed an ongoing clinical trial of a myostatin neutralizing antibody, MYO-029. Loss of myostatin in animals and some human cases is known to cause muscle hypertrophy, faster regeneration after injury, and reduced fibrosis. Increased amounts of myostatin, on the other hand, cause increased fibrosis. A Phase 1/2 trial of MYO-029 was carried out in patients with several different types of MD, and functional data from the trial are still being analyzed. New types of inhibitors of myostatin's production or action are now being developed and tested in animals to determine which has the greatest functional effect. In response to questions following her talk, Dr. Wagner mentioned that old mdx/myostatin null mice have normal regeneration capacity following cardiotoxin injury, suggesting that there is no negative long-term effect  of myostatin inhibition (such as reduction in satellite cell number). However,  she acknowledges that inhibiting myostatin in adults could  cause long-term effects that do not show up in knockout animals, which might be born with increased numbers of satellite cells.<br>

				<br>

				In discussion of this approach, an important question was how to monitor the effectiveness of myostatin inhibitors, other than looking for  large-scale functional effects. Dr. Wagner mentioned that it is very difficult to detect even normal levels of myostatin in human serum, and detection must be done by immuno-precipitation. Furthermore, many inhibitors affect myostatin activity but not its serum concentration, so it is important to monitor downstream pathways. This may be a difficult task if myostatin targets many different pathways.<br>

				<br>

				Dr. Elizabeth McNally discussed studies of myoferlin, the human ferlin family member most closely related to dysferlin. Myoferlin is highly expressed in regions of muscle damage and can be seen at sites of myoblast fusion into myotubes. Myoferlin null mice have more myoblasts but smaller muscles than wild type mice, suggesting that myoblasts in these mice can proliferate but are unable to fuse into muscle fibers. In contrast to myoferlin, dysferlin is not normally upregulated at damaged sites after cardiotoxin injuy, but there is sometimes a compensatory upregulation of dysferlin in myoblasts of  myoferlin null mice.  Dr. McNally has found that only the C2A domain of both myoferlin and dysferlin binds calcium. Myoferlin proteins with missense mutations in this domain are able to bind calcium at low concentrations but not at higher concentrations, suggesting that changes associated with calcium binding destabilize the missense mutants. Finally, Dr. McNally has shown that myoferlin interacts with EHD2, a protein involved in vesicle trafficking and endocytic recycling. The binding site is in the C2B domain of myoferlin and does not appear to be present in dysferlin. <br><br>

				Subsequent discussion focused on the therapeutic possibility of 

				upregulating myoferlin to compensate for dysferlin deficiency. There has been some confusion about whether myoferlin is already upregulated in this disease. Dr. McNally clarified that myoferlin often appears to be upregulated in many different muscle wasting diseases, but this is because it is highly expressed in myoblasts while wasted muscle is regenerating. It does not remain expressed in mature muscle fibers after myoblast fusion. The goal of this approach, therefore, is to express myoferlin (or to prevent it from being turned off) in mature fibers. Dr. McNally is now attempting to screen for compounds that will activate the myoferlin promoter. <br>

				<br>

				Dr. Terence Partridge discussed exon skipping as a possible treatment for dysferlinopathy. Oligonucleotides that interfere with splicing and make the cell skip particular exons can be used to remove stop or frameshift mutation-containing exons from a processed transcript, assuming that the rest of the transcript remains in frame. U7 constructs can achieve permanent expression of these oligonucleotides; however, sequences  need to be optimized in vivo for each targeted exon, so Dr. Partridge advocates starting treatment with transient skipping (e.g. Morpholinos). The success of an exon skipping approach is highly dependent on the target protein's structure, and revertant muscle fibers are generally a good indicator that exon skipping may be effective. While revertants are common in dystrophin deficiency, there has only been one report of revertant fibers in dysferlin deficiency (by Dr. Jens Reimann in Germany). However, the dysferlin revertants are isolated fibers and have not expanded within the muscle, suggesting that there is no functional benefit of the restored protein. It may be that dysferlin is highly-structured and cannot tolerate loss of whole exons, a possibility supported by the fact that disease-causing missense mutations can be found throughout the protein rather than clustered in domains. <br>

				<br>

				To progress towards an exon-skipping therapy for dysferlin deficiency, Dr. Partridge suggests generating a panel of dysferlin constructs, each missing an exon. These constructs can be transfected into dysferlin null cells to test for functional recovery, and then transgenic mice can be made with the promising constructs to check for improved symptoms relative to dysferlin null mice. 

				Other participants suggested working directly with cells (either myoblasts or monocytes)  from patients who have mutations in candidate exons, such as 21/22 or 32. One question raised in the discussion was  whether the function of the skipped protein should be tested in  other cell types (e.g. immune cells) in addition to muscle. <br><br><br>

