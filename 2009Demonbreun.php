                  <strong>The Myoferlin Promoter Responds to Muscle Damage</strong><br>

Alexis Demonbreun, Konstantina Heretis, Rodney Dale, Eric C. Svensson, Elizabeth
McNally
Committee of Developmental Biology, Department of Medicine, Department of Human
Genetics. The University of Chicago, Chicago, IL 60637 USA<br><br>

Loss of function mutations in dysferlin lead to LGMD2B. In the absence of dysferlin, there is
a marked delay of resealing of muscle membrane tears, and dysferlin loss is associated
with vesicle accumulation under the membrane. Myoferlin is highly related to dysferlin.
Similarly to dysferlin, myoferlin also contains at least six C2 domains and the amino terminal
C2 domain of myoferlin, C2A, binds phospholipids in the presence of calcium. Unlike
dysferlin, myoferlin is highly expressed in pre-fusion myoblasts and is expressed at low
levels in mature, non-injured muscle. Mice lacking myoferlin have smaller muscle fibers
due to defective myoblast fusion during myoaugmentation. Myoferlin is highly upregulated
after muscle damage. The upregulation of myoferlin in both muscular dystrophy and the cardiotoxin model of muscle
injury is consistent with myoferlin’s role in regeneration and repair in muscle. The calcineurin signal transduction
pathway and the nuclear factor of activated T-cells (NFAT) transcription factors have been implicated in regulating
myogenesis and repair. We characterized the highly conserved 1543bp promoter fragment of myoferlin, which was
found to be sufficient to drive high level expression in C2C12 myoblasts. Mutagenesis of the myoferlin promoter
implicated NFAT binding sites in promoter regulation, and NFATc1 and NFATc3 were found to occupy the myoferlin
promoter driving expression in myoblasts with downregulation in myotubes. This promoter region was also shown to be
responsive to myotube damage in vitro and in vivo in the newly created 1543- GFP transgenic mouse model consistent
with the upregulation of myoferlin in damaged muscle, providing an animal model that “senses” muscle damage. </p>

