                  <strong>Blood Monocytes:  a valuable model for screening abnormalities of dysferlin cell localization and  function </strong>

                <br>Nicolas Wein1, Sebastien Courrier1,  Martin Krahn1,2, Veronique Labelle2, France Leturcq3, Pierre Cau1 and Nicolas L&eacute;vy1,2</p>

                1Team 5: Neuromuscular  Disorders and Laminopathies, Inserm U491, Marseille, France; 2Department of Medical  Genetics, Timone hospital, Marseille, France; 3Biochemical and Molecular Genetics,  Cassini Laboratory, Paris, France

                <p>Dysferlinopathies are  a group of autosomal recessive muscular dystrophies, due to defects in the <em>DYSF</em> gene. <em>DYSF</em> is expressed in several tissues including skeletal and heart  muscle, and monocytes/macrophages. The aim of our project is to set-up  functional tests in monocytes and to explore therapeutic approaches in  dysferlinopathies. Presently, after clinical exploration, initial diagnosis of  dysferlinopathies is based on Western blot (WB) from proteins obtained after  muscle biopsy or blood monocytes, and our results

suggest these latter represent an excellent  and easy to obtain cellular model. Dysferlin mRNA and proteins are present in  monocytes from healthy individuals. RT-PCR, WB and IF (immunofluorescence with  NCL-Hamlet antibody) have demonstrated that monocytes express a <em>DYSF</em> mRNA (6.9kb) and produce Dysferlin protein (237kDa). However, Dysferlin cannot  be detected by WB performed on monocytes after a 48h blood storage, whereas  Hamlet antibody still detects a lower yet uncharacterized band (MW = 200 Kda).  IF has shown that Dysferlin is present at the plasma membrane in resting  monocytes, while a cytoplasmic staining is seen when monocytes are submitted to  mechanical stress or electroporation. Furthermore, we have designed a new  diagnosis test using flow cytometry from monocytes. This approach allowed  detection of both Dysferlin and monocytes from only 100&micro;l of whole blood from  healthy individuals. We are now screening a cohort of patients using flow  cytometry and WB analyses, which is a time consuming method, in order to valid  this method. Finally, we are setting up functional tests towards studying  monocyte phagocytosis. Our hypothesis is based on the following data: 1) in  monocytes, Dysferlin is associated, as in skeletal muscle cells, with caveolin  and annexins, both proteins being involved in monocyte phagocytosis; 2)  phagocytosis requires compensatory plasma membrane supply through exocytosis;  3) if Dysferlin may play a role in plasma membrane supply after phagocytosis,  then phagocytosis could be altered in monocytes from patients affected with  dysferlinopathy. In that case, monocyte phagocytosis could be used to evaluate  the disease severity and predict the efficiency of potential therapeutic  strategies.</p>

