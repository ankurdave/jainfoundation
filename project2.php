<p class="home"><strong><font color="#0175C2"><span class="style1">Therapeutic role of dietary lipids in LGMD2B musculodystrophy </span> <br>

                    </font></strong>Joshua Zimmerberg, NICHD/NIH<br>

                        <br>

                         <strong>Project Objective</strong><br>

                         To determine if changes in membrane composition can enhance membrane repair in the absence of dysferlin.<br>

                          <br>

                          At the heart of suffering in LGMD2B musculodystrophy is the death without regeneration of muscle fibers that are not repaired due to the functional absence of a protein, dysferlin, that mediates the fusion of intracellular membrane to the myolemma.  To treat patients in this situation, one would like to augment the natural tendency of membranes to reseal after damage, and minimize the inflammation that impedes regeneration and magnifies tissue destruction.  Since the major component of membrane is lipid, and dysferlin probably works on lipids, it is reasonable to try to bypass the requirement for dysferlin by changing the lipid composition of the myolemma. We propose to test the effect of dietary lipids on membrane repair in planar phospholipid bilayer membranes, myotube muscle membranes, rodents, and because dietary lipids are safe and well tolerated, in human subjects.<br>

