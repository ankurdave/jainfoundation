                       	<strong><font color="#0175C2"><span class="style1">Non-viral Gene Therapy</span><br>

                        </font></strong>Michele Calos, Stanford University

                    (Palo Alto, CA)<br>

                    <br>

                    <strong>Project Objective</strong><br>

To use non-viral gene therapy to introduce the dysferlin   gene safely and effectively into dysferlin-deficient muscles<br>

                         <br>

                        We are pursuing two gene therapy   approaches.&nbsp; One approach involves direct delivery of plasmid DNA   into muscle fibers by using hydrodynamic injection.&nbsp; The other   approach employs transfection of mesoangioblast stem cells with plasmid   DNA, followed by <em>in vivo</em> transplantation of the corrected stem   cells.                         In either strategy, the phiC31   integrase, a site-specific phage recombinase, will be used to carry   out integration of the dysferlin gene into the chromosomes of target   cells.&nbsp; This process should produce robust, long-term expression   of dysferlin at a set of safe integration sites.                         The initial experiments seek   to establish whether hydrodynamic DNA delivery is effective and safe   in dysferlin-deficient A/J mice.&nbsp; In addition, transfection, integration,   and stable expression of plasmid DNA in mesoangioblast stem cells will   be demonstrated, along with differentiation of the transfected stem   cells into skeletal muscle fibers <em>in vitro</em>. If justified by the initial   studies, the next phase of the work will evaluate and compare the levels   of dysferlin production that result from the direct DNA delivery and   stem cell approaches in the muscles of A/J mice <em>in vivo</em>.<br>

