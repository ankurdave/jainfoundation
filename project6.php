                         <strong>Project Objective</strong><br>

                         To explore different ways to transfer the dysferlin

                         cDNA in muscle of animal models<br>

                          <br>

                          This project uses the latest strategies that have

                           been described in the field of gene therapy to bypass

                           the large size limitation of the dysferlin gene. The

                           approach will be tested in vivo on dysferlin deficient

                           mice. The reversal of the pathological signs will

                           be analyzed by histology and using a functional assay

                           for the efficiency of membrane repair.<br>

