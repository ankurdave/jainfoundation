                  </span><strong>Folding,  'shape-shifting' and membrane targeting of dysferlin and dysferlin mutants</strong><br>Sandra T. Cooper1,2, Harriet P. Lo1,2,  Frances J. Evesson1, Rachel A Peat1,  and Kathryn N. North1,2</p>

				<p>1Institute for  Neuromuscular Research, <a name="0.2_01000001"></a>The Children&rsquo;s Hospital at  Westmead, Locked Bag 4001, Sydney, NSW 2145, Australia; 2<a name="0.2_01000002"></a>Discipline of Paediatrics and Child Health, Faculty of  Medicine, University of Sydney, Sydney, Australia</p>

				<p> Many  mutations resulting in dysferlinopathy are only single amino-acid substitutions  within the large dysferlin protein.  &nbsp; It is curious why these dysferlin  missense mutants are so effectively degraded in adult skeletal muscle, and not  retained to perform partial function, in the same way that missense mutants or  &ldquo;in frame&rdquo; deletion mutants of dystrophin remain to perform partial function in  Becker muscular dystrophy.&nbsp; Interestingly, we have shown that disease-causing missense mutants of  dysferlin are robustly expressed in patient myotubes, although are undetectable  or expressed at very low levels in adult skeletal muscle.&nbsp; Does protein  quality control impact disease severity?&nbsp;  By definition, disease-causing dysferlin missense mutants highlight key  residues important for folding, trafficking or function, and are valuable tools  for cell biology. Our initial studies have focused on two patient-derived  mutations located within the C2F domain of dysferlin, to explore their cellular  fate and basis for removal and degradation in adult skeletal myofibres. To  compliment these studies, we have also created a panel of dysferlin C2-domain  deletion constructs, to define which domains of dysferlin are required for  membrane targeting and to assess the role of the C2F domain of dysferlin.&nbsp; Transfection studies and immunocytochemistry  of dysferlin using a panel of antibodies reveals that dysferlin can adopt  different conformations, and that certain conformations preclude antibody  accessibility to particular epitopes. Surface labelling of intact cells reveals  that dysferlin is efficiently targeted to the plasma membrane, and is rapidly  internalized following antibody binding to its cytoplasmic tail.&nbsp;  Interestingly, all C2-domain deletion constructs are transported efficiently to  the membrane, although different constructs exhibit different membrane  properties. Unexpectedly, both patient-derived dysferlin C2F mutants also  target to the plasma membrane, although each shows different efficiencies of  membrane targeting and longer-term protein stability.				  </p>

