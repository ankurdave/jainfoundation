                  <strong>Role of apoptosis in muscular dystrophy and Miyoshi myopathy</strong>

                <br>

                Douglas P. Millay and Jeffery D. Molkentin

                <br>

                <br>

                Muscular  dystrophies (MD) encompass a diverse group of genetic disorders, although most  are caused by mutations in proteins of the dystrophin-glycoprotein complex  (DGC), or other proteins that similarly provide stability to the muscle cell  membrane (sarcolemma).&nbsp; An unstable sarcolemma has been associated with  increased membrane microtears and increased flux of Ca2+, leading to  myofiber degeneration and the initiation of disease.&nbsp; For example, loss of  dysferlin, a calcium sensitive protein important for membrane repair and  vesicular trafficking, results in myofiber degeneration and disease in mice and  humans.&nbsp;&nbsp;Here we are investigating two possible pathways in which  membrane instability and Ca2+ influx could cause disease; first  examining the role of apoptosis and, second, determining the contribution of Ca2+  -induced membrane repair by overexpressing dysferlin in transgenic mice.&nbsp;To  examine the importance of caspases

as effectors in myofiber degeneration we have  recently generated a series of skeletal muscle-specific transgenic mice  overexpressing X-linked inhibitor of apoptosis (XIAP), a caspase inhibitor.&nbsp;  In addition, a transgenic mouse overexpressing Bcl-XL (an  anti-apoptotic protein) was created to further investigate other aspects of  apoptotic processes in MD disease progression.&nbsp; Transgenic mice for each  approach are currently being crossed with two models of MD, the mouse null for  &delta;-sarcoglycan (a member of the DGC) and the more severe model lacking laminin  &alpha;2 (an extracellular matrix protein).&nbsp; Furthermore, we plan to cross the  two aforementioned transgenics with the dysferlin null mouse to examine the  role of apoptosis in this model of MD.&nbsp; Preliminary results from the cross  of laminin &alpha;2 null with XIAP Tg and &delta;-sarcoglycan null with XIAP Tg will be  presented. In order to investigate if membrane repair, or the health of the  sarcolemma associated with optimal vesicular trafficking, in MD leads to any  benefit we generated a skeletal muscle-specific transgenic mouse overexpressing  dysferlin (3-fold overexpression of protein was observed).&nbsp; These  transgenic mice have also been crossed with the two models of MD discussed  above. Preliminary data from these crosses will be presented.&nbsp;The  dysferlin transgenic mouse we created does not seem to exhibit any overt  defects in skeletal muscle at 3-fold overexpression levels; therefore it could  be a good model to further investigate the function of the protein. These  combined approaches will determine the potential primacy of apoptosis as a  mediator of skeletal muscle degeneration in MD, as well as shed additional  light on the function of dysferlin <em>in vivo</em>. </p>

