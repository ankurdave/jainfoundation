                  <strong><span class="style17">John Heuser, MD</span></strong><br>

                  <em>Washington University School of Medicine, St. Louis, MO
jeff.molkentin@cchmc.org</em><br>
<br>

                  <img src="images/Conf09/Heuser.jpg" width="135"><br><br>

                  <strong>Developing the Hypothesis that Dysferlin is Required for Stabilizing Muscle T-tubule
Connections to the Sarcolemma </strong><strong>&nbsp; &nbsp;</strong><br>
John Heuser, Lucy Loultcheva,Tanya Tenkova, Robyn Roth<br>


<br> 
We will advance the hypothesis in this talk that muscle T-tubules are uniquely vulnerable to
damage during eccentric contractions, and that dysferlin plays a role in stabilizing T-tubule
connections with the plasma membrane, or re-establishing connections with the plasma
membrane if and when T-tubules are damaged by this sort of contraction. Further, we will
argue that the reason for this T-tubule vulnerability, which is unique to muscle, is due to the
fact that the surface-membrane of the muscle fiber, the sarcolemma, is pinned down to the
special extracellular matrix that surrounds all muscle fibers and stabilizes them, so the
surface membrane cannot slip laterally. We will then explain how the T-tubule system,
which connects everywhere to the sarcolemma in order to spread excitation throughout the muscle fiber, is itself pinned down to certain positions in the myofilament lattice of the internal sarcoplasmic contractile apparatus (to the A-I junction in warm-blooded animals and the Z-band in cold-blooded animals). These competing physical constraints, we will argue, mean that whenever the internal myofilament lattice becomes displaced laterally from its natural starting-point distribution relative to the surrounding sarcolemma (as it likely does during eccentric contraction), the T-tubule origins and insertions must become stretched and consequently, T-tubules must become vulnerable to rupture or evulsion from the cell surface. We will further consider the likelihood that this sort of T-tubule evulsion would interrupt excitation-contraction coupling in the affected region of the muscle fiber and likely cause a local flaccid paralysis, which could promote further plasmalemmal/sarcomere dislocation and further T-tubule rupture, thus propagating the muscle damage. Finally, we will offer that if dysferlin is not the protein that helps to prevent such T-tubule damage or helps to heal it, then discovering whatever protein does do this job will become just as important for understanding the muscular dystrophies, as is understanding the role of dysferlin itself.                </p>
                
<p><br>

