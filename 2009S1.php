				<p><span class="style10">Session I: Dysferlinopathy - Mechanisms of Pathology I</span><br>

				  <br>

				  <br>

                  <strong><span class="style17">Robert J. Bloch, PhD</span></strong><br>

                  <em>University of Maryland School of Medicine, Baltimore, MD
</em><br>
<br>

                  <img src="images/Conf09/Bloch.jpg" width="135"><br><br>

                  <strong>Response of Dysferlin-Null Muscle to Injury by Lengthening Contractions: Recovery
of Function, Membrane Resealing, and Inflammation </strong><strong>&nbsp; &nbsp;</strong><br>

Robert J. Bloch, Lisa Ru, Joseph A. Roche<br><br>

We examined the response of dysferlin-null TA muscles in the A/J mouse to large-strain
and small-strain injuries induced by lengthening contractions. We tested the survival of
injured fibers, contractile function, uptake and retention of fluorescein dextran (FDx, 10 kDa,
fixable) during and after injury, and labeling by membrane permeability probes on d3 after
injury. We also characterized the mononuclear cells in injured muscle. A/J and control mice
initially respond similarly to injury from large- and small-strain lengthening contractions,
losing 40-45% of their contractile function. Controls recover from large-strain injury rapidly
(by d3) by membrane resealing, whereas A/J mice require ~2 wks to recover completely by
myogenesis. After small-strain injury, both A/J and control mice recover by myogenesis,
but A/J mice recover 1 wk later than controls. Similar numbers of myofibers in A/J and controls retained FDx through d3
after either injury. At d3, many A/J myofibers labeled with FDx failed to label with a second permeability marker,
suggesting that their membranes had resealed at the time of injury and remained resealed until d3. Other fibers were
labeled with the second marker, indicating secondary injury. These results do not depend on the molecular sizes of the
markers used (3–40 kDa). A/J muscles after large-strain injury, and both A/J and controls after small-strain injury, were
infiltrated by mononuclear cells on d3, but 3-5 fold more were present in A/J. Immunolabeling showed that most were
macrophages. Our results suggest that A/J muscles, like controls, can reseal their sarcolemmal membranes after
injuries caused by lengthening contractions, but that within a few days they undergo massive inflammation, leading to
necrotic death of myofibers labeled at the time of injury and of nearby fibers. We postulate that the delayed recovery of
A/J muscles is due to the time needed for fibers killed by macrophages to be replaced by myogenesis.  


                </p>
<p><br>

				  

