                  <strong><span class="style17">Jeffery D. Molkentin, PhD</span></strong><br>

                  <em>Children’s Hospital of Cincinnati, University of Cincinnati, Cincinnati, OH</em><br>
<br>

                  <img src="images/Conf09/Molkentin.jpg" width="135"><br><br>

                  <strong>In Vivo Proof that Calcium Underlies Skeletal Muscle Degeneration in Muscular
Dystrophy</strong><strong>&nbsp; &nbsp;</strong><br>
Doug P. Millay, Sanjeewa Goonasekera, Jeffery D. Molkentin</p>
				<p>				  Muscular Dystrophy (MD) is a muscle wasting disease that arises due to mutations in a
multitude of genes. However, in recent years it has become apparent that many of these
mutant genes function within a similar pathway or overall biologic process that safeguards
the sarcolemma of a skeletal muscle fiber. Intermittent tears in the cell membrane, or
activation of select ion channels, permits infusion of calcium that presumably induces
cellular degeneration and necrosis of whole muscle fibers. While this hypothesis was
proposed over two decades ago, it has yet to be definitively evaluated in vivo, and
establishing such a paradigm should have significant therapeutic implications especially
given existing pharmacologic agents that could be used to address a dysregulation in
calcium homeostasis. For example, we show that in transgenic mice overexpression of transient receptor potential canonical3 (TRPC3) channel (TRPC channels are known to be dysregulated in MD) can induce calcium entry and initiate massive necrotic disease in skeletal muscle that is highly reminiscent of MD. Moreover, inhibition of endogenous TRPC channels by overexpression of dominant negative mutants in skeletal muscle mitigated disease in both mdx and Scgd-/- (delta-sarcoglycan) mice. Similarly, increased expression of the sodium/calcium exchanger 1 (NCX1) isoform induces MD like disease through reverse mode calcium entry in all muscles except the diaphragm, and skeletal muscle specific deletion of the NCX1 gene using Cre-loxP technology, mitigated disease in mdx, A/J and Scgd-/- mice. We also generated transgenic mice with SERCA1 overexpression in skeletal muscle as an independent mechanism of clearing calcium from the cytosol, which also mitigated disease in mdx and Scgd-/- mice. Taken together, these results indicate that calcium itself can induce skeletal muscle dystrophic-like disease, without a defect in the sarcolemma. Moreover, enhancing calcium clearance or preventing increased TRPC activity effectively reduced skeletal muscle disease in 2 different mouse models of MD. Finally, we also generated transgenic mice with skeletal muscle-specific dysferlin overexpression. Crossing the dysferlin transgene into the A/J background completely rescued MD, demonstrating that disease in the A/J mouse is autonomous to skeletal muscle cells only. However, overexpression of dysferlin in Scgd-/- mice had no effect in reducing MD. </p>
