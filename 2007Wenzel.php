                  <strong>Cardiac  ramifications of dysferlin deficiency </strong>

                <p class="style4">Katrin Wenzel1, Christian Geier1, Ralf Dechend1,  Cemil Ozcelik1, Simone Spuler2 </p>

                <p>1Department of  Cardiology, Franz Volhard Clinic, Helios Clinic and Campus Virchow Clinic,  Charit&eacute;, Berlin, 2Muscle Research Group, Department of Neurology,  Charit&eacute;, Berlin, Germany </p>

                <p>Mutations  in the gene encoding dysferlin cause limb-girdle muscular dystrophy 2B  (LGMD2B), a disorder that is believed to spare the heart. We observed dilated  cardiomyopathy in two LGMD2B patients and electrocardiographic abnormalities in  three others. Cardiac biopsies showed that dysferlin was completely absent from  the sarcolemma and appeared to be trapped within the cardiomyocytes. SJL/J mice  (33 weeks old) had diminished end-systolic pressure and reduced dP/dt; however,  the hearts were histologically normal. Gene expression profiles of cardiac  tissue were obtained and

later confirmed by quantitative real-time  PCR. Dysferlin-deficient and control mice had different gene expression  patterns in terms of cardiomyocyte Z-disc and signal transduction proteins.  CapZ, LIM domain binding protein 3 (LDB3, MLP), cypher (ZASP), desmin, and the  cardiac ankyrin-repeated protein (CARP) were differentially expressed, compared  to controls. Mechanical stress  induced by the nonselective beta-adrenergic agonist isoproterenol (5mg/kg body  weight) given daily for 10 days resulted in reduced fractional shortening and  increased cardiac fibrosis in SJL/J mice as compared to controls. Isoproterenol  also caused metalloproteinase-2 upregulation in SJL/J mice. In A/J mice, the  effect of isoproterenol injection was even more dramatic and led to premature  death as well as marked sarcolemmal injury as demonstrated by Evans blue dye  penetration. Our data suggest that disturbances in dysferlin, particularly  under mechanical stress, cause cardiomyopathy. </p>

