                  <strong>Lipid Binding Properties of Dysferlin C2 Domains </strong><strong>&nbsp; &nbsp;</strong><br>

Therrien C, Pickle S, Avedian T, Karpati G and Sinnreich M

 <br><br>
 <strong>Mutations in dysferlin cause limb-girdle muscular dystrophy 2B and Myoshi Myopathy.  Dysferlin is a type II transmembrane protein implicated in sarcolemmal repair following membrane injury.  The protein contains seven C2-domains and two DysF-domains.  C2-domains are implicated in lipid-protein and protein-protein interactions and are found in several proteins responsible for vesicular trafficking or membrane fusion events.  
We are developing gene therapeutic strategies that include micro-dysferlin molecules.  For this reason, understanding the function of individual dysferlin C2 domains is necessary.  
C2-domain sequence consensus derived from 65 C2-domain-containing proteins was used to delineate C2-domain encoding fragments of the dysferlin cDNA.  Dysferlin’s seven C2 domains were amplified by PCR, subcloned and expressed in E.coli.  GST-C2 fusion proteins were affinity purified using glutathione sepharose.  Protein overlay assays were performed with lipid strips containing several biologically relevant lipids.  Interactions were detected by enhanced chemiluminescence.  
All seven GST-C2 domains of dysferlin were able to bind phosphatidylserine (PS), 3-sulfogalactosylceramide (sulfatide) and cardiolipin in the presence of 1 mM Ca2+.  The depletion of calcium ions reduced considerably these protein-lipid interactions.  In addition, the C2A and C2C domains were able to bind PS, phosphatidylinositol-4 phosphate (PIP) and phosphatidylinositol-4,5 biphosphate (PIP2) in the absence of calcium.  
PS and PIP2 are found in the inner monolayer of plasma membranes.  PS could bind dysferlin’s C2 domains in the event of an increase in local calcium concentrations following surface membrane injury while the interaction with PIP2 could contribute to enhance the binding strength or specificity; these interactions may be important for the sarcolemmal resealing process.  On the other hand PIP is found predominantly in the trans Golgi network (TGN) and could play a role in dysferlin trafficking.  Experiments are currently ongoing in our laboratory to address these functions.  
