				<p><span class="style10">Session IV: Membrane Fusion and Repair </span><br>


				  <br><br>

                  <strong><span class="style17">Jianjie Ma, PhD</span></strong><br>

                  <em>Robert Wood Johnson Medical School, Piscataway, NJ
</em><br><br>

                  <img src="images/Conf09/Ma.jpg" width="135"><br><br>

                  <strong>MG53, a Molecular Sensor of the Acute Membrane Repair Process in Striated Muscle</strong><br>

<br>

Defective membrane repair can contribute to the progression of muscular dystrophy. While mutations in caveolin-3 (Cav3) and dysferlin are linked to muscular dystrophy in human patients, the molecular mechanism underlying the functional interplay between Cav3 and dysferlin in membrane repair of muscle physiology and disease has not been fully resolved. We recently discovered that mitsugumin 53 (MG53), a muscle specific TRIM-family protein (TRIM72), contributes to intracellular vesicle trafficking and is an essential component of the membrane repair machinery in striated muscle (Cai et al, 2009, Nature Cell Biology 11: 56-64). We have also obtained evidence suggesting that membrane repair defects in muscular dystrophy are linked to altered interaction between MG53, Cav3 and dysferlin. Here I will update our studies toward understanding the molecular mechanisms underlying the membrane repair process in skeletal and cardiac muscle, and our effort in translating the basic laboratory discovery for therapeutic treatment of muscular dystrophy and other human diseases.

                </p>
<p><a name="2"></a>

