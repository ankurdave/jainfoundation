                  <strong><span class="style17">Norma W. Andrews, PhD</span></strong><br>

                  <em>University of Maryland, College Park, MD
</em><br><br>

                  <img src="images/Conf09/Andrews.jpg" width="135"><br><br>

                  <strong>Exocytosis of Acid Sphingomyelinase Upon Cell Injury Triggers Endocytosis and
Plasma Membrane Repair</strong><br>

Christina Tam, Vincent Idone, Cecilia Devlin, Ira Tabas, Norma W. Andrews<br><br>

Survival of eukaryotic cells after injury requires restoration of plasma membrane integrity. Earlier studies showed that plasma membrane permeabilization triggers a calciumdependent repair response involving lysosomal exocytosis (1) and a rapid form of endocytosis (2). However, it was not known if lysosomal exocytosis and endocytosis were functionally linked. We recently found that lysosomal exocytosis is required for the Ca2+- dependent endocytosis observed after cell wounding. Interfering with lysosomal exocytosis resulted in inhibition of both injury-dependent endocytosis and plasma membrane repair. Importantly, inhibition or depletion of the lysosomal enzyme acid sphingomyelinase (ASMase) specifically blocked endocytosis and also plasma membrane repair, without inhibiting calcium-dependent lysosomal exocytosis. In humans, ASMase mutations cause the lysosomal storage diseases Niemann-Pick type A and B. Similar to what was observed after ASMase inhibition, fibroblasts and lymphoblasts from Niemann-Pick Type A patients were not impaired in beta-hexosaminidase secretion, but were deficient in injury-induced endocytosis and plasma membrane resealing. Importantly, ASMase enzymatic activity was detected in the extracellular medium when cells were injured in the presence of calcium. Thus, calcium influx through plasma membrane wounds delivers lysosomal acid sphingomyelinase to the cell surface. We propose that secreted ASMase converts plasma membrane sphingomyelin into ceramide, a sphingolipid known to induce membrane invagination and the formation of large endosomes (3). These results shed light on the mechanism by which lysosomal exocytosis triggers endocytosis, and the subsequent removal of plasma membrane lesions. (1) Reddy, A et al. Plasma membrane repair is mediated by Ca2+-regulated exocytosis of lysosomes. Cell 106, 157-169 (2001) (2) Idone, V et al. Repair of injured plasma membrane by rapid Ca2+-dependent endocytosis. J Cell Biol. 108, 905-914 (2008) (3) Zha, X et al. Sphingomyelinase treatment induces ATP-independent endocytosis. J Cell Biol. 14), 39-47 (1998)				  							  
				  </p>
