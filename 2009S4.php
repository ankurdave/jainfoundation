                  <strong><span class="style17">Elizabeth McNally, MD, PhD</span></strong><br>

                  <em>The University of Chicago, Chicago, IL

</em><br><br>

                  <img src="images/Conf09/McNally.jpg" width="135"><br><br>

                  <strong>Myoferlin Regulates Endocytic Recycling in Muscle Growth and Repair</strong><br>

<br>

The ferlin family mediates membrane repair and fusion, and muscle growth and repair require the regulated coalescence of two apposed membranes. Fer1 mutants in C. elegans lead to defective vesicle fusion and sperm defects. Loss of dysferlin leads to limb girdle muscular dystrophy 2B, and an accumulation of vesicles under the plasma membrane. Dysferlin null muscle shows a delay in membrane resealing after laser-induced damage. In their full length form, both myoferlin and dysferlin proteins are approximately 220 KDa in length. The first C2 domain of both myoferlin and dysferlin binds negatively charged phospholipids only in the presence of calcium. To understand other proteins necessary for membrane fusion, we characterized proteins associated with myoferlin. We found that myoferlin binds directly to the eps15 homology domain protein, EHD2. EHD2 is a carboxy-terminal eps15 domain-containing protein, and members of this protein family regulate endocytic recycling, a process where membrane proteins internalized by endocytosis are returned to the plasma membrane. EHD2 binds directly to the asparagine-proline-phenylalanine motif present in the second C2 domain of myoferlin, and mutations that disrupt this motif in myoferlin have reduced binding to EHD2. We found that EHD2 is expressed in myoblasts, and EHD2 is reduced in myoferlin null myoblasts. Myoferlin null myoblasts display delayed endocytic recycling characterized by intracellular accumulation of labeled transferrin. In myoblasts undergoing fusion to multinucleate myotubes, the interaction of myoferlin and EHD2 is dynamic. Introduction of mutant EHD2 engineered with a point mutation in the nucleotide-binding domain of EHD2 leads to the sequestration of myoferlin and inhibition of myoblast fusion. The interaction of myoferlin with EHD2 identifies molecular overlap between the endocytic recycling pathway and the machinery that regulates myoblast and muscle membrane fusion.</p>

