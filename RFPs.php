					<br>
<a name="2"></a><strong>Comparative study of pathology in different mouse models of dysferlinopathy</strong><br>
This project involves comparing disease progression between the 4 mouse models of dysferlinopathy &ndash; A/J, SJL, Bla/J and the Bittner mice. The mice will be studied at different ages to compare which types of muscles are involved (and to what extent) by examination of muscle pathology and by functional measurements. Male and female dysferlin-deficient mice will be compared to test whether gender differences play a role in disease progression.
<br><br><br>
<a name="3"></a><strong>Causes of differential muscle involvement</strong><br>
This project examines if differential gene expression and/or fiber-type involvement are contributing factors to differential muscle involvement in LGMD2B/Miyoshi. The effects of muscle fiber type on pathology can be  determined by measuring the muscle fiber type ratios in affected versus unaffected muscles in symptomatic dysferlin-deficient mice. Gene expression analysis of muscles in pre- and post-symptomatic mice can  be used to identify any changes that may contribute to the onset of pathology.  This approach can also look for differences between unaffected and affected muscles in the same symptomatic animals, in order to identify any genes that may contribute to the resistance of some muscle groups to pathology. <br>
<br><br>
<a name="4"></a><strong>Determining the effects of physical activity on pathology in mouse models of dysferlinopathy</strong><br>
This project involves exposing dysferlin-deficient mice of different ages (both pre- and post-symptomatic) to a variety of physical activities and exercise protocols (both weight-bearing and non-weight-bearing) for various lengths of time and in various combinations.  Muscle pathology in these mice (versus un-exercised dysferlin-deficient mice) will be monitored both immediately following exercise and in longer-term follow-up examinations to evaluate the short-term and long-term effects of physical activity on the progress of dysferlinopathy in mice.<br>
<br><br>
<a name="5"></a><strong>Development of natural-history protocols for dysferlin-deficient patients</strong> <br>
The aim of this project is to develop a standardized natural-history protocol for LGMD2B/Miyoshi patients by gathering data from a statistically significant number of patients. Such a protocol is critically needed to confidently determine the outcome measures for clinical trials.<br><br><br>
<a name="6"></a><strong>Dissecting the role of dysferlin in the immune system</strong><br>
The aim of this project is to selectively study the role of dysferlin in immune system function, and to assess the relative contribution of dysferlin deficiency in immune cells to dysferlinopathy. This may be accomplished by:<br>
1. Selective ablation of individual immune cell types in dysferlin-deficient mice: Rag-/- for B and T lymphocytes, dichloro-methylene-bisphosphonate for macrophages.<br>
2. Muscle-specific knockdown of dysferlin.<br><br><br>
<a name="7"></a><strong>Testing whether inflammation can be included as an outcome measure in preclinical studies</strong><br>
This project will rigorously test a variety of assays of muscle inflammation on dysferlin-deficient and control mice to determine the reproducibility of these assays and their statistical power in distinguishing between affected and unaffected mice.  The project should only be done on dysferlin-deficient mice with appropriate controls (e.g. Bla/J vs. Bl6 mice, or Bittner vs. Bl10 mice).<br>
<br><br>
<a name="8"></a><strong>Determining the size and site of membrane tears</strong><br>
This project involves using cryobiopsy to determine and compare the size and site of membrane tears in wild type and dysferlin-deficient muscle fibers.  This could be done on WT and dysferlin-deficient mice immediately after exercise, using biopsy and cryofracture/EM to detail the defects in the sarcolemma. The cryobiopsy approach, for example, is described in Shimoni and Muller, J. Microscopy (1997) 192: 236-247. Care must be taken to obtain the biopsy quickly enough so as to preserve the membrane changes associated with tearing (this may be easier to do in dysferlin-deficient mice due to defective/slower membrane repair). Another approach for determining the size of membrane holes is to use dye-tagged molecules of various sizes (for example, fluorescein- or rhodamine-glycans of various molecular weights). The size of the hole can be estimated based on the molecular size limit of the glycan that can diffuse through it. Care must be taken to use diffusible molecules whose mass cut-off is determined solely by the size of the sarcolemmal hole and not by impediment from extracellular structures such as the matrix, fat deposits, etc. <br>
<br><br>
<a name="9"></a><strong>Developing high throughput assays for screening small molecules</strong><br>
The aim of this project is to design and develop a variety of assays for screening small molecules and drugs that may compensate for dysferlin deficiency in mammalian cells. Specific examples of such assays could include:<br>
1.	Restoration of normal membrane repair in dysferlin-deficient cells after a variety of methods of wounding the cell membrane.<br>
2.	Unpregulation of the rate of membrane repair in wild type cells (to identify compounds that improve membrane repair in general and are not dysferlin-specific).<br>
3.	Restoration of a normal rate of phagocytosis in dysferlin-deficient macrophages.<br>
4.	Restoration of a normal growth rate and normal cell-to-cell fusion in dysferlin-deficient skeletal muscle cell culture.<br>
<br><br>
<a name="10"></a><strong>Determining the lineages of stem cells that are naturally recruited to dysferlin-deficient muscle</strong><br>
This project involves looking for the presence of cells with unusual surface markers (indicating that they represent lineages not normally found in muscle) in muscle samples from dysferlin-deficient mice (both pre- and post-symptomatic) and patients.  The presence (or an increase in number) of such cells in dysferlin-deficient muscle versus controls would suggest a deficiency in the regeneration capacity of dysferlin-deficient muscle stem cells, compensated for by recruitment of other stem cell lineages to muscle.<br>
<br><br>
<a name="11"></a><strong>Determining the identity of the dysferlin doublet band in Western blots</strong><br>
In Western blots, a doublet is often seen for the dysferlin signal.  This could be caused by different dysferlin isoforms having slightly different molecular masses or by post-translational modifications such as glycosylation, phosphorylation, etc.  Determining the identity of the doublet could help clarify the roles of these different isoforms  or  post-translational changes. Experimental approaches for determining the identity of the doublet may involve:<br>
1.	Employing proteolytic cleavage of the full-length protein to enhance size-resolution on the gel (the doublet is not well resolved for the full-length protein).<br>
2.	Testing if glycosylation is involved by tryptic digestion followed by mass spectrometry.<br>
3. Testing for phosphorylation by using phospho-specific antibodies or by mass spectrometry.<br>
