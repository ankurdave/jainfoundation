                  <strong>Glucocorticoid Treatment of Myoblasts Enhanced Myotube Formation via Dysferlindependent
and Independent Pathways.</strong><br>
Joseph J. Belanto1,2, Silvia V. Diaz-Perez2, Clara E. Magyar3, Yasemin Yilmaz1, Kasey<br>
Topp2, Guney Boso2, Catriona H. Jamieson5, Nicholas A. Cacalano4, Christina A.M.<br>
Jamieson 1,2*1<br>
1Dept. of Urology, 2Dept. of Human Genetics, 3Translational Pathology Core Laboratory,<br>
Dept. of Pathology and Laboratory Medicine, and 4Dept. of Radiation Oncology, University<br>
of California, Los Angeles, David Geffen School of Medicine, Los Angeles, CA, 5Dept. of<br>
Medicine, Hematology/Oncology Division, School of Medicine, University of California, San<br>
Diego, La Jolla, CA, USA 92093.5Dept. of Radiation Oncology, University of California, Los<br>
Angeles, David Geffen School of Medicine, Los Angeles, CA.<br><br>

Glucocorticoids are beneficial therapy for many muscular dystrophies and muscle
inflammatory diseases but they are ineffective in treating dysferlinopathy patients. We sought to understand the
molecular basis for this disparity by studying the effects of glucocorticoid treatment on myotube formation and dysferlin
expression in the C2C12 mouse myoblast cell line and in dysferlin-deficient C2C12 cells in which dysferlin has been
knocked down using shRNA (Maxwell, MM and Brown, RH). We found that treatment of differentiating C2C12 cells with
pharmacologic doses of the glucocorticoid, dexamethasone, increased the myogenic fusion index of mature myotubes (4
or more nuclei per myotube). Dex-treatment increased dysferlin mRNA (10.43 +/- 0.71 fold) and protein levels (2.57 +/-
0.26 fold) at earlier times in differentiation over that of the untreated cells. Interestingly, myoferlin levels were not
increased by dex-treatment and remained constant throughout the differentiation time course. Dex-treatment increased
the levels of the muscle differentiation proteins: eMHC, dystrophin, alpha-sarcoglycan and beta-sarcoglycan. Thus, dextreatment
enhanced mature C2C12 myotube differentiation. In contrast, we found that the Dysferlin-deficient C2C12
cells, demonstrated a significant reduction in mature myotubes with a concomitant increase in short, immature myotubes
(2-3 nuclei per myotube) compared to the parental C2C12s. Likewise, the number of mature myotubes in Dex-treated
dysferlin-deficient C2C12 cells was significantly reduced compared to parental C2C12, indicating that dex-enhancement
of myotube formation operates through dysferlin-dependent and independent pathways. Our findings suggest that
dysferlinopathy patients may fail to respond to glucocorticoid therapy because glucocorticoid-driven muscle differentiation
is at least in part dysferlin-dependent. We hypothesize that a critical therapeutic benefit of glucocorticoids may be the upregulation
of both these pathways. In the absence of dysferlin, glucocorticoid therapy only partially rescued the mature
myotube formation and this may explain, in part, the ineffectiveness of glucocorticoids in dysferlinopathy. </p>
