				<p><span class="style10">Session V: Muscle Satellite Cells </span><br>


				  <br><br>

                  <strong><span class="style17">Gillian Butler-Browne, PhD</span></strong><br>

                  <em>Institut de Myologie, Paris, France</em><br><br>

                  <img src="images/Conf09/Butler-Browne.jpg" width="135"><br><br>

                  <strong>Immortalization of Human Myoblasts Isolated from Patients Suffering from
Dysferlinopathies</strong><br>
Anne Bigot1, Nicolas Levy2, Martin Krahn2, Gillian Butler-Browne1 & Vincent Mouly1
1UMR S 974, Institut de Myologie, 105 bd de l’Hôpital, 75634 Paris cedex 13, Laboratoire
de Génétique moléculaire, 2Département de Génétique médicale - Hôpital d'enfants La
Timone, 264 rue St Pierre, 13385 Marseille Cedex 05<br>
<br>


Human myoblast cell lines derived from pathological biopsies represent precious tools to assess pathophysiology and to test different therapeutic strategies in vitro. However, until recently, the only human myoblast cell lines available were derived from rhabdomyosarcomas, and had lost most of their differentiation potential. Unlike murine cells, the proliferation of human myoblasts is strictly limited by telomere shortening. However, thetransduction of telomerase, an enzyme capable of elongating telomeres, is not sufficient to immortalize human myoblasts. Recently, we have shown that, in addition to telomere shortening, a stress pathway is triggered in vitro in these cells, leading to the accumulation of the protein p16 that blocks the cell cycle. Whether this pathway is also triggered in vivo in pathologies is still unknown. Overexpression of cdk-4 can trap p16 and inhibit the cell cycle arrest due to the stress pathway. In this context, we have transduced human
myoblasts with both telomerase (hTERT) and cdk-4, using retroviral transduction, and have described the isolation of the first immortal cell line derived from a biopsy from a control adult subject. These immortalized myoblasts grow indefinitely, and can differentiate as well as normal myoblasts upon serum removal. They are not tumorigenic, and have been injected into regenerating muscles of immunodeficient mice. They participate in the host’s muscle regeneration and are even more efficient in this process than myoblasts isolated from a newborn control subject. We are now in the process of creating several human cell lines derived from myoblasts isolated from dysferlin deficient patients. We aim at creating cell lines harbouring some of the major mutations in the dysferlin gene that can be targeted by therapeutic approaches, and these tools will be available to assess these strategies.

                </p>
<p><a name="2"></a>

