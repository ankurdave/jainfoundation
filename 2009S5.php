                  <strong>Therapeutic Approaches and Disease Monitoring for Dysferlin Deficiency</strong><br>

Katie Maguire, Leland Lim, Pinky Tripathi, Thomas A. Rando<br><br>


We are developing two different strains of mice that will allow non-invasive and serum monitoring of disease activity for muscular dystrophies. We refer to these as the “Regeneration Monitor” (RM) and the “Degeneration Monitor” (DM) strains. We are further along in developing the RM strain, in which an inducible Cre (Cre-ER) is expressed under the control of the Pax7 promoter in mice that have luciferase (LUC) and secreted alkaline phosphatase (SEAP) reporter genes knocked into the Rosa locus. The administration of Tamoxifen to these mice results in LUC and SEAP expression, permanently, in satellite cells. The lineage of these cells can then be followed and we have preliminary data during normal growth. We are now crossing this strain with a dysferlin deficient strain. Upon administration of Tamoxifen to the progeny of those breedings, we will be able to follow overall satellite cell activity (as a read-out of the regenerative response) non-invasively during disease progression. The DM strain is similar except that Cre-ER is expressed under the control of the MRF4 promoter. Administration of Tamoxifen to these mice results in reporter gene expression only in differentiated fibers, so the loss of signal over time in the dysferlin-deficient strain will be used as a read-out of fiber degeneration associated with disease activity. We have continued our studies of oligonucleotide-mediated gene repair for dysferlin deficiency due to point mutations. These have been hampered in our studies in the SJL mouse because of the high level of normal dysferlin transcript and protein in the muscles and muscle cells of those mice. We have been developing quantitative methods to assess small changes in transcript levels to be able to measure oligonucleotide-mediated gene correction on top of the background levels. Finally, in collaboration with Michele Calos’ laboratory, we have begun isolating, characterizing, and expanding in culture mesoangioblasts from dysferlin-deficient mice. Our strategy is to employ gene therapy strategies for ex vivo gene therapy followed by systemic cell transplantation as an approach for treatment of dysferlin deficiency. We have successfully isolated and cultured mesoangioblasts from adult mice and current studies are focused on gene correction and cell transplantation.</p>



