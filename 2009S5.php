                  <strong><span class="style17">Terence A. Partridge, PhD</span></strong><br>

                  <em>Children’s National Medical Center, Washington, D.C.</em><br><br>

                  <img src="images/Conf09/Partridge.jpg" width="135"><br><br>

                  <strong>Use of Thymidine Analogues to Study Loss and Regeneration of Muscle Fibers in
Mouse Models of Myopathies</strong><br>

<br>


Mouse models of human myopathies are much used to study the course of the diseases, to identify potential therapeutic targets and to conduct preliminary therapeutic tests, but there is a lack quantitative measures of muscle loss or regeneration. We are developing such methods on the mdx mouse with the intention of applying them to dysferlinopathies and other myopathic conditions. It involves labeling muscle fiber nuclei with synthetic thymidine analogues, Bromodeoxyuridine (BrdU) or 5-ethynyl-2’-deoxyuridine (EdU), which are incorporated into a cell’s DNA when the cell is dividing, and following the fate of this labeled cohort of muscle nuclei. Label applied throughout the first postnatal week enters many labeled myogenic cells that fuse into the growing muscle fibers where they remain until the fibers die. During further growth BrdU-labeled myonuclei become diluted by fresh input from nonlabeled myogenic cells. In mdx mice, this dilution is enhanced by necrotic loss of muscle fibers containing labeled myonuclei accompanied by repair by unlabeled myoblasts. This excess drop in the proportion of BrdU-labeled myonuclei in mdx mice provides an index of loss of muscle nuclei due to the loss of muscle. In preliminary experiments the frequency of BrdU labeling is indistinguishable between mdx and wild-type mice up to 16 days of age. In the mdx it declines rapidly beyond the age of 20-21 days when the myopathy begins and is almost totally lost by 50 days of age. Stereological analysis of this system will permit estimates of the absolute numbers of labeled and unlabeled myonuclei per anatomical muscle at intervals throughout the course of the disease, permitting sensitive detection of the effects of any given intervention on the survival and/or regeneration of muscle. This approach will be applied to dysferlin-deficient strains of mouse. The paucity of information on the time-course of the myopathy in these models requires that we initially space sampling of labeled mice over a period of 1 year, intercalating more frequent samples into the protocol as we progressively gather information on the time of onset and the period of maximum pathological change.				  
				  </p>

