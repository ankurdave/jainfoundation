				<p><span class="style10">Session V: New Research Tools and Technologies </span><br>


				  <br><br>

                  <strong><span class="style17">Thomas A. Rando, MD PhD</span></strong><br>

                  <em>Stanford University School of Medicine
rando@stanford.edu



</em><br><br>

                  <img src="images/conference2008/Rando.jpg" width="135"><br><br>

                  <strong>Monitoring of Disease Activity and Gene Therapy for Dysferlin Deficiency</strong><strong>&nbsp; &nbsp;</strong><br>

<br>


We are developing two different strains of mice that will allow non-invasive and serum monitoring of disease activity for muscular dystrophies.  In order to follow the degeneration of myofibers, we are engineering a strain that will have an inducible reporter gene that will be expressed only in mature muscle fibers that are present at the time of induction.  This will be accomplished by crossing a strain of mice that expresses the Cre-ER construct under the control of the MRF4 promoter with a strain that expresses a luciferase and secreted alkaline phosphatase reporter construct in the Rosa locus.  When this double-transgenic strain is crossed to a dysferlin-deficient strain and progeny of the appropriate genotype are identified, we will be able to follow the time course of decline of luciferase expression, non-invasively, and serum alkaline phosphatase levels, both of which will reflect the time course of fiber degeneration.  The other strain will instead have Cre-ER expression under the control of the Pax7 promoter.  Therefore, the increase in signals for luciferase and serum AP will mirror the regenerative activity of the disease.  Together, these models should allow for detailed characterization of natural history of disease activity as well as responses to therapy. 
We are also pursuing a gene correction approach to point mutations in the dysferlin gene.  We have developed single-stranded oligonucleotides that are designed to pair with and induce alteration of a single base of the mutant dysferlin gene in muscle cells of the SJL mice in order to restore normal splicing, and thus normal dysferlin protein production.  We are currently testing these oligonucleotides in isolated myoblast cultures from the mice, and testing for gene correction at the genomic level.

                </p>
