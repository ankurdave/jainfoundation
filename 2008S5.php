                  <strong>Current Issues and Perspectives in Imaging Technologies for Dysferlinopathy: From Fundamental Study to the Monitoring of a Treatment</strong><strong>&nbsp; &nbsp;</strong><br>

<br>


Different imaging technologies have been used for the study of dysferlinopathy, but fluorescence microscopy combined with pulsed laser has probably had the biggest impact on fundamental study with the demonstration that the pathology is related to a deficiency in membrane repair.  Confocal microscopy can also be used for  3 dimensional high resolution images of muscle in live mice.  A few examples that have been obtained in Genethon will be shown.  The potentials and examples of the FRAP (Fluorescence Recovery After Photobleaching) and FRET (Forster Resonance Energy Transfer) technologies for the understanding of specific molecular mechanism will be presented.  Photonic imaging is well suited for microscopy and fundamental studies but has limited tissue penetration power and few medical applications exist. The second part of the presentation will therefore be based on real non invasive in vivo imaging for small animals derived from medical imaging.  The advantages and limitations of technologies like X-Ray, MRI and gamma-scintigraphy will be discussed.  In addition, our project “Technimaging” as well as preliminary data, will be presented.  The approach is based on scintigraphic quantification of technetium-labelled albumin uptake in damaged muscle fibers in live mice. The commonly used blue-evans fluorophore for evaluating the muscle fiber permeability has inspired this approach.  We hope to develop a rapid and reproducible non-invasive method for the quantification of muscle integrity in dysferlin deficient mice that should be easily transposable to clinical applications.  A short presentation of a quantitative analysis of CT-scan that has been recently studied for the natural history of a limb girdle muscular dystrophy will also be presented in order to discuss the potential of the multimodal-imaging techniques associating CT-Scan and scintigraphy for the precise evaluation of the effect of a treatment for dysferlinopathy.
				  
				  </p>

