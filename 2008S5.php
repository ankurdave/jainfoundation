                  <strong>Generation and Characterization of Dysferlin-Deficient C2C12 Cell Lines</strong><strong>&nbsp; &nbsp;</strong><br>

Maxwell M, Newton K, Reshev D, Glover L, Brown RH Jr.<br><br>

Studies of normal and dystrophic muscle are facilitated by the availability of myogenic cell lines with specific genotypes.  To facilitate studies of dysferlinopathies, we have used RNAi techniques to generate myogenic C2C12 lines that are dysferlin-deficient.  For this purpose, we first generated dysferlin-targeting shRNA vectors, using sequences corresponding to mouse dysferlin, beginning with the mouse dysferlin shRNA target set from Sigma.  Oligos encoding five different shRNAs and one non-targeting shRNA were cloned into the pSiren vector (an oncoretroviral  RNAi vector that permits expression of the hairpin RNA and has a puromycin-selectable marker, from Clontech).  We then tested the efficacy of the five dysferlin shRNAs by transfection of plasmid DNA into C2C12 mouse myoblasts.  Four shRNAs yielded >80% knockdown of dysferlin in C2C12 myotubes.  We then packaged these plasmids into transduction-competent ecotropic retrovirus and transducted C2C12 cells, which were subsequently cloned in puromycin.  Puromycin-selected myoblasts were fused into myotubes and analyzed for expression of dysferlin and desmin.  Using limiting dilution, multiple single-cell clones were obtained from pools expressing distinct anti-dysferlin shRNAs.  Clones were then analyzed for fusion capacity and for dysferlin and desmin expression.  In subsequent studies, we have characterized and compared the properties of dysferlin-deficient and dysferlin-positive C2C12 lines, with respect to growth properties, fusion indices, membrane repair, and profiles of mRNA and microRNA expression.</p>

