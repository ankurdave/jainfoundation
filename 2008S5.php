                  <strong>Attempts to Optimize Methods for Study of Muscle-Gene Mutations in Tissue Culture</strong><strong>&nbsp; &nbsp;</strong><br>

<br>


There are several general problems with the use of tissue culture for the study of genetic defects in myogenic cells. 
First, there is uncertainty as to the relationship between the behaviour of cells in tissue culture and their behaviour in vivo.  We propose that the best approach to this problem is the use of freshly isolated muscle fibers.  In such preparations, satellite cells represent the myogenic resources of the tissue sample and behave in a manner very similar to that of satellite cells in the body.
Second, it is difficult to routinely establish standard conditions in primary cultures from which to make comparisons of behaviour and function of myogenic cells.  In addition to the use of freshly isolated fibres described above, it is possible to generate conditionally immortal myogenic cell lines by crossing mice carrying a desired mutation with the immortomouse.  Myoblasts derived in this way can be perpetuated as clones and maintain good genetic and phenotypic stability over long periods.  Moreover, on cessation of ‘permissive’ conditions, the cells synchronously and uniformly differentiate into myotubes.  This permits their use in routine screening assays.
Third, in cell cultures established from pathological muscle samples, it is difficult to differentiate between effects that are directly attributable to the endogenous genetic defect and those that are secondary to the pathological disturbance of the environment from which they have been derived.  This again can be allowed for, in part at least, by the comparative study of immortomouse cell lines obtained from mice at different stages in the course of the disease.
Each of these issues will be illustrated by example and discussed. </p>

