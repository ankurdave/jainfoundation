                  <strong>The &Phi;C31 integrase system for gene therapy of  LGMD2B/MM</strong></span><br>

                  <br>

                  Limb  girdle muscular dystrophy 2B and Miyoshi myopathy (LGMD2B/MM) are genetic  diseases caused by deficiency of dysferlin. Consequently, these conditions  could potentially be cured by the introduction and sufficient expression of a  wild-type <em>dysferlin</em> gene in affected muscle fibers. Such gene  therapeutic approaches involve either <em>ex vivo</em> gene transfer into stem  cells followed by delivery to muscles or direct <em>in vivo</em> gene transfer  into muscle fibers. We propose that the &Phi;C31  integrase system, used to genetically correct cells in a safe and site-specific  manner, provides an advantageous strategy for both of these  approaches.&nbsp;The &Phi;C31  integrase system is non-viral and uses plasmid DNA, so the approach is simple,  inexpensive, has no size limit, and is non-immunogenic.&nbsp; Furthermore,  integration is non-random and uses a set of characterized integration sites  that confer strong, durable gene expression, reducing the risks of

silencing and of oncogene activation.&nbsp; Preferential targets for the <em>ex vivo</em> approach are mesodermal stem cells called mesoangioblasts, isolated from blood  vessels by G. Cossu and colleagues. These cells have the ability to transit out  of blood vessels and migrate to sites of damage in muscle, where they  differentiate into fibers and lead to muscle repair. The use of &Phi;C31 integrase, when combined with  mesoangioblasts, creates an exciting opportunity for the development of a  treatment for LGMD2B/MM.&nbsp; With this approach, autologous patient cells can  be used as the targets for gene therapy, avoiding the use of heterologous cells  and eliminating the need for immune suppression.&nbsp; We have obtained mouse  mesoangioblasts from the laboratory of Giulio Cossu and, using transfection,  have used &Phi;C31 integrase to  permanently integrate the luciferase gene.&nbsp; The resulting marked cells are  being used to follow mesoangioblast migration and muscle repair.&nbsp;  Subsequently, we will characterize &Phi;C31  integrase-mediated integration of a mouse <em>dysferlin</em> gene into the genome  of mouse mesoangioblasts. Corrected mesoangioblasts will be transplanted by  femoral intra-arterial injection into a mouse model of LGMD2B/MM (<em>dysferlin</em> knockout mice).&nbsp; We will demonstrate incorporation of the corrected  mesoangioblasts into the skeletal muscles of the mouse model of LGMD2B/MM and  amelioration of muscle function. &nbsp;As an alternative approach, direct DNA  delivery to muscle fibers by using a hydrodynamic approach may be sufficient to  correct the disorder, if gene addition is made permanent by use of &Phi;C31 integrase. &nbsp;We will use an <em>in vivo</em> hydrodynamic  method to transfect muscle fibers of wild-type and <em>dysferlin</em> knockout  mice with plasmid DNAs encoding the <em>dysferlin</em> gene and &Phi;C31 integrase.&nbsp; The results will  be evaluated to see if the hydrodynamic approach is effective and well  tolerated, despite the defect in sarcolemma repair in the <em>dysferlin</em> knockout mice.</p>

