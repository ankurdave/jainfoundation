				<p><span class="style10">Session II: Dysferlinopathy Mechanisms of Pathology II </span><br>

				  <br>

				  <br>

                  <strong><span class="style17">Steven S. Vogel, PhD</span></strong><br>

                  <em>NIAAA, National Institutes of Health, Rockville, MD</em><br>
<br>

                  <img src="images/Conf09/Vogel.jpg" width="135"><br><br>
                  <strong>Plasma Membrane Wounding Triggers an Intercellular Signaling Pathway Involving
Dysferlin Mediated ATP Secretion</strong><strong>&nbsp; &nbsp;</strong><br>
J. Fernando Covian-Nares, Srinagesh V. Koushik, Steven S. Vogel
</p>
				<p>				 Dysferlin is an integral membrane protein containing multiple calcium binding domains.
Mutations in dysferlin cause Limb Girdle Muscular Dystrophy (LGMD2B) and Miyoshi
Muscular Dystrophy (MMD), but the actual function of dysferlin is not known. In muscle,
dysferlin is involved in membrane repair mechanisms, and it has been hypothesized that
wounding, and subsequent calcium influx triggers dysferlin mediated membrane fusion.
Here we report evidence for an intercellular signaling pathway activated by plasmamembrane
wounding that evokes calcium spikes in neighboring cells. This signaling
pathway was mimicked by perfusion with ATP or ADP but not AMP, and was inhibited by
extracellular apyrase, an enzyme that degrades ATP and ADP to AMP. ATP secretion was
observed using a firefly luciferase assay from embryos incubated with 50 mM KCl, and the release of ATP was blocked
by cadmium, an inhibitor of voltage-gated calcium channels. The intercellular signaling pathway described above was
also blocked by treatment with cadmium, as well as by omega-agatoxin IVA, a specific inhibitor of P-type voltage-gated
calcium channels, suggesting that plasma membrane wounding depolarizes the membrane potential of the wounded cell
and thus opens voltage-gated calcium channels to trigger ATP secretion. Microinjection of dysferlin antisense
phosphorodiamidate morpholino oligonucleotide s blocked this signaling pathway while dysferlin sense strand
morpholinos and random control morpholinos did not. Co-injection of mRNA encoding human dysferlin with the sea
urchin specific dysferlin anti-sense morpholino rescued signaling activity. These data support the hypothesis that
dysferlin mediates the calcium triggered release of ATP in response to plasma membrane wounding.
 </p>
                <p><a name="2"></a><br>
  
  
  <strong><span class="style17">Todd Lamitina, PhD</span></strong><br>
  
  <em>University of Pennsylvania, Philadelphia, PA </em><br>
    <br>
  
    <img src="images/Conf09/Lamitina.jpg" width="135"><br><br>
  
    <strong>The C. elegans Dysferlin Homolog fer-1 Promotes Cholinergic Synaptic
Transmission Through Regulation of Acetylcholine Receptor Localization in Muscle</strong><br>
    Predrag Krajacic1,2, Matias Mosqueira1,2 , Olga Lozynska1,2, Jane Harmonowski1,2, Tejvir S.<br>
    Khurana1,2,, Todd Lamitina1<br>
    1Dept of Physiology and 2Pennsylvania Muscle Institute, University of Pennsylvania,<br>
    Philadelphia, PA<br><br>
  
 Background: The C. elegans gene fer-1 is the founding member of the ferlin gene family. The autosomally inherited disease Limb Girdle Muscular Dystrophy 2B (LGMD2B) is caused by mutations in dysferlin, the human homolog of fer-1. Dysferlin mutations cause enigmatically disparate phenotypes in LGMD2B, but dysferlin's function and the molecular mechanisms underlying disease pathogenesis are not fully understood. Despite the many advantages of the C. elegans model for functional analyses and drug discovery, the role of fer-1/dysferlin in C. elegans muscle has not been investigated. To better understand the function of dysferlin, as well as to identify potential therapies for LGMD2B, we investigated whether the C. elegans dysferlin homolog fer-1 plays a functional role in muscle. Results: We found that, in addition to its previously noted sperm-enriched expression, fer-1 is also expressed in C. elegans body wall muscle, the anatomical and functional equivalent of human skeletal muscle in worms. Affymetrix GeneChip based expression profiling demonstrated that genes involved in locomotion are mis-regulated in fer-1 mutants, suggesting that a lack of fer-1 alters muscle functional properties. Unlike other muscular dystrophy mutants, fer-1 does not affect sarcolemmal integrity but instead diminishes the localization of levamisole-sensitive acetylcholine receptors (AChRs) to synaptic varicosities. The localization of presynaptic neurotransmitter-containing vesicles and post-synaptic GABA receptors is unaffected in fer-1 mutants, suggesting a new and specific role for dysferlin as a regulator of cholinergic synaptic transmission in worms. Consistent with these data, fer-1 mutant animals are resistant to drugs that increase cholinergic signaling, and electrophysiological approaches confirm that fer-1 mutants have an acetylcholine-dependent synaptic signaling defect that phenocopies a known AChR mutant. Acute administration of a Ach esterase inhibitor improves the excitability and functional properties of fer-1 muscle. Conclusions: Given the strong evolutionary conservation in synaptic physiology between C. elegans and humans, these data suggest that disruption of cholinergic signaling may contribute to LGMD2B pathology and that drugs enhancing cholinergic signaling (Ach esterase inhibitors) might be an effective therapeutic strategy for treating this disease.<br>
Acknowledgements: This work was supported by an NIH training grant in Muscle Biology (P.K.), the Pennsylvania
Muscle Institute (T.S.K & T.L.), and the University of Pennsylvania Institute for Translational Medicine and Therapeutics
(T.S.K., T.L.)</p>

<p><br>

