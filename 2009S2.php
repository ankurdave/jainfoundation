                  <strong><span class="style17">Tejvir Khurana, MD, PhD</span></strong><br>

                  <em>University of Pennsylvania, Philadelphia, PA</em><br><br>

                  <img src="images/Conf09/Khurana.jpg" width="135"><br><br>

                  <strong>Correction of Synaptic Deficit and Improved Survival of LGMD2B Mice Using
Acetylcholine Esterase Inhibitor (AChE.I) Treatment</strong><strong>&nbsp; &nbsp;</strong><br>
Emidio E. Pistilli1,2*, Predrag Krajacic 1,2*, Sasha Bogdanovich 1,2, Todd Lamitina1, Tejvir S.
Khurana,1,2
1Dept of Physiology and 2Pennsylvania Muscle Institute, University of Pennsylvania,
Philadelphia, PA (*equal contributors)<br>
<br>

Background: The dysferlin protein has a well described role as a regulator of membrane repair. We have recently demonstrated that dysferlin also plays a role in the regulation of AcetylCholine Receptor (AChR) synaptic localization and NeuroMuscular Junction (NMJ) activity in C. elegans (Krajacic et al. 2009 Submitted). This newly described synaptic function for dysferlin appears to be evolutionarily conserved, since we have also demonstrated the existence of NMJ (myasthenic) deficits in a mouse model of LGMD2B (Pistilli et al. 2009; In Prep). Given the nosological and mechanistic implications of our findings we hypothesized that acetylcholinesterase inhibitor (AChE.I) drugs that are currently used to improve the efficiency of NMJ transmission may also prove useful in treating LGMD2B. Results: To test this hypothesis, we initiated a pilot trial in the A/J mouse model of LGMD2B with the AChE.I pyridostigmine bromide. Three cohorts of adult mice (Background He/J, A/J untreated and A/J treated with AChE.I) were treated after the onset of disease till c. 16 months. While 43% (3/7) of the untreated A/J died during the trial, all of the treated A/J mice (8/8) and control He/J mice (8/8) survived (Kaplan-Meyer test p-value = 0.0442). Upon termination of the trial, we characterized in vivo synaptic function by simultaneously analyzing muscle force and electromyogram (EMG) activity upon low-frequency neuronal stimulation in live anesthetized animals using a custom-made apparatus. While background He/J animals maintained stable forces and EMG activity across a range of stimulation frequencies (0.1 - 10 Hz), 2/3 surviving A/j mice exhibited rapid rundown of force & EMG deficits at the 0.5-10 Hz range but not at lowest (0.1 Hz) frequency stimulation. Remarkably, none of the pyridostigmine bromide treated mice exhibited signs of force or EMG deficits that were observed in untreated A/J mice. Conclusions: These data support the hypothesis that loss of dysferlin results in NMJ deficits and that pharmacological correction of the deficit would be beneficial in LGMD2B. Our findings provide preliminary evidence that AChE.I treatment ameliorates the myasthenic defect and improves survival in the A/J mouse. We suggest that administration of AChE.I(e.g. Mestinon®) may prove useful for the management and/or treatment of LGMD2B patients. <br>
Acknowledgements: We thank Prof. R. Balice-Gordan and members of her lab for guidance and suggestions. This work was supported by an NIH training grant in Muscle Biology (P.K.), the Pennsylvania Muscle Institute (T.S.K & T.L.), and the University of Pennsylvania Institute for Translational Medicine and Therapeutics (T.S.K. & T.L). </p>

