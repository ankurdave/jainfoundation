				<p><span class="style10">Session II: Dysferlinopathy and the Immune System </span><br>

				  <br>

				  <br>

                  <strong><span class="style17">Eric P. Hoffman, PhD</span></strong><br>

                  <em>Children’s National Medical Center
ehoffman@cnmcresearch.org</em><br>
<br>

                  <img src="images/conference2008/Hoffman.jpg" width="135"><br><br>
                  <strong>Dysferlin-Deficiency Shows Compensatory  Induction of Rab27A/Slp2a Alternative Vesicle Trafficking Pathway, Macrophage  Hyper-Activation, and Inflammasome Formation that Drives the Inflammatory Onset  Seen in Many Patients</strong><strong>&nbsp; &nbsp;</strong><br>
Rashmi Rawat1, Akanchha Kesari1, Dwight Francia1, Susan Sparks1, Tatiana Cohen1, Kanneboyina Nagaraju1, Mitsunori Fukuda2, Susan Knoblach1, Rumaisa Bashir3, and Eric P Hoffman1.
1Research Center for Genetic Medicine, Children's National Medical Center, Washington DC,  2Dept. of Developmental Biology and Neurosciences, Tohoku University, Miyagi, Japan,  3 School of Biological and Biochemical Sciences, Univ. of Durham, Durham, UK
</p>
				<p>				 Mutations in the dysferlin gene cause Limb Girdle Muscular Dystrophy 2B (LGMD2B) and Miyoshi Myopathy (MM).  Dysferlin-deficient cells show abnormalities in vesicle traffic and membrane repair, although onset of symptoms is not until late teens, often associated with sub-acute onset and marked muscle inflammation.  To identify molecular networks specific to dysferlin-deficient muscle that might explain disease pathogenesis, muscle mRNA profiles from 10 mutation-positive LGMD2B patients were compared to a disease control (LGMD2I [FKRP]; n=9), and normal muscle samples (n=11).  Query of inflammatory pathways suggested LGMD2B-specific increase in co-stimulatory signaling between dendritic cells and T cells (CD86, CD28, CTLA4), associated with localized expression of versican and tenascin.  LGMD2B muscle also showed an increase in vesicle trafficking pathway proteins not normally observed in muscle (Synaptotagmin-like protein SYTL2 and small GTPase Rab27A). We propose that Rab27A/Slp2a expression in LGMD2B muscle provides a compensatory vesicle trafficking pathway, able to repair membrane damage in the absence of dysferlin.  However, this same compensatory pathway may release endocytotic vesicle contents, resulting in an inflammatory microenvironment.  
To further investigate the inflammatory aspects of dysferlin deficient human and mouse muscle tissues, we show that components of inflammasome pathway (e.g., P2X7 receptors, ASC-1, NALP, caspase-1, and IL-1 beta) are differentially upregulated and activated in dysferlin deficient but not normal mouse and human skeletal muscle.  We then showed that normal primary skeletal muscle cells are capable of secreting IL-1 beta in response to LPS and P2X7 receptor agonist, benzylated ATP. We have further found that inflammasome pathway adaptor proteins (e.g., ASC-1, Caspase -1) as well IL-1 beta are highly activated in dysferlin deficient compared to normal primary mouse muscle cells in response to LPS and benzylated ATP.  These data clearly demonstrate that inflammasome pathway is not only highly activated in dysferlin deficiency but also for the first time demonstrate that skeletal muscle is an active contributor of IL-1 beta and may be involved in initiation and progression of inflammatory response in this disease.  Strategies that interfere with this pathway may be therapeutically useful for LGMD2B patients.  
In conclusion, our assembled human and mouse data suggests that dysferlin-deficiency enhances phagocytosis by macrophages (Nagaraju et al. 2008), and this coupled with our findings of abnormal myofiber endocytosis pathways and dendritic-T cell activation markers suggests a model of over-stimulation of T cell – dendritic cell networks.  These networks inappropriately and aggressively activate the inflammasome pathway, and this in turn drives the sub-acute inflammatory presentation in many patients.
 </p>
