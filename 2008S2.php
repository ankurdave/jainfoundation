    <strong>Complement Inhibitory Factor CD55 is Downregulated on Dysferlin-Deficient Muscle Fibers</strong><strong>&nbsp; &nbsp;</strong><br>
    <br>
  
  We compared intra-individual mRNA expression profiles of skeletal muscle in dysferlin-deficient SJL/J and C57BL/6 mice and found downregulation of the complement inhibitor decay accelerating factor (DAF/CD55) in dysferlin-deficient skeletal muscle.  This finding was confirmed at the mRNA and protein levels, in two additional dysferlin-deficient mouse strains, A/J mice and Dysf-/- mice, as well as in patients with dysferlin-deficient muscular dystrophy.  The muscle fibers are decorated with C5b9 membrane attack complex.  In vitro, the absence of CD55 led to an increased susceptibility of human myotubes to complement attack. 
  We hypothesize that inhibition of the complement system or upregulation of CD55 are therapeutic strategies in dysferlin deficiency that will slow down the disease course.  We tested this hypothesis by several means:  1. Intravenous immunoglobulins (IVIG) in 3 LGMD2B patients because of the complement inhibitory properties of IVIG.  2. Anti-C5 antibody treatment in SJL/J mice.  3. Comparison of complement factor C5 levels in different dysferlin-deficient mouse strains.</p>

<p><br>

				  <a name="3"></a>

                  <strong><span class="style17">Christina Jamieson, PhD</span></strong><br>

                  <em>University of California, Los Angeles School of Medicine
cjamieson@mednet.ucla.edu</em><br><br>

                  <img src="images/conference2008/Jamieson.jpg" width="135"><br><br>

                  <strong>Dysferlin Regulation and Function in T Cell Death and Survival</strong><strong>&nbsp; &nbsp;</strong><br>

<br>

Background:  Inflammation and progressive loss of muscle occur in patients with Limb Girdle Muscular Dystrophy 2B and Myoshi Myopathy (LGMD2B/MM) due to loss-of-function mutations in the dysferlin gene.  Although glucocorticoids suppress inflammation and decrease disease progression in most muscular dystrophies and muscle inflammatory diseases, they are ineffective and sometimes exacerbate muscle damage in LGMD2B/MM patients.  We used global gene expression profiling in time series experiments to investigate the effects of glucocorticoid treatment of T cells on dysferlin expression.  
Methods and Results:  Genome-wide DNA microarrays revealed that dexamethasone (Dex), a synthetic glucocorticoid, induced dysferlin expression in the 2B4.11 T cell line in time course experiments (0, 0.5h, 1h, then every 2 hours up to 16 hours of Dex, n=2). Dysferlin was not induced in cells treated with Dex plus anti-CD3 antibody which inhibited apoptosis.  Synaptotagmins-1, -3 and XVb were induced with a gene expression signature similar to dysferlin.  Gene expression changes were verified using quantitative RT-PCR.  Whole body gamma-irradiation (10 Gy) of mice, which activates apoptosis in T cells, induced dysferlin in thymus and in quadriceps muscle within 1-3 hours.  Treatment of C2C12 cells with Dex in myoblast differentiation time course experiments resulted in increased and accelerated myotube formation.  Notably, Western blot analysis demonstrated that this treatment also increased dysferlin protein levels. 
Conclusions:  Our data suggest that dysferlin is inducible in T cells and in muscles in response to at least two cell stressors.  We are investigating this novel regulation and function of dysferlin in T cell apoptosis.  We propose that a critical therapeutic benefit of glucocorticoids may be the induction of dysferlin and up-regulation of muscle membrane repair.  In dysferlinopathy this component is missing, and therefore, may explain, at least in part, the ineffectiveness of glucocorticoids in this disease.  Understanding the mechanisms that regulate dysferlin during muscle repair and inflammation mediated by T cells may lead to therapies that both augment dysferlin function and reduce muscle inflammation in dysferlinopathy patients. </p>

