                  <strong>Session III: Mutation Analysis and Diagnosis </strong><br>

                  <br>

                  This session included presentations   from three research groups regarding dysferlin mutation analysis, protein   testing, and other aspects of diagnosis of dysferlinopathies.&nbsp;   For patients to benefit from any future treatments, they will first   have to be accurately diagnosed.&nbsp; Additionally, many mutation-specific   therapies (exon-skipping, homologous recombination, premature stop codon   readthrough) that are currently being contemplated will only be applicable   to patients with certain mutation types, or will require knowledge of   the exact mutation a patient has.&nbsp;<br>

                  <br>

                  Unfortunately, diagnosis of   dysferlinopathies has been very difficult historically, especially before   this particular form of muscular dystrophy was recognized and reported   in the literature.&nbsp; In particular, many patients have been misdiagnosed   as having an inflammatory myopathy, due to the inflammatory features   often present in dysferlinopathy (see Session VII).&nbsp; In spite of   greater awareness of dysferlin deficiency in the literature and among   neurologists, a definitive diagnosis remains challenging in many cases   for the following reasons:&nbsp;<br>

                  <br>

                  1) Clinical symptoms can vary   widely among patients, even in the same family; symptoms of dysferlinopathy   can also overlap those of other forms of muscular dystrophy.&nbsp; This   necessitates diagnosis at the molecular level. &nbsp;<br>

                  2) The dysferlin gene is very   large, having approximately 6900 bp of cDNA and over 55 exons.&nbsp;   Therefore, muatation screening without prior knowledge of a likely deficiency   based on protein expression is not practical.&nbsp;<br>

                  3) There do not seem to be any   mutational &ldquo;hot spots&rdquo; as have been identified in the dystrophin   gene, nor are there any mutations which account for a large proportion   of cases, except for &ldquo;founder effects&rdquo; in certain specific populations.&nbsp;<br>

                  4) Dysferlin protein expression   can sometimes be reduced or mislocalized as a secondary result of a   mutation in another gene (particularly calpain-3 and caveolin-3 have   been identified in this regard).&nbsp; Thus intepreting whether dysferlin   deficiency is a primary or secondary effect from a protein analysis   of a muscle biopsy can sometimes be difficult.&nbsp;<br><br>

                  <br>

                  Dr. Nicolas Levy&rsquo;s research group   has done sequencing of dysferlin at both the DNA and RNA levels.&nbsp;   One interesting fact is that a large proportion of identified dysfelrin   mutations are missense, often involving the substitution of a single   amino acid.&nbsp; Since polymorphisms (non-pathological amino acid substitutions)   also occur, it is important to know whether an identified amino acid   substitution is pathological.&nbsp;To this end, Dr. Levys group   has constructed a database containing all mutations identified in his   laboratory or written up in the literature, together with predictions   of the result of a missense mutation, and whether it is likely to be   pathological, based on the substitution which occurs and the location   in the protein.&nbsp;Dr. Levy thinks that some of   the cases for which mutations have not been found are due to large deletions,   which are not well detected with the techniques curently employed.&nbsp;   He expects to begin employing a method designed to detect large deletions   in the near future.&nbsp;<br>

				  &nbsp;<br>

				  Dr. Eduard Gallardo&rsquo;s group has   also done extensive mutation screening.&nbsp; The presentation included   a number of different research topics. Among their findings have been   the identification of a premature stop codon (R1905X) in a patient cluster.&nbsp;   A recently published report in which Dr. Gallardo&rsquo;s research group   collaborated was the identification of two manifesting carriers of   dysferlin mutations.&nbsp; The two Spanish patients discussed in the   study are both family members of patients in whom mutations on both   dysferlin alleles were identified.&nbsp; Therefore, because the family&rsquo;s   mutation(s) were known, the patients could be confirmed as having only   one mutant allele.&nbsp;Another research result presented   was the finding that dysferlin-deficient myoblasts do not form normal   myotubes <em>in vitro</em>, supporting a role for dysferlin in myogenesis   as well as in membrane maintenance and repair.&nbsp;<br>

				  <br>				  

				  Dr. Steven Moore discussed findings   of protein analysis and correlations with genetic analysis based on   several years of patient diagnosis at the University of Iowa.&nbsp;   Typically, patient biopsies are used for protein testing (Western Blot   or immunohistochemistry), followed in some cases by genetic sequencing.&nbsp;   The easiest way to tell that a patient is likely to have a primary dysferlin   deficiency is by the complete absence of dysferlin staining.&nbsp; This   is often, but not always the case, and Dr. Moore showed a number of   biopsy slides which give less clear results: e.g. splotcy staining pattern (the   cell membrane does not uniformly give a dysferlin antibody signal, or   only some muscle fibers show dysferlin staining) or mislocalization (sometimes   the absolute quantity of dysferlin is normal, but it resides primarily   in the cell cytoplasm, rather than in the cell membrane).&nbsp;Some of these latter staining   patterns can result from a secondary deficiency of dysferlin due to   a deficiency in another muscle protein; thus genetic analysis is required   to identify which gene causes the primary deficiency.&nbsp; Also, Dr.   Moore showed a slide with an abnormal staining pattern for dysferlin,   in which the mutation analysis found one dysferlin mutation, and one   calpain-3 mutation.&nbsp; Was the second mutation in either calpain   or dysferlin simply not identified in the sequencing, or can being a   heterozygote for dysferlin and calpain-3 mutations be pathological?<br>

