                  <strong>Dysferlin Expression in Human and Mouse Perycites.</strong><br>
                  J. D&iacute;az-Manera1,2, G. Messina1, A. Dellavalle1, R. Tonlorenzi1, G. Ugarte1, E. Gallardo2, I.<br>
                  Illa2, Y. Torrente3, G. Cossu1<br>
                  1Division of Regenerative Medicine, San Raffaelle Scientific Institute. Milano. Italy,<br>
                  2Neuromuscular Diseases Unit. Hospital de la Santa Creu i Sant Pau de Barcelona.<br>
                  Spain, 3Stem Cell Laboratory, Department of Neurological Science, Fondazione IRCCS<br>
                  Ospedale Maggiore Policlinico, Centro Dino Ferrari, University of Milan, Milan, Italy<br><br>

Introduction and objective: Human perycites improve muscle structure when
transplanted into mdx mice and probably represent the human counterpart of murine
mesoangioblasts. Under adequate culture conditions, perycites are able to differentiate
into skeletal muscle but their role in muscle development and regeneration is not yet fully
understood. Dysferlin is important for myogenesis in vitro and its expression by activated
satellite cells may be related with their role in muscle regeneration in vivo. Our aim was to
study the expression of dysferlin in human and mouse perycites. Methods: We isolated human and mouse perycites
from muscle biopsies and induced them to differentiate to striated muscle in vitro. We studied the presence of dysferlin,
myosin heavy chain, MyoD, Myf5, MEF2 and myogenin at days 0, 2, 4, 6 and 8 of differentiation by
immunohistochemistry (IH), Western-Blot or RT-PCR. To study dysferlin expression by perycites in vivo we performed
IH on muscle biopsies from control and dystrophic patients and from control mice, using NG2 or AP to detect perycites.
Results: Dysferlin was expressed in human and mouse perycites in vitro from day 2 of differentiation peaking off at
day 6 and 8, coinciding with the formation of myotubes. The expression of dysferlin was associated with the presence
of MyoD, Myf5 and MEF2 and preceded the appearance of myogenin, detectable starting day 4. We confirmed the
expression of dysferlin in human and mouse perycites in vivo. Moreover, the global NG2+ cell population and the
proportion of double positive dysferlin/Ng2 cells was higher in human dystrophic muscles than in normal control
muscles. Conclusions: Isolated human and mouse perycites express dysferlin early during differentiation to striated
muscle, together with the expression of MyoD and Myf5 and precede myogenin expression. Its higher expression
levels coincide with the formation of myotubes at day 6 and 8, after the peak of myogenin, suggesting a positive
regulation by these three nuclear factors. Dysferlin is also expressed in vivo by these cells. The higher number of NG2
and dysferlin positive cells in human dystrophic muscles may be related to their role in muscle formation and
regeneration. </p>
