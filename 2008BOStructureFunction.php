<strong>Dysferlin Structure and Function </strong><br><br> 

Moderators:  Brad Williams, Nupur Garg. <br>
Participants:  Norma Andrews, Gayathri Balasundaram, Rumaisa Bashir, Bob Brown, Sandra Cooper, Jyoti Jaiswal, Nicholas Keep, Nicolas Lévy, Silvère van der Maarel, Antoine de Morrée, John Robinson, Xiomara Rosales, Srinagesh Koushik, Christian Therrien, Steve Vogel, Barbara Wakimoto<br><br>

1.	What do we know about the structure-function relationships of different dysferlin domains?<br>
•	What clues can be gained from studies with synaptotagmin?<br>
•	What can be learned from reports of mild phenotypes from patients expressing deleted or mutated dysferlin?<br>
•	How can this information serve as a rationale for designing mini-dysferlin constructs for gene therapy?<br><br>

2.	What can be learned about dysferlin’s function from its protein-protein interactions?<br>
•	What are the best strategies for identifying novel interacting partners of dysferlin?<br>
•	How and where do these interactions occur? (eg. Transient association vs. a complex)<br>
•	What does the secondary reduction of some known dysferlin-interactors in dysferlinopathy tell us about dysferlin’s function?<br><br>

3.	Can myoferlin or other ferlin proteins compensate for dysferlin deficiency?<br>
•	Validity of idea (given sequence homology, expression patterns, interacting partners etc.)<br>
•	What are the best approaches to test for compensation?<br>
•	If dysferlin has multiple roles, can all of them be compensated for?<br><br><br>

<span class="style13">The discussion yielded several experiments that could help address some of the questions listed above. These experiments have been organized into the project listed below. </span><br>
<br>

<span class="style13">Determining the identity of the dysferlin-doublet band in Western blots</span><br>

In Western blots, a doublet is often seen for the dysferlin signal.  This could be caused by different isoforms having slightly different molecular masses, or due to post-translational modifications such as glycosylation, phosphorylation etc.  Determining the identity of the doublet could help clarify the roles of different isoforms (if it involves two isoforms) or of post-translational changes in signaling (if glycosylation/phosphorylation is involved). Experimental approaches for determining the identity of the doublet may involve:<br>
(1)	employing proteolytic cleavage of the full-length protein to enhance size-resolution on the gel (the doublet is not well-resolved for the full-length protein),<br>
(2)	 testing if glycosylation is involved by tryptic digestion followed by mass spectrometry, and<br>
(3)	 testing for phosphorylation by using phospho-specific antibodies or by mass spectrometry.<br>

