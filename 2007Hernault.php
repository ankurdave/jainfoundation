                  </span><strong>Genetic and cell biological analyses of  secretory vesicle biogenesis during <em>C. elegans </em>spermatogenesis</strong><br>

Steven W. L&rsquo;Hernault1,2, E. J.  Gleason1, K. L. Hill1,2, P. D. Hartley1, T. L.  Kroft1 and G.-D. Zhu1<strong></strong></p>

				Department of Biology1, and Graduate  Program in Genetics and Molecular Biology2, Emory  University, Atlanta, GA 30322<strong>&nbsp;  &nbsp;</strong><br>

				    <br>

				    <p><em>C. elegans</em> spermatids contain specialized  secretory vesicles (MOs) that must fuse with the cell surface if spermatozoa  are to participate in fertilization. Prior ultrastructural analyses revealed  that MOs first emerge early in spermatogenesis as vesicular derivatives of  Golgi complexes (Wolf et al. 1978. J Ultrastruct Res 63: 155-169).&nbsp; We  have shown that membrane traffic from the Golgi to the developing MOs requires  proteins in lysosomal trafficking pathways.&nbsp; SPE-39 is required for ~100  nm vesicles to

				      fuse  and form ~800 nm MOs, and it interacts with both CeVPS33A and CeVPS33B; these  are homologs of the yeast homotypic fusion and vacuole protein sorting (HOPS)  complex subunit Vps33p. Knockdown of CeVPS33B by RNAi caused spermatogenesis  defects that are similar to those observed in <em>spe-39</em> mutants.&nbsp;  SPE-39 has orthologs in animals but is not found in yeast, so it participates  in an animal cell specific aspect of vesicular trafficking. Once formed, MOs  undergo a series of transport and maturation steps that are correlated with  sperm development.&nbsp; pH sensitive vital dye staining reveals that MOs are  acidified in a manner that is tightly correlated with spermatid  formation.&nbsp; MO acidification is blocked by bafilomycin, which is a  specific inhibitor of the vacuolar (V) ATPase.&nbsp; Since the V-ATPase is  essential, it is a challenge to study its role in a specific tissue of most  animal species.&nbsp; Genome wide microarray experiments uncovered one V-ATPase  B subunit paralog that is up-regulated during spermatogenesis.&nbsp; We  discovered that the spermatogenesis defective <em>spe-5</em> gene encodes this  V-ATPase B subunit.&nbsp; A monoclonal antibody to the SPE-5 protein reveals  that the V-ATPase localizes to the MO during spermatid formation and <em>spe-5 </em>null  mutants have severe defects in their MOs.&nbsp; Once properly formed, MO  function requires that it fuse with the cell surface.&nbsp; The FER-1 dysferlin  family member mediates MO fusion and we have recently initiated studies of <em>fer-1</em>.&nbsp;  We have been characterizing four newly recovered <em>fer-1</em> alleles and  apparent intragenic suppressors of two previously characterized <em>fer-1</em> mutants. These suppressors might offer insight into FER-1 function that is not  easily obtainable by other techniques. </p>

