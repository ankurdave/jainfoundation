                    <p class="home"><strong><font color="#0175C2"><span class="style1">Comparative pathology in different mouse models of dysferlinopathy and in conditionally immortal myoblast lines derived from these models</span>.<br>

                    </font></strong>Terence Partridge, Childrens National Medical Center, Washington DC<br>

                        <br>

                         <strong>Project Objective</strong><br>

<br>
The central aim of this project is to establish and characterize a highly standardizable system for in vitro studies of dysferlin-deficient myogenic cells and differentiated myotubes. 
Investigations of these preparations will provide information on the molecular basis of functional changes in myoblasts and myotubes lacking dysferlin.  An immediate use of this information is that it will identify mechanisms of intrinsic muscle pathology associated with this defect that we can investigate in vivo by following expression of the transcripts and proteins.  At the same time any validated biomarkers will provide outcome criteria as a basis for screening of small molecules as potential therapies.
 In addition, these findings will be correlated with comparative pathological studies of different dysferlin deficient mouse models, to gain a better understanding of how they impact the progression and severity of muscle pathology in dyferlin deficiency. <br>

