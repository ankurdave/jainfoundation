<a name="2"></a>						<p class="home"><strong><font color="#0175C2"><span class="style1">Evaluation of therapies to enhance muscle function in dysferlinpathies</span><br>

                    </font></strong>Elisabeth Barton, University of Pennsylvania<br>

                        <br>

                         <strong>Project Objective</strong><br>

<br>
The mouse models for dysferlin deficiency recapitulate the primary membrane-resealing defect in
muscle. However, the disease progresses slowly, lengthening the time for evaluating any
potential therapies. Histologic characterization has shown that the abdominal muscles of the A/J
mouse provide the most severe pathology, yet this muscle (the rectus abdominus) is difficult to
use for functional measurements due to its fiber geometry/architecture. Therefore, the goal of
this study is to define a functional signature for the skeletal muscles in the A/J mouse over the
course of 1 year. Isolated force measurements will be performed in the limb muscles (extensor
digitorum longus (EDL) and soleus), and the diaphragm from 1 - 12 months of age. This will
provide a solid basis for evaluating any potential therapies in terms of force generating capacity.
 <br>

