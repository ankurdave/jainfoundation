					  <a name="3"></a><p class="home"><strong><font color="#0175C2"><span class="style1">Dysferlin in healthy and injured skeletal muscle</span><br>

                    </font></strong>Robert J. Bloch, University of Maryland School of Medicine (Baltimore, MD)<br>

                        <br>

                         <strong>Project Objective</strong><br>

<br>
Dysferlin is thought to play an important role in repairing tears in the sarcolemma of striated muscle that occur during normal physiological levels of activity of muscle (Bansal et al. 2003). To fulfill its purported role in repairing and resealing tears in the sarcolemma, dysferlin has been proposed to reside in cytoplasmic vesicles, which fuse with the damaged plasma membrane in a Ca2+-dependent manner, recreating in the process a contiguous bilayer that restores sarcolemmal integrity. Dysferlin's vesicular distribution prior to injury, and its fusion with the plasma membrane after injury, along with other proteins such as AHNAK and annexin 2, has been suggested (Huang et al. 2007) but not confirmed. Rather, most reports indicate that dysferlin is present at the sarcolemma of resting muscle, calling into question the possibility that it resides in vesicles that fuse with the torn edges of the sarcolemma after an injury. The subcellular localization of dysferlin in resting muscle has not been investigated across muscles, or as a function of age or gender. Perhaps more significantly, it has never been studied in muscle injured in vivo. In this project, we have begun to address these questions using normal rat hindlimb muscle prior to or following a large strain injury.
 <br>

