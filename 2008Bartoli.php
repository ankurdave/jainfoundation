                  <strong>Can Myoferlin Compensate for Dysferlin Deficiency in LGMD2B?</strong><strong>&nbsp; &nbsp;</strong><br>

Marc Bartoli1, Carinne Roudaut1, Nathalie Bourg1, William Lostal1, Rumaisa Bashir2 and Isabelle Richard1
1Généthon, CNRS UMR8115, 1 rue de l'Internationale, 91000 Évry, France
2School of Biological and Biomedical Sciences, Durham, UK
<br><br>

To test the hypothesis that myoferlin, which is very homologous to dysferlin, may be a good candidate for compensatory therapy in dysferlinopathies, we constructed transgenic mice [Tg(myof)1Gene] over expressing myoferlin using Speedy mouse technology.  Last year, we presented that the mice were fertile, produced normal litter size and that the transgenic newborn pups were healthy, showing no gross developmental abnormalities compared to control littermates.  At the mRNA level, we detected a 100 fold increase in myoferlin messenger compared to wild-type mice. We measured the amount of myoferlin expressed in muscle by immunoblotting and immunohistochemistry using monoclonal antibodies.  Interestingly, the mice expressed a reduced level of dysferlin, suggesting the possibility of cross-talk between the two proteins.  Histological characterization showed no dystrophic pattern, suggesting the absence of deleterious effects caused by the increase in myoferlin expression.  During the past months, we crossed the Tg(myof) mice with dysferlin deficient mice (B6.A/J-dysfprmd).  Offspring from this cross will be characterized at the molecular, histological and functional levels including analysis with a membrane repair assay.  Myoferlin/Bla/J mice characterization and compensation efficiency will be presented.


