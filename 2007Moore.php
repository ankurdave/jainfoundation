                  </span><strong>Muscle biopsy evaluation in the diagnosis of dysferlinopathies</strong><br>

<br>

While  clinically quite similar, the limb-girdle muscular dystrophies (LGMD) are  genetically heterogeneous.&nbsp; LGMD type 2B is one of the more common  autosomal recessive LGMD subtypes.&nbsp; Mutations in the dysferlin gene (<em>DYSF</em>)  are known to cause LGMD2B, as well as a distal myopathy phenotype, Miyoshi  myopathy (MM).&nbsp; Genetic analysis of <em>DYSF</em> has recently become  available as a clinical test; however, protein based studies are likely to  continue to play a key role in patient evaluation.&nbsp; This is because LGMD  genes are numerous, and the genetic analysis of each one can be complex and  expensive.&nbsp; <em>DYSF</em> is particularly problematic since point mutations  are relatively uniformly distributed across more than 50 exons.&nbsp;  Protein-based tests are commonly used to guide the genetic testing in LGMD and  distal myopathy patients.&nbsp; Currently, there are two such tests readily  available for LGMD2B and MM:&nbsp; 1) dysferlin immunostaining and/or  immunoblotting of a muscle biopsy and 2) dysferlin immunoblotting of peripheral  blood monocytes (this latter

test will be covered by other  speakers).&nbsp;Muscle biopsy continues to be a valuable diagnostic procedure  in patients with neuromuscular diseases, especially myopathies including LGMD  and distal myopathy.&nbsp; A dystrophic etiology can often be readily  recognized in cryosections of skeletal muscle, but H&amp;E histopathology is  quite nonspecific.&nbsp; Much greater specificity is achieved by immunostaining  cryosections with a broad panel of antibodies.&nbsp; Normal immunostaining of  dystrophin-glycoprotein complex proteins coupled with an absence of dysferlin  is highly predictive of <em>DYSF</em> mutations, especially when dysferlin  western blots are also negative.&nbsp; However, a wide variety of abnormal  dysferlin immunostaining patterns are seen in biopsies from muscular dystrophy  patients.&nbsp; The following descriptions are some of the more common abnormal  patterns that are seen: a) partial deficiency of sarcolemmal dysferlin, b)  absence of sarcolemmal dysferlin with immunoreactive sarcoplasmic dysferlin, c)  mosaic patterns of muscle fibers with total or partial loss of sarcolemmal  dysferlin (with or without sarcoplasmic dysferlin).&nbsp;&nbsp; Interpretation  of these staining patterns varies widely, because a broad range of myopathic  conditions may have abnormal expression of dysferlin.&nbsp; Furthermore,  immunostaining may poorly correlate with western blot analysis even when  dysferlin is greatly reduced to undetectable in immunostained  cryosections.&nbsp; Some patients with these types of dysferlin abnormalities  are known to have mutations in other LGMD genes (e.g. <em>CAPN3</em>).&nbsp;  Other patients have been reported to have single mutant <em>DYSF</em> alleles  (&ldquo;symptomatic dysferlin mutation carriers&rdquo;).&nbsp; Further study will likely  determine that yet others are LGMD2B or MM patients.</p>

