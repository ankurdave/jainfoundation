                  <strong>Development of a novel  model of dysferlin deficiency</strong><br>

Leland E. Lim1 and Thomas A. Rando1,2,3</p>

                <p>1Neurology Service, VA  Palo Alto Health Care System, Palo Alto, CA; 2Geriatric Research,  Education, and Clincal Center, VA Palo Alto Health Care System, Palo Alto, CA; 3Department  of Neurology and Neurological Sciences, Stanford University School of Medicine,  Stanford, CA&nbsp;</p>

                <p>The investigation of dysferlinopathies, like  other forms of muscular dystrophy, requires the use of animal models in which  pathogenesis can be studied and treatments can be tested.&nbsp; Several models  of dysferlin deficiency already exist.&nbsp; However, with the current models,  it is not possible to study the effect of treatment over time in a single  animal, because analysis requires the euthanizing of the experimental animal  for histological examination.&nbsp; We are creating a novel dysferlin-negative  luciferase reporter mouse

strain.&nbsp;  This is a compound transgenic mouse that consists of a muscle-specific,  estrogen-responsive <em>Cre</em> transgene and a luciferase reporter transgene  whose expression is suppressed by an upstream neo cassette flanked by <em>loxP</em> sequences, bred onto a dysferlin-null background.&nbsp; Muscle-specific  luciferase expression can be &quot;turned on&quot; by the administration of tamoxifen.&nbsp;  Because dysferlin-negative muscle undergoes degeneration, luciferase activity,  which can be measured noninvasively, would diminish over time.&nbsp; Response  to prospective therapies would be demonstrated by attenuation in the loss of  luciferase activity, which can be followed in individual animals at multiple  timepoints during the course of treatment.&nbsp; We believe that this mouse  will be a useful tool for the study of potential treatments for LGMD2B and  Miyoshi myopathy.<strong><span class="style17"><br>

