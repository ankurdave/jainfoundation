				<p><strong>GENERAL SUMMARY </strong><br><br>

				  The First Annual Dysferlin Conference -- held from July 9-12, 2007 in Bermuda -- brought together leading scientists to discuss the latest research towards a therapy for dysferlin deficiency (LGMD2B/Miyoshi) and to build collaborations for future research.   

				  The conference opened with a speech by the founder of the Jain Foundation, describing his personal experience with LGMD2B and highlighting the importance of focused research on this disease. <br>

				  <br>

				  An important goal of the conference was to allow discussion between the leaders of different fields that may contribute to the development of a therapy or cure:<br>

				  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- clinician scientists who see dysferlin patients and study their genetic mutations;<br>

				  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- researchers who study the role of the dysferlin protein and how it causes this disease;<br>

				  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- experts in the processes of membrane fusion and repair (in which dysferlin is now <br>

				  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;known to participate); and <br>

				  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- leaders in emerging muscle-targeted and gene-targeted therapies (including gene <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;therapy, stem cell therapy, muscle regeneration, stop-codon skipping, and exon <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;skipping).<br><br>

				  That goal was a great success, as the 30 invited speakers representing these fields engaged in lively discussions and brainstorming throughout the conference. Many new collaborations were formed, across scientific fields and across continents. <br><br>

				  The Jain Foundation implemented a number of unique initiatives designed to stimulate discussion and encourage collaboration at the conference:<br>

				  <br>

				  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1) The conference schedule included time after each set of 4-5 research presentations <br>

				  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(arranged thematically) for 30-50 minutes of general &quot;brainstorming&quot; on that topic. This <br>

				  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;experimental schedule was very successful. In fact, the brainstorming sessions were <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;essential to the schedule -- providing enough time for all  participants' questions and <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;suggestions on each area of research to be considered. <br>

				  <br>

				  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2) Each invited speaker was asked to provide a list of resources available in his or her <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;laboratory that could be shared with other scientists, as well as a wish-list of resources <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;to which he or she would like to have access. These lists were made available to all of <br>

				  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;the conference participants and were a major success. Many scientists mentioned that <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;they used these lists to make connections with others -- with whom they now plan to <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;share their resources for future research projects in the field. <br><br><br>

