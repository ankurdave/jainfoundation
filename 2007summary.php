				  <br><br>

				    <strong>Goals for next year: </strong><br>

				    <br>

				    We were very pleased with the scientific quality of the First Annual Dysferlin Conference and the amount of discussion that took place. In 2008, we intend to keep the size of the conference about the same in order to facilitate the same level of discussion and maintain the intimate feel of the meeting. The laboratory resource lists were also a great success and will be requested from the speakers again at all future conferences. <br>

				    <br>The brainstorming sessions following each topic were a unique feature of this conference and are very important to the goals of the Jain Foundation. We hope to make the brainstorming sessions even more effective next year by preparing questions with the moderators ahead of time, splitting the participants into smaller brainstorming groups, and encouraging more targeted output from the brainstorming. <br><br>

				    Finally, we would like the future conferences to build on this first conference by including almost exclusively new and unpublished results. This will be an important requirement for speakers invited in the future.<br>

