                  <strong>Session VII: Compensating  for Dysferlin Deficiency</strong><br>

                  <br>

This session focused on ways to counter the pathological process that is initiated by the absence of dysferlin.  The current hypothesis in the field is that when dysferlin is absent, skeletal muscle cells are unable to efficiently repair membrane tears that occur as a consequence of normal muscle use.  These unrepaired tears precipitate a cascade of events that ultimately leads to the destruction of the muscle fiber or severe damage that necessitates recovery by regenerative mechanisms.  Therefore, it is possible that preventing this destructive process or increasing the regenerative response of the muscle may slow, or even stop, the progression of the disease in patients.   <br><br>

<strong>Dr. Se-Jin Lee</strong> (Johns Hopkins University School of Medicine) is exploring how pathways regulating the muscle growth inhibitor myostatin might be used to increase muscle regeneration.  Previous work by Dr. Lee and others has demonstrated that inhibiting myostatin can increase the muscle mass of normal and dystrophic mice.  While simply increasing muscle mass won’t prevent the pathological process initiated by dysferlin deficiency from occurring, it may still help patients retain greater muscle function and substantially improve their quality of life.  Anti-myostatin antibodies are already being tested in clinical trials as a potential therapy for muscular dystrophy (see above); however, it is unclear whether directly targeting myostatin will be an effective approach in patients.  Dr. Lee’s talk focused on how myostatin’s interacting protein partners regulate its activity.  In particular, myostatin’s activity is regulated by the BMP-1/tolloid family of proteases, which cleave the inactive form of myostatin to create the active molecule, and the activin receptors that initiate a cascade of intracellular signals upon binding myostatin.  Multiple members of these two protein families are involved in myostatin processing and signaling, and represent potential targets for small molecule therapies to increase muscle mass by inhibiting the negative effects of myostatin on muscle growth.<br><br>

<strong>Dr. Joshua Zimmerberg</strong> (National Institutes of Health) is directly assessing whether changes in the lipid composition of myoblast membranes can alter their ability to reseal in the absence of dysferlin.  The composition of a lipid bilayer determines many of its properties, including its curvature and stability.   The intrinsic curvature of different lipids can influence the spontaneous resealing of membrane tears or promote specific steps in the membrane fusion process.  Therefore, precisely determining the composition of the myoblast membrane and altering it through the introduction of specific dietary lipids may provide a way to mitigate the effects of dysferlin deficiency.  Dr. Zimmerberg described the development of several tools designed to help him address this question, including a dysferlin-deficient myoblast cell line, a method for assessing lipid composition that makes use of mass spectrometry, and a patch clamp based wounding assay.  Dr. Zimmerberg has shown that dysferlin-deficient myoblasts wound more easily than wild type cells using the laser wounding method, and fail to repair their wounds as quickly.  In addition, treatment of the dysferlin-deficient myoblasts with a poloxamer had a beneficial effect on the rate of membrane resealing.  Experiments to test the effects of specific dietary lipids on these processes are in progress. <br>
<br>

<strong>Dr. Mohan Viswanathan</strong> (Cambria Pharmaceuticals Inc.) is taking advantage of a temperature sensitive mutation in the <em>fer-1</em> gene of <em>C. elegans</em> to do a high throughput screen for small molecules that can correct for the loss of <em>fer-1</em>.  <em>fer-1</em> is the <em>C. elegans</em> orthologue of dysferlin.  <em>fer-1</em> was originally identified as a protein that plays a role in membrane fusion during sperm development, a function that is mechanistically similar to dysferlin’s role in wound repair.  As a result, <em>fer-1</em> mutant worms are sterile.  Thus it is possible to use a simple proliferation assay to screen for molecules that can compensate for the absence of <em>fer-1</em> and restore the worm’s ability to proliferate.  Of 85,000 compounds screened, 260 positives have been found in the primary screen and passed a preliminary retest.  189 of these compounds have been repurchased and retested, yielding 29 (15%) false positives and 160 (85%) that continue to be positive in the worm proliferation assay.  These remaining 160 positive compounds have been grouped into structural subclasses to assess whether common features can be identified that correlate with activity.  The compounds will then be subjected to a secondary screen in <em>C. elegans</em> using a different <em>fer-1</em> mutant, and then to a cell wounding assay in mammalian cells.  The results of these experiments will be used to further narrow down the number of positive compounds and to select a small number of lead compounds that will proceed to the next stage of drug development. <br>
<br>

<strong>Dr. Kanneboyina Nagaraju</strong> (Children’s National Medical Center) tied the session together by describing assays that can be used pre-clinically to evaluate muscle pathology, and any reduction of pathology, in dysferlin-deficient mice.  Any therapeutic strategy developed for dysferlinopathy will likely be tested in dysferlin-deficient mice first to assess efficacy and safety.  Therefore, it is critical to develop a series of tests that are effective at evaluating the severity and progression of this disease in mice, so that mice treated with potential therapies can be accurately monitored for improvement.  In behavioral tests, Dr. Nagaraju found that A/J mice are markedly different from wild type mice in their ability to stay on a rotating rod (rotarod) and that they showed a dramatic decrease in voluntary movement in an open field apparatus measuring total distance moved, movement time, number of movements, and both horizontal and vertical movements (Versamax activity monitor).  <em>In vitro</em> assessment of muscle function using electrode stimulation of a muscle attached to a force monitor revealed no significant difference between normal and dysferlin-deficient mice.  
