                         <strong>Project Objective</strong><br>

                         Test the systemic delivery of morpholinos in a dog model of muscular dystrophy in order to optimize conditions for exon skipping<br>



                          <br>

                          Exon skipping is a technique that is potentially  applicable to a variety of genetic diseases because it can remove a  small portion of a protein that would have contained a premature stop  or a frameshift mutation. This technique, however, will require  frequent, long-term delivery of the exon skipping compound (antisense  oligonucleotides) to the patient's muscle throughout life. This project  will study the systemic delivery method to determine how effective it  is at distributing one type of exon skipping oligonucleotide  (morpholinos) to the muscle of dogs with muscular dystrophy (the CXMD  dog model). Both short-term and long-term delivery to dogs will be  tested. These results will help optimize a delivery method for exon  skipping oligonucleotides in a large animal model of muscular dystrophy.<br>



