<strong><font color="#0175C2"><span class="style1">Regulation of Muscle Growth by the TGF-&szlig; family</span></font><font color="#0175C2"><br>

                        </font></strong>Se-Jin Lee, Johns Hopkins University School of Medicine (Baltimore, MD) <br>

                        <br>

                        <strong>Project Objective</strong><br>Elucidate the regulatory components involved in the control of muscle growth by myostatin and other TGF-&szlig; family members.<br>

  <br>

  Myostatin (MSTN) is a secreted protein that acts to suppress skeletal muscle growth.  As a result, there has been considerable interest in developing strategies to target MSTN signaling to increase muscle growth and regeneration in patients with muscle degenerative diseases.  In recent work, we have demonstrated that other signaling molecules related to MSTN also limit muscle growth in vivo and that the capacity for increasing muscle growth by targeting this general signaling pathway is much greater than previously appreciated. The goal of this project is to conduct a detailed comparative analysis of the effects of various potential inhibitors of this signaling pathway with respect to their potency and efficacy.  Our focus will be on engineered biologics based on proteins that can bind and inhibit the activities of various members of the TGF-ß superfamily, including MSTN.  Because these binding proteins have varying specificities with respect to the spectrum of signaling molecules that they are capable of blocking, our studies will provide essential information regarding the optimal specificity of therapeutic agents aimed at promoting muscle growth and regeneration in patients with muscular dystrophy.<br>

