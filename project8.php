                        <strong><font color="#0175C2"><span class="style1">Dysferlin and the Repair of Injured Muscle </span></font><font color="#0175C2"><br>

                        </font></strong>Robert J. Bloch, University of Maryland School of Medicine (Baltimore, MD) <br>

                        <br>

                        <strong>Project Objective</strong><br>

                      Assess the role of dysferlin in the recovery of muscles from injury in vivo<br>

  <br>

  Dysferlin promotes the resealing of the sarcolemma  of muscle cells injured in vitro, but it has not been shown to have the same  activity in vivo, in muscle fibers injured under physiological conditions.&nbsp; We are studying the role of dysferlin in  mediating the recovery of muscle in vivo in mice subjected to two different kinds  of injuries.&nbsp; Normal muscle recovers from one kind of injury by resealing its sarcolemmal membrane and from the other by replacing  damaged myofibers through myogenesis and regeneration.&nbsp; We are examining the recovery of injured muscles  in dysferlin-null mice to characterize the mechanism of recovery, to learn if  the myogenic potential is diminished, and to determine if electroporation of  cDNA encoding dysferlin is sufficient to restore normal membrane resealing.<br>

