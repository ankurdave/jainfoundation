                  <strong>AHNAK Function in the Dysferlin Protein Complex: Mini-AHNAK as a Biochemical
Tool</strong><br>

Antoine de Morrée, Rune R. Frants, Silvère M. van der Maarel<br><br>

Limb-girdle muscular dystrophies (LGMD) comprise a genetically heterogeneous group of
degenerative muscle disorders. LGMD2B is caused by mutations in dysferlin (DYSF). DYSF
is proposed to play a role in calcium dependent patch-fusion membrane repair, but its exact
role is unclear. By immunoprecipitation experiments with dysferlin-specific antibody
fragments selected from a non-immune llama-derived VhH phage display library we have
previously identified AHNAK and Calpain-3 as protein interaction partners with dysferlin.
AHNAK is a 700 kDa protein found on enlargosomes. It is involved in subsarcolemmal
cytoarchitecture and implicated in membrane enlargement and repair. AHNAK can directly
interact with DYSF, and redistributes to the sarcolemma in regenerating skeletal muscle.
The second DYSF binder we identified is the skeletal muscle protease Calpain-3. Mutations in Calpain-3 cause
LGMD2A. In some LGMD2B patients a secondary reduction of both AHNAK and Calpain-3 can be observed. AHNAK
can act as a substrate for Calpain-3, and, upon cleavage AHNAK fragments lose binding affinity for DYSF indicating the
physiological relevance of the interaction between these proteins. We hypothesize that DYSF function can be further
refined by studying its interaction partners. However, AHNAK is challenging as presently there are no good
(commercially available) antibodies that can specifically detect AHNAK (and the highly similar AHNAK2) and cloning of
the full-length cDNA is difficult due to its large size. Therefore, by analogy with mini-Dystrophin, we generated a mini-
AHNAK construct. This recombinant protein leaves intact the proposed domain structure of AHNAK (N- and C-terminus
separated by a large body of Repeat units) but has a reduced number of repeats (6 instead of 43). Mini-AHNAK was
fused to different tags on either terminus and is expressed as a stable protein. Its localization and behaviour are similar
to endogenous AHNAK indicating this recombinant protein as a strong biochemical tool to study AHNAK function with
respect to DYSF. </p>

