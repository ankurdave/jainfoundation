                <td valign="top"><p class="section">Research Projects - Mutation-Targeted Therapy </p>



                    <p class="home"><strong><font color="#0175C2"><span class="style5">Mutation Identification and Therapies</span> <br>



</font></strong>Nicolas Levy, Inserm,  Hopital d'enfants de la Timone (Marseille, France)<br>



                        <br>



                         <strong>Project Objective</strong><br>



                         Explore dysferlin function and mutation-targeted therapies using information gained from extensive sequencing of mutations in dysferlin-deficient patients<br>



                          <br>



The goal of our project is to better understand the   functions of dysferlin, to analyze the fate of abnormal dysferlin protein, and finally to set  up a therapeutic trial in human patients affected with  dysferlinopathies. The project can be split into three parts and  will be performed on a collaborative basis with others labs  in  the field.<br>



&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;- For diagnostic purposes, our group  has identified 180 mutations in DYSF, using a  strategy of DHPLC screening and further sequencing. This extensive  mutation screen is also used to set up a DYSF specific database  allowing genotype-phenotype correlations.<br>



 &nbsp;&nbsp;&nbsp;&nbsp; &nbsp;- Based on the identified human mutations, cell cultures from control individuals and patients will be established in order to analyze dysferlin's role in  plasma membrane supply phenomena, in both physiological (after  phagocytosis : monocytes/macrophages) or experimental conditions  (mechanical stress and membrane damage : monocytes/macrophages,  myoblasts/myotubes). Fibroblasts will be transfected with constructs  allowing the synthesis of tagged dysferlin (vector provided by K.  Bushby). <br>



                      &nbsp;&nbsp;&nbsp;&nbsp; &nbsp;- According to each mutation's type and position, specific and targeted  therapeutic approaches will be addressed first in culture, then in mouse  models, before including the patients in therapeutic trials. In  collaboration with G&eacute;n&eacute;thon and the Institute of Myology, the exon skipping  method will be used for homozygous mutations. We also plan to test  Aminoglycosides (PTC124) for mutations resulting in a premature STOP  codon. <br>



