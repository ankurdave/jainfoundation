<strong><font color="#0175C2"><span class="style5">Oligonucleotide-Mediated Gene Repair </span> <br>

</font></strong>Thomas Rando, Stanford University (Palo Alto, CA) 

<br>

<br>

<strong>Project Objective</strong><br>

Test the potential of oligonucleotides to induce  single base changes in the dysferlin gene to correct point mutations or alter  splicing<br>

<br>

LGMD2B is due to mutations in the dysferlin  gene, some of which are point mutations resulting in stop codons. Previous  studies have revealed the potential of small oligonucleotide vectors to induce  single base changes in genomic DNA, thereby offering the possibility of  therapeutic applications in diseases due to point mutations. This project  explores the potential of custom-designed oligonucleotides to effect single  base changes in the dysferlin gene in a mouse model of LGDM2B. Studies will be  done on isolated muscle cells from those mice as well as directly on muscles in  vivo. The effectiveness will be assessed by the restoration of dysferlin  expression and by direct tests of gene correction.<br><br>

We are also working to generate strains of mice in which the degenerative and regenerative   phases of muscular dystrophies can be assessed non-invasively, primarily   to be used in experimental therapeutic trials.&nbsp;One of the major limitations to the study of therapeutic agents for   the treatment of muscular dystrophies such as LGMD2B is the absence   of reliable markers of disease activity in the living mouse. We aim   to generate strains of mice in which the luciferase gene will be used   either to register degeneration (by loss of activity when myofibers   die) or regeneration (when muscle progenitors contribute to new myofibers   formation), allowing these processesto be monitored by bioluminescence   imaging.<br>

