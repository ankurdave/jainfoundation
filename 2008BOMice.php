<span class="style13">Modeling Disease in Mice</span><br>
<br>

Moderators:  Doug Albrecht, Nilah Monnier  <br>
Participants:  Beth Barton, Bob Bloch, Ling Guo, Christina Jamieson, Kanneboyina Nagaraju, Terry Partridge, Morayma Reyes, Isabelle Richard, Joseph Roche, Jennifer Shadrach, Daniel Stockholm, Peter Tiidus, Amy Wagers, Woody White<br><br>

1.	Which dysferlin deficient muscles show signs of pathology, and at what age?<br>
•	Do all muscles express dysferlin? <br>
•	Does dysferlin’s presence on the membrane respond dynamically to load or injury?<br>
•	Why are some muscles affected before others?<br><br>

2.	Can comparisons between the different dysf-/- mouse strains give us some clues to its function?<br>
•	What are the differences in pathology in the A/J vs SJL vs dysf KO vs Bla/J<br>
•	Why are SJL mice significantly better than Wt at recovering from muscle injury?<br>
•	Should other models of dysferlin deficiency be considered?  dysf-/- / mdx, dysf-/- / scgd, dysf-/- / myof-/-, dysf-/-/SCID <br><br>

3.	Physical perturbations-how do you distinguish a dysf-/- mouse from a Wt mouse? <br>
•	Treadmill running<br>
•	Cardiotoxin injection<br>
•	Eccentric contraction/ injury protocols<br>
•	Activity assays<br>
•	Grip strength testing<br>
•	Rotarod<br><br><br>

<span class="style13">The discussion yielded several experiments that could help address some of the questions listed above. These experiments have been organized into several discrete projects that are listed below. </span><br>
<br>
<strong>Comparative study of pathology in different mouse models of dysferlinopathy</strong><br>
This project focuses on comparing disease progression in the 4 mouse models of dysferlinopathy – A/J, SJL, Bla/J and the Bittner mice. The mice will be studied at different ages to compare which types of muscles are involved and to what extent by determination of muscle pathology as well as functional outcomes.<br><br>

<strong>Determining the effects of physical activity on pathology in mouse models of dysferlinopathy</strong><br>
This project involves exposing dysferlin-deficient mice of different ages (both pre- and post-symptomatic) to a variety of physical activities (both weight-bearing and non-weight-bearing) for various lengths of time and in various combinations.  Muscle pathology in these mice (versus un-exercised dysferlin-deficient mice) will be monitored both immediately following exercise and in longer-term follow-up examinations to evaluate the short-term and long-term effects of physical activity on the progress of dysferlinopathy in mice.<br><br>

<strong>Gene expression analysis of affected and unaffected muscles in pre- and post-symptomatic mice</strong><br>
This project will look for differences in gene expression between muscles in pre-symptomatic dysferlin-deficient mice versus affected muscles in post-symptomatic mice, in order to identify any changes that may contribute to the onset of pathology.  It will also look for differences between unaffected and affected muscles in the same symptomatic animals, in order to identify any genes that may contribute to the resistance of some muscle groups to pathology. <br><br>

<strong>Investigating the effects of muscle fiber type on pathology</strong><br>
This project involves determining the muscle fiber type ratios in affected versus unaffected muscles in symptomatic dysferlin-deficient mice, in order to investigate a possible difference in susceptibility of the different fiber types to pathology.<br><br>

<strong>Testing whether inflammation can be included as an outcome measure in preclinical studies</strong><br>
This project will rigorously test a variety of assays of muscle inflammation on dysferlin-deficient and control mice to determine the reproducibility of these assays and their statistical power in distinguishing between affected and unaffected mice.  The project should only be done on dysferlin-deficient mice with appropriate controls (e.g. Bla/J vs. Bl6 mice, or Bittner vs. Bl10 mice).<br><br>



